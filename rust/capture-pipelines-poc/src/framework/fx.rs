//! Effects (`Fx`): cross-cutting concerns as compile-time capabilities, not hardcoded
//! context fields.
//!
//! The framework core knows nothing about ingestion warnings, TopHog, or app metrics.
//! Each is a *sink* — per-chunk accumulation state a step writes into — reached
//! through a capability trait. A pipeline composes its own `Fx` struct from the sinks
//! it wants ([`compose_fx!`] generates the wiring), and a step declares the sinks it
//! uses as trait bounds (`Fx: WarningEffects`). A step can't touch a sink the pipeline
//! didn't register, and a pipeline that forgets a sink won't compile a step that needs
//! it — capability misuse is a compile error, not a silent drop.

/// Internal wiring: "this `Fx` holds a sink of type `S`". Steps never call this
/// directly — capability extension traits (like [`WarningEffects`]) route to it.
/// [`compose_fx!`] generates one impl per sink.
pub trait HasSink<S> {
    /// Mutable access to the stored sink.
    fn sink(&mut self) -> &mut S;
}

/// A single ingestion warning accumulated during a chunk.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct Warning {
    /// Team the warning is attributed to (the *data* demands a team in hand).
    pub team_id: u64,
    /// Warning type, a low-cardinality static string.
    pub kind: &'static str,
    /// Human-readable detail.
    pub details: String,
}

/// The ingestion-warnings sink: warnings collected this chunk, flushed at chunk end.
#[derive(Default)]
pub struct WarningSink {
    warnings: Vec<Warning>,
}

impl WarningSink {
    /// Append a warning.
    pub fn push(&mut self, warning: Warning) {
        self.warnings.push(warning);
    }

    /// The warnings collected so far.
    pub fn warnings(&self) -> &[Warning] {
        &self.warnings
    }

    /// Number of warnings collected.
    pub fn len(&self) -> usize {
        self.warnings.len()
    }

    /// Whether any warnings were collected.
    pub fn is_empty(&self) -> bool {
        self.warnings.is_empty()
    }
}

/// The capability a step bounds on to emit ingestion warnings. Provided by the
/// warnings "plugin" as a blanket impl over any `Fx` that holds a [`WarningSink`] —
/// so `fx.warn(...)` appears directly on the effects struct, with no `.sink()` at the
/// call site.
pub trait WarningEffects {
    /// Record an ingestion warning for `team_id`.
    fn warn(&mut self, team_id: u64, kind: &'static str, details: String);
}

impl<Fx: HasSink<WarningSink>> WarningEffects for Fx {
    fn warn(&mut self, team_id: u64, kind: &'static str, details: String) {
        self.sink().push(Warning {
            team_id,
            kind,
            details,
        });
    }
}

/// Declare a pipeline's composed `Fx` struct and generate its [`HasSink`] impls — one
/// field, one impl per sink. This is the "derive later" from the design doc, done as
/// `macro_rules!` for the POC.
///
/// ```
/// use capture_pipelines_poc::compose_fx;
/// use capture_pipelines_poc::{WarningSink, WarningEffects};
///
/// compose_fx!(MyFx { warnings: WarningSink });
///
/// let mut fx = MyFx::default();
/// fx.warn(42, "invalid_property", "bad".into()); // capability present because the sink is
/// assert_eq!(fx.warnings.len(), 1);
/// ```
///
/// A pipeline whose `Fx` lacks the sink cannot compile a step that needs it:
///
/// ```compile_fail
/// use capture_pipelines_poc::compose_fx;
/// use capture_pipelines_poc::WarningEffects;
///
/// // An Fx with NO warning sink registered.
/// compose_fx!(BareFx {});
///
/// let mut fx = BareFx::default();
/// fx.warn(1, "nope", "x".into()); // ERROR: `WarningEffects` is not implemented for `BareFx`
/// ```
#[macro_export]
macro_rules! compose_fx {
    ($name:ident { $($field:ident : $sink:ty),* $(,)? }) => {
        /// A pipeline-composed effects struct (generated by `compose_fx!`).
        #[derive(Default)]
        pub struct $name {
            $(
                /// Sink field (generated).
                pub $field: $sink,
            )*
        }

        $(
            impl $crate::framework::fx::HasSink<$sink> for $name {
                fn sink(&mut self) -> &mut $sink {
                    &mut self.$field
                }
            }
        )*
    };
}

#[cfg(test)]
mod tests {
    use super::*;

    compose_fx!(TestFx {
        warnings: WarningSink,
    });

    #[test]
    fn warn_lands_in_the_composed_sink() {
        let mut fx = TestFx::default();
        fx.warn(7, "invalid_property", "value too long".into());
        fx.warn(7, "invalid_property", "again".into());
        assert_eq!(fx.warnings.len(), 2);
        assert_eq!(fx.warnings.warnings()[0].team_id, 7);
    }
}

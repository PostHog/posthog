"""Run the checked-in ``model.ubj`` on fixed example sessions (no ClickHouse).

``fixtures/example_sessions.json`` holds deterministic feature vectors and
golden scores generated by ``bin/generate_surfacing_example_fixtures.py``.
These tests catch regressions in model loading, validation, and predict against
the same artifact production uses.
"""

from __future__ import annotations

import os
import json
from collections.abc import Generator
from pathlib import Path
from typing import Any

import pytest

import numpy as np
import pandas as pd

from posthog.temporal.session_replay.surfacing_scoring_sweep import scorer as scorer_mod
from posthog.temporal.session_replay.surfacing_scoring_sweep.features import (
    MODEL_FEATURE_SCHEMA_VERSION,
    validate_features,
)
from posthog.temporal.session_replay.surfacing_scoring_sweep.scorer import get_feature_names, predict, warmup

FIXTURES_PATH = Path(__file__).parent / "fixtures" / "example_sessions.json"


def _load_example_sessions() -> dict[str, Any]:
    if not FIXTURES_PATH.exists():
        pytest.fail(f"Missing {FIXTURES_PATH}. Run: uv run python bin/generate_surfacing_example_fixtures.py")
    return json.loads(FIXTURES_PATH.read_text(encoding="utf-8"))


def _sessions_to_dataframe(payload: dict[str, Any], feature_names: tuple[str, ...]) -> pd.DataFrame:
    rows: list[dict[str, Any]] = []
    for session in payload["sessions"]:
        row: dict[str, Any] = {
            "team_id": session["team_id"],
            "session_id": session["session_id"],
            "distinct_id": session["distinct_id"],
            "min_first_timestamp": pd.Timestamp(session["min_first_timestamp"]),
        }
        row.update(session["features"])
        rows.append(row)
    columns = ["team_id", "session_id", "distinct_id", "min_first_timestamp", *feature_names]
    return pd.DataFrame(rows, columns=columns)


@pytest.fixture(scope="module", autouse=True)
def bundled_booster_loaded() -> Generator[None, None, None]:
    old_path = os.environ.pop("SESSION_INTERESTINGNESS_MODEL_PATH", None)
    scorer_mod._BOOSTER = None
    scorer_mod._FEATURE_NAMES = None
    warmup()
    yield
    scorer_mod._BOOSTER = None
    scorer_mod._FEATURE_NAMES = None
    if old_path is not None:
        os.environ["SESSION_INTERESTINGNESS_MODEL_PATH"] = old_path


@pytest.fixture(scope="module")
def example_payload() -> dict[str, Any]:
    return _load_example_sessions()


@pytest.fixture(scope="module")
def feature_names() -> tuple[str, ...]:
    return get_feature_names()


class TestBundledModelExampleSessions:
    def test_fixture_matches_current_schema(
        self, example_payload: dict[str, Any], feature_names: tuple[str, ...]
    ) -> None:
        assert example_payload["schema_version"] == 2
        assert example_payload["model_feature_schema_version"] == MODEL_FEATURE_SCHEMA_VERSION
        assert example_payload["feature_count"] == len(feature_names)

    def test_warmup_loads_bundled_model(self) -> None:
        assert scorer_mod._BOOSTER is not None
        assert scorer_mod._FEATURE_NAMES is not None

    def test_predict_matches_golden_scores_per_session(
        self,
        example_payload: dict[str, Any],
        feature_names: tuple[str, ...],
    ) -> None:
        for session in example_payload["sessions"]:
            df = _sessions_to_dataframe({"sessions": [session]}, feature_names)
            validate_features(df, feature_names=feature_names)
            scores = predict(df)
            assert scores.shape == (1,)
            assert float(scores[0]) == pytest.approx(session["expected_score"], rel=1e-5, abs=1e-5)

    def test_predict_batch_matches_golden_scores(
        self,
        example_payload: dict[str, Any],
        feature_names: tuple[str, ...],
    ) -> None:
        df = _sessions_to_dataframe(example_payload, feature_names)
        validate_features(df, feature_names=feature_names)
        scores = predict(df)
        expected = np.array([session["expected_score"] for session in example_payload["sessions"]], dtype=np.float32)
        np.testing.assert_allclose(scores, expected, rtol=1e-5, atol=1e-5)

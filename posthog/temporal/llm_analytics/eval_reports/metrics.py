"""Prometheus metrics for the LLMA evaluation reports temporal workflows.

Follows the same pattern as trace_clustering/metrics.py and sentiment/metrics.py.
Metrics are emitted via Temporal's built-in metric meter (activity/workflow context)
and scraped by the Prometheus endpoint on the worker pod.
"""

import typing
import datetime as dt

from django.conf import settings

from temporalio import activity, workflow
from temporalio.worker import (
    ActivityInboundInterceptor,
    ExecuteActivityInput,
    ExecuteWorkflowInput,
    Interceptor,
    WorkflowInboundInterceptor,
    WorkflowInterceptorClassInput,
)

from posthog.temporal.llm_analytics.metrics import ExecutionTimeRecorder, get_metric_meter

# ---------------------------------------------------------------------------
# Histogram bucket config (imported by common/worker.py for PrometheusConfig)
# ---------------------------------------------------------------------------

EVAL_REPORTS_LATENCY_HISTOGRAM_METRICS = (
    "llma_eval_reports_activity_execution_latency",
    "llma_eval_reports_activity_schedule_to_start_latency",
    "llma_eval_reports_workflow_execution_latency",
)
EVAL_REPORTS_LATENCY_HISTOGRAM_BUCKETS = [
    1_000.0,  # 1 second
    5_000.0,  # 5 seconds
    10_000.0,  # 10 seconds
    30_000.0,  # 30 seconds
    60_000.0,  # 1 minute
    120_000.0,  # 2 minutes
    300_000.0,  # 5 minutes
    600_000.0,  # 10 minutes
    900_000.0,  # 15 minutes
]

# ---------------------------------------------------------------------------
# Activity / workflow type sets for the interceptor
# ---------------------------------------------------------------------------

EVAL_REPORTS_ACTIVITY_TYPES = {
    "fetch_due_eval_reports_activity",
    "fetch_count_triggered_eval_reports_activity",
    "prepare_report_context_activity",
    "run_eval_report_agent_activity",
    "store_report_run_activity",
    "deliver_report_activity",
    "update_next_delivery_date_activity",
}

EVAL_REPORTS_WORKFLOW_TYPES = {
    "llma-eval-reports-scheduled-coordinator",
    "llma-eval-reports-count-triggered-coordinator",
    "llma-eval-reports-generate-and-deliver",
}

# ---------------------------------------------------------------------------
# Counter helpers
# ---------------------------------------------------------------------------


def increment_workflow_started(workflow_type: str) -> None:
    meter = get_metric_meter({"workflow_type": workflow_type})
    meter.create_counter(
        "llma_eval_reports_workflow_started",
        "Eval reports workflows started",
    ).add(1)


def increment_workflow_finished(status: str, workflow_type: str) -> None:
    meter = get_metric_meter({"status": status, "workflow_type": workflow_type})
    meter.create_counter(
        "llma_eval_reports_workflow_finished",
        "Eval reports workflows finished",
    ).add(1)


def record_coordinator_reports_found(count: int, trigger_type: str) -> None:
    if not activity.in_activity() and not workflow.in_workflow():
        return
    meter = get_metric_meter({"trigger_type": trigger_type})
    meter.create_counter(
        "llma_eval_reports_coordinator_reports_found",
        "Reports found by coordinator per poll cycle",
    ).add(count)


def increment_report_generated(outcome: str) -> None:
    if not activity.in_activity() and not workflow.in_workflow():
        return
    meter = get_metric_meter({"outcome": outcome})
    meter.create_counter(
        "llma_eval_reports_generated",
        "Reports generated by outcome (completed/fallback/error)",
    ).add(1)


def increment_delivery(delivery_type: str, status: str) -> None:
    if not activity.in_activity() and not workflow.in_workflow():
        return
    meter = get_metric_meter({"delivery_type": delivery_type, "status": status})
    meter.create_counter(
        "llma_eval_reports_deliveries",
        "Report delivery attempts by type and status",
    ).add(1)


def increment_errors(error_type: str) -> None:
    if not activity.in_activity() and not workflow.in_workflow():
        return
    meter = get_metric_meter({"error_type": error_type})
    meter.create_counter(
        "llma_eval_reports_errors",
        "Error counts by type",
    ).add(1)


def record_coordinator_check_count(count: int, trigger_type: str) -> None:
    """How many report rows the coordinator checked (total, not just triggered)."""
    if not activity.in_activity() and not workflow.in_workflow():
        return
    meter = get_metric_meter({"trigger_type": trigger_type})
    meter.create_counter(
        "llma_eval_reports_coordinator_checked",
        "Report rows checked by coordinator per poll cycle",
    ).add(count)


# ---------------------------------------------------------------------------
# Interceptor — automatic timing for activities and workflows
# ---------------------------------------------------------------------------


class EvalReportsMetricsInterceptor(Interceptor):
    """Interceptor to emit Prometheus metrics for eval reports workflows."""

    task_queue = settings.LLMA_TASK_QUEUE

    def intercept_activity(self, next: ActivityInboundInterceptor) -> ActivityInboundInterceptor:
        return _EvalReportsActivityInterceptor(super().intercept_activity(next))

    def workflow_interceptor_class(
        self,
        input: WorkflowInterceptorClassInput,  # noqa: A002
    ) -> type[WorkflowInboundInterceptor] | None:
        return _EvalReportsWorkflowInterceptor


class _EvalReportsActivityInterceptor(ActivityInboundInterceptor):
    async def execute_activity(self, input: ExecuteActivityInput) -> typing.Any:
        activity_info = activity.info()
        activity_type = activity_info.activity_type

        if activity_type not in EVAL_REPORTS_ACTIVITY_TYPES:
            return await super().execute_activity(input)

        # Schedule-to-start latency (queue depth indicator)
        scheduled_time = activity_info.scheduled_time
        started_time = activity_info.started_time
        if scheduled_time and started_time:
            schedule_to_start_ms = int((started_time - scheduled_time).total_seconds() * 1000)
            meter = get_metric_meter({"activity_type": activity_type})
            meter.create_histogram_timedelta(
                name="llma_eval_reports_activity_schedule_to_start_latency",
                description="Time between activity scheduling and start",
                unit="ms",
            ).record(dt.timedelta(milliseconds=schedule_to_start_ms))

        with ExecutionTimeRecorder(
            "llma_eval_reports_activity_execution_latency",
            description="Execution latency for eval reports activities",
            histogram_attributes={"activity_type": activity_type},
        ):
            return await super().execute_activity(input)


class _EvalReportsWorkflowInterceptor(WorkflowInboundInterceptor):
    async def execute_workflow(self, input: ExecuteWorkflowInput) -> typing.Any:
        workflow_info = workflow.info()
        workflow_type = workflow_info.workflow_type

        if workflow_type not in EVAL_REPORTS_WORKFLOW_TYPES:
            return await super().execute_workflow(input)

        increment_workflow_started(workflow_type)

        with ExecutionTimeRecorder(
            "llma_eval_reports_workflow_execution_latency",
            description="End-to-end workflow execution latency",
        ):
            try:
                result = await super().execute_workflow(input)
                increment_workflow_finished("completed", workflow_type)
                return result
            except Exception as e:
                increment_errors(type(e).__name__)
                increment_workflow_finished("failed", workflow_type)
                raise

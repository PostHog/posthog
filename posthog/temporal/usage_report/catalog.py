"""Declarative catalog of usage-report metrics (v2 only — the legacy Celery
path in `posthog.tasks.usage_report` never reads this).

A metric is one frozen declaration: the payload field name, the SQL condition
that counts it, whether it's billable, and the fixture cases that pin its
semantics. Everything else is derived from the `EVENTS_METRICS` list:

* `compiler.py` fuses all metrics of a family into one ClickHouse scan,
* `queries.py` registers that scan as a single multi-output `QuerySpec`,
* the destination keys the aggregator reads are `metric.all_data_key`,
* `test_catalog.py` runs every `Case` through the production compiler.

Adding an events-family metric is a single entry here. A metric declared
without cases fails CI (`test_every_metric_has_cases`) — a billed number
without a pinned semantic is exactly how we've over/undercharged before.
"""

import dataclasses
from typing import Any, Optional

from posthog.schema_enums import AIEventType

# Single source of truth while v1 and v2 run side by side; moves here when v1 dies.
from posthog.tasks.usage_report import CONVERSATIONS_EVENTS


def _quoted(values: tuple[str, ...]) -> str:
    escaped = (v.replace("\\", "\\\\").replace("'", "\\'") for v in values)
    return ", ".join(f"'{v}'" for v in escaped)


@dataclasses.dataclass(frozen=True)
class Expr:
    """A boolean SQL fragment over the family's table, composable with `&`, `|`, `~`."""

    sql: str

    def __and__(self, other: "Expr") -> "Expr":
        return Expr(f"({self.sql} AND {other.sql})")

    def __or__(self, other: "Expr") -> "Expr":
        return Expr(f"({self.sql} OR {other.sql})")

    def __invert__(self) -> "Expr":
        return Expr(f"(NOT {self.sql})")


def event_in(*events: str) -> Expr:
    return Expr(f"event IN ({_quoted(events)})")


def person_mode_in(*modes: str) -> Expr:
    return Expr(f"person_mode IN ({_quoted(modes)})")


def any_group_set() -> Expr:
    return Expr("($group_0 != '' OR $group_1 != '' OR $group_2 != '' OR $group_3 != '' OR $group_4 != '')")


@dataclasses.dataclass(frozen=True)
class EventFixture:
    """One event a `Case` inserts. `at_hours` is relative to the report
    period's start (the period is one day, so anything outside [0, 24) is
    out of period). Fixtures sharing a `dup` key are inserted with the same
    uuid to exercise ClickHouse-level deduplication.
    """

    event: str
    person_mode: str = "full"
    at_hours: float = 12.0
    dup: Optional[str] = None
    properties: dict[str, Any] = dataclasses.field(default_factory=dict)


def ev(event: str, **kwargs: Any) -> EventFixture:
    return EventFixture(event, **kwargs)


@dataclasses.dataclass(frozen=True)
class Case:
    given: tuple[EventFixture, ...]
    expect: int
    note: str = ""


@dataclasses.dataclass(frozen=True)
class EventsMetric:
    """A per-team counter computed from the `events` table for the report period."""

    name: str
    where: Expr
    dedup: bool = False
    billable: bool = False
    cases: tuple[Case, ...] = ()

    @property
    def all_data_key(self) -> str:
        return f"teams_with_{self.name}"


# Events excluded from the billable event counts: flag calls and survey
# responses are billed via their own metrics, $exception via error tracking,
# AI events via ai_event_count_in_period, and the Conversations widget events
# are generated by our own support widget rather than customer instrumentation.
NON_BILLABLE_EVENTS: tuple[str, ...] = (
    "$feature_flag_called",
    "survey sent",
    "survey shown",
    "survey dismissed",
    "$exception",
    *(event.value for event in AIEventType),
    *CONVERSATIONS_EVENTS,
)


EVENTS_METRICS: tuple[EventsMetric, ...] = (
    EventsMetric(
        name="event_count_in_period",
        where=~event_in(*NON_BILLABLE_EVENTS),
        dedup=True,
        billable=True,
        cases=(
            Case(given=(ev("$pageview"), ev("checkout")), expect=2),
            Case(
                given=(ev("$pageview", dup="a"),) * 3,
                expect=1,
                note="pipeline duplicates are deduplicated, not billed",
            ),
            Case(
                given=(ev("$ai_generation"), ev("$exception"), ev("$feature_flag_called"), ev("$conversations_loaded")),
                expect=0,
                note="billed via other metrics, or PostHog-generated",
            ),
            Case(
                given=(ev("$pageview", at_hours=-1.0), ev("$pageview", at_hours=25.0)),
                expect=0,
                note="outside the report period",
            ),
        ),
    ),
    EventsMetric(
        name="enhanced_persons_event_count_in_period",
        where=~event_in(*NON_BILLABLE_EVENTS) & person_mode_in("full", "force_upgrade"),
        dedup=True,
        billable=True,
        cases=(
            Case(
                given=(
                    ev("$pageview", person_mode="full"),
                    ev("$pageview", person_mode="force_upgrade"),
                    ev("$pageview", person_mode="propertyless"),
                ),
                expect=2,
            ),
        ),
    ),
    EventsMetric(
        name="event_count_with_groups_in_period",
        where=any_group_set(),
        cases=(
            Case(
                given=(ev("$pageview", properties={"$group_0": "org:5"}), ev("$pageview")),
                expect=1,
            ),
        ),
    ),
)

import re
import copy
from collections.abc import Callable
from dataclasses import dataclass, field, fields
from enum import Enum
from typing import TYPE_CHECKING, Any, Literal, Optional, TypeVar

from posthog.hogql.constants import ConstantDataType
from posthog.hogql.errors import NotImplementedError

if TYPE_CHECKING:
    from posthog.hogql.context import HogQLContext

# Given a string like "CorrectHorseBS", match the "H" and "B", so that we can convert this to "correct_horse_bs"
camel_case_pattern = re.compile(r"(?<!^)(?<![A-Z])(?=[A-Z])")


# Class-name → snake-case overrides that don't fall out of the default regex.
# NOTE: Sync with ./test/test_visitor.py#test_hogql_visitor_naming_exceptions
_VISIT_NAME_REPLACEMENTS = {
    "hog_qlxtag": "hogqlx_tag",
    "hog_qlxattribute": "hogqlx_attribute",
    "uuidtype": "uuid_type",
    "string_jsontype": "string_json_type",
}


def _compute_visit_method_name(cls_name: str) -> str:
    """Camel → snake conversion of a class name, with the replacements above
    applied. Computed once per AST subclass at class-creation time (see
    ``AST.__init_subclass__``) and cached on the class as
    ``_visit_method_name``."""
    name = camel_case_pattern.sub("_", cls_name).lower()
    for old, new in _VISIT_NAME_REPLACEMENTS.items():
        name = name.replace(old, new)
    return f"visit_{name}"


@dataclass(kw_only=True, slots=True)
class AST:
    start: Optional[int] = field(default=None)
    end: Optional[int] = field(default=None)

    # Populated by __init_subclass__ — every concrete AST class gets its own
    # method name computed once at class-creation time, so accept() is just
    # an attribute read and a getattr per visit instead of a regex + four
    # str.replace + f-string on every node walk.
    _visit_method_name = "visit_ast"

    def __init_subclass__(cls, **kwargs):
        # Deliberately not calling super().__init_subclass__: AST's parent is
        # `object` (whose hook is a no-op anyway), and `super()` here trips
        # the same class-identity quirk that affects AST.__str__ below — the
        # method's __class__ closure cell points at the pre-
        # @dataclass(slots=True) AST, which isn't in the new MRO.
        #
        # This body runs twice per slotted subclass (once before the
        # @dataclass(slots=True) rebuild, once after) — keep it idempotent.
        # Extension point for overriding the cached name is
        # `_VISIT_NAME_REPLACEMENTS` above, not a class-level assignment
        # of `_visit_method_name` (which would be clobbered here).
        cls._visit_method_name = _compute_visit_method_name(cls.__name__)

    # This is part of the visitor pattern from visitor.py.
    def accept(self, visitor):
        method_name = self._visit_method_name
        visit = getattr(visitor, method_name, None)
        if visit is not None:
            return visit(self)
        visit_unknown = getattr(visitor, "visit_unknown", None)
        if visit_unknown is not None:
            return visit_unknown(self)
        raise NotImplementedError(f"{visitor.__class__.__name__} has no method {method_name}")

    def __deepcopy__(self, memo: dict[int, "AST"]) -> "AST":
        # copy.deepcopy() dispatches here automatically. The stdlib pickle-protocol
        # path (__reduce_ex__ + _reconstruct) is ~8x slower per node, so we instead run
        # a straight-line clone function generated once per class (exec-codegen, the same
        # technique dataclasses uses for __init__) and cached. Registering `new` in `memo`
        # before recursing keeps the cyclic resolved-type graph (FieldType <-> SelectQueryType)
        # and shared subtrees intact, matching stock deepcopy semantics.
        cls = self.__class__
        clone = _clone_fn_cache.get(cls)
        if clone is None:
            clone = _build_clone_fn(cls)
            _clone_fn_cache[cls] = clone
        return clone(self, memo)

    def to_hogql(self):
        from posthog.hogql.context import HogQLContext
        from posthog.hogql.printer import print_prepared_ast

        return print_prepared_ast(
            node=self,
            context=HogQLContext(enable_select_queries=True, limit_top_select=False),
            dialect="hogql",
        )

    def __str__(self):
        if isinstance(self, Type):
            # `repr(self)` instead of `super().__str__()` because
            # @dataclass(slots=True) rebuilds the class object, and the
            # method's super() closure cell points at the pre-slots class
            # which is no longer in self's MRO. object.__str__ falls back
            # to __repr__ anyway, and __repr__ is generated by @dataclass.
            return repr(self)
        return f"sql({self.to_hogql()})"


# --- deep-copy fast path (see AST.__deepcopy__) ---------------------------------
# Immutable scalars deepcopy returns as-is; everything else is rebuilt.
_CLONE_ATOMIC_TYPES = frozenset({str, bytes, int, float, bool, complex, type(None)})
_clone_fn_cache: dict[type, Callable[["AST", dict[int, "AST"]], "AST"]] = {}


def _clone_value(value: Any, memo: dict[int, Any]) -> Any:
    cls = type(value)
    if cls in _CLONE_ATOMIC_TYPES:
        return value
    if cls is list:
        return [_clone_value(v, memo) for v in value]
    if isinstance(value, AST):
        existing = memo.get(id(value))
        return existing if existing is not None else value.__deepcopy__(memo)
    if cls is dict:
        return {k: _clone_value(v, memo) for k, v in value.items()}
    if cls is tuple:
        return tuple(_clone_value(v, memo) for v in value)
    if isinstance(value, Enum):
        return value
    # Sets, embedded non-AST objects (e.g. database Tables): defer to the stdlib,
    # which shares the same `memo` so cross-boundary cycles/shared refs still hold.
    return copy.deepcopy(value, memo)


def _build_clone_fn(cls: type) -> Callable[["AST", dict[int, "AST"]], "AST"]:
    # Generate a per-class clone over its dataclass fields. start/end are always
    # Optional[int], so they pass straight through without a dispatch call.
    body = ["def _clone(self, memo):", "    new = _cls.__new__(_cls)", "    memo[id(self)] = new"]
    for f in fields(cls):
        if f.name in ("start", "end"):
            body.append(f"    new.{f.name} = self.{f.name}")
        else:
            body.append(f"    new.{f.name} = _cv(self.{f.name}, memo)")
    body.append("    return new")
    namespace: dict[str, Any] = {"_cls": cls, "_cv": _clone_value}
    exec("\n".join(body), namespace)  # noqa: S102 — codegen over a fixed field list, like dataclasses' own __init__
    return namespace["_clone"]


_T_AST = TypeVar("_T_AST", bound=AST)


@dataclass(kw_only=True, slots=True)
class Type(AST):
    def get_child(self, name: str, context: "HogQLContext") -> "Type":
        raise NotImplementedError("Type.get_child not overridden")

    def has_child(self, name: str, context: "HogQLContext") -> bool:
        return self.get_child(name, context) is not None

    def resolve_constant_type(self, context: "HogQLContext") -> "ConstantType":
        raise NotImplementedError(f"{self.__class__.__name__}.resolve_constant_type not overridden")

    def resolve_column_constant_type(self, name: str, context: "HogQLContext") -> "ConstantType":
        raise NotImplementedError(f"{self.__class__.__name__}.resolve_column_constant_type not overridden")


@dataclass(kw_only=True, slots=True)
class Expr(AST):
    type: Optional[Type] = field(default=None)


@dataclass(kw_only=True, slots=True)
class CTE(Expr):
    """A common table expression."""

    name: str
    expr: Expr
    # Whether the CTE is an inlined column "WITH 1 AS a" or a subquery "WITH a AS (SELECT 1)"
    cte_type: Literal["column", "subquery"]
    columns: Optional[list[str]] = field(default=None)  # e.g. ["a", "b"] for `WITH x(a, b) AS (SELECT 1, 2)`
    recursive: bool = field(default=False)
    materialized: Optional[bool] = field(default=None)
    using_key: Optional[list[str]] = field(default=None)  # e.g. ["a"] for USING KEY (a)


@dataclass(kw_only=True, slots=True)
class ConstantType(Type):
    data_type: ConstantDataType
    nullable: bool = field(default=True)

    def resolve_constant_type(self, context: "HogQLContext") -> "ConstantType":
        return self

    def print_type(self) -> str:
        raise NotImplementedError("ConstantType.print_type not implemented")


@dataclass(kw_only=True, slots=True)
class UnknownType(ConstantType):
    data_type: ConstantDataType = field(default="unknown", init=False)
    # True when the type genuinely could not be determined (e.g. an unmapped function), so the
    # value could be anything. Such a type poisons supertype unification rather than being absorbed.
    # The default (False) marks a vacuous unknown — a null literal or empty container — that imposes
    # no constraint and is absorbed, letting a known sibling type win.
    unanalyzable: bool = field(default=False)

    def print_type(self) -> str:
        return "Unknown"

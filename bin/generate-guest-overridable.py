#!/usr/bin/env python3
"""Emit posthog/rbac/_generated_guest_overridable.py from schema.json.

Reads frontend/src/queries/schema.json (the output of ts-json-schema-generator)
and extracts every object property that carries `"guestOverridable": true`
(originating from a `@guestOverridable` JSDoc tag in schema-general.ts).

The output is a small Python file mapping query-kind -> frozenset of field names
a guest user may override in a `/query/` request body. The guest rescoper
(posthog/rbac/guest_query_scope.py) imports this map at runtime.

Run after bin/build-schema-python.sh in the build pipeline. This keeps the guest
whitelist co-located with the TypeScript schema definitions without requiring
schema.json to ship to production.
"""

from __future__ import annotations

import sys
import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
SCHEMA_PATH = ROOT / "frontend" / "src" / "queries" / "schema.json"
OUTPUT_PATH = ROOT / "posthog" / "rbac" / "_generated_guest_overridable.py"

HEADER = (
    "# AUTO-GENERATED by bin/generate-guest-overridable.py. Do not edit.\n"
    "# Source: frontend/src/queries/schema/schema-general.ts "
    "(fields annotated with @guestOverridable JSDoc).\n"
    "\n"
    "from typing import Final\n"
    "\n"
    "GUEST_OVERRIDABLE_FIELDS: Final[dict[str, frozenset[str]]] = {\n"
)
FOOTER = "}\n"


def collect_overridable_fields(schema: dict) -> dict[str, frozenset[str]]:
    """Return {definition_name: frozenset(overridable_field_names)} for every
    top-level definition that has at least one overridable property."""
    out: dict[str, frozenset[str]] = {}
    definitions = schema.get("definitions") or {}
    for def_name, body in definitions.items():
        if not isinstance(body, dict):
            continue
        # Skip template expansions of generic base types (e.g. `InsightsQueryBase<TrendsQueryResponse>`).
        # Concrete query kinds don't contain `<` in their name.
        if "<" in def_name:
            continue
        props = body.get("properties")
        if not isinstance(props, dict):
            continue
        overridable = {
            field_name
            for field_name, field_spec in props.items()
            if isinstance(field_spec, dict) and field_spec.get("guestOverridable") is True
        }
        if overridable:
            out[def_name] = frozenset(overridable)
    return out


def render(mapping: dict[str, frozenset[str]]) -> str:
    lines = [HEADER]
    for kind in sorted(mapping):
        fields = ", ".join(repr(f) for f in sorted(mapping[kind]))
        lines.append(f'    "{kind}": frozenset({{{fields}}}),\n')
    lines.append(FOOTER)
    return "".join(lines)


def main() -> None:
    with SCHEMA_PATH.open() as f:
        schema = json.load(f)
    mapping = collect_overridable_fields(schema)
    OUTPUT_PATH.write_text(render(mapping))
    sys.stderr.write(f"Wrote {OUTPUT_PATH.relative_to(ROOT)}: {len(mapping)} query kinds\n")


if __name__ == "__main__":
    main()

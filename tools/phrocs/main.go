// phrocs is a PostHog-branded dev process runner built with Bubble Tea.
//
// It is a drop-in replacement for mprocs: it reads the same YAML config
// that `hogli dev:generate` produces and renders a customisable TUI with a
// per-process sidebar, scrollable output panes, and hogli-aware keybindings.
//
// While running, phrocs also exposes a local MCP HTTP server so that AI coding
// agents (e.g. Claude Code) can inspect live process state without needing
// file-based log mode. The server runs on 127.0.0.1:5835 by default and
// implements two tools: get_process_status and get_process_logs.
//
// Usage:
//
//	phrocs [--debug] [--mcp-addr host:port] --config <config.yaml>
//
// Flags:
//
//	--config    Path to the YAML config file generated by `hogli dev:generate`.
//	--debug     Write a debug log to /tmp/phrocs-debug.log (key inputs, proc
//	            selection changes, status transitions, etc.)
//	--mcp-addr  Address for the embedded MCP HTTP server (host:port).
//	            Set to "" to disable. Default: 127.0.0.1:5835.
package main

import (
	"fmt"
	"log"
	"os"

	tea "charm.land/bubbletea/v2"
	"github.com/posthog/posthog/phrocs/internal/config"
	"github.com/posthog/posthog/phrocs/internal/mcp"
	"github.com/posthog/posthog/phrocs/internal/process"
	"github.com/posthog/posthog/phrocs/internal/tui"
)

func main() {
	var configPath string
	var logger *log.Logger
	mcpAddr := mcp.DefaultAddr

	// Parse flags: --config <path>, --debug, --mcp-addr <host:port>
	for i := 1; i < len(os.Args); i++ {
		switch os.Args[i] {
		case "--config":
			if i+1 < len(os.Args) {
				configPath = os.Args[i+1]
				i++
			}
		case "--debug":
			f, err := os.OpenFile("/tmp/phrocs-debug.log", os.O_CREATE|os.O_WRONLY|os.O_TRUNC, 0o644)
			if err != nil {
				fmt.Fprintf(os.Stderr, "phrocs: open debug log: %v\n", err)
				os.Exit(1)
			}
			logger = log.New(f, "", log.LstdFlags|log.Lmicroseconds)
			logger.Println("debug logging started")
		case "--mcp-addr":
			if i+1 < len(os.Args) {
				mcpAddr = os.Args[i+1]
				i++
			}
		}
	}

	if configPath == "" {
		fmt.Fprintln(os.Stderr, "usage: phrocs [--debug] [--mcp-addr host:port] --config <config.yaml>")
		os.Exit(1)
	}

	cfg, err := config.Load(configPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "phrocs: load config: %v\n", err)
		os.Exit(1)
	}

	mgr := process.NewManager(cfg)

	// Start the embedded MCP server before the TUI takes over the terminal.
	// Failures are non-fatal: the TUI works regardless.
	if mcpAddr != "" {
		srv := mcp.NewServer(mgr)
		addr, err := srv.ListenAndServe(mcpAddr)
		if err != nil {
			fmt.Fprintf(os.Stderr, "phrocs: mcp: %v\n", err)
		} else if logger != nil {
			logger.Printf("MCP server listening on http://%s/mcp", addr)
		}
	}

	m := tui.New(mgr, cfg.MouseScrollSpeed, logger)
	p := tea.NewProgram(m)

	// StartAll is launched in a goroutine so it doesn't block: p.Send() inside
	// Start() will block briefly on the Bubble Tea channel until p.Run() starts
	// its event loop, at which point everything unblocks naturally. Calling
	// StartAll synchronously before p.Run() deadlocks because the main goroutine
	// would be stuck in p.Send() and p.Run() would never be reached.
	mgr.SetSend(p.Send)
	go mgr.StartAll()

	if _, err := p.Run(); err != nil {
		fmt.Fprintf(os.Stderr, "phrocs: %v\n", err)
		os.Exit(1)
	}
}

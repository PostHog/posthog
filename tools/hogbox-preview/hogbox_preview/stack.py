"""The preview *stack* — provider-agnostic.

``PostHogPreviewStack`` knows how to bring PostHog up inside any
``PreviewBackend``: which compose files, which image, migrate, seed demo data,
wait for health. It talks to the box only through ``backend.exec`` /
``write_file`` / ``run_long`` / ``wait_http_ok`` — it never knows or cares
whether the box is a hogbox or a droplet. Swapping the layer changes the
backend, not this file.

Recipe (validated 2026-06-10, single-origin on the box's web port):
  - Run the PUBLISHED image as-is. master's ``build: .`` is broken today (stale
    apt pin), AND the prod image runs its own baked code from /code — the
    dev-full ``.:/app/posthog`` mount is ignored (workdir=/code). So this is
    effectively "run the image", like hobby: to preview a specific PR, pull
    that PR's image (``ghcr.io/posthog/posthog:pr-<n>``), don't mount a branch.
  - prod image -> prod settings: DEBUG=0, or DEBUG=1 pulls dev-only
    INSTALLED_APPS the prod image doesn't ship (ModuleNotFoundError).
  - The DB must be COHERENT with the image: migrate Postgres AND ClickHouse
    with the image's own migrations on a fresh DB. A golden's pre-migrated DB
    drifts from a different image (so a fast-restore golden must bake the
    image + its migrated DB together; the DB is not a reusable asset across
    image versions).
  - serve at the box's own clean URL so the SPA's absolute asset/API paths
    resolve at the origin root (that's what the single-port edge buys us).
  - seed demo data with ``manage.py generate_demo_data`` (the step hobby-ci
    uses) so the preview opens populated with a login. Best-effort: needs a
    coherent image — bleeding ``:master`` can have a model ahead of its
    migrations and fail the seed (the preview still serves, just empty).
"""

from __future__ import annotations

import sys
import secrets

from .backend import PreviewBackend

# Overlay merged on top of docker-compose.dev-full.yml. Only the `web` service
# is touched: point it at the published image and feed it the serving URL + a
# few prod-mode knobs. ${...} placeholders are filled by render_override().
_OVERRIDE_YAML = """\
# Generated by tools/hogbox-preview — serve PostHog at the box's clean edge
# origin (single port) from the published image with live source mounted over it.
services:
  web:
    image: {image}
    environment:
      - SITE_URL={url}
      - JS_URL={url}
      - DISABLE_SECURE_SSL_REDIRECT=1
      - EXTRA_CSRF_TRUSTED_ORIGINS={url}
      - DEBUG=0
      - SECRET_KEY={secret_key}
"""


class PostHogPreviewStack:
    IMAGE = "ghcr.io/posthog/posthog:master"
    REPO_DIR = "/home/hog/posthog"
    COMPOSE = "docker-compose.dev-full.yml"
    OVERRIDE = "docker-compose.preview.yml"
    # Dependency services that don't need the posthog image (cached on the box).
    DEPS = ["db", "redis7", "clickhouse", "zookeeper", "kafka", "objectstorage"]

    def __init__(
        self,
        backend: PreviewBackend,
        *,
        branch: str | None = None,
        image: str | None = None,
        repo_dir: str | None = None,
        seed_demo_data: bool = True,
        reset_db: bool = False,
    ):
        self.backend = backend
        self.branch = branch
        self.image = image or self.IMAGE
        self.repo_dir = repo_dir or self.REPO_DIR
        self.seed_demo_data = seed_demo_data
        # reset_db wipes pg + clickhouse before migrating, so the DB is migrated
        # fresh by `image` rather than inheriting a snapshot's drifted DB. Needed
        # when baking a coherent golden (or pointing an existing box at a
        # different image); a plain restore-and-go leaves it False.
        self.reset_db = reset_db

    # --- public API ----------------------------------------------------------
    def bring_up(self) -> str:
        """Provision the box and bring PostHog up; return the public URL."""
        self.backend.provision()
        url = self.backend.web_url
        if self.branch:
            self.checkout_branch(self.branch)
        self.write_override(url)
        self.pull_image()
        if self.reset_db:
            self.reset_database()
        self.up_services()
        self.migrate()
        if self.seed_demo_data:
            # Best-effort: a transiently-broken image (e.g. `:master` with a
            # model ahead of its migrations) shouldn't sink an otherwise-good
            # preview — it just opens empty. Pin a coherent tag (a release, or
            # the PR's own pr-<n> image like hobby-ci) for reliable demo data.
            try:
                self.generate_demo_data()
            except Exception as e:  # noqa: BLE001
                sys.stderr.write(f"[hogbox-preview] demo-data seeding skipped (preview still usable): {e}\n")
        self.wait_for_health()
        return url

    # --- steps (each usable standalone, mirroring bin/hobby-ci.py) -----------
    def checkout_branch(self, branch: str) -> None:
        self.backend.run_long(
            f"cd {self.repo_dir} && git fetch --depth 1 origin {branch} && git checkout --force FETCH_HEAD",
            name="checkout",
            timeout=600,
        )

    def write_override(self, url: str) -> None:
        body = _OVERRIDE_YAML.format(image=self.image, url=url, secret_key=secrets.token_urlsafe(50))
        self.backend.write_file(f"{self.repo_dir}/{self.OVERRIDE}", body)

    def pull_image(self, *, attempts: int = 3) -> None:
        # ghcr pulls flake (TLS handshake timeouts mid-layer); retry. docker
        # resumes from already-pulled layers, so a retry is cheap.
        last: Exception | None = None
        for _ in range(attempts):
            try:
                self.backend.run_long(f"docker pull {self.image}", name="pull", timeout=1800)
                return
            except RuntimeError as e:
                last = e
        raise RuntimeError(f"docker pull {self.image} failed after {attempts} attempts: {last}")

    def reset_database(self) -> None:
        # Drop the snapshot's pre-migrated DB so migrate() rebuilds it fresh and
        # coherent with `image`. Postgres keeps its data in a named volume that
        # survives container recreation, so the volume must be removed by name;
        # ClickHouse is volume-less, so recreating its container is enough.
        cmd = (
            f"cd {self.repo_dir} && "
            f"docker compose -f {self.COMPOSE} -f {self.OVERRIDE} stop db clickhouse web ; "
            f"docker compose -f {self.COMPOSE} -f {self.OVERRIDE} rm -f db clickhouse web ; "
            "docker volume rm posthog_postgres-15-data || true"
        )
        self.backend.run_long(cmd, name="reset-db", timeout=300)

    def up_services(self) -> None:
        services = " ".join([*self.DEPS, "web"])
        self.backend.run_long(self._compose(f"up -d --no-build {services}"), name="up", timeout=900)

    def migrate(self) -> None:
        # PostHog needs both: Postgres (schema) and ClickHouse (events DB +
        # tables). The dev all-in-one `manage.py n` does both but is dev-only;
        # the prod image exposes them separately. ClickHouse must be migrated
        # before demo-data generation, which writes events to it.
        self.backend.run_long(
            self._compose("run --rm -T web python manage.py migrate --noinput"),
            name="migrate",
            timeout=1800,
        )
        self.backend.run_long(
            self._compose("run --rm -T web python manage.py migrate_clickhouse"),
            name="migrate-clickhouse",
            timeout=1800,
        )

    def generate_demo_data(self) -> None:
        # Same command hobby-ci uses (bin/hobby-ci.py). Seeds a demo org + the
        # test@posthog.com / 12345678 login so the preview opens populated.
        # (`manage.py n` is a dev-only alias, not registered in the prod image.)
        self.backend.run_long(
            self._compose("run --rm -T web python manage.py generate_demo_data"),
            name="seed",
            timeout=1800,
        )

    def wait_for_health(self) -> None:
        # web must be (re)started after migrate/seed so unit re-checks health.
        self.backend.run_long(self._compose("restart web"), name="restart", timeout=300)
        self.backend.wait_http_ok("/_health", expect=200, timeout=600)

    # --- internal ------------------------------------------------------------
    def _compose(self, args: str) -> str:
        return f"cd {self.repo_dir} && docker compose -f {self.COMPOSE} -f {self.OVERRIDE} {args}"

"""The preview *stack* — provider-agnostic.

``PostHogPreviewStack`` knows how to bring PostHog up inside any
``PreviewBackend``: which compose files, which image, migrate, seed demo data,
wait for health. It talks to the box only through ``backend.exec`` /
``write_file`` / ``run_long`` / ``wait_http_ok`` — it never knows or cares
whether the box is a hogbox or a droplet. Swapping the layer changes the
backend, not this file.

Recipe (proven 2026-06-10, single-origin on the box's web port):
  - master's ``build: .`` is broken today (stale apt pin), so we run the
    PUBLISHED image with the live source mounted over it.
  - prod image -> prod settings: DEBUG=0, or DEBUG=1 pulls dev-only
    INSTALLED_APPS the prod image doesn't ship (ModuleNotFoundError).
  - serve at the box's own clean URL so the SPA's absolute asset/API paths
    resolve at the origin root (that's what the single-port edge buys us).
  - seed demo data with ``manage.py n`` — same step hobby/sandbox use, so the
    preview opens to a populated instance with a login, not an empty preflight.
"""

from __future__ import annotations

import secrets

from .backend import PreviewBackend

# Overlay merged on top of docker-compose.dev-full.yml. Only the `web` service
# is touched: point it at the published image and feed it the serving URL + a
# few prod-mode knobs. ${...} placeholders are filled by render_override().
_OVERRIDE_YAML = """\
# Generated by tools/hogbox-preview — serve PostHog at the box's clean edge
# origin (single port) from the published image with live source mounted over it.
services:
  web:
    image: {image}
    environment:
      - SITE_URL={url}
      - JS_URL={url}
      - DISABLE_SECURE_SSL_REDIRECT=1
      - EXTRA_CSRF_TRUSTED_ORIGINS={url}
      - DEBUG=0
      - SECRET_KEY={secret_key}
"""


class PostHogPreviewStack:
    IMAGE = "ghcr.io/posthog/posthog:master"
    REPO_DIR = "/home/hog/posthog"
    COMPOSE = "docker-compose.dev-full.yml"
    OVERRIDE = "docker-compose.preview.yml"
    # Dependency services that don't need the posthog image (cached on the box).
    DEPS = ["db", "redis7", "clickhouse", "zookeeper", "kafka", "objectstorage"]

    def __init__(
        self,
        backend: PreviewBackend,
        *,
        branch: str | None = None,
        image: str | None = None,
        repo_dir: str | None = None,
        seed_demo_data: bool = True,
    ):
        self.backend = backend
        self.branch = branch
        self.image = image or self.IMAGE
        self.repo_dir = repo_dir or self.REPO_DIR
        self.seed_demo_data = seed_demo_data

    # --- public API ----------------------------------------------------------
    def bring_up(self) -> str:
        """Provision the box and bring PostHog up; return the public URL."""
        self.backend.provision()
        url = self.backend.web_url
        if self.branch:
            self.checkout_branch(self.branch)
        self.write_override(url)
        self.pull_image()
        self.up_services()
        self.migrate()
        if self.seed_demo_data:
            self.generate_demo_data()
        self.wait_for_health()
        return url

    # --- steps (each usable standalone, mirroring bin/hobby-ci.py) -----------
    def checkout_branch(self, branch: str) -> None:
        self.backend.run_long(
            f"cd {self.repo_dir} && git fetch --depth 1 origin {branch} && git checkout --force FETCH_HEAD",
            name="checkout",
            timeout=600,
        )

    def write_override(self, url: str) -> None:
        body = _OVERRIDE_YAML.format(image=self.image, url=url, secret_key=secrets.token_urlsafe(50))
        self.backend.write_file(f"{self.repo_dir}/{self.OVERRIDE}", body)

    def pull_image(self) -> None:
        self.backend.run_long(f"docker pull {self.image}", name="pull", timeout=1800)

    def up_services(self) -> None:
        services = " ".join([*self.DEPS, "web"])
        self.backend.run_long(self._compose(f"up -d --no-build {services}"), name="up", timeout=900)

    def migrate(self) -> None:
        self.backend.run_long(
            self._compose("run --rm -T web python manage.py migrate --noinput"),
            name="migrate",
            timeout=1800,
        )

    def generate_demo_data(self) -> None:
        # `manage.py n` (the setup_dev successor) seeds a demo org + the
        # test@posthog.com / 12345678 login — same step hobby-ci uses.
        self.backend.run_long(
            self._compose("run --rm -T web python manage.py n --skip-flag-sync"),
            name="seed",
            timeout=1800,
        )

    def wait_for_health(self) -> None:
        # web must be (re)started after migrate/seed so unit re-checks health.
        self.backend.run_long(self._compose("restart web"), name="restart", timeout=300)
        self.backend.wait_http_ok("/_health", expect=200, timeout=600)

    # --- internal ------------------------------------------------------------
    def _compose(self, args: str) -> str:
        return f"cd {self.repo_dir} && docker compose -f {self.COMPOSE} -f {self.OVERRIDE} {args}"

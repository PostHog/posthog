/**
 * A stable identifier for an event-like object: its `uuid`, falling back to `id`. Shared with
 * `EventDetails`'s `getEventId`, which needs the same fallback to key per-event UI state.
 */
export function eventIdentityKey(candidate: any): ExpandedRowKey | null {
    if (candidate && typeof candidate === 'object') {
        return candidate.uuid || candidate.id || null
    }
    return null
}

/**
 * Derive a stable identity for a row so expansion survives refreshes and reordering.
 *
 * For event-backed tables the `*` response column holds the full event; its `uuid` (then
 * `id`) is unique across results and stable across reloads, so the same event stays
 * expanded even when rows shift position. Rows without an event (label rows, non-event
 * queries, or events lacking both ids) fall back to positional index.
 */
export function getRowExpansionKey(
    row: DataTableRow,
    rowIndex: number,
    starColumnIndex: number | null
): ExpandedRowKey {
    // Array results are raw query rows: the event object lives at the `*` column, if selected.
    // Non-array results (e.g. revenue example events) are the event object directly.
    const candidate = Array.isArray(row.result)
        ? starColumnIndex !== null
            ? row.result[starColumnIndex]
            : null
        : row.result
    return eventIdentityKey(candidate) ?? rowIndex
}


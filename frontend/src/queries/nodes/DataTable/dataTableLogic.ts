export const dataTableLogic = kea<dataTableLogicType>([
    props({} as DataTableLogicProps),
    key((props) => {
        if (!props.vizKey) {
            throw new Error('dataTableLogic must contain a vizKey in props')
        }
        if (!isDataTableNode(props.query)) {
            throw new Error('dataTableLogic only accepts queries of type DataTableNode')
        }
        return props.vizKey
    }),
    path(['queries', 'nodes', 'DataTable', 'dataTableLogic']),
    actions({
        setColumnsInQuery: (columns: HogQLExpression[]) => ({ columns }),
        toggleRowExpanded: (rowKey: ExpandedRowKey) => ({ rowKey }),
    }),
    reducers(({ props }) => ({
        columnsInQuery: [getColumnsForQuery(props.query), { setColumnsInQuery: (_, { columns }) => columns }],
    })),
    connect((props: DataTableLogicProps) => ({
        values: [
            featureFlagLogic,
            ['featureFlags'],
            teamLogic,
            ['currentTeam'],
            tabUiStateLogic,
            ['expandedRowsFor'],
            dataNodeLogic({
                key: props.dataNodeLogicKey ?? props.dataKey,
                query: props.query.source,
                dataNodeCollectionId: insightVizDataCollectionId(
                    props.context?.insightProps,
                    props.dataNodeLogicKey ?? props.dataKey
                ),
                loadPriority: props.context?.insightProps?.loadPriority,
            }),
            ['response', 'responseLoading', 'responseError'],
        ],
        actions: [
            tabUiStateLogic,
            ['toggleExpandedRow', 'reconcileExpandedRows'],
            dataNodeLogic({
                key: props.dataNodeLogicKey ?? props.dataKey,
                query: props.query.source,
                dataNodeCollectionId: insightVizDataCollectionId(
                    props.context?.insightProps,
                    props.dataNodeLogicKey ?? props.dataKey
                ),
                loadPriority: props.context?.insightProps?.loadPriority,
            }),
            ['loadDataSuccess'],
        ],
    })),
    listeners(({ props, actions, values }) => ({
        toggleRowExpanded: ({ rowKey }) => {
            actions.toggleExpandedRow(undefined, props.vizKey, rowKey)
        },
        // Reconcile stale expansion whenever a fresh result set arrives: drop keys for
        // events no longer visible so the collection can't grow unbounded across
        // pagination/query changes, and a reordered result can't inherit expansion.
        loadDataSuccess: () => {
            // Nothing to reconcile against an empty expansion set — skip computing
            // visibleExpandedRowKeys (an O(rows) pass) on every load/poll when it can't matter.
            if (values.expandedRowKeys.length === 0) {
                return
            }
            // A `null` result (no team in export/shared context, an empty query, a validation
            // failure) means "no data received", not "no rows are visible" — reconciling against
            // it would wipe every persisted expansion key for this vizKey.
            if (values.dataTableRows === null) {
                return
            }
            const visibleKeys = values.visibleExpandedRowKeys
            actions.reconcileExpandedRows(undefined, props.vizKey, visibleKeys)
        },
    })),
    selectors({
        context: [() => [(_, props) => props.context], (context) => context],
        expandedRowKeys: [
            (s) => [s.expandedRowsFor, (_, p) => p.vizKey],
            (
                expandedRowsFor: (tabId: string | undefined, vizKey: string) => ExpandedRowKey[],
                vizKey: string
            ): ExpandedRowKey[] => expandedRowsFor(undefined, vizKey),
            { resultEqualityCheck: objectsEqual },
        ],
        sourceKind: [(_, p) => [p.query], (query: DataTableNode): NodeKind | null => query.source?.kind],
        sourceFeatures: [
            (_, p) => [p.query, (_, props) => props.context],
            (query: DataTableNode, context): Set<QueryFeature> => {
                const sourceFeatures = getQueryFeatures(query.source)
                if (context?.extraDataTableQueryFeatures) {
                    for (const feature of context.extraDataTableQueryFeatures) {
                        sourceFeatures.add(feature)
                    }
                }
                return sourceFeatures
            },
        ],
        orderBy: [
            (s, p) => [p.query, s.sourceFeatures],
            (query: DataTableNode, sourceFeatures: Set<QueryFeature>): string[] | null =>
                sourceFeatures.has(QueryFeature.selectAndOrderByColumns)
                    ? 'orderBy' in query.source // might not be EventsQuery, but something else with orderBy
                        ? ((query.source as EventsQuery).orderBy ?? null)
                        : isEventsQuery(query.source)
                          ? ['timestamp DESC']
                          : null
                    : null,
            { resultEqualityCheck: objectsEqual },
        ],
        columnsInResponse: [
            (s) => [s.response],
            (response: AnyDataNode['response']): string[] | null =>
                response && 'columns' in response && Array.isArray(response.columns) ? response?.columns : null,
            // `response` gets a new object identity on every reload/poll even when its columns
            // are unchanged; without this, getExpandedRowKey (and DataTable's expandableConfig
            // useMemo, which depends on it) would rebuild every poll tick.
            { resultEqualityCheck: objectsEqual },
        ],
        dataTableRows: [
            (s) => [
                s.sourceKind,
                s.orderBy,
                s.response,
                s.columnsInResponse,
                (_, props) => props.context,
                s.currentTeam,
            ],
            (
                sourceKind: NodeKind | null,
                orderBy: string[] | null,
                response: AnyDataNode['response'],
                columnsInResponse: string[] | null,
                context: QueryContext<DataTableNode> | undefined,
                currentTeam: { timezone?: string } | null
            ): DataTableRow[] | null => {
                if (response && sourceKind === NodeKind.EventsQuery) {
                    const queryResponse = response as AnyResponseType
                    if (queryResponse) {
                        let results: any[] | null = []
                        if ('results' in queryResponse) {
                            results = queryResponse.results
                        } else if ('result' in queryResponse) {
                            results = queryResponse.result
                        }

                        if (!results) {
                            return []
                        }

                        const orderKey = orderBy?.[0]?.endsWith(' DESC')
                            ? orderBy[0].replace(/ DESC$/, '')
                            : orderBy?.[0]
                        const orderKeyIndex =
                            columnsInResponse?.findIndex(
                                (column) =>
                                    removeExpressionComment(column) === orderKey ||
                                    removeExpressionComment(column) === `-${orderKey}`
                            ) ?? -1

                        // Add a label between results if the day changed for events with timestamp.
                        // Use the project timezone so date headers match the date filter.
                        if (orderKey === 'timestamp' && orderKeyIndex !== -1) {
                            const tz = currentTeam?.timezone ?? 'UTC'
                            let lastResult: any = null
                            const newResults: DataTableRow[] = []
                            for (const result of results) {
                                if (
                                    result &&
                                    lastResult &&
                                    !dayjs(result[orderKeyIndex])
                                        .tz(tz)
                                        .isSame(dayjs(lastResult[orderKeyIndex]).tz(tz), 'day')
                                ) {
                                    newResults.push({
                                        label: dayjs(result[orderKeyIndex]).tz(tz).format('LL'),
                                    })
                                }
                                newResults.push({ result })
                                lastResult = result
                            }
                            return newResults
                        }
                        return results.map((result) => ({ result }))
                    }
                }

                const results = !response
                    ? null
                    : 'results' in response && Array.isArray(response.results)
                      ? response.results
                      : 'result' in response && Array.isArray(response.result)
                        ? response.result
                        : null

                const rows = results ? (results.map((result: any) => ({ result })) ?? null) : null
                return context?.dataTableRowsTransformer ? context.dataTableRowsTransformer(rows ?? []) : rows
            },
            // A reload or poll reparses JSON, so unchanged results still arrive as new identities;
            // keeping the previous array lets memoized rows skip re-rendering.
            { resultEqualityCheck: objectsEqual },
        ],
        getExpandedRowKey: [
            (s) => [s.columnsInResponse],
            (columnsInResponse: string[] | null): ((row: DataTableRow, rowIndex: number) => ExpandedRowKey) => {
                // Only a column holding `*` guarantees the full event is at that position — an
                // events query can `select` other columns first (e.g. `person`), so guessing
                // index 0 without `*` would key expansion off the wrong object.
                const starColumnIndex = columnsInResponse?.includes('*') ? columnsInResponse.indexOf('*') : null
                return (row, rowIndex) => getRowExpansionKey(row, rowIndex, starColumnIndex)
            },
        ],
        visibleExpandedRowKeys: [
            (s) => [s.dataTableRows, s.getExpandedRowKey],
            (
                dataTableRows: DataTableRow[] | null,
                getExpandedRowKey: (row: DataTableRow, rowIndex: number) => ExpandedRowKey
            ): ExpandedRowKey[] => {
                if (!dataTableRows) {
                    return []
                }
                const keys: ExpandedRowKey[] = []
                for (let i = 0; i < dataTableRows.length; i++) {
                    const row = dataTableRows[i]
                    // Label rows (date dividers etc.) have no result and can't expand.
                    if (row.label === undefined || row.label === null) {
                        keys.push(getExpandedRowKey(row, i))
                    }
                }
                return keys
            },
            { resultEqualityCheck: objectsEqual },
        ],
        queryWithDefaults: [
            (s, p) => [p.query, s.columnsInQuery, s.featureFlags, (_, props) => props.context],
            (
                query: DataTableNode,
                columnsInQuery: string[],
                featureFlags: import('lib/logic/featureFlagLogic').FeatureFlagsSet,
                context
            ): RequiredExcept<
                Omit<DataTableNode, 'response'>,
                'version' | 'tags' | 'defaultColumns' | 'contextKey'
            > => {
                const { kind, columns: _columns, source, ...rest } = query
                const showIfFull = !!query.full
                const flagQueryRunningTimeEnabled = !!featureFlags[FEATURE_FLAGS.QUERY_RUNNING_TIME]
                const flagQueryTimingsEnabled = !!featureFlags[FEATURE_FLAGS.QUERY_TIMINGS]
                return {
                    kind,
                    columns: columnsInQuery,
                    hiddenColumns: [],
                    pinnedColumns: query.pinnedColumns ?? [],
                    source,
                    context: query.context ?? { type: 'team_columns' },
                    ...sortedKeys({
                        ...rest,
                        full: query.full ?? false,

                        // The settings under features.tsx override some of these
                        expandable: query.expandable ?? true,
                        embedded: query.embedded ?? false,
                        propertiesViaUrl: query.propertiesViaUrl ?? false,
                        showPropertyFilter: query.showPropertyFilter ?? showIfFull,
                        showEventFilter: query.showEventFilter ?? showIfFull,
                        showEventsFilter: query.showEventsFilter ?? false,
                        showSearch: query.showSearch ?? showIfFull,
                        showActions: query.showActions ?? true,
                        showDateRange: query.showDateRange ?? showIfFull,
                        showTestAccountFilters: query.showTestAccountFilters ?? showIfFull,
                        showExport: query.showExport ?? showIfFull,
                        showReload: query.showReload ?? showIfFull,
                        showTimings: query.showTimings ?? flagQueryTimingsEnabled,
                        showElapsedTime:
                            (query.showTimings ?? flagQueryTimingsEnabled) ||
                            (query.showElapsedTime ??
                                ((flagQueryRunningTimeEnabled || source.kind === NodeKind.HogQLQuery) && showIfFull)),
                        showColumnConfigurator: query.showColumnConfigurator ?? showIfFull,
                        showPersistentColumnConfigurator: query.showPersistentColumnConfigurator ?? false,
                        showSavedQueries: query.showSavedQueries ?? false,
                        showSavedFilters: query.showSavedFilters ?? false,
                        showTableViews: query.showTableViews ?? false,
                        showAbsoluteTime: query.showAbsoluteTime ?? false,
                        showHogQLEditor: query.showHogQLEditor ?? showIfFull,
                        allowSorting: query.allowSorting ?? true,
                        showOpenEditorButton:
                            context?.showOpenEditorButton !== undefined
                                ? context.showOpenEditorButton
                                : (query.showOpenEditorButton ?? true),
                        showResultsTable: query.showResultsTable ?? true,
                        showRecordingColumn: query.showRecordingColumn ?? false,
                        showSourceQueryOptions: query.showSourceQueryOptions ?? true,
                        showCount: query.showCount ?? false,
                    }),
                }
            },
        ],
        canSort: [
            (s) => [s.queryWithDefaults, s.sourceFeatures],
            (query: DataTableNode, sourceFeatures: Set<QueryFeature>): boolean =>
                sourceFeatures.has(QueryFeature.selectAndOrderByColumns) && !!query.allowSorting,
        ],
    }),
    propsChanged(({ actions, props }, oldProps) => {
        const newColumns = getColumnsForQuery(props.query)
        const oldColumns = getColumnsForQuery(oldProps.query)
        if (JSON.stringify(newColumns) !== JSON.stringify(oldColumns)) {
            actions.setColumnsInQuery(newColumns)
        }
    }),
])

const DEFAULT_VARIABLE: StringVariable = {
    id: '',
    type: 'String',
    name: '',
    default_value: '',
    code_name: '',
}

export interface AddVariableLogicProps {
    key: string
}

const getDefaultVariableForType = (variableType: VariableType): Variable => {
    if (variableType === 'String') {
        return {
            id: '',
            type: 'String',
            name: '',
            default_value: '',
            code_name: '',
        } as StringVariable
    }

    if (variableType === 'Number') {
        return {
            id: '',
            type: 'Number',
            name: '',
            default_value: 0,
            code_name: '',
        } as NumberVariable
    }

    if (variableType === 'Boolean') {
        return {
            id: '',
            type: 'Boolean',
            name: '',
            default_value: false,
            code_name: '',
        } as BooleanVariable
    }

    if (variableType === 'List') {
        return {
            id: '',
            type: 'List',
            name: '',
            values: [],
            default_value: '',
            code_name: '',
        } as ListVariable
    }

    if (variableType === 'Date') {
        return {
            id: '',
            type: 'Date',
            name: '',
            default_value: dayjs().format('YYYY-MM-DD HH:mm:00'),
            code_name: '',
        } as DateVariable
    }

    throw new Error(`Unsupported variable type ${variableType}`)
}


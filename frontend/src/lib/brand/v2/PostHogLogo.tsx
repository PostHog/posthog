import { useId } from 'react'

type Variant = 'gradient' | 'print' | 'mono'

export interface PostHogLogoProps {
    variant?: Variant
    color?: string
    wordmark?: boolean
    code?: boolean
    stacked?: boolean
    className?: string
}

type FillProps = { fill?: string; className?: string }

function resolveSolidFill(variant: Variant, color: string): FillProps {
    if (variant !== 'mono') {
        return { fill: '#111' }
    }
    if (color === 'primary') {
        return { className: 'fill-primary' }
    }
    if (color === 'white') {
        return { fill: '#FAFAFA' }
    }
    if (color === 'black') {
        return { fill: '#111' }
    }
    return { fill: color }
}

function getViewBox(stacked: boolean, wordmark: boolean, code: boolean): string {
    if (!wordmark) {
        return '0 0 52 28'
    }
    if (stacked) {
        return code ? '0 0 52 59' : '0 0 52 47'
    }
    return code ? '0 0 229 28' : '0 0 160 28'
}

export function PostHogLogo({
    variant = 'gradient',
    color = 'black',
    wordmark = true,
    code = false,
    stacked = false,
    className = '',
}: PostHogLogoProps): JSX.Element {
    const uid = useId().replace(/:/g, '')
    const blueMaskId = `posthog-logo-${uid}-mask-blue`
    const redMaskId = `posthog-logo-${uid}-mask-red`
    const yellowMaskId = `posthog-logo-${uid}-mask-yellow`
    const clipId = `posthog-logo-${uid}-clip`

    const solid = resolveSolidFill(variant, color)
    const viewBox = getViewBox(stacked, wordmark, code)

    return (
        <svg
            className={className}
            viewBox={viewBox}
            fill="none"
            xmlns="http://www.w3.org/2000/svg"
            aria-label="PostHog logo"
            role="img"
        >
            {variant === 'gradient' && <GradientLogomark uid={uid} clipId={clipId} solid={solid} />}
            {variant === 'print' && (
                <PrintLogomark
                    blueMaskId={blueMaskId}
                    redMaskId={redMaskId}
                    yellowMaskId={yellowMaskId}
                    clipId={clipId}
                />
            )}
            {variant === 'mono' && <MonoLogomark solid={solid} />}

            {wordmark && !stacked && <LandscapeWordmark solid={solid} />}
            {wordmark && !stacked && code && <LandscapeCodeWordmark solid={solid} />}
            {wordmark && stacked && <StackedWordmark solid={solid} />}
            {wordmark && stacked && code && <StackedCodeWordmark solid={solid} />}

            {variant === 'gradient' && <GradientDefs uid={uid} clipId={clipId} />}
            {variant === 'print' && <PrintDefs clipId={clipId} />}
        </svg>
    )
}


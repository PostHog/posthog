export const tabUiStateLogic = kea<tabUiStateLogicType>([
    path(['lib', 'logic', 'tabUiStateLogic']),
    actions({
        toggleExpandedRow: (tabId: string | undefined, vizKey: string, rowKey: ExpandedRowKey) => ({
            tabId: tabId ?? NO_TAB,
            vizKey,
            rowKey,
        }),
        reconcileExpandedRows: (tabId: string | undefined, vizKey: string, visibleKeys: ExpandedRowKey[]) => ({
            tabId: tabId ?? NO_TAB,
            vizKey,
            visibleKeys,
        }),
        clearTabUiState: (tabId: string) => ({ tabId }),
        setSavedQueryForTab: (tabId: string | undefined, sceneKey: string, query: Node | null) => ({
            tabId: tabId ?? NO_TAB,
            sceneKey,
            query,
        }),
        setChatDraftForTab: (tabId: string | undefined, draft: string) => ({
            tabId: tabId ?? NO_TAB,
            draft,
        }),
    }),
    reducers({
        expandedRowsByTabAndVizKey: [
            {} as ExpandedRowsByTabAndVizKey,
            {
                toggleExpandedRow: (state, { tabId, vizKey, rowKey }) => {
                    const tabState = state[tabId] ?? {}
                    const existing = tabState[vizKey] ?? []
                    const next = existing.includes(rowKey)
                        ? existing.filter((k: ExpandedRowKey) => k !== rowKey)
                        : [...existing, rowKey]
                    return { ...state, [tabId]: { ...tabState, [vizKey]: next } }
                },
                reconcileExpandedRows: (state, { tabId, vizKey, visibleKeys }) => {
                    const tabState = state[tabId] ?? {}
                    const existing = tabState[vizKey] ?? []
                    // Drop keys for events no longer in the result set. Positional (numeric)
                    // keys are reconciled too so a reordered result can't inherit expansion.
                    const visible = new Set(visibleKeys)
                    const next = existing.filter((k: ExpandedRowKey) => visible.has(k))
                    if (next.length === existing.length) {
                        return state
                    }
                    if (next.length === 0) {
                        // Drop the emptied slot rather than keeping `[vizKey]: []`. Ad-hoc tables
                        // get a fresh vizKey every mount, so an emptied slot is never reused and
                        // would otherwise grow this bucket per session. (Partial — the toggle
                        // collapse path keeps a `[]` slot, but that path can't fire for a vizKey
                        // whose table has unmounted, so this bounds the unbounded-growth case.)
                        const nextTabState = { ...tabState }
                        delete nextTabState[vizKey]
                        return { ...state, [tabId]: nextTabState }
                    }
                    return { ...state, [tabId]: { ...tabState, [vizKey]: next } }
                },
                clearTabUiState: (state, { tabId }) => {
                    if (!(tabId in state)) {
                        return state
                    }
                    const next = { ...state }
                    delete next[tabId]
                    return next
                },
            },
        ],
        savedQueriesByTabAndScene: [
            {} as SavedQueriesByTabAndScene,
            {
                setSavedQueryForTab: (state, { tabId, sceneKey, query }) => {
                    if (query === null) {
                        // Clear: drop the scene slot, and the whole tab entry if it becomes empty.
                        const tabState = state[tabId]
                        if (!tabState || !(sceneKey in tabState)) {
                            return state
                        }
                        const nextTabState = { ...tabState }
                        delete nextTabState[sceneKey]
                        if (Object.keys(nextTabState).length === 0) {
                            const next = { ...state }
                            delete next[tabId]
                            return next
                        }
                        return { ...state, [tabId]: nextTabState }
                    }
                    return {
                        ...state,
                        [tabId]: { ...state[tabId], [sceneKey]: query },
                    }
                },
                clearTabUiState: (state, { tabId }) => {
                    if (!(tabId in state)) {
                        return state
                    }
                    const next = { ...state }
                    delete next[tabId]
                    return next
                },
            },
        ],
        chatDraftsByTab: [
            {} as ChatDraftsByTab,
            {
                setChatDraftForTab: (state, { tabId, draft }) => {
                    if (draft === '') {
                        if (!(tabId in state)) {
                            return state
                        }
                        const next = { ...state }
                        delete next[tabId]
                        return next
                    }
                    return { ...state, [tabId]: draft }
                },
                clearTabUiState: (state, { tabId }) => {
                    if (!(tabId in state)) {
                        return state
                    }
                    const next = { ...state }
                    delete next[tabId]
                    return next
                },
            },
        ],
    }),
    selectors({
        expandedRowsFor: [
            (s) => [s.expandedRowsByTabAndVizKey],
            (state: ExpandedRowsByTabAndVizKey): ((tabId: string | undefined, vizKey: string) => ExpandedRowKey[]) =>
                (tabId, vizKey) =>
                    state[tabId ?? NO_TAB]?.[vizKey] ?? [],
        ],
        savedQueryFor: [
            (s) => [s.savedQueriesByTabAndScene],
            (state: SavedQueriesByTabAndScene): ((tabId: string | undefined, sceneKey: string) => Node | null) =>
                (tabId, sceneKey) =>
                    state[tabId ?? NO_TAB]?.[sceneKey] ?? null,
        ],
        chatDraftFor: [
            (s) => [s.chatDraftsByTab],
            (state: ChatDraftsByTab): ((tabId: string | undefined) => string) =>
                (tabId) =>
                    state[tabId ?? NO_TAB] ?? '',
        ],
    }),
])

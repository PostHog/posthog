export const buyHedgehogCoffeeLogic = kea<buyHedgehogCoffeeLogicType>([
    path(['lib', 'components', 'BuyHedgehogCoffee', 'buyHedgehogCoffeeLogic']),

    connect(() => ({
        values: [billingLogic, ['billing'], organizationLogic, ['currentOrganization'], userLogic, ['user']],
    })),

    actions({
        closeModal: true,
        donate: true,
        // Bumped when another tab records a "shown" timestamp so this tab's shouldShowModal selector
        // re-evaluates without a full page navigation.
        acknowledgeStorageChange: true,
    }),

    reducers({
        locallyClosed: [
            false,
            {
                closeModal: () => true,
                donate: () => true,
            },
        ],
        storageTick: [
            0,
            {
                closeModal: (state) => state + 1,
                acknowledgeStorageChange: (state) => state + 1,
            },
        ],
    }),

    selectors({
        isEligible: [
            (s) => [s.billing, s.currentOrganization],
            (billing: BillingType | null, currentOrganization: OrganizationType | null): boolean => {
                if (!currentOrganization?.created_at) {
                    return false
                }
                const orgOldEnough = dayjs().diff(dayjs(currentOrganization.created_at), 'month') >= MIN_ORG_AGE_MONTHS
                return orgOldEnough && isUnderFreeAllowance(billing)
            },
        ],
        shouldShowModal: [
            // storageTick is a dependency purely to re-run this after a "shown" timestamp is written.
            (s) => [s.isEligible, s.locallyClosed, s.user, s.currentOrganization, s.storageTick],
            (
                isEligible: boolean,
                locallyClosed: boolean,
                user: UserType | null,
                currentOrganization: OrganizationType | null,
                _storageTick: number
            ): boolean => {
                if (!isEligible || locallyClosed) {
                    return false
                }
                const lastShownAt = getLastShownAt(user?.uuid, currentOrganization?.id)
                if (!lastShownAt) {
                    return true
                }
                return dayjs().diff(dayjs(lastShownAt), 'month') >= SHOW_EVERY_MONTHS
            },
        ],
    }),

    listeners(({ values }) => ({
        closeModal: () => {
            rememberShown(values.user?.uuid, values.currentOrganization?.id)
        },
        donate: () => {
            rememberShown(values.user?.uuid, values.currentOrganization?.id)
            posthog.capture('hedgehog coffee donation clicked')
        },
    })),

    events(({ actions, cache }) => ({
        afterMount: () => {
            if (typeof window === 'undefined') {
                return
            }
            cache.onStorage = (event: StorageEvent): void => {
                if (event.key?.startsWith(LAST_SHOWN_KEY_PREFIX)) {
                    actions.acknowledgeStorageChange()
                }
            }
            window.addEventListener('storage', cache.onStorage)
        },
        beforeUnmount: () => {
            if (typeof window !== 'undefined' && cache.onStorage) {
                window.removeEventListener('storage', cache.onStorage)
            }
        },
    })),
])

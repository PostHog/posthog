import { MakeLogicType, actions, connect, events, kea, listeners, path, reducers, selectors } from 'kea'
import posthog from 'posthog-js'

import { dayjs } from 'lib/dayjs'
import { billingLogic } from 'scenes/billing/billingLogic'
import { organizationLogic } from 'scenes/organizationLogic'
import { userLogic } from 'scenes/userLogic'

import type { BillingType, OrganizationType, UserType } from '~/types'

export const HERTS_WILDLIFE_TRUST_DONATE_URL = 'https://www.hertswildlifetrust.org.uk/donate'

// "Twice a year" — we show the popup at most once per this window. Once the user closes it we
// record the timestamp and the popup stays away until this many months have passed.
const SHOW_EVERY_MONTHS = 6
// The org must have existed at least this long for us to treat it as a long-term free user.
const MIN_ORG_AGE_MONTHS = 6

const LAST_SHOWN_KEY_PREFIX = 'posthog_hedgehog_coffee_last_shown:'

function lastShownKey(userUuid: string | undefined, orgId: string | undefined): string | null {
    return userUuid && orgId ? `${LAST_SHOWN_KEY_PREFIX}${userUuid}:${orgId}` : null
}

function getLastShownAt(userUuid: string | undefined, orgId: string | undefined): string | null {
    const key = lastShownKey(userUuid, orgId)
    if (typeof window === 'undefined' || !key) {
        return null
    }
    try {
        return window.localStorage.getItem(key)
    } catch {
        // localStorage can be unavailable (privacy mode, etc.) — degrade gracefully.
        return null
    }
}

function rememberShown(userUuid: string | undefined, orgId: string | undefined): void {
    const key = lastShownKey(userUuid, orgId)
    if (typeof window === 'undefined' || !key) {
        return
    }
    try {
        window.localStorage.setItem(key, dayjs().toISOString())
    } catch {
        // localStorage can be unavailable (privacy mode, etc.) — degrade gracefully.
    }
}

// A team is "under the free allowance" when it's on the free plan and no product has spilled past
// its free allocation. Note: the billing payload the frontend holds only describes the *current*
// period, so this is a client-side approximation of "under the free allowance for the past six
// months" — org age stands in for the trailing window. A backend-computed flag on BillingType would
// be the precise signal; see the PR description.
function isUnderFreeAllowance(billing: BillingType | null): boolean {
    if (!billing || billing.has_active_subscription || billing.subscription_level !== 'free') {
        return false
    }
    return (billing.products ?? []).every(
        (product) =>
            !product.has_exceeded_limit &&
            (product.free_allocation == null || (product.current_usage ?? 0) <= product.free_allocation)
    )
}


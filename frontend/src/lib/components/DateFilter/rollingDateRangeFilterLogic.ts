export interface rollingDateRangeFilterLogicValues {
    counter: number | null;
    dateOption: DateOption;
    formattedDate: string | null;
    isDateOptionsSelectorOpen: boolean;
    startOfDateRange: string | null;
    value: string;
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface rollingDateRangeFilterLogicActions {
    decreaseCounter: () => {
        value: true;
    };
    increaseCounter: () => {
        value: true;
    };
    select: () => {
        value: true;
    };
    setCounter: (counter: number | null | undefined) => {
        counter: number | null | undefined;
    };
    setDateOption: (option: DateOption) => {
        option: DateOption;
    };
    toggleDateOptionsSelector: () => {
        value: true;
    };
}


export interface SharingLogicProps {
    dashboardId?: number
    insightShortId?: InsightShortId
    recordingId?: string
    notebookShortId?: string
    additionalParams?: Record<string, any>
    onSharingEnabledChange?: (enabled: boolean) => void
}

export interface EmbedConfig {
    width: string
    height: string
}

const defaultIframeConfig = {
    width: '100%',
    height: '400',
}

const defaultSharingSettings = {
    whitelabel: false,
    legend: false,
    noHeader: false,
    showInspector: false,
    hideExtraDetails: false,
    theme: 'system',
}

const propsToApiParams = async (
    props: SharingLogicProps
): Promise<{
    dashboardId?: number
    insightId?: number
    recordingId?: string
    notebookShortId?: string
}> => {
    const insightId = props.insightShortId ? await getInsightId(props.insightShortId) : undefined
    return {
        dashboardId: props.dashboardId,
        insightId,
        recordingId: props.recordingId,
        notebookShortId: props.notebookShortId,
    }
}


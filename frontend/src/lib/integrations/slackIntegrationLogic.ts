import { MakeLogicType, actions, connect, kea, key, listeners, path, props, reducers, selectors } from 'kea'
import { loaders } from 'kea-loaders'

import api from 'lib/api'
import { dayjs } from 'lib/dayjs'
import { getRecentSlackChannelIds, slackChannelId } from 'lib/integrations/slackChannel'
import { preflightLogic } from 'lib/logic/preflightLogic'

import { SlackChannelType } from '~/types'

import type { PreflightStatus } from '../../types'

export const SLACK_CHANNELS_MIN_REFRESH_INTERVAL_SECONDS = 30

// Matches the backend `SlackIntegrationInactiveError.default_code`. When the workspace token is
// revoked/expired the channels endpoint returns this instead of a 500, so we can surface guidance
// inline in the picker rather than spamming a generic error toast on every refresh/search.
export const SLACK_INTEGRATION_INACTIVE_ERROR_CODE = 'slack_integration_inactive'
const SLACK_INTEGRATION_INACTIVE_FALLBACK_MESSAGE =
    'Your Slack connection is no longer active. Reconnect Slack to load channels and pick a destination.'


import { actions, isBreakpoint, kea, key, listeners, path, props, reducers } from 'kea'

import api from 'lib/api'

import type { googleSearchConsoleIntegrationLogicType } from './googleSearchConsoleIntegrationLogicType'

export interface GoogleSearchConsoleSite {
    // Canonical Google format: `https://example.com/` (URL-prefix, trailing slash required)
    // or `sc-domain:example.com` (Domain property). We surface the string verbatim so the
    // backend comparison in `validate_credentials` matches without normalization.
    siteUrl: string
    permissionLevel: 'siteOwner' | 'siteFullUser' | 'siteRestrictedUser' | 'siteUnverifiedUser'
}

interface SitesResponse {
    sites: GoogleSearchConsoleSite[]
}

export const googleSearchConsoleIntegrationLogic = kea<googleSearchConsoleIntegrationLogicType>([
    props({} as { id: number }),
    key((props) => props.id),
    path((key) => ['lib', 'integrations', 'googleSearchConsoleIntegrationLogic', key]),

    actions({
        loadSites: true,
        loadSitesSuccess: (sites: GoogleSearchConsoleSite[]) => ({ sites }),
        loadSitesFailure: true,
    }),

    reducers({
        sites: [
            [] as GoogleSearchConsoleSite[],
            {
                loadSites: () => [],
                loadSitesSuccess: (_, { sites }) => sites,
            },
        ],
        sitesLoading: [
            false,
            {
                loadSites: () => true,
                loadSitesSuccess: () => false,
                loadSitesFailure: () => false,
            },
        ],
    }),

    listeners(({ actions, props }) => ({
        loadSites: async (_, breakpoint) => {
            try {
                // Manual typing for now — the autogenerated `api.integrations.googleSearchConsoleSites`
                // will replace this once `hogli build:openapi` succeeds. Path mirrors the @action
                // decorator on `IntegrationViewSet.google_search_console_sites`.
                const response = (await api.get(
                    `api/projects/@current/integrations/${props.id}/google_search_console_sites/`
                )) as SitesResponse
                await breakpoint()
                actions.loadSitesSuccess(response.sites)
            } catch (e: any) {
                if (isBreakpoint(e)) {
                    throw e
                }
                actions.loadSitesFailure()
            }
        },
    })),
])

function getApiErrorMessage(error: unknown, fallback: string): string {
    if (typeof error === 'object' && error !== null && 'detail' in error && typeof error.detail === 'string') {
        return error.detail
    }
    return fallback
}

function getCreationFailureCategory(error: unknown): 'validation' | 'permission' | 'rate_limit' | 'unknown' {
    if (typeof error !== 'object' || error === null || !('status' in error) || typeof error.status !== 'number') {
        return 'unknown'
    }
    if (error.status === 400) {
        return 'validation'
    }
    if (error.status === 401 || error.status === 403) {
        return 'permission'
    }
    if (error.status === 429) {
        return 'rate_limit'
    }
    return 'unknown'
}

function isValidPageUrl(url: string | null): boolean {
    if (!url) {
        return true
    }
    if (isUrlPattern(url)) {
        return false
    }
    try {
        new URL(url)
        return url.includes('://')
    } catch {
        return false
    }
}

// Screenshot heatmaps store a same-origin API path as `screenshotUrl`; the export backend's
// SSRF validation rejects URLs without an http(s) scheme, so we resolve it to an absolute URL.
export function resolveHeatmapExportUrl(
    type: HeatmapType,
    screenshotUrl: string | null,
    displayUrl: string | null,
    origin: string = window.location.origin
): string {
    if (type === 'screenshot') {
        if (!screenshotUrl) {
            return ''
        }
        try {
            return new URL(screenshotUrl, origin).toString()
        } catch {
            return ''
        }
    }
    return displayUrl ?? ''
}


// Helper function to detect if a URL contains regex pattern characters
export const isUrlPattern = (url: string): boolean => {
    return /[*+?^${}()|[\]\\]/.test(url)
}

const normalizeUrlPath = (urlObj: URL): string => {
    if (urlObj.pathname === '') {
        urlObj.pathname = '/'
    }
    return urlObj.toString()
}


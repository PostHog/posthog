export type surveyLoadDetectorLogicType = MakeLogicType<
    surveyLoadDetectorLogicValues,
    surveyLoadDetectorLogicActions,
    Record<string, any>,
    surveyLoadDetectorLogicMeta
>

export const surveyLoadDetectorLogic = kea<surveyLoadDetectorLogicType>([
    path(['scenes', 'surveys', 'surveyLoadDetectorLogic']),

    connect(() => ({
        values: [teamLogic, ['currentTeam', 'currentTeamId', 'currentTeamLoading']],
        actions: [teamLogic, ['updateCurrentTeam']],
    })),

    actions({
        setConfigValue: (key: keyof SurveyLoadDetectorConfigApi, value: number) => ({ key, value }),
        saveAsTeamDefault: true,
        loadAnalysis: true,
    }),

    reducers({
        configOverrides: [
            {} as Partial<SurveyLoadDetectorConfigApi>,
            {
                setConfigValue: (state, { key, value }) => ({ ...state, [key]: value }),
            },
        ],
    }),

    loaders(({ values }) => ({
        analysis: [
            null as SurveyLoadDetectorResponseApi | null,
            {
                loadAnalysis: async (_, breakpoint) => {
                    // Debounce rapid config changes into a single request
                    await breakpoint(300)
                    if (!values.currentTeamId) {
                        return values.analysis
                    }
                    const response = await surveysLoadDetectorRetrieve(String(values.currentTeamId), values.config)
                    breakpoint()
                    return response
                },
            },
        ],
    })),

    selectors({
        savedConfig: [
            (s) => [s.currentTeam],
            (currentTeam: TeamPublicType | TeamType | null): Partial<SurveyLoadDetectorConfigApi> =>
                sanitizeSavedConfig(currentTeam?.survey_config?.load_detector),
        ],
        config: [
            (s) => [s.savedConfig, s.configOverrides],
            (
                savedConfig: Partial<SurveyLoadDetectorConfigApi>,
                configOverrides: Partial<SurveyLoadDetectorConfigApi>
            ): SurveyLoadDetectorConfigApi => ({
                ...DEFAULT_LOAD_DETECTOR_CONFIG,
                ...savedConfig,
                ...configOverrides,
            }),
        ],
        hasUnsavedChanges: [
            (s) => [s.savedConfig, s.config],
            (savedConfig: Partial<SurveyLoadDetectorConfigApi>, config: SurveyLoadDetectorConfigApi): boolean =>
                JSON.stringify({ ...DEFAULT_LOAD_DETECTOR_CONFIG, ...savedConfig }) !== JSON.stringify(config),
        ],
    }),

    listeners(({ actions, values }) => ({
        setConfigValue: () => {
            actions.loadAnalysis()
        },
        saveAsTeamDefault: () => {
            actions.updateCurrentTeam({
                survey_config: {
                    ...values.currentTeam?.survey_config,
                    load_detector: values.config,
                },
            })
        },
    })),

    afterMount(({ actions }) => {
        actions.loadAnalysis()
    }),
])

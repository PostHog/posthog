import { MakeLogicType, actions, afterMount, connect, kea, listeners, path, reducers, selectors } from 'kea'
import { loaders } from 'kea-loaders'

import { teamLogic } from 'scenes/teamLogic'

import type { TeamPublicType, TeamSurveyConfigType, TeamType } from '~/types'

import { surveysLoadDetectorRetrieve } from 'products/surveys/frontend/generated/api'
import type {
    SurveyLoadDetectorConfigApi,
    SurveyLoadDetectorResponseApi,
} from 'products/surveys/frontend/generated/api.schemas'

export const DEFAULT_LOAD_DETECTOR_CONFIG: SurveyLoadDetectorConfigApi = {
    window_seconds: 24 * 60 * 60,
    overload_threshold: 2,
    lookback_days: 30,
}

function sanitizeSavedConfig(saved?: TeamSurveyConfigType['load_detector']): Partial<SurveyLoadDetectorConfigApi> {
    const sanitized: Partial<SurveyLoadDetectorConfigApi> = {}
    for (const key of ['window_seconds', 'overload_threshold', 'lookback_days'] as const) {
        const value = saved?.[key]
        if (typeof value === 'number' && Number.isFinite(value)) {
            sanitized[key] = value
        }
    }
    return sanitized
}


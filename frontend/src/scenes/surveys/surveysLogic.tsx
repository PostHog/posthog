                lemonToast.success('Survey duplicated', {
                    toastId: `survey-duplicated-${createdSurvey.id}`,
                    button: {
                        label: 'View survey',
                        action: () => {
                            router.actions.push(urls.survey(createdSurvey.id))
                        },
                    },
                })


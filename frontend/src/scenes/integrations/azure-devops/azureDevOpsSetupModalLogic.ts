export type azureDevOpsSetupModalLogicType = MakeLogicType<
    azureDevOpsSetupModalLogicValues,
    azureDevOpsSetupModalLogicActions,
    AzureDevOpsSetupModalLogicProps
>

export const azureDevOpsSetupModalLogic = kea<azureDevOpsSetupModalLogicType>([
    path(['integrations', 'azureDevOps', 'azureDevOpsSetupModalLogic']),
    props({} as AzureDevOpsSetupModalLogicProps),
    connect(() => ({
        values: [teamLogic, ['currentTeam']],
        actions: [integrationsLogic, ['loadIntegrations']],
    })),
    forms(({ props, actions, values }) => ({
        azureDevOpsIntegration: {
            defaults: {
                organization: '',
                project: '',
                personalAccessToken: '',
            },
            errors: ({ organization, project, personalAccessToken }) => ({
                organization: organization.trim() ? undefined : 'Organization is required',
                project: project.trim() ? undefined : 'Project is required',
                personalAccessToken: personalAccessToken.trim() ? undefined : 'Personal access token is required',
            }),
            submit: async () => {
                if (!values.currentTeam?.id) {
                    throw new Error('Select a project before connecting Azure DevOps')
                }

                try {
                    const integration = await integrationsCreate(String(values.currentTeam.id), {
                        kind: 'azure-devops',
                        config: {
                            organization: values.azureDevOpsIntegration.organization,
                            project: values.azureDevOpsIntegration.project,
                            personal_access_token: values.azureDevOpsIntegration.personalAccessToken,
                        },
                    })
                    actions.loadIntegrations()
                    lemonToast.success('Azure DevOps connected.')
                    props.onComplete(integration.id)
                } catch (error: unknown) {
                    const detail =
                        typeof error === 'object' && error !== null && 'detail' in error
                            ? String(error.detail)
                            : 'Could not connect Azure DevOps'
                    lemonToast.error(detail)
                    throw error
                }
            },
        },
    })),
])

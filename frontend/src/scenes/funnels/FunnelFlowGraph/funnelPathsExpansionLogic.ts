import { MakeLogicType, actions, connect, kea, key, listeners, path, props, reducers, selectors } from 'kea'

import { lemonToast } from '@posthog/lemon-ui'

import { eventUsageLogic } from 'lib/utils/eventUsageLogic'
import { keyForInsightLogicProps } from 'scenes/insights/sharedUtils'

import { performQuery } from '~/queries/query'
import { PathsLink, PathsQuery } from '~/queries/schema/schema-general'
import { InsightLogicProps } from '~/types'

import type { FunnelsQuery } from '../../../queries/schema/schema-general'
import { funnelDataLogic } from '../funnelDataLogic'
import { buildPathsQuery, PathExpansion, pathExpansionCacheKey } from './pathFlowUtils'

const DEFAULT_LOGIC_KEY = 'default_funnel_paths_expansion'

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface funnelPathsExpansionLogicValues {
    querySource: FunnelsQuery | null // funnelDataLogic
    expandedPath: PathExpansion | null
    expandedPathCacheKey: string | null
    expandedPathResults: PathsLink[] | null
    pathsLoading: boolean
    pathsResultsCache: Record<string, PathsLink[]>
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface funnelPathsExpansionLogicActions {
    collapsePath: () => {
        value: true
    }
    expandPath: (expansion: PathExpansion) => {
        expansion: PathExpansion
    }
    setPathsResults: (
        cacheKey: string,
        results: PathsLink[]
    ) => {
        cacheKey: string
        results: PathsLink[]
    }
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface funnelPathsExpansionLogicMeta {
    key: string
    __keaTypeGenInternalSelectorTypes: {
        expandedPathCacheKey: (expandedPath: PathExpansion | null) => string | null
        expandedPathResults: (
            expandedPathCacheKey: string | null,
            pathsResultsCache: Record<string, PathsLink[]>
        ) => PathsLink[] | null
    }
}

export type funnelPathsExpansionLogicType = MakeLogicType<
    funnelPathsExpansionLogicValues,
    funnelPathsExpansionLogicActions,
    InsightLogicProps,
    funnelPathsExpansionLogicMeta
>

export const funnelPathsExpansionLogic = kea<funnelPathsExpansionLogicType>([
    path((key) => ['scenes', 'funnels', 'FunnelFlowGraph', 'funnelPathsExpansionLogic', key]),
    props({} as InsightLogicProps),
    key(keyForInsightLogicProps(DEFAULT_LOGIC_KEY)),

    connect((props: InsightLogicProps) => ({
        values: [funnelDataLogic(props), ['querySource']],
    })),

    actions({
        expandPath: (expansion: PathExpansion) => ({ expansion }),
        collapsePath: true,
        setPathsResults: (cacheKey: string, results: PathsLink[]) => ({ cacheKey, results }),
    }),

    reducers({
        expandedPath: [
            null as PathExpansion | null,
            {
                expandPath: (_, { expansion }) => expansion,
                collapsePath: () => null,
            },
        ],
        pathsResultsCache: [
            {} as Record<string, PathsLink[]>,
            {
                setPathsResults: (state, { cacheKey, results }) => ({ ...state, [cacheKey]: results }),
            },
        ],
        pathsLoading: [
            false,
            {
                expandPath: () => true,
                setPathsResults: () => false,
                collapsePath: () => false,
            },
        ],
    }),

    selectors({
        expandedPathCacheKey: [
            (s) => [s.expandedPath],
            (expandedPath: PathExpansion | null): string | null =>
                expandedPath ? pathExpansionCacheKey(expandedPath) : null,
        ],
        expandedPathResults: [
            (s) => [s.expandedPathCacheKey, s.pathsResultsCache],
            (cacheKey: string | null, pathsResultsCache: Record<string, PathsLink[]>): PathsLink[] | null =>
                cacheKey ? (pathsResultsCache[cacheKey] ?? null) : null,
        ],
    }),

    listeners(({ actions, values }) => ({
        expandPath: async ({ expansion }, breakpoint) => {
            eventUsageLogic.actions.reportCustomerJourneyPathExpanded(
                expansion.pathType,
                expansion.dropOff,
                expansion.stepIndex
            )
            if (values.expandedPathCacheKey && values.expandedPathResults) {
                actions.setPathsResults(values.expandedPathCacheKey, values.expandedPathResults)
                return
            }

            const { querySource } = values
            if (!querySource || querySource.aggregation_group_type_index != null) {
                actions.collapsePath()
                lemonToast.info('Cannot expand paths for group aggregation')
                return
            }

            const query = buildPathsQuery(expansion, querySource)
            try {
                const response = await performQuery<PathsQuery>(query, undefined, 'blocking')
                breakpoint()
                const results = (response?.results ?? []) as PathsLink[]
                if (results.length === 0) {
                    actions.collapsePath()
                    lemonToast.info('Path expansion returned no results')
                } else {
                    actions.setPathsResults(values.expandedPathCacheKey!, results)
                }
            } catch {
                breakpoint()
                lemonToast.error('An error occurred when expanding paths')
                actions.collapsePath()
            }
        },
    })),
])

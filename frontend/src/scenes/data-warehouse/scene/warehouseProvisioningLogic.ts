export const warehouseProvisioningLogic = kea<warehouseProvisioningLogicType>([
    path(['scenes', 'data-warehouse', 'scene', 'warehouseProvisioningLogic']),

    connect(() => ({
        values: [preflightLogic, ['preflight']],
    })),

    actions({
        provisionWarehouse: (params: { databaseName: string; schemaName: string }) => params,
        provisionWarehouseComplete: true,
        deprovisionWarehouse: true,
        deprovisionWarehouseComplete: true,
        deleteOrg: true,
        deleteOrgComplete: (success: boolean) => ({ success }),
        resetPassword: true,
        resetPasswordComplete: true,
        setInitialPassword: (password: string) => ({ password }),
        clearInitialPassword: true,
        pollStatus: true,
        stopPolling: true,
        setDatabaseName: (name: string) => ({ name }),
        setLastRequestedDatabaseName: (name: string | null) => ({ name }),
        checkDatabaseName: (name: string) => ({ name }),
        setDatabaseNameAvailable: (available: boolean | null) => ({ available }),
        setDatabaseNameChecking: (checking: boolean) => ({ checking }),
        setSchemaName: (name: string) => ({ name }),
        checkSchemaName: (name: string) => ({ name }),
        setSchemaNameAvailable: (available: boolean | null) => ({ available }),
        setSchemaNameChecking: (checking: boolean) => ({ checking }),
        onboardTeam: (params: { schemaName: string }) => params,
        onboardTeamComplete: (schemaName: string | null) => ({ schemaName }),
    }),

    loaders({
        warehouseStatus: [
            null as WarehouseStatusResponseApi | null,
            {
                loadWarehouseStatus: async () => {
                    try {
                        return await dataWarehouseWarehouseStatusRetrieve(currentProjectId())
                    } catch (e: any) {
                        if (e.status === 404) {
                            return null
                        }
                        throw e
                    }
                },
            },
        ],
    }),

    reducers({
        isProvisioning: [
            false,
            {
                provisionWarehouse: () => true,
                provisionWarehouseComplete: () => false,
            },
        ],
        isDeprovisioning: [
            false,
            {
                deprovisionWarehouse: () => true,
                deprovisionWarehouseComplete: () => false,
            },
        ],
        // Consecutive `deleting` status reads, to detect a teardown that's stuck (no terminal
        // failed state exists, so it would otherwise poll forever). Reset whenever the state
        // isn't `deleting` and when a fresh deprovision starts.
        deletingPollCount: [
            0,
            {
                loadWarehouseStatusSuccess: (state, { warehouseStatus }) =>
                    warehouseStatus?.state === 'deleting' ? state + 1 : 0,
                deprovisionWarehouse: () => 0,
            },
        ],
        // In-flight flag for the automatic delete-org call. Doubles as the fire guard: while a
        // delete-org is running we don't start another, but once it settles (success or failure)
        // the next `deleted` poll can retry.
        isDeletingOrg: [
            false,
            {
                deleteOrg: () => true,
                deleteOrgComplete: () => false,
            },
        ],
        // Latched once delete-org succeeds so a status read still reporting `deleted` (provisioner
        // propagation lag before the record 404s) can't trigger a second delete against the now-gone
        // org. Cleared on a fresh deprovision so a later cycle can delete again.
        orgDeletionSucceeded: [
            false,
            {
                deleteOrgComplete: (state, { success }) => success || state,
                deprovisionWarehouse: () => false,
            },
        ],
        // Whether the last delete-org attempt failed, so we notify the user once per failure
        // streak instead of on every 10s retry. Cleared on success and on a new deprovision.
        orgDeletionFailed: [
            false,
            {
                deleteOrgComplete: (_, { success }) => !success,
                deprovisionWarehouse: () => false,
            },
        ],
        pollingActive: [
            false,
            {
                pollStatus: () => true,
                stopPolling: () => false,
            },
        ],
        databaseName: [
            '',
            {
                setDatabaseName: (_, { name }) => name,
                setLastRequestedDatabaseName: (state, { name }) => name ?? state,
            },
        ],
        lastRequestedDatabaseName: [
            null as string | null,
            {
                setLastRequestedDatabaseName: (_, { name }) => name,
                deprovisionWarehouse: () => null,
            },
        ],
        databaseNameAvailable: [
            null as boolean | null,
            {
                setDatabaseNameAvailable: (_, { available }) => available,
                setDatabaseName: () => null,
            },
        ],
        databaseNameChecking: [
            false,
            {
                setDatabaseNameChecking: (_, { checking }) => checking,
                setDatabaseName: () => false,
            },
        ],
        initialPassword: [
            null as string | null,
            {
                setInitialPassword: (_, { password }) => password,
                clearInitialPassword: () => null,
                deprovisionWarehouse: () => null,
            },
        ],
        isResettingPassword: [
            false,
            {
                resetPassword: () => true,
                resetPasswordComplete: () => false,
            },
        ],
        schemaName: [
            '',
            {
                setSchemaName: (_, { name }) => name,
            },
        ],
        schemaNameAvailable: [
            null as boolean | null,
            {
                setSchemaNameAvailable: (_, { available }) => available,
                setSchemaName: () => null,
            },
        ],
        schemaNameChecking: [
            false,
            {
                setSchemaNameChecking: (_, { checking }) => checking,
                setSchemaName: () => false,
            },
        ],
        isOnboardingTeam: [
            false,
            {
                onboardTeam: () => true,
                onboardTeamComplete: () => false,
            },
        ],
        // Whether this project is onboarded onto the organization's warehouse. When false on a
        // running warehouse, the onboarding screen is shown instead of the normal one.
        teamOnboarded: [
            false,
            {
                loadWarehouseStatusSuccess: (_, { warehouseStatus }) => warehouseStatus?.team_onboarded ?? false,
                onboardTeamComplete: (state, { schemaName }) => (schemaName ? true : state),
                deprovisionWarehouse: () => false,
            },
        ],
        // The schema this project's data lands in. Fixed once onboarded — it cannot be changed.
        teamSchemaName: [
            null as string | null,
            {
                loadWarehouseStatusSuccess: (_, { warehouseStatus }) => warehouseStatus?.schema_name ?? null,
                onboardTeamComplete: (state, { schemaName }) => schemaName ?? state,
                deprovisionWarehouse: () => null,
            },
        ],
        // Whether this project already has a backfill configured. When true, the schema name is
        // fixed (immutable), so we show a read-only state instead of re-offering the form.
        hasBackfill: [
            false,
            {
                loadWarehouseStatusSuccess: (_, { warehouseStatus }) => warehouseStatus?.has_backfill ?? false,
                onboardTeamComplete: (state, { schemaName }) => (schemaName ? true : state),
                deprovisionWarehouse: () => false,
            },
        ],
    }),

    selectors({
        isActionable: [
            (s) => [s.warehouseStatus],
            (status: WarehouseStatusResponseApi | null): boolean => {
                if (!status) {
                    return true
                }
                return status.state === 'ready' || status.state === 'failed' || status.state === 'deleted'
            },
        ],
        isInProgress: [
            (s) => [s.warehouseStatus],
            (status: WarehouseStatusResponseApi | null): boolean => {
                if (!status) {
                    return false
                }
                return status.state === 'pending' || status.state === 'provisioning' || status.state === 'deleting'
            },
        ],
        // True once teardown has sat in `deleting` past the warn threshold, so the UI can show a
        // "still working" note rather than an indefinite spinner.
        deprovisionTakingLong: [
            (s) => [s.warehouseStatus, s.deletingPollCount],
            (status: WarehouseStatusResponseApi | null, deletingPollCount: number): boolean =>
                status?.state === 'deleting' && deletingPollCount >= DELETING_POLL_WARN_THRESHOLD,
        ],
        isValidDatabaseName: [(s) => [s.databaseName], (name: string): boolean => WAREHOUSE_NAME_REGEX.test(name)],
        // DNS zone for the connection host, resolved from the deployment region (null when unknown).
        warehouseDomain: [
            (s) => [s.preflight],
            (preflight: null | import('~/types').PreflightStatus): string | null => {
                const region = preflight?.region
                return region ? (MANAGED_WAREHOUSE_DOMAINS[region] ?? null) : null
            },
        ],
        retryDatabaseName: [
            (s) => [s.databaseName, s.lastRequestedDatabaseName],
            (databaseName: string, lastRequestedDatabaseName: string | null): string =>
                databaseName || lastRequestedDatabaseName || '',
        ],
        // The schema name is used verbatim as a SQL identifier, so it must already be valid
        // (mirrors the backend validator) — we reject rather than silently rewrite.
        isValidSchemaName: [(s) => [s.schemaName], (name: string): boolean => SCHEMA_NAME_REGEX.test(name)],
        // The warehouse is running but this project hasn't picked a schema yet — show onboarding.
        needsTeamOnboarding: [
            (s) => [s.warehouseStatus, s.teamOnboarded],
            (status: WarehouseStatusResponseApi | null, teamOnboarded: boolean): boolean =>
                status?.state === 'ready' && !teamOnboarded,
        ],
        canProvision: [
            (s) => [s.isValidDatabaseName, s.databaseNameAvailable, s.isValidSchemaName],
            (valid: boolean, available: boolean | null, validSchema: boolean): boolean =>
                valid && available === true && validSchema,
        ],
        canRetryProvision: [
            (s) => [s.retryDatabaseName, s.isValidSchemaName],
            (retryDatabaseName: string, validSchema: boolean): boolean =>
                !!retryDatabaseName && WAREHOUSE_NAME_REGEX.test(retryDatabaseName) && validSchema,
        ],
        canOnboardTeam: [
            (s) => [s.isValidSchemaName, s.schemaNameAvailable],
            (validSchema: boolean, available: boolean | null): boolean => validSchema && available === true,
        ],
    }),

    listeners(({ actions, values }) => {
        let debounceTimer: ReturnType<typeof setTimeout> | null = null
        let schemaDebounceTimer: ReturnType<typeof setTimeout> | null = null

        return {
            setDatabaseName: ({ name }) => {
                if (debounceTimer) {
                    clearTimeout(debounceTimer)
                }
                if (WAREHOUSE_NAME_REGEX.test(name)) {
                    actions.setDatabaseNameChecking(true)
                    debounceTimer = setTimeout(() => {
                        actions.checkDatabaseName(name)
                    }, 400)
                }
            },

            checkDatabaseName: async ({ name }) => {
                // Same staleness guard as checkSchemaName: only the current name's request may
                // write availability or clear the spinner.
                try {
                    const result = await dataWarehouseCheckDatabaseNameRetrieve(currentProjectId(), { name })
                    if (values.databaseName === name) {
                        actions.setDatabaseNameAvailable(result.available)
                    }
                } catch {
                    if (values.databaseName === name) {
                        actions.setDatabaseNameAvailable(null)
                    }
                }
                if (values.databaseName === name) {
                    actions.setDatabaseNameChecking(false)
                }
            },

            setSchemaName: ({ name }) => {
                if (schemaDebounceTimer) {
                    clearTimeout(schemaDebounceTimer)
                }
                // Availability only matters when joining an existing warehouse — at provision
                // time this is the org's first schema, so there is nothing to collide with.
                if (values.needsTeamOnboarding && SCHEMA_NAME_REGEX.test(name)) {
                    actions.setSchemaNameChecking(true)
                    schemaDebounceTimer = setTimeout(() => {
                        actions.checkSchemaName(name)
                    }, 400)
                }
            },

            checkSchemaName: async ({ name }) => {
                // Guard every state write on the request's name still being current: a stale
                // completion (success or failure) must not clobber a newer check's result.
                try {
                    const result = await dataWarehouseCheckSchemaNameRetrieve(currentProjectId(), { name })
                    if (values.schemaName === name) {
                        actions.setSchemaNameAvailable(result.available)
                    }
                } catch {
                    if (values.schemaName === name) {
                        actions.setSchemaNameAvailable(null)
                    }
                }
                if (values.schemaName === name) {
                    actions.setSchemaNameChecking(false)
                }
            },

            provisionWarehouse: async ({ databaseName, schemaName }) => {
                actions.setLastRequestedDatabaseName(databaseName)
                window.localStorage.setItem(databaseNameStorageKey(teamLogic.values.currentTeamId), databaseName)
                try {
                    const result = await dataWarehouseProvisionCreate(currentProjectId(), {
                        database_name: databaseName,
                        schema_name: schemaName,
                    })
                    if (result.password) {
                        actions.setInitialPassword(result.password)
                    }
                    lemonToast.success('Warehouse provisioning started')
                    actions.loadWarehouseStatus()
                    actions.pollStatus()
                } catch (e: any) {
                    if (e.status === 409) {
                        // Another project in this organization already provisioned the shared warehouse.
                        lemonToast.info('This organization already has a managed warehouse')
                        actions.loadWarehouseStatus()
                        actions.pollStatus()
                    } else {
                        lemonToast.error(`Failed to provision warehouse: ${e.message || 'Unknown error'}`)
                    }
                }
                actions.provisionWarehouseComplete()
            },

            onboardTeam: async ({ schemaName }) => {
                try {
                    const result = await dataWarehouseOnboardTeamCreate(currentProjectId(), {
                        schema_name: schemaName,
                    })
                    lemonToast.success('This project is now part of the managed warehouse')
                    actions.onboardTeamComplete(result.schema_name ?? schemaName)
                    // Refresh from the server so the read-only state reflects the now-fixed schema.
                    actions.loadWarehouseStatus()
                } catch (e: any) {
                    if (e.status === 409) {
                        // The schema name was taken between the live check and submit.
                        lemonToast.error(
                            e.detail ||
                                'That schema name is already used by another project in your organization. Pick a different one.'
                        )
                        actions.setSchemaNameAvailable(false)
                    } else {
                        lemonToast.error(`Failed to enable this project: ${e.detail || e.message || 'Unknown error'}`)
                    }
                    actions.onboardTeamComplete(null)
                }
            },

            resetPassword: async () => {
                try {
                    const result = await dataWarehouseResetPasswordCreate(currentProjectId())
                    if (result.password) {
                        actions.setInitialPassword(result.password)
                    }
                    lemonToast.success('Password has been reset')
                } catch (e: any) {
                    lemonToast.error(`Failed to reset password: ${e.message || 'Unknown error'}`)
                }
                actions.resetPasswordComplete()
            },

            deprovisionWarehouse: async () => {
                window.localStorage.removeItem(databaseNameStorageKey(teamLogic.values.currentTeamId))
                try {
                    await dataWarehouseDeprovisionCreate(currentProjectId())
                    lemonToast.success('Warehouse deprovisioning started')
                    actions.loadWarehouseStatus()
                    actions.pollStatus()
                } catch (e: any) {
                    lemonToast.error(`Failed to deprovision warehouse: ${e.message || 'Unknown error'}`)
                }
                actions.deprovisionWarehouseComplete()
            },

            pollStatus: async (_, breakpoint) => {
                await breakpoint(10000)
                if (!values.pollingActive) {
                    return
                }
                actions.loadWarehouseStatus()
            },

            deleteOrg: async () => {
                try {
                    // Teardown is done (status is `deleted`); remove the now-empty org record so
                    // its database_name is freed. On success the next status read 404s and the UI
                    // returns to the provisioning form.
                    await dataWarehouseDeleteOrgDestroy(currentProjectId())
                    actions.loadWarehouseStatus()
                    actions.deleteOrgComplete(true)
                } catch (e: any) {
                    // Polling stays active while `deleted`, so the next tick retries this; notify
                    // once per failure streak rather than on every retry.
                    if (!values.orgDeletionFailed) {
                        lemonToast.error(`Failed to finish removing the warehouse: ${e.message || 'Unknown error'}`)
                    }
                    actions.deleteOrgComplete(false)
                }
            },

            loadWarehouseStatusSuccess: ({ warehouseStatus }) => {
                const state = warehouseStatus?.state
                if (state === 'deleted') {
                    actions.setLastRequestedDatabaseName(null)
                    window.localStorage.removeItem(databaseNameStorageKey(teamLogic.values.currentTeamId))
                    // Teardown finished, so remove the org row to free the name. Skip if a delete is
                    // already in flight or has already succeeded (a stale `deleted` read during
                    // propagation lag must not re-delete); a failed attempt retries on the next poll.
                    if (!values.isDeletingOrg && !values.orgDeletionSucceeded) {
                        actions.deleteOrg()
                    }
                }
                // Keep polling while teardown is in flight, and while the org record still needs
                // removing (`deleted`) so a failed delete-org retries on the next tick. A
                // successful delete-org 404s the next read, which falls through to stopPolling.
                if (state === 'pending' || state === 'provisioning' || state === 'deleting' || state === 'deleted') {
                    actions.pollStatus()
                } else {
                    actions.stopPolling()
                }
                // The default schema name is prefilled on mount, before this response can flip
                // needsTeamOnboarding to true — so its availability was never checked. Re-set it
                // here to run the debounced check once the onboarding form is actually shown.
                if (
                    values.needsTeamOnboarding &&
                    values.schemaName &&
                    values.isValidSchemaName &&
                    values.schemaNameAvailable === null &&
                    !values.schemaNameChecking
                ) {
                    actions.setSchemaName(values.schemaName)
                }
            },
        }
    }),

    afterMount(({ actions }) => {
        const persistedDatabaseName = window.localStorage.getItem(
            databaseNameStorageKey(teamLogic.values.currentTeamId)
        )
        if (persistedDatabaseName) {
            actions.setLastRequestedDatabaseName(persistedDatabaseName)
        }
        // Prefill the schema name with a valid default derived from the project name (lowercased,
        // non-identifier chars collapsed to underscores). It's only a starting point — the user
        // edits/confirms it, and the input is validated verbatim from there.
        const projectName = teamLogic.values.currentTeam?.name
        const defaultSchemaName = projectName
            ?.toLowerCase()
            .replace(/[^a-z0-9_]+/g, '_')
            .replace(/^_+|_+$/g, '')
            .slice(0, 63)
        if (defaultSchemaName) {
            actions.setSchemaName(defaultSchemaName)
        }
        actions.loadWarehouseStatus()
    }),
])

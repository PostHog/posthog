/** The synthetic "count of rows" measure — aggregates across all rows instead of a column. */
export const COUNT_STAR_COLUMN = '*'

export function defaultAggregationForField(field: Pick<BuilderField, 'isNumerical'>): InsightBuilderAggregation {
    return field.isNumerical ? 'sum' : 'count_distinct'
}


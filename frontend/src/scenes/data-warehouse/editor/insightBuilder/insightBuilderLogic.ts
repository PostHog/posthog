const applyToWell = <T>(list: T[], replace: boolean | undefined, item: T): T[] =>
    replace ? [...list.slice(0, -1), item] : [...list, item]

const moveInList = <T>(list: T[], fromIndex: number, toIndex: number): T[] => {
    if (fromIndex === toIndex || fromIndex < 0 || fromIndex >= list.length || toIndex < 0 || toIndex >= list.length) {
        return list
    }
    const next = [...list]
    const [item] = next.splice(fromIndex, 1)
    next.splice(toIndex, 0, item)
    return next
}

export const insightBuilderLogic = kea<insightBuilderLogicType>([
    path(['data-warehouse', 'editor', 'insightBuilderLogic']),
    props({} as InsightBuilderLogicProps),
    key((props) => props.tabId),
    connect((props: InsightBuilderLogicProps) => ({
        values: [
            sqlEditorLogic({ tabId: props.tabId }),
            ['sourceQuery', 'queryInput', 'isMultiQuery', 'dataLogicKey', 'editingView'],
            outputPaneLogic({ tabId: props.tabId }),
            ['editorMode'],
        ],
        actions: [
            sqlEditorLogic({ tabId: props.tabId }),
            ['setSourceQuery', 'runQuery'],
            outputPaneLogic({ tabId: props.tabId }),
            ['setEditorMode'],
        ],
    })),
    actions({
        addField: (
            well: BuilderWell,
            column: string,
            options?: {
                aggregation?: InsightBuilderAggregation
                dateGrain?: InsightBuilderDateGrain
                replace?: boolean
            }
        ) => ({ well, column, options }),
        removeField: (well: BuilderWell, index: number) => ({ well, index }),
        moveField: (well: BuilderWell, fromIndex: number, toIndex: number) => ({ well, fromIndex, toIndex }),
        setDateGrain: (well: 'rows' | 'columns', index: number, dateGrain: InsightBuilderDateGrain | null) => ({
            well,
            index,
            dateGrain,
        }),
        setAggregation: (index: number, aggregation: InsightBuilderAggregation) => ({ index, aggregation }),
        setBuilderDisplay: (display: ChartDisplayType) => ({ display }),
        hydrateFromNode: (builder: InsightBuilderConfig, display?: ChartDisplayType) => ({ builder, display }),
        setBaseSnapshot: (baseQuery: string, baseViewName: string | null) => ({ baseQuery, baseViewName }),
        applyWells: true,
        loadBaseColumns: true,
    }),
    loaders(({ values }) => ({
        baseFields: [
            [] as BuilderField[],
            {
                loadBaseColumns: async (_, breakpoint) => {
                    const base = values.baseQuery.trim()
                    if (!base || !isCompilableBase(base)) {
                        return []
                    }
                    await breakpoint(100)

                    const statement = splitQueries(base)[0].query
                    const response = await api.query<HogQLQuery>({
                        kind: NodeKind.HogQLQuery,
                        query: `SELECT * FROM (\n${statement}\n) LIMIT 0`,
                        variables: values.sourceQuery.source.variables,
                        connectionId: values.sourceQuery.source.connectionId,
                        tags: { productKey: 'sql_editor' },
                    })
                    breakpoint()

                    const columns: string[] = Array.isArray(response?.columns) ? response.columns : []
                    const types: (string[] | undefined)[] = Array.isArray(response?.types)
                        ? (response.types as string[][])
                        : []
                    return columns.map((name, index) => {
                        const typeName = toFriendlyClickhouseTypeName(types[index]?.[1])
                        return {
                            name,
                            typeName,
                            isNumerical: isNumericalType(typeName),
                            isDate: typeName === 'DATE' || typeName === 'DATETIME',
                        }
                    })
                },
            },
        ],
    })),
    reducers({
        rows: [
            [] as InsightBuilderDimension[],
            {
                addField: (state, { well, column, options }) =>
                    well === 'rows'
                        ? applyToWell(state, options?.replace, { column, dateGrain: options?.dateGrain })
                        : state,
                removeField: (state, { well, index }) =>
                    well === 'rows' ? state.filter((_, i) => i !== index) : state,
                moveField: (state, { well, fromIndex, toIndex }) =>
                    well === 'rows' ? moveInList(state, fromIndex, toIndex) : state,
                setDateGrain: (state, { well, index, dateGrain }) =>
                    well === 'rows'
                        ? state.map((dim, i) => (i === index ? { ...dim, dateGrain: dateGrain ?? undefined } : dim))
                        : state,
                hydrateFromNode: (_, { builder }) => builder.rows,
            },
        ],
        columnDims: [
            [] as InsightBuilderDimension[],
            {
                addField: (state, { well, column, options }) =>
                    well === 'columns'
                        ? applyToWell(state, options?.replace, { column, dateGrain: options?.dateGrain })
                        : state,
                removeField: (state, { well, index }) =>
                    well === 'columns' ? state.filter((_, i) => i !== index) : state,
                moveField: (state, { well, fromIndex, toIndex }) =>
                    well === 'columns' ? moveInList(state, fromIndex, toIndex) : state,
                setDateGrain: (state, { well, index, dateGrain }) =>
                    well === 'columns'
                        ? state.map((dim, i) => (i === index ? { ...dim, dateGrain: dateGrain ?? undefined } : dim))
                        : state,
                hydrateFromNode: (_, { builder }) => builder.columns,
            },
        ],
        measures: [
            [] as InsightBuilderMeasure[],
            {
                addField: (state, { well, column, options }) =>
                    well === 'values'
                        ? applyToWell(state, options?.replace, {
                              column,
                              aggregation: options?.aggregation ?? (column === COUNT_STAR_COLUMN ? 'count' : 'sum'),
                          })
                        : state,
                removeField: (state, { well, index }) =>
                    well === 'values' ? state.filter((_, i) => i !== index) : state,
                moveField: (state, { well, fromIndex, toIndex }) =>
                    well === 'values' ? moveInList(state, fromIndex, toIndex) : state,
                setAggregation: (state, { index, aggregation }) =>
                    state.map((measure, i) => (i === index ? { ...measure, aggregation } : measure)),
                hydrateFromNode: (_, { builder }) => builder.values,
            },
        ],
        builderDisplay: [
            ChartDisplayType.ActionsTable as ChartDisplayType,
            {
                setBuilderDisplay: (_, { display }) => display,
                hydrateFromNode: (state, { display }) =>
                    display && display !== ChartDisplayType.Auto ? display : state,
            },
        ],
        baseQuery: [
            '',
            {
                setBaseSnapshot: (_, { baseQuery }) => baseQuery,
                hydrateFromNode: (_, { builder }) => builder.baseQuery,
            },
        ],
        baseViewName: [
            null as string | null,
            {
                setBaseSnapshot: (_, { baseViewName }) => baseViewName,
                hydrateFromNode: (_, { builder }) => builder.baseView ?? null,
            },
        ],
    }),
    selectors({
        wells: [
            (s) => [s.rows, s.columnDims, s.measures],
            (
                rows: InsightBuilderDimension[],
                columnDims: InsightBuilderDimension[],
                measures: InsightBuilderMeasure[]
            ): BuilderWells => ({ rows, columns: columnDims, values: measures }),
        ],
        builderConfig: [
            (s) => [s.wells, s.baseQuery, s.baseViewName],
            (wells: BuilderWells, baseQuery: string, baseViewName: string | null): InsightBuilderConfig => ({
                enabled: true,
                baseQuery,
                ...(baseViewName ? { baseView: baseViewName } : {}),
                rows: wells.rows,
                columns: wells.columns,
                values: wells.values,
            }),
        ],
        hasAnyField: [
            (s) => [s.wells],
            (wells: BuilderWells): boolean => wells.rows.length + wells.columns.length + wells.values.length > 0,
        ],
        wellProblems: [
            (s) => [s.wells, s.builderDisplay],
            (wells: BuilderWells, builderDisplay: ChartDisplayType): string[] =>
                validateWellsForDisplay(wells, builderDisplay),
        ],
        canCompile: [
            (s) => [s.hasAnyField, s.baseQuery],
            (hasAnyField: boolean, baseQuery: string): boolean => hasAnyField && isCompilableBase(baseQuery),
        ],
        buildModeDisabledReason: [
            (s) => [s.isMultiQuery, s.queryInput, s.sourceQuery],
            (isMultiQuery: boolean, queryInput: string | null, sourceQuery: DataVisualizationNode): string | null => {
                if (sourceQuery.source.sendRawQuery) {
                    return "Build mode isn't available for raw SQL queries"
                }
                if (isMultiQuery) {
                    return 'Build mode needs a single SELECT statement — remove extra statements in the Data tab'
                }
                if (!queryInput?.trim() && !sourceQuery.builder?.enabled) {
                    return 'Write and run a query in the Data tab first'
                }
                return null
            },
        ],
    }),
    listeners(({ actions, values }) => ({
        addField: ({ well }) => {
            // First field dropped picks a sensible chart automatically; later drops respect the explicit choice
            if (
                values.builderDisplay === ChartDisplayType.ActionsTable &&
                values.wells.rows.length + values.wells.columns.length + values.wells.values.length === 1
            ) {
                const firstRow = values.baseFields.find((field) => field.name === values.wells.rows[0]?.column)
                actions.setBuilderDisplay(
                    bestDisplayForWells(values.wells, {
                        firstRowIsDate: !!firstRow?.isDate || !!values.wells.rows[0]?.dateGrain,
                    })
                )
            }
            posthog.capture('sql-editor-builder-field-added', { well })
            actions.applyWells()
        },
        removeField: () => actions.applyWells(),
        moveField: () => actions.applyWells(),
        setDateGrain: () => actions.applyWells(),
        setAggregation: () => actions.applyWells(),
        setBuilderDisplay: ({ display }) => {
            posthog.capture('sql-editor-builder-chart-changed', { display })
            actions.applyWells()
        },
        applyWells: async (_, breakpoint) => {
            await breakpoint(300)

            if (!values.canCompile) {
                return
            }

            let compiled
            try {
                compiled = compileBuilderQuery(values.builderConfig)
            } catch {
                // Invalid configuration (e.g. multi-statement base) — surfaced via wellProblems/disabled states
                return
            }

            const prev = values.sourceQuery
            const next: DataVisualizationNode = {
                ...prev,
                display: values.builderDisplay,
                chartSettings: mapWellsToChartSettings(
                    prev.chartSettings,
                    compiled,
                    values.builderDisplay,
                    values.builderConfig
                ),
                builder: values.builderConfig,
                source: { ...prev.source, query: compiled.sql },
            }
            actions.setSourceQuery(next)

            // Only spend a query when the wells satisfy the chart — the preview shows guidance otherwise
            if (values.wellProblems.length === 0) {
                actions.runQuery(compiled.sql)
            }
        },
        // Hydrate wells whenever a builder-carrying node lands in the editor (e.g. opening a saved
        // insight). Deep-equality is the loop guard: nodes produced by applyWells round-trip as an
        // identical config, so our own updates no-op here.
        setSourceQuery: ({ sourceQuery }) => {
            const builder = sourceQuery.builder
            if (builder?.enabled && !objectsEqual(builder, values.builderConfig)) {
                actions.hydrateFromNode(builder, sourceQuery.display)
                actions.loadBaseColumns()
            }
        },
        setEditorMode: ({ mode }) => {
            if (mode !== EditorMode.Build) {
                return
            }
            posthog.capture('sql-editor-builder-opened')

            const node = values.sourceQuery
            if (node.builder?.enabled && !objectsEqual(node.builder, values.builderConfig)) {
                actions.hydrateFromNode(node.builder, node.display)
            }

            // Snapshot the Data tab as the builder's base. When the tab is an unmodified saved view,
            // compile against the view by name so the insight tracks future view updates.
            const currentBase = values.queryInput ?? ''
            const editingView = values.editingView
            const viewName =
                editingView && currentBase.trim() === (editingView.query?.query ?? '').trim()
                    ? (editingView.name ?? null)
                    : null
            const baseChanged = values.baseQuery !== currentBase && currentBase.trim() !== ''
            if (baseChanged || (values.baseQuery === '' && currentBase !== '')) {
                actions.setBaseSnapshot(currentBase, viewName)
                actions.loadBaseColumns()
                if (values.hasAnyField) {
                    actions.applyWells()
                }
            } else if (values.baseFields.length === 0 && !values.baseFieldsLoading) {
                actions.loadBaseColumns()
            }
        },
    })),
    afterMount(({ actions, values }) => {
        // Covers mounting into a tab whose node already carries a builder (e.g. page refresh)
        const builder = values.sourceQuery.builder
        if (builder?.enabled && !objectsEqual(builder, values.builderConfig)) {
            actions.hydrateFromNode(builder, values.sourceQuery.display)
            actions.loadBaseColumns()
        }
    }),
])

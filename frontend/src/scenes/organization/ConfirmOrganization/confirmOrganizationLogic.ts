import { MakeLogicType, actions, kea, path, reducers } from 'kea'
import { forms } from 'kea-forms'
import type { DeepPartial, DeepPartialMap, FieldName, ValidationErrorType } from 'kea-forms'
import { urlToAction } from 'kea-router'

import api from 'lib/api'
import { lemonToast } from 'lib/lemon-ui/LemonToast/LemonToast'
import { getRelativeNextPath } from 'lib/utils/url'

export interface ConfirmOrganizationFormValues {
    organization_name?: string
    first_name?: string
    role_at_organization?: string
    referral_source?: string
    referral_source_ai_prompt?: string
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface confirmOrganizationLogicValues {
    confirmOrganization: ConfirmOrganizationFormValues
    confirmOrganizationAllErrors: Record<string, any>
    confirmOrganizationChanged: boolean
    confirmOrganizationErrors: DeepPartialMap<ConfirmOrganizationFormValues, ValidationErrorType>
    confirmOrganizationHasErrors: boolean
    confirmOrganizationManualErrors: Record<string, any>
    confirmOrganizationTouched: boolean
    confirmOrganizationTouches: Record<string, boolean>
    confirmOrganizationValidationErrors: DeepPartialMap<ConfirmOrganizationFormValues, ValidationErrorType>
    email: string
    isConfirmOrganizationSubmitting: boolean
    isConfirmOrganizationValid: boolean
    showConfirmOrganizationErrors: boolean
    showNewOrgWarning: boolean
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface confirmOrganizationLogicActions {
    resetConfirmOrganization: (values?: ConfirmOrganizationFormValues) => {
        values?: ConfirmOrganizationFormValues
    }
    setConfirmOrganizationManualErrors: (errors: Record<string, any>) => {
        errors: Record<string, any>
    }
    setConfirmOrganizationValue: (
        key: FieldName,
        value: any
    ) => {
        name: FieldName
        value: any
    }
    setConfirmOrganizationValues: (values: DeepPartial<ConfirmOrganizationFormValues>) => {
        values: DeepPartial<ConfirmOrganizationFormValues>
    }
    setEmail: (email: string) => {
        email: string
    }
    setShowNewOrgWarning: (show: boolean) => {
        show: boolean
    }
    submitConfirmOrganization: () => {
        value: boolean
    }
    submitConfirmOrganizationFailure: (
        error: Error,
        errors: Record<string, any>
    ) => {
        error: Error
        errors: Record<string, any>
    }
    submitConfirmOrganizationRequest: (confirmOrganization: ConfirmOrganizationFormValues) => {
        confirmOrganization: ConfirmOrganizationFormValues
    }
    submitConfirmOrganizationSuccess: (confirmOrganization: ConfirmOrganizationFormValues) => {
        confirmOrganization: ConfirmOrganizationFormValues
    }
    touchConfirmOrganizationField: (key: string) => {
        key: string
    }
}

export type confirmOrganizationLogicType = MakeLogicType<
    confirmOrganizationLogicValues,
    confirmOrganizationLogicActions
>

export const confirmOrganizationLogic = kea<confirmOrganizationLogicType>([
    path(['scenes', 'organization', 'confirmOrganizationLogic']),

    actions({
        setEmail: (email: string) => ({
            email,
        }),
        setShowNewOrgWarning: (show: boolean) => ({ show }),
    }),

    reducers({
        showNewOrgWarning: [
            false,
            {
                setShowNewOrgWarning: (_, { show }) => show,
            },
        ],
        email: [
            '',
            {
                setEmail: (_, { email }) => email,
            },
        ],
    }),

    forms(() => ({
        confirmOrganization: {
            defaults: {} as ConfirmOrganizationFormValues,
            errors: ({ organization_name, first_name }) => ({
                first_name: !first_name ? 'Please enter your name' : undefined,
                organization_name: !organization_name ? 'Please enter your organization name' : undefined,
            }),

            submit: async (formValues) => {
                await api
                    .create('api/social_signup/', {
                        ...formValues,
                    })
                    .then(() => {
                        const nextUrl = getRelativeNextPath(new URLSearchParams(location.search).get('next'), location)

                        // this url is validated in getRelativeNextPath as either being relative or on the same origin
                        // nosemgrep: javascript.browser.security.open-redirect.js-open-redirect
                        location.href = nextUrl || '/'
                    })
                    .catch((error: any) => {
                        console.error('error', error)
                        lemonToast.error(error.detail || 'Failed to create organization')
                    })
            },
        },
    })),

    urlToAction(({ actions }) => ({
        '/organization/confirm-creation': (_, { email, organization_name, first_name }) => {
            actions.setConfirmOrganizationValues({ organization_name, first_name })
            actions.setEmail(email)
        },
    })),
])

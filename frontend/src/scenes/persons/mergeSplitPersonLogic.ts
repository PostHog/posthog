import { MakeLogicType, actions, connect, events, kea, key, listeners, path, props, reducers } from 'kea'
import { loaders } from 'kea-loaders'
import { router } from 'kea-router'

import api from 'lib/api'
import { lemonToast } from 'lib/lemon-ui/LemonToast/LemonToast'
import { eventUsageLogic } from 'lib/utils/eventUsageLogic'

import { PersonType } from '~/types'

import { personsLogic } from './personsLogic'
import type { CountedPaginatedResponse } from '../../lib/api'

export interface SplitPersonLogicProps {
    person: PersonType
    urlId: string
}

export type PersonUuids = NonNullable<PersonType['uuid']>[]

export type SplitMode = 'all' | 'partial'


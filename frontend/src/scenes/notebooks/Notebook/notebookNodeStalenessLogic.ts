/**
 * Journey 10: UI-only staleness for revamped (SQLV2/PythonV2) cells, plus the chain runner.
 *
 * When a cell's run lands, every transitive dependent is marked stale (its result now derives
 * from outdated data). "Run stale cells" re-runs the stale set in document order — which IS
 * dependency order, because a cell can only reference exports of earlier cells. Runs go
 * through each cell's own notebookNodeSQLV2Logic (the node logics listen for
 * `dispatchChainRun`), so operation serialization, polling, and interrupt all apply per link.
 * Flags are session-local by design (walkthrough decision: UI-only, no backend call).
 */
export const notebookNodeStalenessLogic = kea<notebookNodeStalenessLogicType>([
    path((key) => ['scenes', 'notebooks', 'Notebook', 'notebookNodeStalenessLogic', key]),
    props({} as NotebookNodeStalenessLogicProps),
    key((props) => props.shortId),
    actions({
        // Reported by every V2 node logic from each of its run's terminal paths, whether the
        // run was user-clicked or chain-dispatched. `content` rides along on success so the
        // downstream set is computed against the document as it was when the result landed.
        nodeRunFinished: (nodeId: string, status: NotebookNodeRunTerminalStatus, content: JSONContent | null) => ({
            nodeId,
            status,
            content,
        }),
        markStaleNodeIds: (nodeIds: string[]) => ({ nodeIds }),
        clearNodeStale: (nodeId: string) => ({ nodeId }),
        // With a rootNodeId the chain is scoped to that cell's transitive downstream (the
        // "run downstream cells" button on the cell that just ran); without one it runs
        // every stale cell in the notebook.
        runStaleChain: (content: JSONContent | null, rootNodeId: string | null = null) => ({ content, rootNodeId }),
        // Consumed by the matching notebookNodeSQLV2Logic, which builds refs and runs itself.
        dispatchChainRun: (nodeId: string) => ({ nodeId }),
        setChainQueue: (nodeIds: string[]) => ({ nodeIds }),
        setChainRoot: (nodeId: string | null) => ({ nodeId }),
        setLastRun: (nodeId: string, downstreamNodeIds: string[]) => ({ nodeId, downstreamNodeIds }),
        abortChain: (reason: string | null) => ({ reason }),
        // Called by every V2 node logic on mount/unmount so the chain only ever dispatches
        // to a cell whose logic is mounted and listening.
        registerChainNode: (nodeId: string) => ({ nodeId }),
        unregisterChainNode: (nodeId: string) => ({ nodeId }),
    }),
    reducers({
        staleNodeIds: [
            {} as Record<string, true>,
            {
                markStaleNodeIds: (state, { nodeIds }) => {
                    if (!nodeIds.length) {
                        return state
                    }
                    const next = { ...state }
                    nodeIds.forEach((nodeId) => {
                        next[nodeId] = true
                    })
                    return next
                },
                clearNodeStale: (state, { nodeId }) => {
                    if (!(nodeId in state)) {
                        return state
                    }
                    const next = { ...state }
                    delete next[nodeId]
                    return next
                },
            },
        ],
        // Stale cells still to run; the head is the one currently running.
        chainQueue: [
            [] as string[],
            {
                setChainQueue: (_, { nodeIds }) => nodeIds,
                abortChain: () => [],
            },
        ],
        // The cell whose "run downstream cells" started the active chain; scopes the queue
        // rebuild so unrelated stale cells are left alone.
        chainRootNodeId: [
            null as string | null,
            {
                setChainRoot: (_, { nodeId }) => nodeId,
                abortChain: () => null,
            },
        ],
        // The last cell whose run landed, and the downstream set that run marked stale.
        // Drives the "run downstream cells" affordance on that cell.
        lastRunNodeId: [
            null as string | null,
            {
                setLastRun: (_, { nodeId }) => nodeId,
            },
        ],
        lastRunDownstreamNodeIds: [
            [] as string[],
            {
                setLastRun: (_, { downstreamNodeIds }) => downstreamNodeIds,
            },
        ],
        mountedNodeIds: [
            {} as Record<string, true>,
            {
                registerChainNode: (state, { nodeId }) => ({ ...state, [nodeId]: true as const }),
                unregisterChainNode: (state, { nodeId }) => {
                    if (!(nodeId in state)) {
                        return state
                    }
                    const next = { ...state }
                    delete next[nodeId]
                    return next
                },
            },
        ],
    }),
    selectors({
        staleCount: [
            (s) => [s.staleNodeIds],
            (staleNodeIds: Record<string, true>): number => Object.keys(staleNodeIds).length,
        ],
        isChainRunning: [(s) => [s.chainQueue], (chainQueue: string[]): boolean => chainQueue.length > 0],
        // The still-stale part of the last run's downstream; empties as cells re-run, which
        // is what hides the "run downstream cells" button once everything is fresh again.
        lastRunStaleDownstreamNodeIds: [
            (s) => [s.lastRunDownstreamNodeIds, s.staleNodeIds],
            (lastRunDownstreamNodeIds: string[], staleNodeIds: Record<string, true>): string[] =>
                lastRunDownstreamNodeIds.filter((nodeId) => staleNodeIds[nodeId]),
        ],
    }),
    listeners(({ actions, values }) => ({
        nodeRunFinished: ({ nodeId, status, content }) => {
            if (status === 'done') {
                actions.clearNodeStale(nodeId)
                const downstream = content ? staleDownstreamNodeIds(buildNotebookDependencyGraph(content), nodeId) : []
                actions.markStaleNodeIds(downstream)
                actions.setLastRun(nodeId, downstream)
            }

            if (values.chainQueue[0] !== nodeId) {
                return
            }
            if (status !== 'done') {
                actions.abortChain(nodeId)
                return
            }
            // Rebuild the queue from the live stale set instead of shifting the snapshot:
            // cells marked stale while the chain ran (including by this very run) join in,
            // and cells deleted or unmounted while queued drop out rather than receiving a
            // dispatch nobody picks up. A rooted chain stays scoped to the root's downstream
            // so unrelated stale cells are never run by it.
            const graph = content ? buildNotebookDependencyGraph(content) : null
            let queue = graph ? runnableStaleNodeIds(graph, values.staleNodeIds, values.mountedNodeIds) : []
            if (graph && values.chainRootNodeId) {
                const scope = collectDependencyNodeIds(graph, values.chainRootNodeId, 'downstream')
                queue = queue.filter((queuedNodeId) => scope.has(queuedNodeId))
            }
            actions.setChainQueue(queue)
            if (queue.length) {
                actions.dispatchChainRun(queue[0])
            } else {
                actions.setChainRoot(null)
                lemonToast.success('Stale cells re-run.')
            }
        },
        runStaleChain: ({ content, rootNodeId }) => {
            if (values.isChainRunning) {
                lemonToast.info('Stale cells are already being re-run.')
                return
            }
            if (!content) {
                return
            }
            const graph = buildNotebookDependencyGraph(content)
            let queue = runnableStaleNodeIds(graph, values.staleNodeIds, values.mountedNodeIds)
            if (rootNodeId) {
                const scope = collectDependencyNodeIds(graph, rootNodeId, 'downstream')
                queue = queue.filter((queuedNodeId) => scope.has(queuedNodeId))
            }
            if (!queue.length) {
                lemonToast.info('No stale cells to run.')
                return
            }
            actions.setChainRoot(rootNodeId)
            actions.setChainQueue(queue)
            actions.dispatchChainRun(queue[0])
        },
        abortChain: ({ reason }) => {
            if (reason) {
                lemonToast.warning('Stopped re-running stale cells: a cell did not finish successfully.')
            }
        },
    })),
])

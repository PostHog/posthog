/**
 * Backs the experiment "Recordings" tab: sessions exposed to the experiment over its run window,
 * narrowed by the variant facet and the metric facet ("replays that reached this metric").
 */
export const experimentReplayTabLogic = kea<experimentReplayTabLogicType>([
    props({} as ExperimentReplayTabLogicProps),
    key((props) => props.experiment.id ?? 'new'),
    path((key) => ['scenes', 'experiments', 'ExperimentView', 'experimentReplayTabLogic', String(key)]),
    connect((props: ExperimentReplayTabLogicProps) => ({
        // Same key as the metric buttons' lookup, so the two surfaces share one `seen_together` request.
        values: [
            viewRecordingsLinkabilityLogic({ experiment: props.experiment }),
            ['unlinkableEventNames', 'linkabilityLoaded'],
        ],
    })),
    actions({
        setSelectedVariantKey: (variantKey: string | null) => ({ variantKey }),
        setSelectedMetricUuid: (metricUuid: string | null) => ({ metricUuid }),
    }),
    reducers({
        // null = "All" (every exposed session, regardless of variant). Persisted (keyed per
        // experiment via the logic path) so the facet stays in step with the playlist across tab
        // switches — the playlist persists its own filters and rehydrates them on remount.
        selectedVariantKey: [
            null as string | null,
            { persist: true },
            {
                setSelectedVariantKey: (_, { variantKey }) => variantKey,
            },
        ],
        // null = no metric filter. Persisted for the same reason as the variant facet.
        selectedMetricUuid: [
            null as string | null,
            { persist: true },
            {
                setSelectedMetricUuid: (_, { metricUuid }) => metricUuid,
            },
        ],
    }),
    selectors({
        variantKeys: [
            () => [(_, props) => props.experiment],
            (experiment: Experiment): string[] => getExperimentVariants(experiment).map((variant) => variant.key),
        ],
        // Exposure is the whole list here, so an unmatchable exposure event means there is nothing to
        // show at all. Fails open while the check is in flight, matching the metric buttons.
        exposureUnlinkable: [
            (s) => [s.linkabilityLoaded, s.unlinkableEventNames, (_, props) => props.experiment],
            (linkabilityLoaded: boolean, unlinkableEventNames: Set<string>, experiment: Experiment): boolean =>
                // Fail open while the check is in flight, matching the metric buttons' posture.
                linkabilityLoaded &&
                applySessionLinkability(getViewRecordingFiltersForVariant(experiment), unlinkableEventNames)
                    .exposureUnlinkable,
        ],
        effectiveVariantKey: [
            (s) => [s.selectedVariantKey, s.variantKeys],
            (selectedVariantKey: string | null, variantKeys: string[]): string | null =>
                selectedVariantKey !== null && variantKeys.includes(selectedVariantKey) ? selectedVariantKey : null,
        ],
        // Inline primary + secondary metrics with at least one event/action source; saved/shared
        // metrics live in a separate payload and are not offered yet. A metric is unlinkable only
        // when every one of its sources is a never-session-linked event — with no linkable source
        // at all, its filter could only match zero sessions. Fails open while the check loads.
        metricOptions: [
            (s) => [s.linkabilityLoaded, s.unlinkableEventNames, (_, props) => props.experiment],
            (
                linkabilityLoaded: boolean,
                unlinkableEventNames: Set<string>,
                experiment: Experiment
            ): ExperimentReplayMetricOption[] =>
                [...(experiment.metrics || []), ...(experiment.metrics_secondary || [])]
                    .filter((metric): metric is ExperimentMetric => metric?.kind === NodeKind.ExperimentMetric)
                    .map((metric, index) => ({
                        uuid: metric.uuid ?? `unsaved-${index}`,
                        name: metric.name || getDefaultMetricTitle(metric),
                        filters: getMetricSessionFilters(metric),
                    }))
                    .filter((option) => option.filters.length > 0)
                    .map((option) => ({
                        ...option,
                        unlinkable:
                            linkabilityLoaded &&
                            option.filters.every((filter) => isUnlinkableEventFilter(filter, unlinkableEventNames)),
                    })),
        ],
        effectiveMetricUuid: [
            (s) => [s.selectedMetricUuid, s.metricOptions],
            (selectedMetricUuid: string | null, metricOptions: ExperimentReplayMetricOption[]): string | null => {
                const option = metricOptions.find((candidate) => candidate.uuid === selectedMetricUuid)
                return option && !option.unlinkable ? option.uuid : null
            },
        ],
        recordingsFilters: [
            (s) => [
                s.effectiveVariantKey,
                s.effectiveMetricUuid,
                s.metricOptions,
                s.unlinkableEventNames,
                (_, props) => props.experiment,
            ],
            (
                effectiveVariantKey: string | null,
                effectiveMetricUuid: string | null,
                metricOptions: ExperimentReplayMetricOption[],
                unlinkableEventNames: Set<string>,
                experiment: Experiment
            ): RecordingUniversalFilters => {
                const metricFilters = metricOptions.find((option) => option.uuid === effectiveMetricUuid)?.filters ?? []
                return {
                    ...DEFAULT_RECORDING_FILTERS,
                    date_from: experiment.start_date ?? DEFAULT_RECORDING_FILTERS.date_from,
                    date_to: experiment.end_date ?? null,
                    filter_test_accounts: experiment.exposure_criteria?.filterTestAccounts ?? false,
                    filter_group: {
                        type: FilterLogicalOperator.And,
                        values: [
                            {
                                type: FilterLogicalOperator.And,
                                values: [
                                    ...getViewRecordingFiltersForVariant(experiment, effectiveVariantKey ?? undefined),
                                    // Never AND a never-session-linked event into the query — it
                                    // could only zero out the whole list.
                                    ...metricFilters.filter(
                                        (filter) => !isUnlinkableEventFilter(filter, unlinkableEventNames)
                                    ),
                                ],
                            },
                        ],
                    },
                }
            },
        ],
    }),
    afterMount(() => {
        // Opening the tab is a session-replay cross-sell from experiments.
        void addProductIntentForCrossSell({
            from: ProductKey.EXPERIMENTS,
            to: ProductKey.SESSION_REPLAY,
            intent_context: ProductIntentContext.EXPERIMENT_VIEW_RECORDINGS,
        })
    }),
])

import { MakeLogicType, actions, connect, kea, key, listeners, path, props, reducers } from 'kea'
import { subscriptions } from 'kea-subscriptions'

import { keyForInsightLogicProps } from 'scenes/insights/sharedUtils'

import { HogQLQueryModifiers } from '~/queries/schema/schema-general'
import { InsightLogicProps } from '~/types'

import type {
    FunnelsQuery,
    LifecycleQuery,
    PathsQuery,
    RetentionQuery,
    StickinessQuery,
    TrendsQuery,
    WebOverviewQuery,
    WebStatsTableQuery,
} from '../../../queries/schema/schema-general'
import { insightVizDataLogic } from '../insightVizDataLogic'

export type PoeModeTypes = HogQLQueryModifiers['personsOnEventsMode'] | null

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface poeFilterLogicValues {
    querySource:
        | FunnelsQuery
        | LifecycleQuery
        | PathsQuery
        | RetentionQuery
        | StickinessQuery
        | TrendsQuery
        | WebOverviewQuery
        | WebStatsTableQuery
        | null // insightVizDataLogic
    poeMode: PoeModeTypes
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface poeFilterLogicActions {
    updateQuerySource: (querySource: import('../insightVizDataLogic').QuerySourceUpdate) => {
        querySource: import('../insightVizDataLogic').QuerySourceUpdate
    } // insightVizDataLogic
    setPoeMode: (poeMode: PoeModeTypes) => {
        poeMode: PoeModeTypes
    }
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface poeFilterLogicMeta {
    key: string
}

export type poeFilterLogicType = MakeLogicType<
    poeFilterLogicValues,
    poeFilterLogicActions,
    InsightLogicProps,
    poeFilterLogicMeta
>

export const poeFilterLogic = kea<poeFilterLogicType>([
    props({} as InsightLogicProps),
    key(keyForInsightLogicProps('new')),
    path((key) => ['scenes', 'insights', 'EditorFilters', 'poeFilterLogic', key]),
    connect((props: InsightLogicProps) => ({
        values: [insightVizDataLogic(props), ['querySource']],
        actions: [insightVizDataLogic(props), ['updateQuerySource']],
    })),
    actions(() => ({
        setPoeMode: (poeMode: PoeModeTypes) => ({ poeMode }),
    })),
    reducers({
        poeMode: [
            null as PoeModeTypes,
            {
                setPoeMode: (_, { poeMode }) => poeMode || null,
            },
        ],
    }),
    listeners(({ actions, values }) => ({
        setPoeMode: () => {
            actions.updateQuerySource({
                modifiers: {
                    personsOnEventsMode: values.poeMode || undefined,
                },
            })
        },
    })),
    subscriptions(({ values, actions }) => ({
        querySource: (querySource) => {
            const newPoeMode = querySource?.modifiers?.personsOnEventsMode
            if ((!!newPoeMode || !!values.poeMode) && newPoeMode != values.poeMode) {
                actions.setPoeMode(newPoeMode)
            }
        },
    })),
])

export const funnelDataWarehouseStepDefinitionPopoverLogic = kea<funnelDataWarehouseStepDefinitionPopoverLogicType>([
    props({} as FunnelDataWarehouseStepDefinitionPopoverLogicProps),
    key((props) => props.table.name),
    path((key) => ['scenes', 'insights', 'EditorFilters', 'funnelDataWarehouseStepDefinitionPopoverLogic', key]),
    connect((props: FunnelDataWarehouseStepDefinitionPopoverLogicProps) => ({
        values: [
            definitionPopoverLogic,
            ['definition', 'localDefinition'],
            funnelDataLogic(props.insightProps),
            ['querySource'],
        ],
        actions: [definitionPopoverLogic, ['setLocalDefinition']],
    })),
    actions(() => ({
        setActiveFieldKey: (activeFieldKey: FunnelFieldKey) => ({
            activeFieldKey,
        }),
        selectTable: true,
    })),
    selectors({
        dataWarehousePopoverFields: [
            (_, props) => [props.dataWarehousePopoverFields],
            (fields: DataWarehousePopoverField[]) => fields,
        ],
        selectedItemMeta: [() => [(_, props) => props.selectedItemMeta], (selectedItemMeta) => selectedItemMeta],
    }),
    reducers({
        activeFieldKey: [
            'aggregation_target_field' as FunnelFieldKey,
            {
                setActiveFieldKey: (_, { activeFieldKey }) => activeFieldKey,
            },
        ],
    }),
    selectors({
        columnOptions: [
            (_, p) => [p.table],
            // `table.fields` may be missing when the table is reconstructed
            // from a committed filter (new menu rebuild path passes
            // `{ name, id }` without re-attaching the full schema). Empty
            // array prevents the `Object.values(undefined)` crash; the
            // user reopens via the table picker to get full options.
            (table: DataWarehouseTableForInsight) =>
                table.fields
                    ? Object.values(table.fields).map((column) => ({
                          label: `${column.name} (${column.type})`,
                          value: column.name,
                          type: column.type,
                      }))
                    : [],
        ],
        linkedTables: [
            (_, p) => [p.table],
            (table: DataWarehouseTableForInsight) =>
                table.fields
                    ? Object.values(table.fields)
                          .filter((field) => LINKED_TABLE_TYPES.includes(field.type))
                          .map((field) => field.name)
                    : [],
        ],
        activeFieldKeyOptions: [
            (s) => [s.dataWarehousePopoverFields],
            (dataWarehousePopoverFields: DataWarehousePopoverField[]) =>
                EDITABLE_FIELD_ORDER.map((key) => ({
                    value: key,
                    label: dataWarehousePopoverFields.find((f) => f.key === key)?.label ?? key,
                })),
        ],
        activeField: [
            (s) => [s.dataWarehousePopoverFields, s.activeFieldKey],
            (dataWarehousePopoverFields: DataWarehousePopoverField[], activeFieldKey: FunnelFieldKey) =>
                dataWarehousePopoverFields.find((f) => f.key === activeFieldKey),
        ],
        scopedLocalDefinition: [
            (s, p) => [s.definition, s.localDefinition, p.table, s.selectedItemMeta],
            (
                definition: Partial<TaxonomicDefinitionTypes>,
                localDefinition: Partial<TaxonomicDefinitionTypes>,
                table: DataWarehouseTableForInsight,
                selectedItemMeta
            ) =>
                definition.name === table.name
                    ? localDefinition
                    : getDataWarehouseItemWithFieldDefaults(table, selectedItemMeta),
        ],
        activeFieldValue: [
            (s) => [s.scopedLocalDefinition, s.activeFieldKey],
            (scopedLocalDefinition: Partial<TaxonomicDefinitionTypes>, activeFieldKey: FunnelFieldKey) =>
                (scopedLocalDefinition as Partial<Record<FunnelFieldKey, string | undefined>>)[activeFieldKey],
        ],
        previewTable: [
            (_, p) => [p.table],
            (table: DataWarehouseTableForInsight) => ({
                ...table,
                fields: table.fields
                    ? Object.fromEntries(
                          Object.entries(table.fields).filter(([_, field]) => !HIDDEN_FIELD_TYPES.includes(field.type))
                      )
                    : {},
            }),
        ],
        previewExpressionColumns: [
            (s, p) => [p.table, s.dataWarehousePopoverFields, s.scopedLocalDefinition],
            (
                table: DataWarehouseTableForInsight,
                dataWarehousePopoverFields: DataWarehousePopoverField[],
                scopedLocalDefinition: Partial<TaxonomicDefinitionTypes>
            ): TablePreviewExpressionColumn[] => {
                const tableFieldNames = new Set(
                    table.fields ? Object.values(table.fields).map((field) => field.name) : []
                )
                const usedKeys = new Set(tableFieldNames)
                return EDITABLE_FIELD_ORDER.flatMap((fieldKey) => {
                    const configuredValue = (
                        scopedLocalDefinition as Partial<Record<FunnelFieldKey, string | undefined>>
                    )[fieldKey]
                    if (typeof configuredValue !== 'string') {
                        return []
                    }

                    const expression = configuredValue.trim()
                    if (!expression || tableFieldNames.has(expression)) {
                        return []
                    }

                    const label = dataWarehousePopoverFields.find((field) => field.key === fieldKey)?.label ?? fieldKey
                    const keyBase = `__${fieldKey}_hogql_expression`
                    let key = keyBase
                    let suffix = 2

                    while (usedKeys.has(key)) {
                        key = `${keyBase}_${suffix}`
                        suffix += 1
                    }

                    usedKeys.add(key)

                    return [
                        {
                            key,
                            expression,
                            label: `${label} (SQL expression)`,
                            type: 'SQL expression',
                        },
                    ]
                })
            },
        ],
        previewSelectedKey: [
            (s) => [s.previewExpressionColumns, s.activeFieldKey, s.activeFieldValue],
            (
                previewExpressionColumns: TablePreviewExpressionColumn[],
                activeFieldKey: FunnelFieldKey,
                activeFieldValue: string | undefined
            ) =>
                previewExpressionColumns.find((column) => column.key.startsWith(`__${activeFieldKey}_hogql_expression`))
                    ?.key ?? activeFieldValue,
        ],
        activeFieldOptions: [
            (s) => [s.columnOptions, s.activeField, s.activeFieldKey],
            (
                columnOptions: {
                    label: string
                    type: any
                    value: any
                }[],
                activeField: DataWarehousePopoverField | undefined,
                activeFieldKey: FunnelFieldKey
            ) => {
                const filteredColumnOptions = columnOptions.filter((column) =>
                    ALLOWED_COLUMN_TYPES_BY_FIELD_KEY[activeFieldKey].includes(column.type)
                )

                return activeField
                    ? [
                          ...filteredColumnOptions,
                          ...(activeField.allowHogQL ? [{ label: 'SQL Expression', value: '' }] : []),
                      ]
                    : filteredColumnOptions
            },
        ],
        activeFieldIsHogQL: [
            (s, p) => [s.activeFieldValue, p.table],
            (activeFieldValue: string | undefined, table: DataWarehouseTableForInsight) =>
                !table.fields || !Object.values(table.fields).some((field) => field.name === activeFieldValue),
        ],
        isAggregatingByGroup: [
            (s) => [s.querySource],
            (querySource: FunnelsQuery | null) => querySource?.aggregation_group_type_index != null,
        ],
        isAggregatingByHogQL: [
            (s) => [s.querySource, s.isAggregatingByGroup],
            (querySource: FunnelsQuery | null, isAggregatingByGroup: boolean) =>
                Boolean(querySource?.funnelsFilter?.funnelAggregateByHogQL) && !isAggregatingByGroup,
        ],
    }),
    listeners(({ values, props }) => ({
        selectTable: () => {
            props.onSelectItem(props.group, props.table.name, values.scopedLocalDefinition)
            posthog.capture('funnel data warehouse step selected')
        },
    })),
    afterMount(() => {
        posthog.capture('funnel data warehouse step popover viewed')
    }),
])

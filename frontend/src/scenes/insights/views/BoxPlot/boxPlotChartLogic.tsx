import { MakeLogicType, connect, kea, key, path, props, selectors } from 'kea'

import { insightVizDataLogic } from 'scenes/insights/insightVizDataLogic'

import { BoxPlotDatum } from '~/queries/schema/schema-general'
import { InsightLogicProps } from '~/types'

import type {
    FunnelsQuery,
    LifecycleQuery,
    PathsQuery,
    RetentionQuery,
    StickinessQuery,
    TrendsFilter,
    TrendsQuery,
    WebOverviewQuery,
    WebStatsTableQuery,
} from '../../../../queries/schema/schema-general'
import type { IntervalType } from '../../../../types'
import { keyForInsightLogicProps } from '../../sharedUtils'

export interface BoxPlotChartDatum {
    min: number
    q1: number
    median: number
    q3: number
    max: number
    mean: number
    whiskerMin: number
    whiskerMax: number
}

export interface BoxPlotSeriesData {
    seriesIndex: number
    seriesLabel: string
    data: BoxPlotChartDatum[]
    rawData: BoxPlotDatum[]
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface boxPlotChartLogicValues {
    insightData: Record<string, any> // insightVizDataLogic
    interval: IntervalType | null | undefined // insightVizDataLogic
    querySource:
        | FunnelsQuery
        | LifecycleQuery
        | PathsQuery
        | RetentionQuery
        | StickinessQuery
        | TrendsQuery
        | WebOverviewQuery
        | WebStatsTableQuery
        | null // insightVizDataLogic
    trendsFilter: TrendsFilter | null | undefined // insightVizDataLogic
    yAxisScaleType: string | null | undefined // insightVizDataLogic
    boxplotData: BoxPlotDatum[]
    dateLabels: string[]
    seriesGroups: BoxPlotSeriesData[]
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface boxPlotChartLogicMeta {
    key: string
    __keaTypeGenInternalSelectorTypes: {
        boxplotData: (insightData: Record<string, any>) => BoxPlotDatum[]
        seriesGroups: (
            boxplotData: BoxPlotDatum[],
            trendsFilter: TrendsFilter | null | undefined
        ) => BoxPlotSeriesData[]
        dateLabels: (seriesGroups: BoxPlotSeriesData[]) => string[]
    }
}

export type boxPlotChartLogicType = MakeLogicType<boxPlotChartLogicValues, {}, InsightLogicProps, boxPlotChartLogicMeta>

export const boxPlotChartLogic = kea<boxPlotChartLogicType>([
    props({} as InsightLogicProps),
    key(keyForInsightLogicProps('new')),
    path((key) => ['scenes', 'insights', 'BoxPlot', 'boxPlotChartLogic', key]),
    connect((props: InsightLogicProps) => ({
        values: [
            insightVizDataLogic(props),
            ['insightData', 'yAxisScaleType', 'querySource', 'interval', 'trendsFilter'],
        ],
    })),
    selectors({
        boxplotData: [
            (s) => [s.insightData],
            (insightData: Record<string, any>): BoxPlotDatum[] => {
                const result = insightData?.result ?? insightData?.results
                // TODO: remove boxplot_data fallback once cached responses have rotated (added 2026-04-16)
                const data = Array.isArray(result) && result.length > 0 ? result : insightData?.boxplot_data
                if (!Array.isArray(data) || data.length === 0) {
                    return []
                }
                return data
            },
        ],
        seriesGroups: [
            (s) => [s.boxplotData, s.trendsFilter],
            (boxplotData: BoxPlotDatum[], trendsFilter: TrendsFilter | null | undefined): BoxPlotSeriesData[] => {
                const excludeOutliers = trendsFilter?.excludeBoxPlotOutliers !== false
                const groupMap = new Map<number, { label: string; data: BoxPlotDatum[] }>()

                for (const d of boxplotData) {
                    const idx = d.series_index ?? 0
                    const label = d.series_label ?? 'Distribution'
                    if (!groupMap.has(idx)) {
                        groupMap.set(idx, { label, data: [] })
                    }
                    groupMap.get(idx)!.data.push(d)
                }

                return Array.from(groupMap.entries())
                    .sort(([a], [b]) => a - b)
                    .map(([seriesIndex, group]) => ({
                        seriesIndex,
                        seriesLabel: group.label,
                        rawData: group.data,
                        data: group.data.map((d) => {
                            const iqr = d.p75 - d.p25
                            return {
                                min: d.min,
                                q1: d.p25,
                                median: d.median,
                                q3: d.p75,
                                max: d.max,
                                mean: d.mean,
                                whiskerMin: excludeOutliers ? Math.max(d.min, d.p25 - 1.5 * iqr) : d.min,
                                whiskerMax: excludeOutliers ? Math.min(d.max, d.p75 + 1.5 * iqr) : d.max,
                            }
                        }),
                    }))
            },
        ],
        dateLabels: [
            (s) => [s.seriesGroups],
            (seriesGroups: BoxPlotSeriesData[]): string[] => {
                if (seriesGroups.length === 0) {
                    return []
                }
                return seriesGroups[0].rawData.map((d) => d.label)
            },
        ],
    }),
])

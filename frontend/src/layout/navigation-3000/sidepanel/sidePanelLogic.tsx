import { MakeLogicType, connect, kea, path, selectors } from 'kea'
import { combineUrl, router, urlToAction } from 'kea-router'

import { preflightLogic } from 'scenes/PreflightCheck/preflightLogic'
import { teamLogic } from 'scenes/teamLogic'
import { urls } from 'scenes/urls'
import { userLogic } from 'scenes/userLogic'

import { sceneLayoutLogic } from '~/layout/scenes/sceneLayoutLogic'
import { AvailableFeature, SidePanelTab } from '~/types'

import type { TeamPublicType, TeamType } from '../../../types'
import { sidePanelContextLogic } from './sidePanelContextLogic'
import { sidePanelStateLogic } from './sidePanelStateLogic'
import type { SidePanelSceneContext } from './types'

const TABS_REQUIRING_A_TEAM = [
    SidePanelTab.Max,
    SidePanelTab.Notebooks,
    SidePanelTab.Activity,
    SidePanelTab.Discussion,
    SidePanelTab.AccessControl,
    SidePanelTab.Exports,
]

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface sidePanelLogicValues {
    isCloudOrDev: boolean | undefined // preflightLogic
    scenePanelIsPresent: boolean // sceneLayoutLogic
    sceneSidePanelContext: SidePanelSceneContext // sidePanelContextLogic
    selectedTab: SidePanelTab | null // sidePanelStateLogic
    sidePanelOpen: boolean // sidePanelStateLogic
    currentTeam: TeamPublicType | TeamType | null // teamLogic
    hasAvailableFeature: (feature: AvailableFeature, currentUsage?: number | undefined) => boolean // userLogic
    enabledTabs: SidePanelTab[]
    visibleTabs: SidePanelTab[]
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface sidePanelLogicActions {
    closeSidePanel: (tab?: SidePanelTab | undefined) => {
        tab: SidePanelTab | undefined
    } // sidePanelStateLogic
    openSidePanel: (
        tab: SidePanelTab,
        options?: string | undefined
    ) => {
        options: string | undefined
        tab: SidePanelTab
    } // sidePanelStateLogic
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface sidePanelLogicMeta {
    __keaTypeGenInternalSelectorTypes: {
        enabledTabs: (
            sceneSidePanelContext: SidePanelSceneContext,
            currentTeam: TeamPublicType | TeamType | null,
            scenePanelIsPresent: boolean,
            isCloudOrDev: boolean | undefined,
            hasAvailableFeature: (feature: AvailableFeature, currentUsage?: number | undefined) => boolean
        ) => SidePanelTab[]
        visibleTabs: (enabledTabs: SidePanelTab[]) => SidePanelTab[]
    }
}

export type sidePanelLogicType = MakeLogicType<
    sidePanelLogicValues,
    sidePanelLogicActions,
    Record<string, any>,
    sidePanelLogicMeta
>

export const sidePanelLogic = kea<sidePanelLogicType>([
    path(['scenes', 'navigation', 'sidepanel', 'sidePanelLogic']),
    connect(() => ({
        values: [
            sidePanelStateLogic,
            ['selectedTab', 'sidePanelOpen'],
            sidePanelContextLogic,
            ['sceneSidePanelContext'],
            teamLogic,
            ['currentTeam'],
            sceneLayoutLogic,
            ['scenePanelIsPresent'],
            preflightLogic,
            ['isCloudOrDev'],
            userLogic,
            ['hasAvailableFeature'],
        ],
        actions: [sidePanelStateLogic, ['closeSidePanel', 'openSidePanel']],
    })),

    selectors({
        enabledTabs: [
            (s) => [
                s.sceneSidePanelContext,
                s.currentTeam,
                s.scenePanelIsPresent,
                s.isCloudOrDev,
                s.hasAvailableFeature,
            ],
            (
                sceneSidePanelContext: import('./types').SidePanelSceneContext,
                currentTeam: null | import('~/types').TeamPublicType | import('~/types').TeamType,
                scenePanelIsPresent: boolean,
                isCloudOrDev: boolean | undefined,
                hasAvailableFeature: (feature: AvailableFeature, currentUsage?: number | undefined) => boolean
            ) => {
                const tabs: SidePanelTab[] = []

                if (scenePanelIsPresent) {
                    tabs.push(SidePanelTab.Info)
                }

                tabs.push(SidePanelTab.Max)
                tabs.push(SidePanelTab.Notebooks)

                if (sceneSidePanelContext?.activity_scope && hasAvailableFeature(AvailableFeature.AUDIT_LOGS)) {
                    tabs.push(SidePanelTab.Activity)
                }
                tabs.push(SidePanelTab.Discussion)

                if (sceneSidePanelContext.access_control_resource && sceneSidePanelContext.access_control_resource_id) {
                    tabs.push(SidePanelTab.AccessControl)
                }

                // Exports and Support are openable programmatically but not shown in the nav bar
                tabs.push(SidePanelTab.Exports)

                if (isCloudOrDev) {
                    tabs.push(SidePanelTab.Support)
                }

                if (!currentTeam) {
                    return tabs.filter((tab) => !TABS_REQUIRING_A_TEAM.includes(tab))
                }

                return tabs
            },
        ],

        /** Tabs shown in the navigation bar */
        visibleTabs: [
            (s) => [s.enabledTabs],
            (enabledTabs: SidePanelTab[]): SidePanelTab[] => {
                // Some tabs are openable programmatically but not shown in the nav bar
                const hiddenTabs: SidePanelTab[] = [SidePanelTab.Exports]
                return enabledTabs.filter((tab) => !hiddenTabs.includes(tab))
            },
        ],
    }),
    urlToAction(() => {
        return {
            '/': (_, _searchParams, hashParams): void => {
                // Redirect old feature preview side panel links to new settings page
                if (hashParams.panel?.startsWith('feature-previews')) {
                    // it will be encoded as %3A, so we need to split on :
                    const parts = hashParams.panel.split(':')
                    // from: ${url}/#panel=feature-previews
                    // to:   ${url}/settings/user-feature-previews
                    if (parts.length > 1) {
                        // from: ${url}/#panel=feature-previews%3A${flagKey} or ${url}/#panel=feature-previews:${flagKey}
                        // to:   ${url}/settings/user-feature-previews#${flagKey}
                        router.actions.replace(combineUrl(urls.settings('user-feature-previews'), {}, parts[1]).url)
                    } else {
                        router.actions.replace(urls.settings('user-feature-previews'))
                    }
                }
            },
        }
    }),
])

export interface featureFlagTemplatesSceneLogicValues {
    featureFlags: FeatureFlagsSet // enabledFeaturesLogic
    intentsEnabled: boolean
    selectedTemplate: SelectedTemplate | null
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface featureFlagTemplatesSceneLogicActions {
    selectTemplate: (template: SelectedTemplate) => {
        template: SelectedTemplate
    }
    setSelectedTemplate: (template: SelectedTemplate | null) => {
        template: SelectedTemplate | null
    }
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface featureFlagTemplatesSceneLogicMeta {
    __keaTypeGenInternalSelectorTypes: {
        intentsEnabled: (featureFlags: FeatureFlagsSet) => boolean
    }
}

export type featureFlagTemplatesSceneLogicType = MakeLogicType<
    featureFlagTemplatesSceneLogicValues,
    featureFlagTemplatesSceneLogicActions,
    Record<string, any>,
    featureFlagTemplatesSceneLogicMeta
>

export const featureFlagTemplatesSceneLogic = kea<featureFlagTemplatesSceneLogicType>([
    path(['products', 'feature_flags', 'frontend', 'featureFlagTemplatesSceneLogic']),
    connect({
        values: [enabledFeaturesLogic, ['featureFlags']],
    }),
    actions({
        setSelectedTemplate: (template: SelectedTemplate | null) => ({ template }),
        selectTemplate: (template: SelectedTemplate) => ({ template }),
    }),
    reducers({
        selectedTemplate: [
            null as SelectedTemplate | null,
            {
                setSelectedTemplate: (_, { template }) => template,
            },
        ],
    }),
    selectors({
        intentsEnabled: [
            (s) => [s.featureFlags],
            (featureFlags: FeatureFlagsSet) => !!featureFlags[FEATURE_FLAGS.FEATURE_FLAG_CREATION_INTENTS],
        ],
    }),
    listeners(({ actions, values }) => ({
        selectTemplate: ({ template }) => {
            posthog.capture('feature flag template selected', { template_key: template })

            // Remote config skips the intent step — its evaluation warnings only apply to client-side flags.
            if (values.intentsEnabled && template !== 'remote-config') {
                actions.setSelectedTemplate(template)
            } else {
                navigateToNewFlag(router.values.searchParams, template)
            }
        },
    })),
])

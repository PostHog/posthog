export const logsMetricRuleFormLogic = kea<logsMetricRuleFormLogicType>([
    path(['products', 'logs', 'frontend', 'components', 'LogsMetricRules', 'logsMetricRuleFormLogic']),
    props({} as LogsMetricRuleFormLogicProps),
    key(({ rule }) => rule?.id ?? 'new'),

    connect(() => ({
        values: [teamLogic, ['currentTeamId']],
        actions: [logsMetricRulesSectionLogic, ['loadRules', 'closeRuleModal']],
    })),

    forms(({ actions, props, values }) => ({
        metricRuleForm: {
            defaults: buildMetricRuleFormDefaults(props.rule),
            errors: (form: LogsMetricRuleFormType) => {
                const valueAttribute = form.value_attribute.trim()
                return {
                    name: !form.name?.trim() ? 'Name is required' : undefined,
                    metric_name: !form.metric_name?.trim()
                        ? 'Metric name is required'
                        : !METRIC_NAME_PATTERN.test(form.metric_name.trim())
                          ? 'Must start with a letter and contain only letters, digits, dots, underscores, and dashes'
                          : undefined,
                    value_attribute:
                        valueAttribute &&
                        !ATTRIBUTE_KEY_PREFIXES.some(
                            (p) => valueAttribute.startsWith(p) && valueAttribute.length > p.length
                        )
                            ? 'Must be prefixed with `attributes.` or `resource_attributes.`'
                            : undefined,
                    group_by:
                        form.group_by.length > MAX_GROUP_BY_KEYS
                            ? `At most ${MAX_GROUP_BY_KEYS} group-by keys`
                            : form.group_by.some((k) => !isValidGroupByKey(k))
                              ? 'Keys must be service_name, severity_text, event_name, or prefixed with `attributes.` / `resource_attributes.`'
                              : undefined,
                }
            },
            submit: async (form: LogsMetricRuleFormType) => {
                const projectId = String(values.currentTeamId)
                const hasFilters = form.filter_group.values.length > 0
                try {
                    if (props.rule) {
                        // metric_name and value_attribute are immutable after creation — omitted here.
                        const patch: PatchedLogsMetricRuleApi = {
                            name: form.name.trim(),
                            enabled: form.enabled,
                            filter_group: hasFilters ? wrapFilterGroup(form.filter_group) : null,
                            group_by: form.group_by,
                        }
                        await logsMetricRulesPartialUpdate(projectId, props.rule.id, patch)
                        lemonToast.success('Metric rule updated')
                    } else {
                        await logsMetricRulesCreate(projectId, {
                            name: form.name.trim(),
                            metric_name: form.metric_name.trim(),
                            enabled: form.enabled,
                            filter_group: hasFilters ? wrapFilterGroup(form.filter_group) : null,
                            value_attribute: form.value_attribute.trim() || null,
                            group_by: form.group_by,
                        })
                        lemonToast.success('Metric rule created')
                    }
                    actions.loadRules()
                    actions.closeRuleModal()
                } catch (e: any) {
                    lemonToast.error(e?.detail ?? e?.message ?? 'Failed to save metric rule')
                    throw e
                }
            },
        },
    })),
])

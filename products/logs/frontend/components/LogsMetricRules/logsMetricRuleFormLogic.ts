const EMPTY_FILTER_GROUP: UniversalFiltersGroup = {
    type: FilterLogicalOperator.And,
    values: [],
}

// Mirrors METRIC_NAME_PATTERN in products/logs/backend/presentation/views/metric_rules_api.py.
const METRIC_NAME_PATTERN = /^[a-zA-Z][a-zA-Z0-9._-]*$/

export const MAX_GROUP_BY_KEYS = 5
const GROUP_BY_TOP_LEVEL_KEYS = ['service_name', 'severity_text', 'event_name']
const ATTRIBUTE_KEY_PREFIXES = ['attributes.', 'resource_attributes.']

export const GROUP_BY_SUGGESTIONS = GROUP_BY_TOP_LEVEL_KEYS

function isValidGroupByKey(k: string): boolean {
    return (
        GROUP_BY_TOP_LEVEL_KEYS.includes(k) ||
        ATTRIBUTE_KEY_PREFIXES.some((prefix) => k.startsWith(prefix) && k.length > prefix.length)
    )
}

/** Same wrapped `{type, values: [innerGroup]}` wire shape as drop rules (see logsSamplingFormLogic). */
function extractFilterGroup(stored: unknown): UniversalFiltersGroup {
    if (!stored || typeof stored !== 'object') {
        return EMPTY_FILTER_GROUP
    }
    const candidate = stored as { type?: unknown; values?: unknown[] }
    if (!Array.isArray(candidate.values)) {
        return EMPTY_FILTER_GROUP
    }
    const first = candidate.values[0] as { type?: unknown; values?: unknown[] } | undefined
    if (first && Array.isArray(first.values) && typeof first.type === 'string') {
        return first as UniversalFiltersGroup
    }
    return candidate as UniversalFiltersGroup
}

function wrapFilterGroup(inner: UniversalFiltersGroup): UniversalFiltersGroup {
    return { type: FilterLogicalOperator.And, values: [inner] as never }
}

export interface LogsMetricRuleFormType {
    name: string
    metric_name: string
    enabled: boolean
    filter_group: UniversalFiltersGroup
    value_attribute: string
    group_by: string[]
}

const DEFAULT_FORM: LogsMetricRuleFormType = {
    name: '',
    metric_name: '',
    enabled: true,
    filter_group: EMPTY_FILTER_GROUP,
    value_attribute: '',
    group_by: [],
}

export function buildMetricRuleFormDefaults(rule: LogsMetricRuleApi | null): LogsMetricRuleFormType {
    if (!rule) {
        return { ...DEFAULT_FORM }
    }
    return {
        name: rule.name,
        metric_name: rule.metric_name,
        enabled: rule.enabled ?? false,
        filter_group: extractFilterGroup(rule.filter_group),
        value_attribute: rule.value_attribute ?? '',
        group_by: rule.group_by ?? [],
    }
}


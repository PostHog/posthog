export const scoreLabLogic = kea<scoreLabLogicType>([
    path(['products', 'growth', 'frontend', 'scoreLab', 'scoreLabLogic']),
    actions({
        setSelectedLabel: (label: string | null) => ({ label }),
        loadVersionIntoEditor: (config: ConfigVersionApi) => ({ config }),
        setEditorPromptText: (promptText: string) => ({ promptText }),
        setEditorModel: (model: GrowthScoreLabModelEnumApi) => ({ model }),
        toggleEditorInputField: (field: string) => ({ field }),
        setSampleSize: (sampleSize: number) => ({ sampleSize }),
        setContainsFilter: (contains: string) => ({ contains }),
        setNewVersionInput: (version: string) => ({ version }),
        runClassification: true,
        consumeRunLine: (line: string) => ({ line }),
        appendRunRow: (row: ScoreLabVerdictRow) => ({ row }),
        setRunSummary: (summary: ScoreLabRunSummary) => ({ summary }),
        resetRunResults: true,
        setIsRunning: (isRunning: boolean) => ({ isRunning }),
    }),
    loaders(({ values }) => ({
        labels: [
            null as LabelListResponseApi | null,
            {
                loadLabels: async () => await growthScoreLabLabelsRetrieve(),
            },
        ],
        configs: [
            null as ConfigListResponseApi | null,
            {
                loadConfigs: async ({ label }: { label: string }) => await growthScoreLabConfigsRetrieve({ label }),
            },
        ],
        activateResult: [
            null as ConfigVersionApi | null,
            {
                activateVersion: async (configId: string) =>
                    await growthScoreLabActivateCreate({ config_id: configId }),
            },
        ],
        saveResult: [
            null as ConfigVersionApi | null,
            {
                saveVersion: async () => {
                    if (!values.selectedLabel) {
                        throw new Error('Cannot save a version without a selected label.')
                    }
                    return await growthScoreLabSaveCreate({
                        label: values.selectedLabel,
                        version: values.newVersionInput,
                        prompt_text: values.editorPromptText,
                        model: values.editorModel,
                        input_fields: values.editorInputFields,
                    })
                },
            },
        ],
    })),
    reducers({
        selectedLabel: [
            null as string | null,
            {
                setSelectedLabel: (_, { label }) => label,
            },
        ],
        selectedVersionId: [
            null as string | null,
            {
                setSelectedLabel: () => null,
                loadVersionIntoEditor: (_, { config }) => config.id,
            },
        ],
        loadedSnapshot: [
            null as ScoreLabEditorSnapshot | null,
            {
                setSelectedLabel: () => null,
                loadVersionIntoEditor: (_, { config }) => ({
                    promptText: config.prompt_text,
                    model: config.model as GrowthScoreLabModelEnumApi,
                    inputFields: config.input_fields,
                }),
            },
        ],
        editorPromptText: [
            '',
            {
                setSelectedLabel: () => '',
                loadVersionIntoEditor: (_, { config }) => config.prompt_text,
                setEditorPromptText: (_, { promptText }) => promptText,
            },
        ],
        editorModel: [
            DEFAULT_MODEL as GrowthScoreLabModelEnumApi,
            {
                loadVersionIntoEditor: (_, { config }) => config.model as GrowthScoreLabModelEnumApi,
                setEditorModel: (_, { model }) => model,
            },
        ],
        editorInputFields: [
            [] as string[],
            {
                setSelectedLabel: () => [],
                loadVersionIntoEditor: (_, { config }) => config.input_fields,
                toggleEditorInputField: (state, { field }) =>
                    state.includes(field) ? state.filter((existing) => existing !== field) : [...state, field],
            },
        ],
        newVersionInput: [
            '',
            {
                loadVersionIntoEditor: (_, { config }) => suggestNextVersion(config.version),
                setNewVersionInput: (_, { version }) => version,
            },
        ],
        sampleSize: [
            DEFAULT_SAMPLE_SIZE as number,
            {
                setSampleSize: (_, { sampleSize }) => sampleSize,
            },
        ],
        containsFilter: [
            '',
            {
                setContainsFilter: (_, { contains }) => contains,
            },
        ],
        runRows: [
            [] as ScoreLabVerdictRow[],
            {
                resetRunResults: () => [],
                appendRunRow: (state, { row }) => [...state, row],
            },
        ],
        runSummary: [
            null as ScoreLabRunSummary | null,
            {
                resetRunResults: () => null,
                setRunSummary: (_, { summary }) => summary,
            },
        ],
        isRunning: [
            false,
            {
                setIsRunning: (_, { isRunning }) => isRunning,
            },
        ],
    }),
    selectors({
        versions: [
            (s) => [s.configs],
            (configs: ConfigListResponseApi | null): ConfigVersionApi[] => configs?.results ?? [],
        ],
        selectedVersion: [
            (s) => [s.versions, s.selectedVersionId],
            (versions: ConfigVersionApi[], selectedVersionId: string | null): ConfigVersionApi | null =>
                versions.find((version) => version.id === selectedVersionId) ?? null,
        ],
        isEditorDirty: [
            (s) => [s.loadedSnapshot, s.editorPromptText, s.editorModel, s.editorInputFields],
            (
                loadedSnapshot: ScoreLabEditorSnapshot | null,
                editorPromptText: string,
                editorModel: GrowthScoreLabModelEnumApi,
                editorInputFields: string[]
            ): boolean => {
                if (!loadedSnapshot) {
                    return editorPromptText.trim().length > 0
                }
                return (
                    loadedSnapshot.promptText !== editorPromptText ||
                    loadedSnapshot.model !== editorModel ||
                    !sameInputFieldSet(loadedSnapshot.inputFields, editorInputFields)
                )
            },
        ],
        canRun: [
            (s) => [s.selectedLabel, s.editorPromptText, s.isRunning],
            (selectedLabel: string | null, editorPromptText: string, isRunning: boolean): boolean =>
                Boolean(selectedLabel) && editorPromptText.trim().length > 0 && !isRunning,
        ],
        canActivateSelectedVersion: [
            (s) => [s.selectedVersion],
            (selectedVersion: ConfigVersionApi | null): boolean =>
                Boolean(selectedVersion) && selectedVersion?.is_active === false,
        ],
    }),
    listeners(({ actions, values, cache }) => ({
        setSelectedLabel: ({ label }) => {
            if (label) {
                actions.loadConfigs({ label })
            }
        },
        loadLabelsSuccess: ({ labels }) => {
            // Auto-enter the only label so the picker is skipped when there's nothing to pick.
            if (!values.selectedLabel && labels && labels.results.length === 1) {
                router.actions.replace(urls.scoreLab(labels.results[0].label))
            }
        },
        loadConfigsSuccess: ({ configs }) => {
            const versions = configs?.results ?? []
            if (versions.length === 0) {
                return
            }
            const pendingId = cache.pendingSelectAfterSaveId as string | null | undefined
            cache.pendingSelectAfterSaveId = null
            const target = (pendingId && versions.find((version) => version.id === pendingId)) || versions[0]
            actions.loadVersionIntoEditor(target)
        },
        activateVersionSuccess: ({ activateResult }) => {
            lemonToast.success(`Activated version ${activateResult.version}.`)
            if (values.selectedLabel) {
                actions.loadConfigs({ label: values.selectedLabel })
            }
        },
        activateVersionFailure: () => {
            lemonToast.error('Failed to activate this version.')
        },
        saveVersionSuccess: ({ saveResult }) => {
            lemonToast.success(`Saved version ${saveResult.version}.`)
            cache.pendingSelectAfterSaveId = saveResult.id
            if (values.selectedLabel) {
                actions.loadConfigs({ label: values.selectedLabel })
            }
        },
        saveVersionFailure: () => {
            lemonToast.error('Failed to save this version.')
        },
        runClassification: async () => {
            if (!values.selectedLabel || !values.editorPromptText.trim()) {
                return
            }
            actions.resetRunResults()
            actions.setIsRunning(true)

            const controller = new AbortController()
            // The run can take a while (one LLM call per sampled org); it should keep streaming
            // even if the staff user switches tabs, so opt out of the disposables plugin's
            // pause-on-hidden behavior. Re-adding under the same key aborts any prior in-flight
            // run before starting a new one.
            cache.disposables.add(() => () => controller.abort(), 'scoreLabRunStream', { pauseOnPageHidden: false })

            try {
                const requestBody: RunRequestApi = {
                    label: values.selectedLabel,
                    prompt_text: values.editorPromptText,
                    model: values.editorModel,
                    input_fields: values.editorInputFields,
                    sample: values.sampleSize,
                    contains: values.containsFilter,
                }
                const response = await fetch(getGrowthScoreLabRunCreateUrl(), {
                    method: 'POST',
                    credentials: 'same-origin',
                    headers: {
                        'Content-Type': 'application/json',
                        Accept: 'application/x-ndjson',
                        'X-CSRFToken': getCookie('posthog_csrftoken') || '',
                    },
                    body: JSON.stringify(requestBody),
                    signal: controller.signal,
                })
                if (!response.ok || !response.body) {
                    throw new Error(`Score lab run failed with status ${response.status}`)
                }

                const reader = response.body.getReader()
                const decoder = new TextDecoder()
                let buffer = ''
                while (true) {
                    const { done, value } = await reader.read()
                    if (value) {
                        buffer += decoder.decode(value, { stream: true })
                        const lines = buffer.split('\n')
                        buffer = lines.pop() ?? ''
                        for (const line of lines) {
                            actions.consumeRunLine(line)
                        }
                    }
                    if (done) {
                        if (buffer.trim()) {
                            actions.consumeRunLine(buffer)
                        }
                        break
                    }
                }
            } catch (e) {
                if (!(e instanceof DOMException && e.name === 'AbortError')) {
                    lemonToast.error('Score lab run failed. Check the prompt and try again.')
                }
            } finally {
                actions.setIsRunning(false)
                cache.disposables.dispose('scoreLabRunStream')
            }
        },
        consumeRunLine: ({ line }) => {
            const trimmed = line.trim()
            if (!trimmed) {
                return
            }
            try {
                const parsed = JSON.parse(trimmed)
                if (parsed && typeof parsed === 'object') {
                    if ('summary' in parsed && parsed.summary) {
                        actions.setRunSummary(parsed.summary as ScoreLabRunSummary)
                        return
                    }
                    if ('verdict' in parsed) {
                        actions.appendRunRow(parsed as ScoreLabVerdictRow)
                    }
                }
            } catch {
                // Defensive: each ndjson line is parsed independently, so a malformed or
                // unexpectedly-split line is skipped instead of aborting the whole run.
            }
        },
    })),
    afterMount(({ actions }) => {
        actions.loadLabels()
    }),
    urlToAction(({ actions, values }) => ({
        '/score_lab': () => {
            if (values.selectedLabel !== null) {
                actions.setSelectedLabel(null)
            }
        },
        '/score_lab/:label': ({ label }) => {
            if (label && label !== values.selectedLabel) {
                actions.setSelectedLabel(label)
            }
        },
    })),
])

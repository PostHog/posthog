import { actions, afterMount, kea, key, listeners, path, props, reducers, selectors } from 'kea'
import { loaders } from 'kea-loaders'

import api from 'lib/api'

import type { landingLivePreviewLogicType } from './landingLivePreviewLogicType'

// Auto-orchestrator for the landing-page preview scene. Drives the full three-stage
// pipeline from one polling loop:
//
//     marketing_page (run_landing_page)  →  scaffold.files (run_scaffold)  →
//     scaffold.repo + scaffold.pages (publish_scaffold)  →  iframe `scaffold.pages.html_url`
//
// The logic re-evaluates state on every successful poll and fires the next action when the
// upstream stage flips to `completed`. Each action eagerly stamps its envelope to `pending`
// server-side, so the polling loop naturally observes the transitions and shows the right
// loading copy.

const POLL_INTERVAL_MS = 3000
const POLL_DISPOSABLE_KEY = 'landing-live-preview-poll'

// We rebuild repo_name on every publish call, so a stale envelope from a previous run
// doesn't trick us into thinking publish already happened. Repo name pattern matches the
// PublishScaffoldRequest validator on the backend.
function _repoName(projectName: string): string {
    const slug =
        projectName
            .toLowerCase()
            .replace(/[^a-z0-9]+/g, '-')
            .replace(/^-+|-+$/g, '')
            .slice(0, 40) || 'founder-mode-page'
    const stamp = new Date()
        .toISOString()
        .replace(/[^0-9]/g, '')
        .slice(0, 14)
    return `${slug}-${stamp}`
}

export type StageStatus = 'pending' | 'running' | 'completed' | 'failed' | null

interface ScaffoldEnvelope {
    status?: StageStatus
    files?: Record<string, string> | null
    file_count?: number | null
    total_bytes?: number | null
    repo?: { html_url: string; commit_sha?: string } | null
    pages?: { html_url: string; pages_status: string } | null
    error?: string
}

interface MarketingPageEnvelope {
    status?: StageStatus
    page?: unknown
    error?: string
}

interface ProjectShape {
    id: string
    name: string
    ideation?: Record<string, unknown>
    marketing_page?: MarketingPageEnvelope | Record<string, never>
    scaffold?: ScaffoldEnvelope | Record<string, never>
}

export interface LandingLivePreviewLogicProps {
    projectId: string
}

const projectUrl = (id: string): string => `api/projects/@current/founder_projects/${id}/`

export type Phase =
    | 'loading' // initial load
    | 'no-ideation' // user can't even start — ideation missing
    | 'generating-spec' // marketing_page is running or about to be kicked
    | 'generating-scaffold' // scaffold file generation is running or about to be kicked
    | 'publishing' // scaffold publish + GH Pages enable in flight
    | 'live' // iframe-ready URL is available
    | 'error'

export const landingLivePreviewLogic = kea<landingLivePreviewLogicType>([
    path(['products', 'founder_mode', 'frontend', 'components', 'landingLivePreviewLogic']),
    props({} as LandingLivePreviewLogicProps),
    key((props) => props.projectId),

    actions({
        startPolling: true,
        stopPolling: true,
        advance: true,
    }),

    loaders(({ props }) => ({
        project: [
            null as ProjectShape | null,
            {
                loadProject: async () => (await api.get(projectUrl(props.projectId))) as ProjectShape,
                runLandingPage: async () =>
                    (await api.create(`${projectUrl(props.projectId)}run_landing_page/`)) as ProjectShape,
                runScaffold: async () =>
                    (await api.create(`${projectUrl(props.projectId)}run_scaffold/`)) as ProjectShape,
                publishScaffold: async (_, breakpoint) => {
                    // Eagerly load the project so we have a fresh name for the repo slug.
                    const current = (await api.get(projectUrl(props.projectId))) as ProjectShape
                    breakpoint()
                    return (await api.create(`${projectUrl(props.projectId)}publish_scaffold/`, {
                        // No `github_token` — server falls back to FOUNDER_MODE_GITHUB_PAT env var.
                        repo_name: _repoName(current.name),
                        visibility: 'public',
                        description: `Landing page for ${current.name}. Generated by PostHog founder mode.`,
                    })) as ProjectShape
                },
            },
        ],
    })),

    reducers({
        isPolling: [false, { startPolling: () => true, stopPolling: () => false }],
        // The publish call ate the freshly-generated file tree. Remember we've kicked it so
        // the state machine doesn't re-fire on the next poll while the task is still
        // transitioning scaffold.status from completed → running.
        publishKicked: [false, { publishScaffoldSuccess: () => true }],
    }),

    selectors({
        marketingPage: [
            (s) => [s.project],
            (project): MarketingPageEnvelope | null => {
                const m = project?.marketing_page
                if (!m || !('status' in m)) {
                    return null
                }
                return m as MarketingPageEnvelope
            },
        ],
        scaffold: [
            (s) => [s.project],
            (project): ScaffoldEnvelope | null => {
                const s = project?.scaffold
                if (!s || !('status' in s)) {
                    return null
                }
                return s as ScaffoldEnvelope
            },
        ],
        hasIdeation: [
            (s) => [s.project],
            (project): boolean => !!project?.ideation && Object.keys(project.ideation).length > 0,
        ],
        liveUrl: [
            (s) => [s.scaffold],
            (scaffold): string | null => {
                if (!scaffold) {
                    return null
                }
                const pages = scaffold.pages
                if (!pages) {
                    return null
                }
                // `built` means GitHub Pages finished provisioning. `building`/`queued`
                // mean it's coming — we don't iframe until built to avoid showing a 404.
                if (pages.pages_status === 'built') {
                    return pages.html_url
                }
                return null
            },
        ],
        phase: [
            (s) => [s.project, s.hasIdeation, s.marketingPage, s.scaffold, s.liveUrl],
            (project, hasIdeation, mp, sc, liveUrl): Phase => {
                if (!project) {
                    return 'loading'
                }
                if (!hasIdeation) {
                    return 'no-ideation'
                }
                if (liveUrl) {
                    return 'live'
                }
                const mpStatus = mp?.status
                if (mpStatus === 'failed') {
                    return 'error'
                }
                if (mpStatus !== 'completed') {
                    return 'generating-spec'
                }
                const scStatus = sc?.status
                if (scStatus === 'failed') {
                    return 'error'
                }
                // The scaffold envelope is used for both `generate` and `publish` calls.
                // Distinguish by what's been produced:
                //   - no `files` yet  → still in the generate-scaffold step
                //   - `files` but no `repo` → still in the publish step
                //   - `repo` but pages not yet `built` → publish step's poll-for-Pages phase
                if (!sc?.files) {
                    return 'generating-scaffold'
                }
                if (!sc.repo || !sc.pages || sc.pages.pages_status !== 'built') {
                    return 'publishing'
                }
                return 'live'
            },
        ],
        errorMessage: [
            (s) => [s.marketingPage, s.scaffold],
            (mp, sc): string => {
                if (mp?.status === 'failed') {
                    return mp.error || 'Landing page generation failed.'
                }
                if (sc?.status === 'failed') {
                    return sc.error || 'Scaffold publish failed.'
                }
                return ''
            },
        ],
        isWaiting: [
            (s) => [s.phase],
            (phase): boolean =>
                phase === 'loading' ||
                phase === 'generating-spec' ||
                phase === 'generating-scaffold' ||
                phase === 'publishing',
        ],
    }),

    listeners(({ actions, values, cache }) => ({
        loadProjectSuccess: () => {
            actions.advance()
        },
        runLandingPageSuccess: () => actions.advance(),
        runScaffoldSuccess: () => actions.advance(),
        publishScaffoldSuccess: () => actions.advance(),
        advance: () => {
            // The state machine: examine the current envelope state and fire whichever next
            // action moves us forward. Idempotent — running already in progress = no-op.
            const phase = values.phase
            const mp = values.marketingPage
            const sc = values.scaffold

            if (phase === 'live' || phase === 'error' || phase === 'no-ideation' || phase === 'loading') {
                // Terminal or pre-condition states — stop the polling loop.
                if (values.isPolling) {
                    actions.stopPolling()
                }
                return
            }

            // Otherwise we're waiting on something. Make sure the poll loop is active.
            if (!values.isPolling) {
                actions.startPolling()
            }

            // Kick the next stage if the upstream finished and the next hasn't started.
            if (phase === 'generating-spec' && !mp?.status) {
                actions.runLandingPage()
                return
            }
            if (phase === 'generating-scaffold' && mp?.status === 'completed' && !sc?.status) {
                actions.runScaffold()
                return
            }
            if (
                phase === 'publishing' &&
                sc?.files &&
                !sc.repo &&
                sc.status !== 'running' &&
                sc.status !== 'pending' &&
                !values.publishKicked
            ) {
                actions.publishScaffold()
                return
            }
        },
        startPolling: () => {
            cache.disposables.add(() => {
                const id = setInterval(() => actions.loadProject(), POLL_INTERVAL_MS)
                return () => clearInterval(id)
            }, POLL_DISPOSABLE_KEY)
        },
        stopPolling: () => {
            cache.disposables.dispose(POLL_DISPOSABLE_KEY)
        },
    })),

    afterMount(({ actions }) => {
        actions.loadProject()
    }),
])

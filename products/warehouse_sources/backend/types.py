import typing
from enum import StrEnum

from django.db import models


class IncrementalFieldType(StrEnum):
    Integer = "integer"
    Numeric = "numeric"  # For snowflake
    DateTime = "datetime"
    Date = "date"
    Timestamp = "timestamp"
    # MongoDB specific
    ObjectID = "objectid"
    # Postgres xmin system column — synthetic cursor, never in information_schema
    XID = "xid"


class IncrementalField(typing.TypedDict, total=False):
    label: typing.Required[str]  # Label shown in the UI
    type: typing.Required[IncrementalFieldType]  # Field type shown in the UI
    field: typing.Required[str]  # Actual DB field accessed
    field_type: typing.Required[IncrementalFieldType]  # Actual DB type of the field
    nullable: bool  # Whether the field allows null values
    # Whether the field is the leading column of an index (or equivalent fast-lookup
    # structure for the engine: clustering key, sortkey, partition column, etc.).
    # Used to warn users picking an incremental field that would force a full scan
    # on every sync. Defaults to True when omitted so missing detection never warns.
    is_indexed: bool


class PartitionSettings(typing.NamedTuple):
    """Settings used when partitioning data warehouse tables.

    Attributes:
        partition_count: Total number of partitions.
        partition_size: Number of rows to include per partition.
    """

    partition_count: int
    partition_size: int



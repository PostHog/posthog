from dataclasses import dataclass, field

from products.warehouse_sources.backend.temporal.data_imports.sources.common.schema import incremental_field
from products.warehouse_sources.backend.types import IncrementalField, IncrementalFieldType

WASABI_BASE_URL = "https://partner.wasabisys.com"

# Wasabi launched its storage service in May 2017, so no utilization data can predate this.
DEFAULT_UTILIZATION_START_DATE = "2017-01-01"

# Utilization rows are one per account (or bucket) per day, so a 30-day window keeps
# individual responses at a manageable size even for partners with many sub-accounts.
UTILIZATION_WINDOW_DAYS = 30

# Utilization metrics are generated by a daily upstream batch job that can lag or backfill;
# re-read a trailing window each incremental run so late-arriving days are picked up
# (the merge on primary keys dedupes the overlap).
UTILIZATION_LOOKBACK_SECONDS = 3 * 24 * 60 * 60

START_TIME_INCREMENTAL: IncrementalField = incremental_field("StartTime", IncrementalFieldType.DateTime)

ACCOUNTS = "accounts"
UTILIZATIONS = "utilizations"
BUCKET_UTILIZATIONS = "bucket_utilizations"
SUB_ACCOUNT_INVOICES = "sub_account_invoices"


@dataclass
class WasabiEndpointConfig:
    name: str
    path: str
    primary_keys: list[str]
    partition_key: str | None = None
    incremental_fields: list[IncrementalField] = field(default_factory=list)
    # Endpoints that accept from/to date filters are walked in ascending date windows.
    date_windowed: bool = False
    # Fan-out endpoints iterate this parent endpoint and resolve {acct_num} per parent row.
    parent: str | None = None


WASABI_ENDPOINTS: dict[str, WasabiEndpointConfig] = {
    ACCOUNTS: WasabiEndpointConfig(
        name=ACCOUNTS,
        path="/v1/accounts",
        primary_keys=["AcctNum"],
        partition_key="CreateTime",
    ),
    UTILIZATIONS: WasabiEndpointConfig(
        name=UTILIZATIONS,
        path="/v1/utilizations",
        primary_keys=["UtilizationNum"],
        # StartTime is the day the utilization was measured for and never changes,
        # unlike CreateTime-adjacent fields on restated rows.
        partition_key="StartTime",
        incremental_fields=[START_TIME_INCREMENTAL],
        date_windowed=True,
    ),
    BUCKET_UTILIZATIONS: WasabiEndpointConfig(
        name=BUCKET_UTILIZATIONS,
        path="/v1/utilizations/buckets",
        primary_keys=["BucketUtilizationNum"],
        partition_key="StartTime",
        incremental_fields=[START_TIME_INCREMENTAL],
        date_windowed=True,
    ),
    SUB_ACCOUNT_INVOICES: WasabiEndpointConfig(
        name=SUB_ACCOUNT_INVOICES,
        path="/v1/accounts/{acct_num}/invoices",
        # SubInvoiceNum looks control-account-global, but Wasabi doesn't document its
        # uniqueness scope, so the parent account number stays in the key.
        primary_keys=["AcctNum", "SubInvoiceNum"],
        partition_key="CreateTime",
        parent=ACCOUNTS,
    ),
}

ENDPOINTS = tuple(WASABI_ENDPOINTS)

INCREMENTAL_FIELDS: dict[str, list[IncrementalField]] = {
    name: config.incremental_fields for name, config in WASABI_ENDPOINTS.items()
}

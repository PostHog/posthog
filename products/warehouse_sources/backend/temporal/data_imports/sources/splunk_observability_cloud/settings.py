from dataclasses import dataclass, field
from typing import Literal, Optional

from products.warehouse_sources.backend.types import IncrementalField, IncrementalFieldType

# Splunk Observability Cloud (formerly SignalFx) REST endpoints live under /v2 on the
# realm-scoped API host. Most list/search endpoints paginate with offset/limit and wrap
# results as {"count": N, "results": [...]}; a couple (incidents, detector events) return
# a bare JSON array instead. Shapes verified against the official signalfx-go client.
PAGE_SIZE = 100

# Splunk Observability Cloud returns at most 10,000 events per detector regardless of
# paging (documented retrieve-operation cap), so log when a detector hits it.
DETECTOR_EVENTS_RESULT_CAP = 10_000

# Hard stop for offset paging so a server that ignores `offset` can't loop us forever.
MAX_OFFSET = 1_000_000

# First metric_time_series sync pulls this window of datapoints when no watermark exists;
# subsequent incremental syncs resume from the stored timestamp watermark.
SIGNALFLOW_DEFAULT_LOOKBACK_DAYS = 30

ResponseStyle = Literal["wrapped", "list"]

_TIMESTAMP_INCREMENTAL_FIELD: list[IncrementalField] = [
    {
        "label": "timestamp",
        "type": IncrementalFieldType.DateTime,
        "field": "timestamp",
        "field_type": IncrementalFieldType.DateTime,
    },
]


@dataclass
class SplunkObservabilityCloudEndpointConfig:
    name: str
    # Path under /v2 on the realm API host. Empty for the SignalFlow-backed endpoint.
    path: str
    # "wrapped" = {"count": N, "results": [...]}; "list" = bare JSON array.
    response_style: ResponseStyle = "wrapped"
    primary_keys: list[str] = field(default_factory=lambda: ["id"])
    # Extra query params sent on every request (e.g. required search queries).
    extra_params: dict[str, str] = field(default_factory=dict)
    incremental_fields: list[IncrementalField] = field(default_factory=list)
    # Fan out one request per detector (path holds a {detector_id} placeholder).
    fan_out_over_detectors: bool = False
    # Rows stream from a SignalFlow computation instead of a REST list endpoint.
    uses_signalflow: bool = False
    # Stable datetime field to partition by; only endpoints whose rows are immutable
    # events set this (metadata objects are small enough to skip partitioning).
    partition_key: Optional[str] = None
    should_sync_default: bool = True


SPLUNK_OBSERVABILITY_CLOUD_ENDPOINTS: dict[str, SplunkObservabilityCloudEndpointConfig] = {
    "detectors": SplunkObservabilityCloudEndpointConfig(
        name="detectors",
        path="/detector",
    ),
    # Alert events generated by each detector. The API takes server-side `from`/`to`
    # epoch-ms bounds, so this endpoint is genuinely incremental on `timestamp`.
    "detector_events": SplunkObservabilityCloudEndpointConfig(
        name="detector_events",
        path="/detector/{detector_id}/events",
        response_style="list",
        # Event ids are not documented as globally unique, so the parent detector id is
        # part of the key. `detectorId` is already a field on every event row.
        primary_keys=["detectorId", "id"],
        incremental_fields=_TIMESTAMP_INCREMENTAL_FIELD,
        fan_out_over_detectors=True,
        partition_key="timestamp",
    ),
    "incidents": SplunkObservabilityCloudEndpointConfig(
        name="incidents",
        path="/incident",
        response_style="list",
        primary_keys=["incidentId"],
        # Without this flag the API only returns currently-active incidents.
        extra_params={"includeResolved": "true"},
    ),
    "alert_muting_rules": SplunkObservabilityCloudEndpointConfig(
        name="alert_muting_rules",
        path="/alertmuting",
    ),
    "dashboards": SplunkObservabilityCloudEndpointConfig(
        name="dashboards",
        path="/dashboard",
    ),
    "dashboard_groups": SplunkObservabilityCloudEndpointConfig(
        name="dashboard_groups",
        path="/dashboardgroup",
    ),
    "charts": SplunkObservabilityCloudEndpointConfig(
        name="charts",
        path="/chart",
    ),
    "teams": SplunkObservabilityCloudEndpointConfig(
        name="teams",
        path="/team",
    ),
    "organization_members": SplunkObservabilityCloudEndpointConfig(
        name="organization_members",
        path="/organization/member",
    ),
    # Metric metadata search. The search endpoints require an Elasticsearch-style query;
    # `name:*` is the documented way to match every metric by name.
    "metrics": SplunkObservabilityCloudEndpointConfig(
        name="metrics",
        path="/metric",
        primary_keys=["name"],
        extra_params={"query": "name:*"},
    ),
    # Dimension metadata search. Off by default: large orgs can have millions of
    # dimension values, and the `key:*` match-everything query is derived from the
    # documented ES query-string syntax but hasn't been verified against a live org.
    "dimensions": SplunkObservabilityCloudEndpointConfig(
        name="dimensions",
        path="/dimension",
        primary_keys=["key", "value"],
        extra_params={"query": "key:*"},
        should_sync_default=False,
    ),
    # Metric datapoints streamed from a SignalFlow computation (SSE), windowed by
    # start/stop epoch-ms params — incremental on the datapoint timestamp. Requires the
    # optional "SignalFlow program" source field, so it's off by default.
    "metric_time_series": SplunkObservabilityCloudEndpointConfig(
        name="metric_time_series",
        path="",
        primary_keys=["tsId", "timestamp"],
        incremental_fields=_TIMESTAMP_INCREMENTAL_FIELD,
        uses_signalflow=True,
        partition_key="timestamp",
        should_sync_default=False,
    ),
}

ENDPOINTS = tuple(SPLUNK_OBSERVABILITY_CLOUD_ENDPOINTS.keys())

INCREMENTAL_FIELDS: dict[str, list[IncrementalField]] = {
    name: config.incremental_fields
    for name, config in SPLUNK_OBSERVABILITY_CLOUD_ENDPOINTS.items()
    if config.incremental_fields
}

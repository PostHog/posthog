import { MakeLogicType, actions, afterMount, kea, listeners, path, reducers } from 'kea'
import { forms } from 'kea-forms'
import { loaders } from 'kea-loaders'

import { lemonToast } from '@posthog/lemon-ui'

import { ApiConfig } from 'lib/api'

import { outcomesCreate, outcomesDestroy, outcomesList } from './generated/api'
import type { OutcomeDefinitionApi } from './generated/api.schemas'

export interface NewOutcomeForm {
    name: string
    description: string
    target_event: string
    threshold: number
}

const NEW_OUTCOME: NewOutcomeForm = { name: '', description: '', target_event: '', threshold: 1 }

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface outcomesLogicValues {
    isNewOutcomeModalOpen: boolean
    outcomes: OutcomeDefinitionApi[]
    outcomesLoading: boolean
    newOutcome: NewOutcomeForm
    isNewOutcomeSubmitting: boolean
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface outcomesLogicActions {
    closeNewOutcomeModal: () => {
        value: true
    }
    deleteOutcome: (id: string) => {
        id: string
    }
    loadOutcomes: () => any
    loadOutcomesFailure: (
        error: string,
        errorObject?: any
    ) => {
        error: string
        errorObject?: any
    }
    loadOutcomesSuccess: (
        outcomes: OutcomeDefinitionApi[],
        payload?: any
    ) => {
        outcomes: OutcomeDefinitionApi[]
        payload?: any
    }
    openNewOutcomeModal: () => {
        value: true
    }
    setNewOutcomeValue: (
        key: string,
        value: any
    ) => {
        name: string
        value: any
    }
    setNewOutcomeValues: (values: Partial<NewOutcomeForm>) => {
        values: Partial<NewOutcomeForm>
    }
    submitNewOutcome: () => {
        value: true
    }
    submitNewOutcomeSuccess: (newOutcome: NewOutcomeForm) => {
        newOutcome: NewOutcomeForm
    }
    submitNewOutcomeFailure: (error: Error) => {
        error: Error
    }
    resetNewOutcome: (values?: NewOutcomeForm) => {
        values?: NewOutcomeForm
    }
}

export type outcomesLogicType = MakeLogicType<outcomesLogicValues, outcomesLogicActions>

export const outcomesLogic = kea<outcomesLogicType>([
    path(['products', 'outcomes', 'frontend', 'outcomesLogic']),
    actions({
        openNewOutcomeModal: true,
        closeNewOutcomeModal: true,
        deleteOutcome: (id: string) => ({ id }),
    }),
    reducers({
        isNewOutcomeModalOpen: [
            false,
            {
                openNewOutcomeModal: () => true,
                closeNewOutcomeModal: () => false,
                submitNewOutcomeSuccess: () => false,
            },
        ],
    }),
    loaders(() => ({
        outcomes: [
            [] as OutcomeDefinitionApi[],
            {
                loadOutcomes: async () => {
                    const response = await outcomesList(String(ApiConfig.getCurrentTeamId()))
                    return response.results
                },
            },
        ],
    })),
    forms(({ actions }) => ({
        newOutcome: {
            defaults: NEW_OUTCOME,
            errors: ({ name, target_event, threshold }: NewOutcomeForm) => ({
                name: name ? undefined : 'An outcome needs a name',
                target_event: target_event ? undefined : 'Pick the event to count',
                threshold: threshold >= 1 ? undefined : 'The threshold must be at least 1',
            }),
            submit: async (formValues: NewOutcomeForm) => {
                await outcomesCreate(String(ApiConfig.getCurrentTeamId()), formValues)
                lemonToast.success('Outcome created. It will be calculated within 30 minutes.')
                actions.resetNewOutcome()
                actions.loadOutcomes()
            },
        },
    })),
    listeners(({ actions }) => ({
        deleteOutcome: async ({ id }) => {
            await outcomesDestroy(String(ApiConfig.getCurrentTeamId()), id)
            lemonToast.success('Outcome deleted')
            actions.loadOutcomes()
        },
    })),
    afterMount(({ actions }) => {
        actions.loadOutcomes()
    }),
])

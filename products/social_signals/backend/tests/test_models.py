@pytest.mark.django_db(databases=PRODUCT_DATABASES)
def test_manager_is_fail_closed(team):
    """Reading without team_scope must raise — that's the whole point of
    ProductTeamModel. The autouse fixture sets scope; we explicitly drop it
    here via the ``unscoped()`` context manager to verify the closed manager
    fires when team context is None (not when using the explicit escape hatch
    queryset method)."""
    from posthog.models.scoping import set_current_team_id, reset_current_team_id

    token = set_current_team_id(None)
    try:
        with pytest.raises(TeamScopeError):
            list(MentionSource.objects.all())
    finally:
        reset_current_team_id(token)

import uuid

import pytest
from django.db import IntegrityError

from posthog.models.scoping.manager import TeamScopeError
from products.social_signals.backend.facade.enums import (
    AnalyzerKind,
    MentionType,
    Platform,
    SourceKind,
)
from products.social_signals.backend.models import (
    Mention,
    MentionAnalysis,
    MentionSource,
)

PRODUCT_DATABASES = {"default", "social_signals_db_writer", "social_signals_db_reader"}


@pytest.mark.django_db(databases=PRODUCT_DATABASES)
class TestMentionSource:
    def test_token_autogenerated(self, team):
        source = MentionSource.objects.create(team_id=team.id, kind=SourceKind.OCTOLENS.value)
        assert source.ingest_token
        assert len(source.ingest_token) >= 32

    def test_unique_per_team_kind(self, team):
        MentionSource.objects.create(team_id=team.id, kind=SourceKind.OCTOLENS.value)
        with pytest.raises(IntegrityError):
            MentionSource.objects.create(team_id=team.id, kind=SourceKind.OCTOLENS.value)

    def test_rotate_token_returns_new_value(self, team):
        source = MentionSource.objects.create(team_id=team.id, kind=SourceKind.OCTOLENS.value)
        original = source.ingest_token
        new_token = source.rotate_token()
        assert new_token != original
        source.refresh_from_db()
        assert source.ingest_token == new_token


@pytest.mark.django_db(databases=PRODUCT_DATABASES)
class TestMention:
    @pytest.fixture
    def source(self, team):
        return MentionSource.objects.create(team_id=team.id, kind=SourceKind.OCTOLENS.value)

    def test_unique_per_source_external_id(self, team, source):
        Mention.objects.create(
            team_id=team.id,
            source=source,
            external_id="abc",
            platform=Platform.X.value,
            mention_type=MentionType.POST.value,
        )
        with pytest.raises(IntegrityError):
            Mention.objects.create(
                team_id=team.id,
                source=source,
                external_id="abc",
                platform=Platform.REDDIT.value,
                mention_type=MentionType.POST.value,
            )

    def test_id_is_uuid(self, team, source):
        mention = Mention.objects.create(
            team_id=team.id,
            source=source,
            external_id="xyz",
            platform=Platform.X.value,
            mention_type=MentionType.POST.value,
        )
        assert isinstance(mention.id, uuid.UUID)



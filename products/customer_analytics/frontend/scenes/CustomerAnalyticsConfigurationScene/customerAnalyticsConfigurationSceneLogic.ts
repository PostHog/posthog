import { MakeLogicType, actions, connect, kea, path, props, reducers, selectors } from 'kea'
import { actionToUrl, router, urlToAction } from 'kea-router'

import { Scene } from 'scenes/sceneTypes'
import { settingsLogic } from 'scenes/settings/settingsLogic'
import { urls } from 'scenes/urls'

import { Breadcrumb } from '~/types'

import { CUSTOMER_ANALYTICS_LOGIC_KEY } from '../../utils'

export type ConfigurationSceneTabType =
    | 'group-analytics'
    | 'customer-analytics-usage-metrics'
    | 'customer-analytics-dashboard-events'
    | 'customer-analytics-accounts'

export interface CustomerAnalyticsConfigurationSceneLogicProps {
    initialTab?: ConfigurationSceneTabType
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface customerAnalyticsConfigurationSceneLogicValues {
    breadcrumbs: Breadcrumb[];
    tab: ConfigurationSceneTabType;
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface customerAnalyticsConfigurationSceneLogicActions {
    selectSetting: (setting: import("scenes/settings/types").SettingId) => {
        setting: import("scenes/settings/types").SettingId;
    }; // settingsLogic
    setTab: (tab: ConfigurationSceneTabType) => {
        tab: ConfigurationSceneTabType;
    };
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface customerAnalyticsConfigurationSceneLogicMeta {
    __keaTypeGenInternalSelectorTypes: {
        breadcrumbs: (tab: ConfigurationSceneTabType) => Breadcrumb[];
    };
}

export type customerAnalyticsConfigurationSceneLogicType = MakeLogicType<customerAnalyticsConfigurationSceneLogicValues, customerAnalyticsConfigurationSceneLogicActions, CustomerAnalyticsConfigurationSceneLogicProps, customerAnalyticsConfigurationSceneLogicMeta>;

export const customerAnalyticsConfigurationSceneLogic = kea<customerAnalyticsConfigurationSceneLogicType>([
    path(['scenes', 'customer-analytics', 'configuration', 'customerAnalyticsConfigurationSceneLogic']),
    props({} as CustomerAnalyticsConfigurationSceneLogicProps),

    connect(({ initialTab }: CustomerAnalyticsConfigurationSceneLogicProps) => ({
        actions: [
            settingsLogic({
                logicKey: CUSTOMER_ANALYTICS_LOGIC_KEY,
                sectionId: 'environment-customer-analytics',
                settingId: initialTab || 'customer-analytics-dashboard-events',
            }),
            ['selectSetting'],
        ],
    })),

    actions({
        setTab: (tab: ConfigurationSceneTabType) => ({ tab }),
    }),

    reducers(({ props }) => ({
        tab: [
            props.initialTab as ConfigurationSceneTabType,
            {
                setTab: (_, { tab }) => tab,
            },
        ],
    })),

    selectors({
        breadcrumbs: [
            (s) => [s.tab],
            (tab: ConfigurationSceneTabType): Breadcrumb[] => [
                {
                    key: Scene.CustomerAnalytics,
                    path:
                        tab === 'customer-analytics-accounts'
                            ? urls.customerAnalyticsAccounts()
                            : urls.customerAnalytics(),
                    name: 'Customer analytics',
                    iconType: 'cohort',
                },
                {
                    key: Scene.CustomerAnalyticsConfiguration,
                    path: urls.customerAnalyticsConfiguration(),
                    name: 'Configuration',
                    iconType: 'cohort',
                },
            ],
        ],
    }),

    actionToUrl({
        selectSetting: ({ setting }) => {
            const { currentLocation } = router.values

            return [
                currentLocation.pathname,
                { ...currentLocation.searchParams, tab: setting },
                currentLocation.hashParams,
            ]
        },
    }),

    urlToAction(({ actions, values }) => ({
        [urls.customerAnalyticsConfiguration()]: (_, searchParams) => {
            const tab = searchParams.tab as ConfigurationSceneTabType | undefined
            if (tab && tab !== values.tab) {
                actions.setTab(tab)
            }
        },
    })),
])

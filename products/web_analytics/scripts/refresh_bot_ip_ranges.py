#!/usr/bin/env python3
"""Refresh the bot IP ranges vendored in bot_ip_networks.py.

Crawler operators publish the IP ranges their bots run from as machine-readable
JSON (all use Google's `prefixes` format). Some of that traffic (notably
Google's mobile rendering service and ChatGPT's browsing) uses real browser
user agents with no bot token, so IP ranges are the only reliable
classification signal. This script fetches the published lists, collapses
adjacent CIDRs, and rewrites
products/web_analytics/backend/hogql_queries/bot_ip_networks.py.

Only operator-published, crawler-dedicated ranges belong here — never general
datacenter/cloud egress (AWS, Cloudflare, GCP run countless real applications
and residential-facing services). That is also why we do NOT vendor Google's
user-triggered-fetchers.json: those ranges are Google Cloud egress that third
parties can run arbitrary workloads (including VPNs and proxies serving real
users) behind, so matching them would misclassify human traffic.

Anthropic publishes no crawler IP list (only API egress IPs) — its crawlers
are covered by UA patterns and Web Bot Auth signature agents instead.

Usage, from the repo root:
    python products/web_analytics/scripts/refresh_bot_ip_ranges.py

Then review the diff, run the bot IP tests, and commit.
"""

import sys
import json
import ipaddress
import urllib.request
from pathlib import Path

# Sanity floors: a published range wider than this is almost certainly an
# upstream mistake (or a policy change that deserves a human decision), since
# it would classify an enormous slice of the internet as bot traffic.
MIN_V4_PREFIXLEN = 16
MIN_V6_PREFIXLEN = 32

PROVIDERS: dict[str, str] = {
    "GOOGLE_COMMON_CRAWLER_NETWORKS": "https://developers.google.com/static/crawling/ipranges/common-crawlers.json",
    "GOOGLE_SPECIAL_CRAWLER_NETWORKS": "https://developers.google.com/static/crawling/ipranges/special-crawlers.json",
    "GOOGLE_USER_TRIGGERED_FETCHER_NETWORKS": "https://developers.google.com/static/crawling/ipranges/user-triggered-fetchers-google.json",
    "BINGBOT_NETWORKS": "https://www.bing.com/toolbox/bingbot.json",
    "APPLEBOT_NETWORKS": "https://search.developer.apple.com/applebot.json",
    "OPENAI_GPTBOT_NETWORKS": "https://openai.com/gptbot.json",
    "OPENAI_CHATGPT_USER_NETWORKS": "https://openai.com/chatgpt-user.json",
    "OPENAI_SEARCHBOT_NETWORKS": "https://openai.com/searchbot.json",
    "PERPLEXITYBOT_NETWORKS": "https://www.perplexity.com/perplexitybot.json",
    "PERPLEXITY_USER_NETWORKS": "https://www.perplexity.com/perplexity-user.json",
    "AHREFSBOT_NETWORKS": "https://api.ahrefs.com/v3/public/crawler-ip-ranges",
}

OUTPUT_PATH = Path(__file__).resolve().parents[1] / "backend" / "hogql_queries" / "bot_ip_networks.py"


def fetch_networks(url: str) -> list[ipaddress.IPv4Network | ipaddress.IPv6Network]:
    # nosemgrep: python.lang.security.audit.dynamic-urllib-use-detected.dynamic-urllib-use-detected (urls come from the hardcoded PROVIDERS constant, never user input)
    with urllib.request.urlopen(url, timeout=30) as response:
        data = json.load(response)
    prefixes = data.get("prefixes")
    if not prefixes:
        raise ValueError(f"{url} returned no prefixes — upstream format may have changed")
    networks: list[ipaddress.IPv4Network | ipaddress.IPv6Network] = []
    for entry in prefixes:
        cidr = entry.get("ipv4Prefix") or entry.get("ipv6Prefix")
        if cidr is None:
            raise ValueError(f"{url} entry has neither ipv4Prefix nor ipv6Prefix: {entry}")
        networks.append(ipaddress.ip_network(cidr))
    return networks


def collapse(networks: list[ipaddress.IPv4Network | ipaddress.IPv6Network]) -> list[str]:
    v4 = sorted(ipaddress.collapse_addresses(n for n in networks if isinstance(n, ipaddress.IPv4Network)))
    v6 = sorted(ipaddress.collapse_addresses(n for n in networks if isinstance(n, ipaddress.IPv6Network)))
    for v4_net in v4:
        if v4_net.prefixlen < MIN_V4_PREFIXLEN:
            raise ValueError(f"IPv4 range {v4_net} wider than /{MIN_V4_PREFIXLEN} floor — refusing")
    for v6_net in v6:
        if v6_net.prefixlen < MIN_V6_PREFIXLEN:
            raise ValueError(f"IPv6 range {v6_net} wider than /{MIN_V6_PREFIXLEN} floor — refusing")
    return [str(n) for n in [*v4, *v6]]


def main() -> int:
    sections: list[str] = []
    for const_name, url in PROVIDERS.items():
        raw = fetch_networks(url)
        collapsed = collapse(raw)
        print(f"{const_name}: {len(raw)} published prefixes -> {len(collapsed)} collapsed networks")  # noqa: T201
        lines = "\n".join(f'    "{cidr}",' for cidr in collapsed)
        sections.append(f"# {url}\n{const_name}: tuple[str, ...] = (\n{lines}\n)")

    body = "\n\n".join(sections)
    OUTPUT_PATH.write_text(
        "# Generated by products/web_analytics/scripts/refresh_bot_ip_ranges.py — do not edit by hand.\n"
        "# Collapsed CIDR ranges of operator-published crawler IPs; see that script for the\n"
        "# source URLs and for why general cloud/datacenter egress is deliberately excluded.\n\n"
        f"{body}\n"
    )
    print(f"wrote {OUTPUT_PATH}")  # noqa: T201
    return 0


if __name__ == "__main__":
    sys.exit(main())

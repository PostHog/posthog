"""Generate the frontend TypeScript for the signal taxonomy from the backend Pydantic contracts.

This is the "no serializer" codegen slice. Signal extras are read from ClickHouse and never pass
through a DRF serializer, so the OpenAPI/Orval pipeline can't see them. Instead we go straight from
`contracts.py` (Pydantic) and `enums.py` (StrEnum) to TypeScript:

  - StrEnums  -> runtime `export enum` (the inbox uses them as values/keys, not just types)
  - BaseModels -> `export interface`
  - the SignalInput union -> `export type`

Run via `hogli build:signals-types` (wired into `build:openapi`, the backend->frontend codegen).
Output is checked into `frontend/src/generated/signals.ts`; CI fails if it drifts from the contracts.
"""

import enum
import types
import typing
import inspect
from pathlib import Path

from products.signals.backend import contracts, enums

OUTPUT = Path(__file__).resolve().parents[3] / "frontend" / "src" / "generated" / "signals.ts"

HEADER = """// DO NOT EDIT THIS FILE DIRECTLY.
// Generated from products/signals/backend/contracts.py + enums.py by
// products/signals/backend/build_frontend_types.py. Run `hogli build:signals-types` to regenerate.
// Signal extras are ClickHouse-sourced (no DRF serializer), so they use the Pydantic -> TS path.
"""

# Enums emitted as runtime TS enums, in this order.
ENUMS: list[type[enum.Enum]] = [enums.SignalSourceProduct, enums.SignalSourceType]

# Named TS type aliases, matched structurally against field annotations so the field references the
# alias name instead of inlining the literal union.
NAMED_ALIASES: dict[str, object] = {
    "HealthCheckSeverity": contracts.HealthCheckSeverity,
}


def _model_classes() -> list[type]:
    """All Pydantic models defined in contracts.py, in definition order."""
    out: list[type] = []
    for obj in vars(contracts).values():
        if (
            inspect.isclass(obj)
            and issubclass(obj, contracts.BaseModel)
            and obj.__module__ == contracts.__name__
            and obj is not contracts.BaseModel
        ):
            out.append(obj)
    return out


def _ts_literal(value: object) -> str:
    if isinstance(value, enum.Enum):
        value = value.value
    if isinstance(value, str):
        return f"'{value}'"
    if isinstance(value, bool):
        return "true" if value else "false"
    return str(value)


def _ts_type(annotation: object, skip_named: str | None = None) -> str:
    # Named alias short-circuit (e.g. HealthCheckSeverity) — skipped when emitting that alias itself.
    for name, alias in NAMED_ALIASES.items():
        if name != skip_named and annotation == alias:
            return name

    origin = typing.get_origin(annotation)
    args = typing.get_args(annotation)

    if origin is typing.Literal:
        return " | ".join(_ts_literal(a) for a in args)

    if origin in (typing.Union, types.UnionType):
        non_none = [a for a in args if a is not type(None)]
        return " | ".join(_ts_type(a) for a in non_none)

    if origin in (list, set, tuple):
        inner = _ts_type(args[0]) if args else "unknown"
        return f"{inner}[]"

    if origin is dict:
        value_type = _ts_type(args[1]) if len(args) == 2 else "unknown"
        return f"Record<string, {value_type}>"

    if annotation is str:
        return "string"
    if annotation is bool:
        return "boolean"
    if annotation in (int, float):
        return "number"
    if annotation is object or annotation is typing.Any:
        return "unknown"

    if inspect.isclass(annotation) and issubclass(annotation, contracts.BaseModel):
        return annotation.__name__

    raise TypeError(f"Unmapped annotation: {annotation!r}")


def _emit_enum(cls: type[enum.Enum]) -> str:
    lines = [f"export enum {cls.__name__} {{"]
    for member in cls:
        lines.append(f"    {member.name} = '{member.value}',")
    lines.append("}")
    return "\n".join(lines)


def _emit_interface(model: type) -> str:
    if not model.model_fields:
        return f"export interface {model.__name__} {{}}"
    lines = [f"export interface {model.__name__} {{"]
    for name, field in model.model_fields.items():
        annotation = field.annotation
        args = typing.get_args(annotation)
        is_optional = typing.get_origin(annotation) in (typing.Union, types.UnionType) and type(None) in args
        ts = _ts_type(annotation)
        if not field.is_required():
            lines.append(f"    {name}?: {ts}")
        elif is_optional:
            lines.append(f"    {name}: {ts} | null")
        else:
            lines.append(f"    {name}: {ts}")
    lines.append("}")
    return "\n".join(lines)


def _emit_union() -> str:
    variants = " | ".join(v.__name__ for v in contracts.SIGNAL_INPUT_VARIANTS)
    return f"export type SignalInput = {variants}"


def render() -> str:
    blocks: list[str] = [HEADER.rstrip()]
    for cls in ENUMS:
        blocks.append(_emit_enum(cls))
    for name, alias in NAMED_ALIASES.items():
        blocks.append(f"export type {name} = {_ts_type(alias, skip_named=name)}")
    for model in _model_classes():
        blocks.append(_emit_interface(model))
    blocks.append(_emit_union())
    return "\n\n".join(blocks) + "\n"


def main() -> None:
    OUTPUT.write_text(render())
    print(f"Wrote {OUTPUT}")  # noqa: T201 — build-time CLI output


if __name__ == "__main__":
    main()

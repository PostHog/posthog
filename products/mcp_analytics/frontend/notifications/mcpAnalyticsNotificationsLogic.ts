import { CyclotronJobFiltersType, HogFunctionSubTemplateIdType, HogFunctionType } from '~/types'

const MCP_NOTIFICATION_PAGE_LIMIT = 200
const MCP_NOTIFICATION_MAX_PAGES = 10
const MCP_NOTIFICATION_SUB_TEMPLATE_IDS: HogFunctionSubTemplateIdType[] = ['mcp-missing-capability', 'mcp-tool-error']

function getMCPNotificationFilterGroups(): CyclotronJobFiltersType[] {
    return MCP_NOTIFICATION_SUB_TEMPLATE_IDS.map(getFiltersFromSubTemplateId).filter(
        (filters): filters is CyclotronJobFiltersType => !!filters
    )
}

export type MCPNotificationUseCase = 'missing-capability' | 'tool-error'

export function getMCPNotificationUseCase(fn: Pick<HogFunctionType, 'filters'>): MCPNotificationUseCase | null {
    const eventIds = new Set((fn.filters?.events ?? []).map((event) => event.id))

    if (eventIds.has('$mcp_missing_capability')) {
        return 'missing-capability'
    }
    if (eventIds.has('$mcp_tool_call')) {
        return 'tool-error'
    }
    return null
}


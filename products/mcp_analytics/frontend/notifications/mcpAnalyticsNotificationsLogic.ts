import { MakeLogicType, actions, afterMount, connect, kea, listeners, path, reducers } from 'kea'
import { loaders } from 'kea-loaders'

import { lemonToast } from '@posthog/lemon-ui'

import api from 'lib/api'
import posthog from 'lib/posthog-typed'
import { deleteWithUndo } from 'lib/utils/deleteWithUndo'
import { getFiltersFromSubTemplateId } from 'scenes/hog-functions/list/LinkedHogFunctions'
import { projectLogic } from 'scenes/projectLogic'

import { CyclotronJobFiltersType, HogFunctionSubTemplateIdType, HogFunctionType } from '~/types'

const MCP_NOTIFICATION_LIST_LIMIT = 200
const MCP_NOTIFICATION_SUB_TEMPLATE_IDS: HogFunctionSubTemplateIdType[] = ['mcp-missing-capability', 'mcp-tool-error']

export type MCPNotificationUseCase = 'missing-capability' | 'tool-error'

export function getMCPNotificationUseCase(fn: Pick<HogFunctionType, 'filters'>): MCPNotificationUseCase | null {
    const eventIds = new Set((fn.filters?.events ?? []).map((event) => event.id))

    if (eventIds.has('$mcp_missing_capability')) {
        return 'missing-capability'
    }
    if (eventIds.has('$mcp_tool_call')) {
        return 'tool-error'
    }
    return null
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface mcpAnalyticsNotificationsLogicValues {
    currentProjectId: number | null // projectLogic
    notifications: HogFunctionType[]
    notificationsFailed: boolean
    notificationsLoading: boolean
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface mcpAnalyticsNotificationsLogicActions {
    deleteNotification: (notification: HogFunctionType) => {
        notification: HogFunctionType
    }
    loadNotifications: () => any
    loadNotificationsFailure: (
        error: string,
        errorObject?: any
    ) => {
        error: string
        errorObject?: any
    }
    loadNotificationsSuccess: (
        notifications: HogFunctionType[],
        payload?: any
    ) => {
        notifications: HogFunctionType[]
        payload?: any
    }
    toggleNotificationEnabled: (
        notificationId: string,
        enabled: boolean
    ) => {
        enabled: boolean
        notificationId: string
    }
}

export type mcpAnalyticsNotificationsLogicType = MakeLogicType<
    mcpAnalyticsNotificationsLogicValues,
    mcpAnalyticsNotificationsLogicActions
>

export const mcpAnalyticsNotificationsLogic = kea<mcpAnalyticsNotificationsLogicType>([
    path(['products', 'mcp_analytics', 'frontend', 'notifications', 'mcpAnalyticsNotificationsLogic']),
    connect(() => ({
        values: [projectLogic, ['currentProjectId']],
    })),
    actions({
        toggleNotificationEnabled: (notificationId: string, enabled: boolean) => ({ notificationId, enabled }),
        deleteNotification: (notification: HogFunctionType) => ({ notification }),
    }),
    loaders(() => ({
        notifications: [
            [] as HogFunctionType[],
            {
                loadNotifications: async (): Promise<HogFunctionType[]> => {
                    const filterGroups = MCP_NOTIFICATION_SUB_TEMPLATE_IDS.map(getFiltersFromSubTemplateId).filter(
                        (filters): filters is CyclotronJobFiltersType => !!filters
                    )
                    const response = await api.hogFunctions.list({
                        filter_groups: filterGroups,
                        types: ['destination'],
                        full: true,
                        limit: MCP_NOTIFICATION_LIST_LIMIT,
                    })

                    return response.results.filter((notification) => !notification.deleted)
                },
            },
        ],
    })),
    reducers({
        notificationsFailed: [
            false,
            {
                loadNotifications: () => false,
                loadNotificationsSuccess: () => false,
                loadNotificationsFailure: () => true,
            },
        ],
    }),
    listeners(({ actions, values }) => ({
        toggleNotificationEnabled: async ({ notificationId, enabled }) => {
            const target = values.notifications.find((notification) => notification.id === notificationId)
            if (!target) {
                return
            }
            const previousEnabled = target.enabled

            const applyEnabled = (next: boolean): void => {
                actions.loadNotificationsSuccess(
                    values.notifications.map((notification) =>
                        notification.id === notificationId ? { ...notification, enabled: next } : notification
                    )
                )
            }

            applyEnabled(enabled)

            try {
                await api.hogFunctions.update(notificationId, { enabled })
            } catch (error) {
                applyEnabled(previousEnabled)
                lemonToast.error('Failed to update notification')
                posthog.captureException(error, {
                    action: 'toggle-mcp-analytics-notification',
                    notification: notificationId,
                })
            }
        },
        deleteNotification: async ({ notification }) => {
            const previous = values.notifications
            actions.loadNotificationsSuccess(previous.filter((item) => item.id !== notification.id))

            let callbackFired = false
            await deleteWithUndo({
                endpoint: `projects/${values.currentProjectId}/hog_functions`,
                object: { id: notification.id, name: notification.name },
                callback: (undo) => {
                    callbackFired = true
                    if (undo) {
                        actions.loadNotifications()
                    }
                },
            })

            if (!callbackFired) {
                actions.loadNotificationsSuccess(previous)
            }
        },
    })),
    afterMount(({ actions }) => {
        actions.loadNotifications()
    }),
])

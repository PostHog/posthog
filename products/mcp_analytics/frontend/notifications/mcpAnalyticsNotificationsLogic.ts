export const mcpAnalyticsNotificationsLogic = kea<mcpAnalyticsNotificationsLogicType>([
    path(['products', 'mcp_analytics', 'frontend', 'notifications', 'mcpAnalyticsNotificationsLogic']),
    connect(() => ({
        values: [projectLogic, ['currentProjectId']],
    })),
    actions({
        toggleNotificationEnabled: (notificationId: string, enabled: boolean) => ({ notificationId, enabled }),
        toggleNotificationEnabledStarted: (notificationId: string) => ({ notificationId }),
        toggleNotificationEnabledSettled: (notificationId: string) => ({ notificationId }),
        deleteNotification: (notification: HogFunctionType) => ({ notification }),
        deleteNotificationStarted: (notificationId: string) => ({ notificationId }),
        deleteNotificationSettled: (notificationId: string) => ({ notificationId }),
    }),
    loaders(({ values }) => ({
        notificationCount: [
            0,
            {
                loadNotificationCount: async (): Promise<number> => {
                    const response = await api.hogFunctions.list({
                        filter_groups: getMCPNotificationFilterGroups(),
                        types: ['destination'],
                        limit: 1,
                    })

                    return response.count
                },
            },
        ],
        notifications: [
            [] as HogFunctionType[],
            {
                loadNotifications: async (): Promise<HogFunctionType[]> => {
                    const notifications: HogFunctionType[] = []

                    // A reload can race in-flight mutations: a GET that read the DB before a
                    // toggle/delete landed would otherwise revert the toggle or resurrect the
                    // deleted row. Keep the local value for rows with a pending mutation.
                    const reconcile = (fetched: HogFunctionType[]): HogFunctionType[] =>
                        fetched
                            .filter((notification) => !values.pendingDeleteIds[notification.id])
                            .map((notification) => {
                                if (!values.pendingToggleIds[notification.id]) {
                                    return notification
                                }
                                const local = values.notifications.find((item) => item.id === notification.id)
                                return local ? { ...notification, enabled: local.enabled } : notification
                            })

                    for (let page = 0; page < MCP_NOTIFICATION_MAX_PAGES; page++) {
                        const response = await api.hogFunctions.list({
                            filter_groups: getMCPNotificationFilterGroups(),
                            types: ['destination'],
                            full: true,
                            limit: MCP_NOTIFICATION_PAGE_LIMIT,
                            offset: page * MCP_NOTIFICATION_PAGE_LIMIT,
                        })
                        notifications.push(...response.results.filter((notification) => !notification.deleted))

                        if (!response.next) {
                            return reconcile(notifications)
                        }
                    }

                    throw new Error(`MCP notification list exceeded ${MCP_NOTIFICATION_MAX_PAGES} pages`)
                },
            },
        ],
    })),
    reducers({
        pendingToggleIds: [
            {} as Record<string, true>,
            {
                toggleNotificationEnabledStarted: (state, { notificationId }) => ({
                    ...state,
                    [notificationId]: true,
                }),
                toggleNotificationEnabledSettled: (state, { notificationId }) => {
                    const nextState = { ...state }
                    delete nextState[notificationId]
                    return nextState
                },
            },
        ],
        pendingDeleteIds: [
            {} as Record<string, true>,
            {
                deleteNotificationStarted: (state, { notificationId }) => ({
                    ...state,
                    [notificationId]: true,
                }),
                deleteNotificationSettled: (state, { notificationId }) => {
                    const nextState = { ...state }
                    delete nextState[notificationId]
                    return nextState
                },
            },
        ],
        notificationsFailed: [
            false,
            {
                loadNotifications: () => false,
                loadNotificationsSuccess: () => false,
                loadNotificationsFailure: () => true,
            },
        ],
        // The list loads lazily when the tab mounts; without this flag the tab's first
        // render (before the load kicks off) would flash the empty state.
        notificationsLoaded: [
            false,
            {
                loadNotificationsSuccess: () => true,
                loadNotificationsFailure: () => true,
            },
        ],
    }),
    listeners(({ actions, values }) => ({
        loadNotificationsSuccess: ({ notifications }) => {
            actions.loadNotificationCountSuccess(notifications.length)
        },
        toggleNotificationEnabled: async ({ notificationId, enabled }) => {
            if (values.pendingToggleIds[notificationId]) {
                return
            }

            const target = values.notifications.find((notification) => notification.id === notificationId)
            if (!target) {
                return
            }
            actions.toggleNotificationEnabledStarted(notificationId)
            const previousEnabled = target.enabled

            const applyEnabled = (next: boolean): void => {
                actions.loadNotificationsSuccess(
                    values.notifications.map((notification) =>
                        notification.id === notificationId ? { ...notification, enabled: next } : notification
                    )
                )
            }

            applyEnabled(enabled)

            try {
                const updatedNotification = await api.hogFunctions.update(notificationId, { enabled })
                actions.loadNotificationsSuccess(
                    values.notifications.map((notification) =>
                        notification.id === notificationId ? updatedNotification : notification
                    )
                )
            } catch (error) {
                applyEnabled(previousEnabled)
                lemonToast.error('Failed to update notification')
                posthog.captureException(error, {
                    action: 'toggle-mcp-analytics-notification',
                    notification: notificationId,
                })
            } finally {
                actions.toggleNotificationEnabledSettled(notificationId)
            }
        },
        deleteNotification: async ({ notification }) => {
            actions.deleteNotificationStarted(notification.id)
            actions.loadNotificationsSuccess(values.notifications.filter((item) => item.id !== notification.id))

            let callbackFired = false
            await deleteWithUndo({
                endpoint: `projects/${values.currentProjectId}/hog_functions`,
                object: { id: notification.id, name: notification.name },
                callback: (undo) => {
                    callbackFired = true
                    // Settle before reloading so the undone row isn't filtered back out
                    actions.deleteNotificationSettled(notification.id)
                    if (undo) {
                        actions.loadNotifications()
                    }
                },
            })

            if (!callbackFired) {
                actions.deleteNotificationSettled(notification.id)
                actions.loadNotifications()
            }
        },
    })),
    afterMount(({ actions }) => {
        actions.loadNotificationCount()
    }),
])

const SCENE_KEY_TO_TAB: Record<string, MCPAnalyticsTab> = {
    mcpAnalyticsActivity: 'activity',
    mcpAnalyticsDashboard: 'dashboard',
    mcpAnalyticsSessions: 'sessions',
    mcpAnalyticsToolQuality: 'tool-quality',
    mcpAnalyticsIntentClustering: 'intent-clustering',
    mcpAnalyticsNotifications: 'notifications',
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface mcpAnalyticsSceneLogicValues {
    sceneKey: string | null // sceneLogic
    activeTab: MCPAnalyticsTab
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface mcpAnalyticsSceneLogicMeta {
    __keaTypeGenInternalSelectorTypes: {
        activeTab: (sceneKey: string | null) => MCPAnalyticsTab
    }
}

export type mcpAnalyticsSceneLogicType = MakeLogicType<
    mcpAnalyticsSceneLogicValues,
    {},
    Record<string, any>,
    mcpAnalyticsSceneLogicMeta
>

export const mcpAnalyticsSceneLogic = kea<mcpAnalyticsSceneLogicType>([
    path(['products', 'mcp_analytics', 'frontend', 'mcpAnalyticsSceneLogic']),
    connect(() => ({
        values: [sceneLogic, ['sceneKey']],
    })),
    selectors({
        activeTab: [
            (s) => [s.sceneKey],
            (sceneKey: string): MCPAnalyticsTab => SCENE_KEY_TO_TAB[sceneKey] ?? 'dashboard',
        ],
    }),
    listeners(({ values, cache }) => ({
        // Never-set-up projects stay in-scene: the scene's `emptyState` (SceneExport)
        // renders the setup screen right here until data arrives, so there's no
        // redirect into the app-wide onboarding flow.
        [mcpAnalyticsOnboardingLogic.actionTypes.loadSignalsSuccess]: () => {
            // Volume decides the default landing tab. Only fires when the manifest's
            // bare-URL redirect tagged the navigation with `landing=auto` — a deep link
            // to /dashboard never re-routes, and an explicit tab click is never overridden.
            const { landing, ...restParams } = router.values.searchParams
            if (landing === 'auto' && values.activeTab === 'dashboard' && !cache.landingResolved) {
                cache.landingResolved = true
                const target =
                    mcpAnalyticsOnboardingLogic.values.dashboardStage === 'activity'
                        ? urls.mcpAnalyticsActivity()
                        : urls.mcpAnalyticsDashboard()
                router.actions.replace(combineUrl(target, restParams).url)
            }
        },
    })),
])

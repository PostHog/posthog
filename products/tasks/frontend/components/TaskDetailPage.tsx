import { useActions, useValues } from 'kea'

import { IconArchive, IconExternal, IconGithub, IconPerson, IconPlay, IconTarget } from '@posthog/icons'
import { LemonButton, LemonCollapse, LemonTag, Spinner } from '@posthog/lemon-ui'

import { dayjs } from 'lib/dayjs'
import { ButtonPrimitive } from 'lib/ui/Button/ButtonPrimitives'
import { pluralize } from 'lib/utils'
import { urls } from 'scenes/urls'

import {
    ScenePanel,
    ScenePanelActionsSection,
    ScenePanelDivider,
    ScenePanelInfoSection,
} from '~/layout/scenes/SceneLayout'
import { SceneContent } from '~/layout/scenes/components/SceneContent'
import { SceneTitleSection } from '~/layout/scenes/components/SceneTitleSection'

import { taskDetailSceneLogic } from '../logics/taskDetailSceneLogic'
import { TaskReferenceList } from './TaskReferenceList'
import { TaskReferenceModal } from './TaskReferenceModal'
import { TaskRunItem } from './TaskRunItem'
import { TaskSessionView } from './TaskSessionView'

export interface TaskDetailPageProps {
    taskId: string
}

export function TaskDetailPage({ taskId }: TaskDetailPageProps): JSX.Element {
    const sceneLogic = taskDetailSceneLogic({ taskId })
    const {
        task,
        runs,
        selectedRunId,
        selectedRun,
        runsLoading,
        logs,
        shouldPoll,
        isAutoGeneratedTask,
        references,
        referencesResponseLoading,
        selectedReference,
    } = useValues(sceneLogic)
    const { setSelectedRunId, runTask, deleteTask, setSelectedReference } = useActions(sceneLogic)

    if (!task) {
        return <div className="text-center py-8 text-muted">Task not found</div>
    }

    const hasBeenRun = runs.length > 0
    const latestRun = runs.length > 0 ? runs[0] : null
    const isLatestRunInProgress = latestRun?.status === 'in_progress' || latestRun?.status === 'queued'
    const isLatestRunCompleted = latestRun?.status === 'completed'
    const runButtonText = !hasBeenRun ? 'Run task' : isLatestRunCompleted ? 'Run again' : 'Retry task'

    const prUrl = selectedRun?.output?.pr_url as string | undefined

    return (
        <SceneContent>
            <ScenePanel>
                <ScenePanelInfoSection>
                    <div className="flex flex-col gap-3">
                        <div>
                            <div className="text-xs text-muted mb-1">Task ID</div>
                            <div className="font-mono text-sm">{task.slug}</div>
                        </div>
                        <div>
                            <div className="text-xs text-muted mb-1">Repository</div>
                            <div className="text-sm">{task.repository}</div>
                        </div>
                        <div>
                            <div className="text-xs text-muted mb-1">Created by</div>
                            <div className="text-sm">
                                {task.created_by?.first_name || task.created_by?.email || 'Unknown'}
                            </div>
                        </div>
                        <div>
                            <div className="text-xs text-muted mb-1">Created</div>
                            <div className="text-sm">
                                {isAutoGeneratedTask ? (
                                    <LemonTag type="highlight" size="small">
                                        Auto-detected
                                    </LemonTag>
                                ) : (
                                    dayjs(task.created_at).format('MMM D, YYYY HH:mm')
                                )}
                            </div>
                        </div>
                    </div>
                </ScenePanelInfoSection>

                <ScenePanelDivider />

                <ScenePanelActionsSection>
                    <ButtonPrimitive menuItem variant="danger" onClick={deleteTask}>
                        <IconArchive />
                        Archive task
                    </ButtonPrimitive>
                </ScenePanelActionsSection>

                {runs.length > 0 && (
                    <>
                        <ScenePanelDivider />
                        <ScenePanelInfoSection>
                            <div className="text-xs font-semibold text-muted mb-2">Run history</div>
                            <div className="flex flex-col gap-1">
                                {runs.map((run) => (
                                    <TaskRunItem
                                        key={run.id}
                                        run={run}
                                        isSelected={run.id === selectedRunId}
                                        onClick={() => setSelectedRunId(run.id, taskId)}
                                    />
                                ))}
                            </div>
                        </ScenePanelInfoSection>
                    </>
                )}
            </ScenePanel>

            <SceneTitleSection
                name={task?.title}
                description={task?.description}
                resourceType={{ type: 'task' }}
                isLoading={false}
                canEdit={false}
                forceBackTo={{
                    key: 'tasks',
                    name: 'Tasks',
                    path: urls.taskTracker(),
                }}
                actions={
                    <div className="flex items-center gap-2">
                        <LemonButton
                            type="secondary"
                            size="small"
                            icon={<IconExternal />}
                            onClick={() => window.open(`twig://task/${task.id}`, '_blank')}
                        >
                            Open in Twig
                        </LemonButton>
                        {prUrl && (
                            <LemonButton
                                type="secondary"
                                size="small"
                                icon={<IconGithub />}
                                onClick={() => window.open(prUrl, '_blank')}
                            >
                                View PR
                            </LemonButton>
                        )}
                        {!isLatestRunInProgress && (
                            <LemonButton type="primary" size="small" icon={<IconPlay />} onClick={runTask}>
                                {runButtonText}
                            </LemonButton>
                        )}
                    </div>
                }
            />

            {isAutoGeneratedTask && (
                <>
                    {/* Stats bar for auto-generated tasks */}
                    <div className="flex items-center gap-6 px-4 py-3 bg-bg-light border rounded">
                        <div className="flex items-center gap-2">
                            <IconPerson className="w-4 h-4 text-muted" />
                            <span className="text-sm">
                                <strong>{pluralize(task.distinct_user_count, 'user')}</strong> affected
                            </span>
                        </div>
                        <div className="flex items-center gap-2">
                            <IconTarget className="w-4 h-4 text-muted" />
                            <span className="text-sm">
                                <strong>{pluralize(task.occurrence_count, 'occurrence')}</strong> seen
                            </span>
                        </div>
                    </div>
                    {/* Reference list */}
                    <LemonCollapse
                        panels={[
                            {
                                key: 'references',
                                header: `References (${task.reference_count})`,
                                content: (
                                    <TaskReferenceList
                                        references={references}
                                        loading={referencesResponseLoading}
                                        onReferenceClick={setSelectedReference}
                                    />
                                ),
                            },
                        ]}
                        defaultActiveKey="references"
                    />
                    <TaskReferenceModal
                        isOpen={!!selectedReference}
                        onClose={() => setSelectedReference(null)}
                        reference={selectedReference}
                    />
                </>
            )}

            {runsLoading ? (
                <div className="flex items-center justify-center h-32">
                    <Spinner />
                </div>
            ) : runs.length === 0 ? (
                <div className="text-center py-16">
                    <p className="text-muted">This task hasn't been run yet</p>
                </div>
            ) : selectedRun ? (
                <div className="flex-1 overflow-hidden">
                    <TaskSessionView logs={logs} isPolling={shouldPoll} run={selectedRun} />
                </div>
            ) : null}
        </SceneContent>
    )
}

import { MakeLogicType, actions, afterMount, connect, kea, listeners, path, reducers } from 'kea'
import { loaders } from 'kea-loaders'

import { ApiConfig } from 'lib/api'
import { dateMapping } from 'lib/utils/dateFilters'

import { engineeringAnalyticsTeamCiHealth } from '../generated/api'
import { SurfaceEnumApi } from '../generated/api.schemas'
import { engineeringAnalyticsLogic } from './engineeringAnalyticsLogic'

const projectId = (): string => String(ApiConfig.getCurrentProjectId())

export type TeamsWindow = '-24h' | '-7d' | '-14d' | '-30d'

export const DEFAULT_TEAMS_WINDOW: TeamsWindow = '-14d'

export const TEAMS_WINDOW_LABELS: Record<TeamsWindow, { prior: string; current: string }> = {
    '-24h': { prior: 'Previous 24 hours', current: 'Last 24 hours' },
    '-7d': { prior: 'Previous 7 days', current: 'Last 7 days' },
    '-14d': { prior: 'Previous 14 days', current: 'Last 14 days' },
    '-30d': { prior: 'Previous 30 days', current: 'Last 30 days' },
}

export function isTeamsWindow(value: unknown): value is TeamsWindow {
    return typeof value === 'string' && value in TEAMS_WINDOW_LABELS
}

// date_from presets the team endpoints accept (max 30d; an equal-length prior twin is scanned).
export const TEAMS_WINDOW_DATE_OPTIONS = dateMapping.filter(({ values }) => isTeamsWindow(values[0]))

export const UNOWNED_TEAM = 'unowned'
export type TestSurface = SurfaceEnumApi
export const DEFAULT_TEST_SURFACE: TestSurface = 'all'
export const TEST_SURFACE_OPTIONS: { label: string; value: TestSurface }[] = [
    { label: 'All', value: 'all' },
    { label: 'Backend', value: 'backend' },
    { label: 'Frontend', value: 'frontend' },
]

export function isTestSurface(value: unknown): value is TestSurface {
    return value === 'all' || value === 'backend' || value === 'frontend'
}

export interface TeamCIHealthRow {
    ownerTeam: string
    /** Owned tests with same-job, same-commit recovery proof: the queue's `confirmed_flake`. */
    flakyTestCount: number
    flakyTestCountPrior: number
    /** Owned tests that failed with no such proof and still hit the blast-radius bar. */
    regressionTestCount: number
    regressionTestCountPrior: number
    failedRunCount: number
    failedRunCountPrior: number
    sameCommitRecoveryRunCount: number
    sameCommitRecoveryRunCountPrior: number
    hasTestActivity: boolean
}

export interface TeamsData {
    rows: TeamCIHealthRow[]
    truncated: boolean
    limit: number
    hasOwnershipCatalog: boolean
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface teamsLogicValues {
    sourceId: string | null // engineeringAnalyticsLogic
    teams: TeamsData | null
    teamsLoading: boolean
    teamsWindow: TeamsWindow
    testSurface: TestSurface
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface teamsLogicActions {
    setSourceId: (sourceId: string | null) => {
        sourceId: string | null
    } // engineeringAnalyticsLogic
    loadTeams: () => any
    loadTeamsFailure: (
        error: string,
        errorObject?: any
    ) => {
        error: string
        errorObject?: any
    }
    loadTeamsSuccess: (
        teams: TeamsData,
        payload?: any
    ) => {
        teams: TeamsData
        payload?: any
    }
    setTeamsWindow: (window: TeamsWindow) => {
        window: TeamsWindow
    }
    setTestSurface: (surface: TestSurface) => {
        surface: SurfaceEnumApi
    }
}

export type teamsLogicType = MakeLogicType<teamsLogicValues, teamsLogicActions>

export const teamsLogic = kea<teamsLogicType>([
    path(['products', 'engineering_analytics', 'frontend', 'scenes', 'teamsLogic']),
    connect(() => ({
        values: [engineeringAnalyticsLogic, ['sourceId']],
        actions: [engineeringAnalyticsLogic, ['setSourceId']],
    })),
    actions({
        setTeamsWindow: (window: TeamsWindow) => ({ window }),
        setTestSurface: (surface: TestSurface) => ({ surface }),
    }),
    reducers({
        teamsWindow: [DEFAULT_TEAMS_WINDOW as TeamsWindow, { setTeamsWindow: (_, { window }) => window }],
        testSurface: [DEFAULT_TEST_SURFACE as TestSurface, { setTestSurface: (_, { surface }) => surface }],
    }),
    loaders(({ values }) => ({
        teams: [
            null as TeamsData | null,
            {
                loadTeams: async (): Promise<TeamsData> => {
                    const data = await engineeringAnalyticsTeamCiHealth(projectId(), {
                        date_from: values.teamsWindow,
                        source_id: values.sourceId ?? undefined,
                        surface: values.testSurface,
                    })
                    return {
                        rows: data.items.map(
                            (it): TeamCIHealthRow => ({
                                ownerTeam: it.owner_team,
                                flakyTestCount: it.flaky_test_count,
                                flakyTestCountPrior: it.flaky_test_count_prior,
                                regressionTestCount: it.regression_test_count,
                                regressionTestCountPrior: it.regression_test_count_prior,
                                failedRunCount: it.failed_run_count,
                                failedRunCountPrior: it.failed_run_count_prior,
                                sameCommitRecoveryRunCount: it.same_commit_recovery_run_count,
                                sameCommitRecoveryRunCountPrior: it.same_commit_recovery_run_count_prior,
                                hasTestActivity: it.has_test_activity,
                            })
                        ),
                        truncated: data.truncated,
                        limit: data.limit,
                        hasOwnershipCatalog: data.has_ownership_catalog ?? false,
                    }
                },
            },
        ],
    })),
    listeners(({ actions }) => ({
        setTeamsWindow: () => actions.loadTeams(),
        setTestSurface: () => actions.loadTeams(),
        setSourceId: () => actions.loadTeams(),
    })),
    afterMount(({ actions }) => {
        actions.loadTeams()
    }),
])

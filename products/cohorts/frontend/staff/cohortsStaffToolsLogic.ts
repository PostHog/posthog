export function parseCohortIds(input: string): number[] {
    // Tolerant of comma/space/newline separated ids, deduped preserving order. Each token must be
    // a plain non-negative integer on its own: a digit-run match (e.g. `\d+` over the whole
    // string) would silently turn "-123" into cohort 123 and "123.5" into cohorts 123 and 5.
    const ids = new Set<number>()
    for (const token of input.split(/[\s,]+/)) {
        if (token && /^\d+$/.test(token)) {
            ids.add(Number(token))
        }
    }
    return Array.from(ids)
}

export const cohortsStaffToolsLogic = kea<cohortsStaffToolsLogicType>([
    path(['products', 'cohorts', 'frontend', 'staff', 'cohortsStaffToolsLogic']),
    actions({
        setCohortIdsInput: (input: string) => ({ input }),
        seedCohortFromDeepLink: (cohortId: number) => ({ cohortId }),
    }),
    loaders(({ values }) => ({
        lookupResponse: [
            null as StaffCohortLookupResponseApi | null,
            {
                lookUpCohorts: async (_: void, breakpoint) => {
                    const cohortIds = parseCohortIds(values.cohortIdsInput)
                    if (cohortIds.length === 0) {
                        return null
                    }
                    const response = await cohortsStaffList({ cohort_ids: cohortIds })
                    breakpoint()
                    return response
                },
            },
        ],
        stuckResponse: [
            null as StaffStuckCohortsResponseApi | null,
            {
                loadStuckCohorts: async (_: void, breakpoint) => {
                    const response = await cohortsStaffStuckRetrieve()
                    breakpoint()
                    return response
                },
            },
        ],
        recalculateResult: [
            null as StaffCohortRecalculateResponseApi | null,
            {
                recalculateCohorts: async ({ cohortIds }: { cohortIds: number[] }) => {
                    return await cohortsStaffRecalculateCreate({ cohort_ids: cohortIds })
                },
            },
        ],
    })),
    reducers({
        cohortIdsInput: [
            '',
            {
                setCohortIdsInput: (_, { input }) => input,
                seedCohortFromDeepLink: (_, { cohortId }) => String(cohortId),
            },
        ],
        // Tracks the last-seeded cohort id so a fresh cohort_id in the URL (e.g. a different
        // support ticket link opened while this scene is already mounted) re-seeds the input,
        // while editing the input after a seed doesn't get clobbered if urlToAction re-runs for
        // the same id (e.g. browser back/forward).
        lastSeededCohortId: [
            null as number | null,
            {
                seedCohortFromDeepLink: (_, { cohortId }) => cohortId,
            },
        ],
        // Reactive per-row in-flight flag for each row's recalculate button.
        pendingRecalculateCohortIds: [
            [] as number[],
            {
                recalculateCohorts: (state, { cohortIds }) => Array.from(new Set([...state, ...cohortIds])),
                recalculateCohortsSuccess: () => [],
                recalculateCohortsFailure: () => [],
            },
        ],
    }),
    selectors({
        lookedUpCohorts: [
            (s) => [s.lookupResponse],
            (lookupResponse: StaffCohortLookupResponseApi | null): StaffCohort[] => lookupResponse?.results ?? [],
        ],
        stuckCohorts: [
            (s) => [s.stuckResponse],
            (stuckResponse: StaffStuckCohortsResponseApi | null): StaffCohort[] => stuckResponse?.results ?? [],
        ],
    }),
    listeners(({ actions, values }) => ({
        seedCohortFromDeepLink: () => {
            actions.lookUpCohorts()
        },
        recalculateCohortsSuccess: ({ recalculateResult }) => {
            const queued = recalculateResult?.queued_cohort_ids ?? []
            const partial = recalculateResult?.partial_cohort_ids ?? []
            const failed = recalculateResult?.failed_cohort_ids ?? []
            const skipped = recalculateResult?.skipped ?? []
            const notFound = recalculateResult?.not_found_cohort_ids ?? []
            if (queued.length > 0) {
                lemonToast.success(
                    `Recalculation enqueued for cohort${queued.length === 1 ? '' : 's'} ${queued.join(', ')}. ` +
                        'Refresh in a bit to see progress.'
                )
            }
            if (partial.length > 0) {
                lemonToast.warning(
                    `Dependency resolution failed for cohort${partial.length === 1 ? '' : 's'} ${partial.join(
                        ', '
                    )}; only the cohort itself was enqueued, not its dependents or dependencies. Those may still be stale.`
                )
            }
            for (const { cohort_id, error } of failed) {
                lemonToast.error(`Cohort ${cohort_id} could not be enqueued: ${error}`)
            }
            for (const { cohort_id, reason } of skipped) {
                lemonToast.warning(`Cohort ${cohort_id} was not enqueued: ${reason}`)
            }
            if (notFound.length > 0) {
                lemonToast.warning(`Cohort ids not found: ${notFound.join(', ')}`)
            }
            // Refresh both panels so is_calculating / pending_version reflect the new state.
            if (parseCohortIds(values.cohortIdsInput).length > 0) {
                actions.lookUpCohorts()
            }
            actions.loadStuckCohorts()
        },
        recalculateCohortsFailure: () => {
            lemonToast.error('Failed to enqueue cohort recalculation.')
        },
        lookUpCohortsFailure: () => {
            lemonToast.error('Failed to look up cohorts.')
        },
        loadStuckCohortsFailure: () => {
            lemonToast.error('Failed to load stuck cohorts.')
        },
    })),
    afterMount(({ actions }) => {
        // Scene logic mounts before CohortsStaffToolsScene's is_staff check renders AccessDenied,
        // so a non-staff user hitting this URL directly would otherwise get a 403 toast on top of it.
        if (userLogic.values.user?.is_staff) {
            actions.loadStuckCohorts()
        }
    }),
    urlToAction(({ actions, values }) => ({
        '/feature_flags/staff/cohorts': (_, searchParams) => {
            // Deep link (e.g. from a support ticket): seed the lookup with the cohort id.
            const cohortId = searchParams.cohort_id ? Number(searchParams.cohort_id) : null
            if (cohortId && cohortId !== values.lastSeededCohortId) {
                actions.seedCohortFromDeepLink(cohortId)
            }
        },
    })),
])

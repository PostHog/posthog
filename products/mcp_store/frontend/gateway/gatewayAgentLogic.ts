export type gatewayAgentLogicType = MakeLogicType<
    gatewayAgentLogicValues,
    gatewayAgentLogicActions,
    GatewayAgentLogicProps,
    gatewayAgentLogicMeta
>

export const gatewayAgentLogic = kea<gatewayAgentLogicType>([
    path((key) => ['products', 'mcp_store', 'frontend', 'gateway', 'gatewayAgentLogic', key]),
    props({} as GatewayAgentLogicProps),
    key((props) => props.id),

    connect(() => ({
        values: [
            mcpGatewayLogic,
            [
                'accountStatusLoadingIds',
                'agentServerAccessLoadingKeys',
                'serviceAccounts',
                'serviceAccountsLoading as accountLoading',
            ],
        ],
        actions: [mcpGatewayLogic, ['setAgentServerAccess', 'toggleAccountStatus']],
    })),

    loaders(({ props }) => ({
        allServers: [
            [] as MCPGatewayServerApi[],
            {
                loadAllServers: async () => {
                    const response = await mcpGatewayServersList(currentProjectId(), { limit: 500 })
                    return response.results
                },
            },
        ],
        recentCalls: [
            [] as MCPAuditEventApi[],
            {
                loadRecentCalls: async () => {
                    const response = await mcpGatewayAuditList(currentProjectId(), {
                        actor_service_account_id: props.id,
                        limit: 10,
                    })
                    return response.results
                },
            },
        ],
    })),

    selectors({
        account: [
            (s) => [s.serviceAccounts, (_, props) => props.id],
            (serviceAccounts: MCPServiceAccountApi[], id: string): MCPServiceAccountApi | null =>
                serviceAccounts.find((account) => account.id === id) ?? null,
        ],
        breadcrumbs: [
            (s) => [s.account],
            (account: MCPServiceAccountApi | null): Breadcrumb[] => [
                { key: 'mcp-gateway', name: 'MCP servers', path: urls.mcpGatewayTab('team') },
                { key: 'mcp-gateway-agent', name: account?.name ?? 'Agent' },
            ],
        ],
        sharedServerIds: [
            (s) => [s.account],
            (account: MCPServiceAccountApi | null): Set<string> => new Set(account?.server_ids ?? []),
        ],
    }),

    afterMount(({ actions }) => {
        actions.loadAllServers()
        actions.loadRecentCalls()
    }),
])

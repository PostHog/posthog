    account: MCPServiceAccountApi | null
    allServers: MCPGatewayServerApi[]
    allServersLoading: boolean
    breadcrumbs: Breadcrumb[]
    recentCalls: MCPAuditEventApi[]
    recentCallsLoading: boolean

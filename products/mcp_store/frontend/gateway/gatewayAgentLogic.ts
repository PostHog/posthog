// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface gatewayAgentLogicMeta {
    key: any;
    __keaTypeGenInternalSelectorTypes: {
        breadcrumbs: (account: any) => Breadcrumb[];
        sharedServerIds: (account: any) => Set<string>;
    };
}

export type gatewayAgentLogicType = MakeLogicType<gatewayAgentLogicValues, gatewayAgentLogicActions, GatewayAgentLogicProps, gatewayAgentLogicMeta>;

export const gatewayAgentLogic = kea<gatewayAgentLogicType>([
    path((key) => ['products', 'mcp_store', 'frontend', 'gateway', 'gatewayAgentLogic', key]),
    props({} as GatewayAgentLogicProps),
    key((props) => props.id),

    connect(() => ({
        actions: [mcpGatewayLogic, ['toggleAccountStatus', 'deleteServiceAccount', 'rotateServiceAccountToken']],
    })),

    actions({
        setServerAccess: (serverId: string, enabled: boolean) => ({ serverId, enabled }),
    }),

    loaders(({ props }) => ({
        account: [
            null as MCPServiceAccountApi | null,
            {
                loadAccount: async () => await mcpGatewayServiceAccountsRetrieve(currentProjectId(), props.id),
            },
        ],
        allServers: [
            [] as MCPGatewayServerApi[],
            {
                loadAllServers: async () => {
                    const response = await mcpGatewayServersList(currentProjectId(), { limit: 500 })
                    return response.results
                },
            },
        ],
        recentCalls: [
            [] as MCPAuditEventApi[],
            {
                loadRecentCalls: async () => {
                    const response = await mcpGatewayAuditList(currentProjectId(), {
                        actor_service_account_id: props.id,
                        limit: 10,
                    })
                    return response.results
                },
            },
        ],
    })),

    selectors({
        breadcrumbs: [
            (s) => [s.account],
            (account): Breadcrumb[] => [
                { key: 'mcp-gateway', name: 'MCP servers', path: urls.mcpGatewayTab('team') },
                { key: 'mcp-gateway-agent', name: account?.name ?? 'Agent' },
            ],
        ],
        sharedServerIds: [(s) => [s.account], (account): Set<string> => new Set(account?.server_ids ?? [])],
    }),

    listeners(({ actions, props }) => ({
        setServerAccess: async ({ serverId, enabled }) => {
            await mcpGatewayServiceAccountsAccessCreate(currentProjectId(), props.id, {
                gateway_server_id: serverId,
                enabled,
                policies: [],
            })
            actions.loadAccount()
            lemonToast[enabled ? 'success' : 'info'](enabled ? 'Server shared with agent' : 'Access revoked')
        },
    })),

    afterMount(({ actions }) => {
        actions.loadAccount()
        actions.loadAllServers()
        actions.loadRecentCalls()
    }),
])

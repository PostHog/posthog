import { MakeLogicType, afterMount, connect, kea, key, path, props, selectors } from 'kea'
import { loaders } from 'kea-loaders'

import { teamLogic } from 'scenes/teamLogic'
import { urls } from 'scenes/urls'

import { Breadcrumb } from '~/types'

import { mcpGatewayAuditList, mcpGatewayServersList } from '../generated/api'
import { MCPAuditEventApi, MCPGatewayServerApi, MCPServiceAccountApi } from '../generated/api.schemas'
import { mcpGatewayLogic } from './mcpGatewayLogic'

export interface GatewayAgentLogicProps {
    id: string
}

function currentProjectId(): string {
    return String(teamLogic.values.currentTeamId)
}


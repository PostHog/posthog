export const mcpGatewayLogic = kea<mcpGatewayLogicType>([
    path(['products', 'mcp_store', 'frontend', 'gateway', 'mcpGatewayLogic']),

    actions({
        setSearchQuery: (query: string) => ({ query }),
        setCategoryFilter: (category: string | null) => ({ category }),
        toggleServerEnabled: (serverId: string, enabled: boolean) => ({ serverId, enabled }),
        toggleAllowPersonal: (serverId: string, allowed: boolean) => ({ serverId, allowed }),
        removeServer: (serverId: string) => ({ serverId }),
        connectServer: (serverId: string) => ({ serverId }),
        disconnectServer: (serverId: string, installationId: string) => ({ serverId, installationId }),
        toggleYourConnectionEnabled: (installationId: string, enabled: boolean) => ({ installationId, enabled }),
        createServiceAccount: (name: string, description: string) => ({ name, description }),
        toggleAccountStatus: (accountId: string, paused: boolean) => ({ accountId, paused }),
        deleteServiceAccount: (accountId: string) => ({ accountId }),
        rotateServiceAccountToken: (accountId: string) => ({ accountId }),
        showNewToken: (account: MCPServiceAccountWithTokenApi) => ({ account }),
        dismissNewToken: true,
        toggleRuleEnabled: (ruleId: string, enabled: boolean) => ({ ruleId, enabled }),
        setAllowCustomServers: (allowed: boolean) => ({ allowed }),
        applyPreset: (audience: AudienceEnumApi, preset: MCPPolicyPresetEnumApi) => ({ audience, preset }),
        setMemberServerAccess: (userId: number, serverId: string, enabled: boolean) => ({
            userId,
            serverId,
            enabled,
        }),
        setAllServersEnabled: (enabled: boolean) => ({ enabled }),
    }),

    loaders(({ values }) => ({
        config: [
            null as TeamMCPGatewayConfigApi | null,
            {
                loadConfig: async () => await mcpGatewayConfigList(currentProjectId()),
            },
        ],
        servers: [
            [] as MCPGatewayServerApi[],
            {
                loadServers: async () => {
                    const response = await mcpGatewayServersList(currentProjectId(), { limit: 500 })
                    return response.results
                },
            },
        ],
        serviceAccounts: [
            [] as MCPServiceAccountApi[],
            {
                loadServiceAccounts: async () => {
                    const response = await mcpGatewayServiceAccountsList(currentProjectId(), { limit: 500 })
                    return response.results
                },
            },
        ],
        rules: [
            [] as MCPOrgRuleApi[],
            {
                loadRules: async () => {
                    const response = await mcpGatewayRulesList(currentProjectId(), { limit: 500 })
                    return response.results
                },
            },
        ],
        members: [
            [] as GatewayMemberSummaryApi[],
            {
                loadMembers: async () => {
                    if (!values.isAdmin) {
                        return []
                    }
                    return await mcpGatewayMembersList(currentProjectId())
                },
            },
        ],
    })),

    reducers({
        searchQuery: ['', { setSearchQuery: (_, { query }) => query }],
        categoryFilter: [null as string | null, { setCategoryFilter: (_, { category }) => category }],
        connectingServerId: [
            null as string | null,
            {
                connectServer: (_, { serverId }) => serverId,
                loadServersSuccess: () => null,
                loadServersFailure: () => null,
            },
        ],
        // Shown once in a modal right after creation or rotation, then discarded.
        newlyIssuedToken: [
            null as MCPServiceAccountWithTokenApi | null,
            {
                showNewToken: (_, { account }) => account,
                dismissNewToken: () => null,
            },
        ],
    }),

    selectors({
        isAdmin: [(s) => [s.config], (config): boolean => Boolean(config?.is_admin)],
        allowCustomServers: [(s) => [s.config], (config): boolean => config?.allow_custom_servers ?? true],
        canAddServers: [
            (s) => [s.isAdmin, s.allowCustomServers],
            (isAdmin, allowCustomServers): boolean => isAdmin || allowCustomServers,
        ],
        categoryCounts: [
            (s) => [s.servers],
            (servers: MCPGatewayServerApi[]): Record<string, number> => {
                const counts: Record<string, number> = {}
                for (const server of servers) {
                    counts[server.category ?? 'dev'] = (counts[server.category ?? 'dev'] || 0) + 1
                }
                return counts
            },
        ],
        filteredServers: [
            (s) => [s.servers, s.searchQuery, s.categoryFilter],
            (
                servers: MCPGatewayServerApi[],
                searchQuery: string,
                categoryFilter: string | null
            ): MCPGatewayServerApi[] => {
                const query = searchQuery.trim().toLowerCase()
                return servers.filter((server) => {
                    if (categoryFilter && server.category !== categoryFilter) {
                        return false
                    }
                    if (!query) {
                        return true
                    }
                    return (
                        server.name.toLowerCase().includes(query) ||
                        (server.description || '').toLowerCase().includes(query) ||
                        server.url.toLowerCase().includes(query)
                    )
                })
            },
        ],
        activeAgentCount: [
            (s) => [s.serviceAccounts],
            (serviceAccounts: MCPServiceAccountApi[]): number =>
                serviceAccounts.filter((account) => account.status === 'active').length,
        ],
        enabledServerCount: [
            (s) => [s.servers],
            (servers: MCPGatewayServerApi[]): number => servers.filter((server) => server.is_team_enabled).length,
        ],
    }),

    listeners(({ actions, values }) => ({
        toggleServerEnabled: async ({ serverId, enabled }) => {
            const server = values.servers.find((candidate) => candidate.id === serverId)
            await mcpGatewayServersPartialUpdate(currentProjectId(), serverId, { is_team_enabled: enabled })
            actions.loadServers()
            lemonToast[enabled ? 'success' : 'info'](
                `${server?.name ?? 'Server'} ${enabled ? 'enabled for the team' : 'disabled'}`
            )
        },
        toggleAllowPersonal: async ({ serverId, allowed }) => {
            const server = values.servers.find((candidate) => candidate.id === serverId)
            await mcpGatewayServersPartialUpdate(currentProjectId(), serverId, {
                allow_personal_connections: allowed,
            })
            actions.loadServers()
            lemonToast[allowed ? 'success' : 'info'](
                allowed
                    ? `Members can now connect personal ${server?.name ?? ''} accounts`
                    : `Personal connections turned off for ${server?.name ?? 'this server'}`
            )
        },
        setAllServersEnabled: async ({ enabled }) => {
            const targets = values.servers.filter((server) => server.is_team_enabled !== enabled)
            await Promise.all(
                targets.map((server) =>
                    mcpGatewayServersPartialUpdate(currentProjectId(), server.id, { is_team_enabled: enabled })
                )
            )
            actions.loadServers()
            lemonToast[enabled ? 'success' : 'info'](
                enabled ? 'Every server is enabled for the team' : 'All servers disabled — enable them one by one'
            )
        },
        removeServer: async ({ serverId }) => {
            const server = values.servers.find((candidate) => candidate.id === serverId)
            await mcpGatewayServersDestroy(currentProjectId(), serverId)
            actions.loadServers()
            lemonToast.info(`${server?.name ?? 'Server'} removed from the gateway`)
        },
        connectServer: async ({ serverId }) => {
            const server = values.servers.find((candidate) => candidate.id === serverId)
            if (!server) {
                return
            }
            const returnPath = router.values.location.pathname
            const projectId = currentProjectId()
            try {
                const response = server.template_id
                    ? await mcpServerInstallationsInstallTemplateCreate(projectId, {
                          template_id: server.template_id,
                          return_path: returnPath,
                      })
                    : await mcpServerInstallationsInstallCustomCreate(projectId, {
                          name: server.name,
                          url: server.url,
                          auth_type: 'oauth',
                          description: server.description || '',
                          return_path: returnPath,
                      })
                if ('redirect_url' in response && response.redirect_url) {
                    window.location.href = response.redirect_url
                    return
                }
                actions.loadServers()
                lemonToast.success(`Authenticated with ${server.name} as you`)
            } catch (error: any) {
                actions.loadServers()
                lemonToast.error(error?.detail || `Could not connect to ${server.name}`)
            }
        },
        disconnectServer: async ({ serverId, installationId }) => {
            const server = values.servers.find((candidate) => candidate.id === serverId)
            await mcpServerInstallationsDestroy(currentProjectId(), installationId)
            actions.loadServers()
            lemonToast.info(`Disconnected from ${server?.name ?? 'server'}`)
        },
        toggleYourConnectionEnabled: async ({ installationId, enabled }) => {
            await mcpServerInstallationsPartialUpdate(currentProjectId(), installationId, { is_enabled: enabled })
            actions.loadServers()
        },
        createServiceAccount: async ({ name, description }, breakpoint) => {
            const account = await mcpGatewayServiceAccountsCreate(currentProjectId(), { name, description })
            breakpoint()
            actions.loadServiceAccounts()
            actions.showNewToken(account)
            lemonToast.success(`${name} created — share servers with it`)
            router.actions.push(urls.mcpGatewayAgent(account.id))
        },
        toggleAccountStatus: async ({ accountId, paused }) => {
            const account = values.serviceAccounts.find((candidate) => candidate.id === accountId)
            await mcpGatewayServiceAccountsPartialUpdate(currentProjectId(), accountId, {
                status: paused ? 'paused' : 'active',
            })
            actions.loadServiceAccounts()
            lemonToast[paused ? 'info' : 'success'](
                paused
                    ? `${account?.name ?? 'Agent'} paused — all access is off`
                    : `${account?.name ?? 'Agent'} is active again`
            )
        },
        deleteServiceAccount: async ({ accountId }) => {
            const account = values.serviceAccounts.find((candidate) => candidate.id === accountId)
            await mcpGatewayServiceAccountsDestroy(currentProjectId(), accountId)
            actions.loadServiceAccounts()
            actions.loadServers()
            router.actions.push(urls.mcpGatewayTab('team'))
            lemonToast.info(`${account?.name ?? 'Agent'} deleted — its token no longer works`)
        },
        rotateServiceAccountToken: async ({ accountId }) => {
            const account = await mcpGatewayServiceAccountsRotateTokenCreate(currentProjectId(), accountId)
            actions.loadServiceAccounts()
            actions.showNewToken(account)
            lemonToast.success('Token rotated — the previous token no longer works')
        },
        toggleRuleEnabled: async ({ ruleId, enabled }) => {
            const rule = values.rules.find((candidate) => candidate.id === ruleId)
            await mcpGatewayRulesPartialUpdate(currentProjectId(), ruleId, { enabled })
            actions.loadRules()
            lemonToast[enabled ? 'success' : 'info'](`${rule?.name ?? 'Rule'} ${enabled ? 'enabled' : 'disabled'}`)
        },
        setAllowCustomServers: async ({ allowed }) => {
            await mcpGatewayConfigUpdateSettingsCreate(currentProjectId(), { allow_custom_servers: allowed })
            actions.loadConfig()
            lemonToast[allowed ? 'success' : 'info'](
                allowed ? 'Members can add custom servers' : 'Custom servers are now admin-only'
            )
        },
        applyPreset: async ({ audience, preset }) => {
            await mcpGatewayConfigApplyPresetCreate(currentProjectId(), { audience, preset })
            actions.loadConfig()
            lemonToast.success(`Baseline applied to ${audience}`)
        },
        setMemberServerAccess: async ({ userId, serverId, enabled }) => {
            const server = values.servers.find((candidate) => candidate.id === serverId)
            await mcpGatewayMembersSetAccessCreate(currentProjectId(), String(userId), {
                gateway_server_id: serverId,
                enabled,
            })
            actions.loadMembers()
            actions.loadServers()
            lemonToast[enabled ? 'success' : 'info'](
                `${enabled ? 'Restored' : 'Turned off'} access to ${server?.name ?? 'server'}`
            )
        },
        loadConfigSuccess: () => {
            // Members are admin-gated; retry once we know the caller is an admin.
            if (values.isAdmin && !values.members.length) {
                actions.loadMembers()
            }
        },
    })),

    afterMount(({ actions }) => {
        actions.loadConfig()
        actions.loadServers()
        actions.loadServiceAccounts()
        actions.loadRules()
        actions.loadMembers()
    }),
])

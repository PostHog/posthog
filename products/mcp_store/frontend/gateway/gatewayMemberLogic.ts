import { MakeLogicType, afterMount, connect, kea, key, listeners, path, props, reducers, selectors } from 'kea'
import { loaders } from 'kea-loaders'

import { teamLogic } from 'scenes/teamLogic'
import { urls } from 'scenes/urls'

import { Breadcrumb } from '~/types'

import { mcpGatewayMembersRetrieve } from '../generated/api'
import { GatewayMemberSummaryApi, MCPGatewayServerApi } from '../generated/api.schemas'
import { mcpGatewayLogic } from './mcpGatewayLogic'

export interface GatewayMemberLogicProps {
    id: string
}

function currentProjectId(): string {
    return String(teamLogic.values.currentTeamId)
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface gatewayMemberLogicValues {
    allServers: MCPGatewayServerApi[] // mcpGatewayLogic
    allServersInitialized: boolean // mcpGatewayLogic
    memberServerAccessLoadingKeys: Set<string> // mcpGatewayLogic
    breadcrumbs: Breadcrumb[]
    member: GatewayMemberSummaryApi | null
    memberInitialized: boolean
    memberLoading: boolean
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface gatewayMemberLogicActions {
    setMemberServerAccess: (
        userId: number,
        serverId: string,
        enabled: boolean
    ) => {
        enabled: boolean
        serverId: string
        userId: number
    } // mcpGatewayLogic
    setMemberServerAccessComplete: (
        userId: number,
        serverId: string
    ) => {
        serverId: string
        userId: number
    } // mcpGatewayLogic
    loadMember: () => any
    loadMemberFailure: (
        error: string,
        errorObject?: any
    ) => {
        error: string
        errorObject?: any
    }
    loadMemberSuccess: (
        member: GatewayMemberSummaryApi,
        payload?: any
    ) => {
        member: GatewayMemberSummaryApi
        payload?: any
    }
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface gatewayMemberLogicMeta {
    key: string
    __keaTypeGenInternalSelectorTypes: {
        breadcrumbs: (member: GatewayMemberSummaryApi | null) => Breadcrumb[]
    }
}

export type gatewayMemberLogicType = MakeLogicType<
    gatewayMemberLogicValues,
    gatewayMemberLogicActions,
    GatewayMemberLogicProps,
    gatewayMemberLogicMeta
>

export const gatewayMemberLogic = kea<gatewayMemberLogicType>([
    path((key) => ['products', 'mcp_store', 'frontend', 'gateway', 'gatewayMemberLogic', key]),
    props({} as GatewayMemberLogicProps),
    key((props) => props.id),

    connect(() => ({
        values: [
            mcpGatewayLogic,
            ['memberServerAccessLoadingKeys', 'servers as allServers', 'serversInitialized as allServersInitialized'],
        ],
        actions: [mcpGatewayLogic, ['setMemberServerAccess', 'setMemberServerAccessComplete']],
    })),

    loaders(({ props }) => ({
        member: [
            null as GatewayMemberSummaryApi | null,
            {
                loadMember: async () => await mcpGatewayMembersRetrieve(currentProjectId(), props.id),
            },
        ],
    })),

    reducers({
        memberInitialized: [
            false,
            {
                loadMember: () => false,
                loadMemberSuccess: () => true,
                loadMemberFailure: () => true,
            },
        ],
    }),

    selectors({
        breadcrumbs: [
            (s) => [s.member],
            (member: GatewayMemberSummaryApi | null): Breadcrumb[] => [
                { key: 'mcp-gateway', name: 'MCP servers', path: urls.mcpGatewayTab('team') },
                {
                    key: 'mcp-gateway-member',
                    name: member ? `${member.user.first_name} ${member.user.last_name}` : 'Member',
                },
            ],
        ],
    }),

    listeners(({ actions, props, values }) => ({
        setMemberServerAccess: ({ userId, serverId, enabled }) => {
            if (String(userId) !== props.id || !values.member) {
                return
            }
            const revokedServerIds = enabled
                ? values.member.revoked_server_ids.filter((revokedServerId) => revokedServerId !== serverId)
                : Array.from(new Set([...values.member.revoked_server_ids, serverId]))
            actions.loadMemberSuccess({ ...values.member, revoked_server_ids: revokedServerIds })
        },
        setMemberServerAccessComplete: ({ userId }) => {
            if (String(userId) === props.id) {
                actions.loadMember()
            }
        },
    })),

    afterMount(({ actions }) => {
        actions.loadMember()
    }),
])

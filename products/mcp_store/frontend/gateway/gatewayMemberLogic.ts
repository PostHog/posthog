import { MakeLogicType, afterMount, connect, kea, key, path, props, selectors } from 'kea'
import { loaders } from 'kea-loaders'

import { teamLogic } from 'scenes/teamLogic'
import { urls } from 'scenes/urls'

import { Breadcrumb } from '~/types'

import { mcpGatewayServersList } from '../generated/api'
import { GatewayMemberSummaryApi, MCPGatewayServerApi } from '../generated/api.schemas'
import { mcpGatewayLogic } from './mcpGatewayLogic'

export interface GatewayMemberLogicProps {
    id: string
}

function currentProjectId(): string {
    return String(teamLogic.values.currentTeamId)
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface gatewayMemberLogicValues {
    members: GatewayMemberSummaryApi[] // mcpGatewayLogic
    allServers: MCPGatewayServerApi[]
    allServersLoading: boolean
    breadcrumbs: Breadcrumb[]
    member: GatewayMemberSummaryApi | null
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface gatewayMemberLogicActions {
    loadMembers: () => any // mcpGatewayLogic
    setMemberServerAccess: (
        userId: number,
        serverId: string,
        enabled: boolean
    ) => {
        enabled: boolean
        serverId: string
        userId: number
    } // mcpGatewayLogic
    loadAllServers: () => any
    loadAllServersFailure: (
        error: string,
        errorObject?: any
    ) => {
        error: string
        errorObject?: any
    }
    loadAllServersSuccess: (
        allServers: MCPGatewayServerApi[],
        payload?: any
    ) => {
        allServers: MCPGatewayServerApi[]
        payload?: any
    }
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface gatewayMemberLogicMeta {
    key: any
    __keaTypeGenInternalSelectorTypes: {
        member: (members: any, arg: any) => GatewayMemberSummaryApi | null
        breadcrumbs: (member: any) => Breadcrumb[]
    }
}

export type gatewayMemberLogicType = MakeLogicType<
    gatewayMemberLogicValues,
    gatewayMemberLogicActions,
    GatewayMemberLogicProps,
    gatewayMemberLogicMeta
>

export const gatewayMemberLogic = kea<gatewayMemberLogicType>([
    path((key) => ['products', 'mcp_store', 'frontend', 'gateway', 'gatewayMemberLogic', key]),
    props({} as GatewayMemberLogicProps),
    key((props) => props.id),

    connect(() => ({
        values: [mcpGatewayLogic, ['members']],
        actions: [mcpGatewayLogic, ['setMemberServerAccess', 'loadMembers']],
    })),

    loaders({
        allServers: [
            [] as MCPGatewayServerApi[],
            {
                loadAllServers: async () => {
                    const response = await mcpGatewayServersList(currentProjectId(), { limit: 500 })
                    return response.results
                },
            },
        ],
    }),

    selectors({
        member: [
            (s) => [s.members, (_, props) => props.id],
            (members: GatewayMemberSummaryApi[], id: string): GatewayMemberSummaryApi | null =>
                members.find((candidate) => String(candidate.user.id) === String(id)) ?? null,
        ],
        breadcrumbs: [
            (s) => [s.member],
            (member): Breadcrumb[] => [
                { key: 'mcp-gateway', name: 'MCP servers', path: urls.mcpGatewayTab('team') },
                {
                    key: 'mcp-gateway-member',
                    name: member ? `${member.user.first_name} ${member.user.last_name}` : 'Member',
                },
            ],
        ],
    }),

    afterMount(({ actions }) => {
        actions.loadAllServers()
        actions.loadMembers()
    }),
])

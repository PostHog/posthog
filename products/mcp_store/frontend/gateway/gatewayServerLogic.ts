export type gatewayServerLogicType = MakeLogicType<
    gatewayServerLogicValues,
    gatewayServerLogicActions,
    GatewayServerLogicProps,
    gatewayServerLogicMeta
>

export const gatewayServerLogic = kea<gatewayServerLogicType>([
    path((key) => ['products', 'mcp_store', 'frontend', 'gateway', 'gatewayServerLogic', key]),
    props({} as GatewayServerLogicProps),
    key((props) => props.id),

    connect(() => ({
        values: [mcpGatewayLogic, ['isAdmin', 'serviceAccounts']],
        actions: [mcpGatewayLogic, ['loadServers']],
    })),

    actions({
        setScope: (scope: PolicyScope) => ({ scope }),
        setToolPolicy: (toolName: string, state: MCPToolApprovalStateEnumApi) => ({ toolName, state }),
        setAllTools: (state: MCPToolApprovalStateEnumApi) => ({ state }),
    }),

    loaders(({ props, values }) => ({
        server: [
            null as MCPGatewayServerApi | null,
            {
                loadServer: async () => await mcpGatewayServersRetrieve(currentProjectId(), props.id),
            },
        ],
        toolPolicies: [
            [] as ResolvedToolPolicyApi[],
            {
                loadToolPolicies: async () => {
                    const scope = values.scope
                    const response = await mcpGatewayServersToolsRetrieve(currentProjectId(), props.id, {
                        scope_type: scope.scopeType,
                        scope_user_id: scope.scopeUserId,
                        scope_service_account_id: scope.scopeServiceAccountId,
                    })
                    return response.results
                },
            },
        ],
    })),

    reducers({
        scope: [
            { scopeType: 'team', label: 'Team default' } as PolicyScope,
            {
                setScope: (_, { scope }) => scope,
            },
        ],
    }),

    selectors({
        breadcrumbs: [
            (s) => [s.server],
            (server): Breadcrumb[] => [
                { key: 'mcp-gateway', name: 'MCP servers', path: urls.mcpGateway() },
                { key: 'mcp-gateway-server', name: server?.name ?? 'Server' },
            ],
        ],
        policyCounts: [
            (s) => [s.toolPolicies],
            (toolPolicies: ResolvedToolPolicyApi[]): Record<MCPToolApprovalStateEnumApi, number> => {
                const counts: Record<MCPToolApprovalStateEnumApi, number> = {
                    approved: 0,
                    needs_approval: 0,
                    do_not_use: 0,
                }
                for (const policy of toolPolicies) {
                    counts[policy.policy_state] += 1
                }
                return counts
            },
        ],
        // Scopes an admin can flip between on the policy bar: team default, you,
        // then one per agent with access to this server.
        availableScopes: [
            (s) => [s.server, s.serviceAccounts],
            (server: MCPGatewayServerApi | null, serviceAccounts: MCPServiceAccountApi[]): PolicyScope[] => {
                const scopes: PolicyScope[] = [{ scopeType: 'team', label: 'Team default' }]
                const agentIds = new Set((server?.agents ?? []).map((agent) => agent.service_account_id))
                for (const account of serviceAccounts) {
                    if (agentIds.has(account.id)) {
                        scopes.push({
                            scopeType: 'agent',
                            scopeServiceAccountId: account.id,
                            label: account.name,
                        })
                    }
                }
                return scopes
            },
        ],
    }),

    listeners(({ actions, values, props }) => ({
        setScope: () => {
            actions.loadToolPolicies()
        },
        setToolPolicy: async ({ toolName, state }) => {
            const scope = values.scope
            await mcpGatewayServersPoliciesCreate(currentProjectId(), props.id, {
                scope_type: scope.scopeType,
                scope_user_id: scope.scopeUserId,
                scope_service_account_id: scope.scopeServiceAccountId,
                policies: [{ tool_name: toolName, policy_state: state }],
            })
            actions.loadToolPolicies()
        },
        setAllTools: async ({ state }) => {
            const editable = values.toolPolicies.filter((policy) => !policy.locked)
            if (!editable.length) {
                return
            }
            const scope = values.scope
            await mcpGatewayServersPoliciesCreate(currentProjectId(), props.id, {
                scope_type: scope.scopeType,
                scope_user_id: scope.scopeUserId,
                scope_service_account_id: scope.scopeServiceAccountId,
                policies: editable.map((policy) => ({ tool_name: policy.tool_name, policy_state: state })),
            })
            actions.loadToolPolicies()
            lemonToast.success('Updated every tool for this scope')
        },
    })),

    actionToUrl(({ values, props }) => ({
        setScope: () => {
            const scope = values.scope
            if (scope.scopeType === 'agent' && scope.scopeServiceAccountId) {
                return urls.mcpGatewayServer(props.id, `agent:${scope.scopeServiceAccountId}`)
            }
            return urls.mcpGatewayServer(props.id)
        },
    })),

    urlToAction(({ actions, values }) => ({
        [urls.mcpGatewayServer(':id')]: (_, searchParams) => {
            const scopeParam = searchParams.scope as string | undefined
            if (scopeParam?.startsWith('agent:')) {
                const accountId = scopeParam.slice('agent:'.length)
                const match = values.availableScopes.find((scope) => scope.scopeServiceAccountId === accountId)
                if (match && values.scope.scopeServiceAccountId !== accountId) {
                    actions.setScope(match)
                }
            }
        },
    })),

    afterMount(({ actions }) => {
        actions.loadServer()
        actions.loadToolPolicies()
    }),
])

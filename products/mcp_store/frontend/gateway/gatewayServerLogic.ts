function currentProjectId(): string {
    return String(teamLogic.values.currentTeamId)
}

function defaultPolicyScope(isAdmin: boolean): PolicyScope {
    return isAdmin ? { scopeType: 'team', label: 'Team default' } : { scopeType: 'member', label: 'You' }
}


export const gatewayServerLogic = kea<gatewayServerLogicType>([
    path((key) => ['products', 'mcp_store', 'frontend', 'gateway', 'gatewayServerLogic', key]),
    props({} as GatewayServerLogicProps),
    key((props) => props.id),

    connect(() => ({
        values: [
            mcpGatewayLogic,
            [
                'canManageAgentAccess',
                'isAdmin',
                'servers',
                'serversInitialized',
                'serversLoading as serverLoading',
                'serviceAccounts',
                'serviceAccountsInitialized',
                'serviceAccountsLoading',
            ],
        ],
        actions: [mcpGatewayLogic, ['loadServersSuccess', 'loadServiceAccountsSuccess']],
    })),

    actions({
        setScope: (scope: PolicyScope) => ({ scope }),
        setRequestedAgentScopeId: (accountId: string | null) => ({ accountId }),
        setToolPolicy: (toolName: string, state: MCPToolApprovalStateEnumApi) => ({ toolName, state }),
        setAllTools: (state: MCPToolApprovalStateEnumApi) => ({ state }),
    }),

    loaders(({ props, values }) => ({
        toolPolicies: [
            [] as ResolvedToolPolicyApi[],
            {
                loadToolPolicies: async () => {
                    const scope = values.scope
                    const response = await mcpGatewayServersToolsRetrieve(currentProjectId(), props.id, {
                        scope_type: scope.scopeType,
                        scope_user_id: scope.scopeUserId,
                        scope_service_account_id: scope.scopeServiceAccountId,
                    })
                    return response.results
                },
            },
        ],
    })),

    reducers({
        requestedAgentScopeId: [
            null as string | null,
            {
                setRequestedAgentScopeId: (_, { accountId }) => accountId,
            },
        ],
        scope: [
            { scopeType: 'team', label: 'Team default' } as PolicyScope,
            {
                setScope: (_, { scope }) => scope,
            },
        ],
    }),

    selectors({
        server: [
            (s) => [s.servers, (_, props) => props.id],
            (servers: MCPGatewayServerApi[], id: string): MCPGatewayServerApi | null =>
                servers.find((server) => server.id === id) ?? null,
        ],
        breadcrumbs: [
            (s) => [s.server],
            (server: MCPGatewayServerApi | null): Breadcrumb[] => [
                { key: 'mcp-gateway', name: 'MCP servers', path: urls.mcpGateway() },
                { key: 'mcp-gateway-server', name: server?.name ?? 'Server' },
            ],
        ],
        policyCounts: [
            (s) => [s.toolPolicies],
            (toolPolicies: ResolvedToolPolicyApi[]): Record<MCPToolApprovalStateEnumApi, number> => {
                const counts: Record<MCPToolApprovalStateEnumApi, number> = {
                    approved: 0,
                    needs_approval: 0,
                    do_not_use: 0,
                }
                for (const policy of toolPolicies) {
                    counts[policy.policy_state] += 1
                }
                return counts
            },
        ],
        // Members always manage their own policy. Admins can also manage the
        // team baseline, and agent scopes follow the team setting.
        availableScopes: [
            (s) => [s.server, s.serviceAccounts, s.isAdmin, s.canManageAgentAccess],
            (
                server: MCPGatewayServerApi | null,
                serviceAccounts: MCPServiceAccountApi[],
                isAdmin: boolean,
                canManageAgentAccess: boolean
            ): PolicyScope[] => {
                const scopes: PolicyScope[] = [{ scopeType: 'member', label: 'You' }]
                if (isAdmin) {
                    scopes.unshift({ scopeType: 'team', label: 'Team default' })
                }
                if (!canManageAgentAccess) {
                    return scopes
                }
                for (const account of serviceAccounts) {
                    if (server && account.server_ids.includes(server.id)) {
                        scopes.push({
                            scopeType: 'agent',
                            scopeServiceAccountId: account.id,
                            label: account.name,
                        })
                    }
                }
                return scopes
            },
        ],
        scopeIsResolving: [
            (s) => [s.requestedAgentScopeId, s.scope],
            (requestedAgentScopeId: string | null, scope: PolicyScope): boolean =>
                requestedAgentScopeId !== null && scope.scopeServiceAccountId !== requestedAgentScopeId,
        ],
    }),

    listeners(({ actions, values, props }) => ({
        setRequestedAgentScopeId: () => {
            const accountId = values.requestedAgentScopeId
            if (!accountId) {
                const defaultScope = defaultPolicyScope(values.isAdmin)
                if (values.scope.scopeType !== defaultScope.scopeType) {
                    actions.setScope(defaultScope)
                }
                return
            }
            const match = values.availableScopes.find((scope) => scope.scopeServiceAccountId === accountId)
            if (match) {
                if (values.scope.scopeServiceAccountId !== accountId) {
                    actions.setScope(match)
                }
                return
            }
            if (
                values.serversInitialized &&
                values.serviceAccountsInitialized &&
                !values.serverLoading &&
                !values.serviceAccountsLoading
            ) {
                actions.setRequestedAgentScopeId(null)
                actions.setScope(defaultPolicyScope(values.isAdmin))
                lemonToast.error('This agent does not have access to this MCP server.')
            }
        },
        loadServersSuccess: () => {
            if (values.requestedAgentScopeId) {
                actions.setRequestedAgentScopeId(values.requestedAgentScopeId)
            }
        },
        loadServiceAccountsSuccess: () => {
            if (values.requestedAgentScopeId) {
                actions.setRequestedAgentScopeId(values.requestedAgentScopeId)
            }
        },
        setScope: () => {
            actions.loadToolPolicies()
        },
        setToolPolicy: async ({ toolName, state }) => {
            const scope = values.scope
            await mcpGatewayServersPoliciesCreate(currentProjectId(), props.id, {
                scope_type: scope.scopeType,
                scope_user_id: scope.scopeUserId,
                scope_service_account_id: scope.scopeServiceAccountId,
                policies: [{ tool_name: toolName, policy_state: state }],
            })
            actions.loadToolPolicies()
        },
        setAllTools: async ({ state }) => {
            const editable = values.toolPolicies.filter(
                (policy) =>
                    !policy.locked &&
                    (values.scope.scopeType === 'team' || isPolicyStateAllowedByCeiling(state, policy.team_state))
            )
            if (!editable.length) {
                return
            }
            const scope = values.scope
            await mcpGatewayServersPoliciesCreate(currentProjectId(), props.id, {
                scope_type: scope.scopeType,
                scope_user_id: scope.scopeUserId,
                scope_service_account_id: scope.scopeServiceAccountId,
                policies: editable.map((policy) => ({ tool_name: policy.tool_name, policy_state: state })),
            })
            actions.loadToolPolicies()
            lemonToast.success('Updated every tool for this scope')
        },
    })),

    actionToUrl(({ values, props }) => ({
        setScope: () => {
            const scope = values.scope
            if (scope.scopeType === 'agent' && scope.scopeServiceAccountId) {
                return urls.mcpGatewayServer(props.id, `agent:${scope.scopeServiceAccountId}`)
            }
            return urls.mcpGatewayServer(props.id)
        },
    })),

    urlToAction(({ actions }) => ({
        [urls.mcpGatewayServer(':id')]: (_, searchParams) => {
            const scopeParam = searchParams.scope as string | undefined
            if (scopeParam?.startsWith('agent:')) {
                actions.setRequestedAgentScopeId(scopeParam.slice('agent:'.length))
                return
            }
            actions.setRequestedAgentScopeId(null)
        },
    })),

    afterMount(({ actions, values }) => {
        const defaultScope = defaultPolicyScope(values.isAdmin)
        if (values.scope.scopeType !== defaultScope.scopeType) {
            actions.setScope(defaultScope)
        } else {
            actions.loadToolPolicies()
        }
    }),
])

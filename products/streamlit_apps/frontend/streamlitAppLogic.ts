export type streamlitAppLogicType = MakeLogicType<
    streamlitAppLogicValues,
    streamlitAppLogicActions,
    StreamlitAppLogicProps,
    streamlitAppLogicMeta
>

export const streamlitAppLogic = kea<streamlitAppLogicType>([
    path(['products', 'streamlit_apps', 'frontend', 'streamlitAppLogic']),
    props({} as StreamlitAppLogicProps),
    key((props) => props.shortId),

    connect(() => ({
        values: [projectLogic, ['currentProjectId']],
    })),

    actions({
        startPolling: true,
        stopPolling: true,
        startHealthPolling: true,
        stopHealthPolling: true,
        scheduleTokenRefresh: (expiresIn: number) => ({ expiresIn }),
        clearTokenRefresh: true,
    }),

    loaders(({ props, values }) => ({
        streamlitApp: [
            null as AppContractApi | null,
            {
                loadStreamlitApp: async () => {
                    return await streamlitAppsRetrieve(String(values.currentProjectId), props.shortId)
                },
            },
        ],
        sandboxStatus: [
            null as AppSandboxContractApi | null,
            {
                loadSandboxStatus: async () => {
                    const status = await streamlitAppsStatusRetrieve(String(values.currentProjectId), props.shortId)
                    // The status action omits version_number when stopped; the embedded sandbox
                    // always carries it, so normalize to one shape.
                    return { ...status, version_number: status.version_number ?? null }
                },
                startApp: async () => {
                    const app = await streamlitAppsStartCreate(String(values.currentProjectId), props.shortId)
                    return app.sandbox ?? null
                },
                stopApp: async () => {
                    const app = await streamlitAppsStopCreate(String(values.currentProjectId), props.shortId)
                    return app.sandbox ?? null
                },
                restartApp: async () => {
                    const app = await streamlitAppsRestartCreate(String(values.currentProjectId), props.shortId)
                    return app.sandbox ?? null
                },
            },
        ],
        connectInfo: [
            null as StreamlitConnectInfoApi | null,
            {
                loadConnectInfo: async () => {
                    return await streamlitAppsConnectInfoRetrieve(String(values.currentProjectId), props.shortId)
                },
            },
        ],
    })),

    reducers({
        isPolling: [
            false,
            {
                startPolling: () => true,
                stopPolling: () => false,
            },
        ],
        isHealthPolling: [
            false,
            {
                startHealthPolling: () => true,
                stopHealthPolling: () => false,
            },
        ],
        connectError: [
            null as string | null,
            {
                loadConnectInfoFailure: (_, { error }) => String(error),
                loadConnectInfoSuccess: () => null,
                startApp: () => null,
                restartApp: () => null,
            },
        ],
        connectInfo: {
            // Restart mints a new sandbox — clear so we refetch fresh tokens.
            restartApp: () => null,
            restartAppSuccess: () => null,
        },
    }),

    selectors({
        breadcrumbs: [
            (s) => [s.streamlitApp],
            (streamlitApp: AppContractApi | null): Breadcrumb[] => [
                {
                    key: 'StreamlitApps',
                    name: 'Apps',
                    path: urls.streamlitApps(),
                },
                {
                    key: ['StreamlitApp', streamlitApp?.short_id || 'new'],
                    name: streamlitApp?.name || 'Loading...',
                },
            ],
        ],
        appStatus: [
            (s) => [s.sandboxStatus],
            (sandboxStatus: AppSandboxContractApi | null): StreamlitAppStatus =>
                toStreamlitAppStatus(sandboxStatus?.status),
        ],
        iframeSrc: [
            (s) => [s.appStatus, s.connectInfo],
            (appStatus: StreamlitAppStatus, connectInfo: StreamlitConnectInfoApi | null): string | null => {
                if (appStatus !== 'running' || !connectInfo) {
                    return null
                }
                return connectInfo.iframe_url
            },
        ],
    }),

    listeners(({ actions, values, cache }) => {
        // Idempotent — kea listeners can fire repeatedly and timers would stack.
        // clearInterval also accepts timeout IDs, so this covers both.
        const clearCached = (key: TimerKey): void => {
            if (cache[key]) {
                clearInterval(cache[key])
                cache[key] = null
            }
        }
        return {
            loadStreamlitAppSuccess: ({ streamlitApp }) => {
                if (!streamlitApp) {
                    return
                }
                // Seed sandboxStatus from app data so appStatus is correct immediately.
                if (streamlitApp.sandbox) {
                    actions.loadSandboxStatusSuccess(streamlitApp.sandbox)
                }
                const status = streamlitApp.sandbox?.status ?? 'stopped'
                if (status === 'starting') {
                    actions.startPolling()
                } else if (status === 'running') {
                    actions.loadConnectInfo()
                }
            },
            startAppSuccess: () => {
                actions.startPolling()
            },
            startPolling: () => {
                clearCached('pollTimer')
                cache.pollTimer = setInterval(() => {
                    actions.loadSandboxStatus()
                }, POLL_INTERVAL_MS)
            },
            stopPolling: () => clearCached('pollTimer'),
            startHealthPolling: () => {
                clearCached('healthPollTimer')
                cache.healthPollTimer = setInterval(() => {
                    actions.loadSandboxStatus()
                }, HEALTH_POLL_INTERVAL_MS)
            },
            stopHealthPolling: () => clearCached('healthPollTimer'),
            loadSandboxStatusSuccess: ({ sandboxStatus }) => {
                if (!sandboxStatus) {
                    return
                }
                if (sandboxStatus.status === 'running') {
                    actions.stopPolling()
                    if (!values.isHealthPolling) {
                        actions.startHealthPolling()
                    }
                    if (!values.connectInfo) {
                        actions.loadConnectInfo()
                    }
                } else if (sandboxStatus.status === 'error' || sandboxStatus.status === 'stopped') {
                    actions.stopPolling()
                    actions.stopHealthPolling()
                    actions.clearTokenRefresh()
                }
            },
            loadConnectInfoSuccess: ({ connectInfo }) => {
                if (connectInfo) {
                    actions.scheduleTokenRefresh(connectInfo.expires_in)
                }
            },
            scheduleTokenRefresh: ({ expiresIn }) => {
                actions.clearTokenRefresh()
                const refreshMs = Math.max(expiresIn * TOKEN_REFRESH_RATIO * 1000, MIN_TOKEN_REFRESH_MS)
                cache.tokenRefreshTimer = setTimeout(() => {
                    actions.loadConnectInfo()
                }, refreshMs)
            },
            loadConnectInfoFailure: () => {
                // Don't reschedule — a 5s retry loop would hammer a broken sandbox.
                actions.clearTokenRefresh()
            },
            clearTokenRefresh: () => clearCached('tokenRefreshTimer'),
            stopAppSuccess: () => {
                actions.stopPolling()
                actions.stopHealthPolling()
                actions.clearTokenRefresh()
            },
            restartAppSuccess: () => {
                actions.startPolling()
                actions.clearTokenRefresh()
            },
        }
    }),

    afterMount(({ actions }) => {
        actions.loadStreamlitApp()
    }),

    beforeUnmount(({ actions }) => {
        actions.stopPolling()
        actions.stopHealthPolling()
        actions.clearTokenRefresh()
    }),
])

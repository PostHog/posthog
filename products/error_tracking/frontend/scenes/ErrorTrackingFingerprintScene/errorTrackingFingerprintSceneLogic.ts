import { MakeLogicType, actions, connect, kea, key, listeners, path, props, reducers } from 'kea'
import { loaders } from 'kea-loaders'
import { router } from 'kea-router'
import { subscriptions } from 'kea-subscriptions'

import { teamLogic } from 'scenes/teamLogic'
import { urls } from 'scenes/urls'

import { errorTrackingFingerprintsResolveRetrieve } from '../../generated/api'
import type { ErrorTrackingFingerprintApi } from '../../generated/api.schemas'

export interface ErrorTrackingFingerprintSceneLogicProps {
    fingerprint: string
    timestamp?: string
}

// A fingerprint links to an issue that may not exist yet — the error can still be in the ingestion
// pipeline when the user follows the link. Retry a bounded number of times before giving up so the
// page resolves once processing catches up instead of dead-ending on a transient Not Found.
const MAX_RESOLVE_RETRIES = 10
const RESOLVE_RETRY_DELAY_MS = 2000

// kea-router runs decodeURI(pathname) before matching the route, so the `:fingerprint` param it
// hands paramsToProps is already partially decoded. Decoding that a second time turns an encoded
// literal `%` into a bare `%` (throwing URIError) and silently mangles `%XX` sequences. Callers
// must pass the raw, still-percent-encoded path segment (see rawFingerprintPathSegment); we decode
// it exactly once here and fall back to the raw text if it isn't valid encoding.
export function decodeFingerprintSegment(rawSegment: string): string {
    try {
        return decodeURIComponent(rawSegment)
    } catch {
        return rawSegment
    }
}

// Recover the original encoded fingerprint segment from the raw pathname. kea-router keeps
// location.pathname percent-encoded — only the transient match inside urlToAction runs decodeURI —
// so this is the single source that survives one clean decodeURIComponent.
export function rawFingerprintPathSegment(pathname: string): string | null {
    const match = pathname.match(/\/error_tracking\/fingerprint\/([^/]+)\/?$/)
    return match ? match[1] : null
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface errorTrackingFingerprintSceneLogicValues {
    currentTeamId: number | null // teamLogic
    notFound: boolean
    resolvedFingerprint: ErrorTrackingFingerprintApi | null
    resolvedFingerprintLoading: boolean
    retries: number
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface errorTrackingFingerprintSceneLogicActions {
    giveUp: () => {}
    resolveFingerprint: () => any
    resolveFingerprintFailure: (
        error: string,
        errorObject?: any
    ) => {
        error: string
        errorObject?: any
    }
    resolveFingerprintSuccess: (
        resolvedFingerprint: ErrorTrackingFingerprintApi | null,
        payload?: any
    ) => {
        resolvedFingerprint: ErrorTrackingFingerprintApi | null
        payload?: any
    }
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface errorTrackingFingerprintSceneLogicMeta {
    key: string
}

export type errorTrackingFingerprintSceneLogicType = MakeLogicType<
    errorTrackingFingerprintSceneLogicValues,
    errorTrackingFingerprintSceneLogicActions,
    ErrorTrackingFingerprintSceneLogicProps,
    errorTrackingFingerprintSceneLogicMeta
>

export const errorTrackingFingerprintSceneLogic = kea<errorTrackingFingerprintSceneLogicType>([
    path((key) => ['products', 'error_tracking', 'scenes', 'errorTrackingFingerprintSceneLogic', key]),
    props({} as ErrorTrackingFingerprintSceneLogicProps),
    key((props) => props.fingerprint),
    connect(() => ({
        values: [teamLogic, ['currentTeamId']],
    })),

    actions({
        giveUp: () => ({}),
    }),

    reducers({
        retries: [0, { resolveFingerprintFailure: (state) => state + 1 }],
        // Terminal "not found" is distinct from "still resolving": once we've exhausted retries the
        // page must stop showing the loading state and render Not Found.
        notFound: [false, { giveUp: () => true, resolveFingerprint: () => false }],
    }),

    loaders(({ props }) => ({
        resolvedFingerprint: [
            null as ErrorTrackingFingerprintApi | null,
            {
                resolveFingerprint: async (): Promise<ErrorTrackingFingerprintApi | null> => {
                    const teamId = teamLogic.values.currentTeamId
                    if (!teamId || !props.fingerprint) {
                        return null
                    }
                    return await errorTrackingFingerprintsResolveRetrieve(String(teamId), {
                        fingerprint: props.fingerprint,
                    })
                },
            },
        ],
    })),

    listeners(({ props, values, actions, cache }) => ({
        // The error may still be in the ingestion pipeline, so a Not Found can be transient. Retry a
        // few times before giving up. Same disposable key so a pending retry is replaced, not stacked.
        resolveFingerprintFailure: () => {
            if (values.retries > MAX_RESOLVE_RETRIES) {
                actions.giveUp()
                return
            }
            cache.disposables.add(() => {
                const timerId = window.setTimeout(() => actions.resolveFingerprint(), RESOLVE_RETRY_DELAY_MS)
                return () => clearTimeout(timerId)
            }, 'resolveRetry')
        },
        resolveFingerprintSuccess: ({ resolvedFingerprint }) => {
            if (!resolvedFingerprint) {
                return
            }
            // Preserve the alert attribution the link arrived with (see errorTrackingIssueLinkHogTemplate)
            // so the redirect to the issue page keeps its channel attribution.
            const { utm_source, utm_campaign, utm_medium } = router.values.searchParams
            router.actions.replace(
                urls.errorTrackingIssue(resolvedFingerprint.issue_id, {
                    fingerprint: props.fingerprint,
                    // Only forward a timestamp the link actually carried — without one, the issue
                    // scene defaults to the newest event via its summary query.
                    timestamp: props.timestamp,
                    utm_source,
                    utm_campaign,
                    utm_medium,
                })
            )
        },
    })),

    subscriptions(({ actions, values }) => ({
        // The team may not be loaded yet when the scene mounts from a direct link or hard refresh.
        // Resolving then returns null and the page falls into a permanent Not Found, so re-run
        // resolution once the team id is available. This fires on mount too, so it's the only
        // trigger we need.
        currentTeamId: (currentTeamId: number | null) => {
            if (currentTeamId && !values.resolvedFingerprint && !values.resolvedFingerprintLoading) {
                actions.resolveFingerprint()
            }
        },
    })),
])

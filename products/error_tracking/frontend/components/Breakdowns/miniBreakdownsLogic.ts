import { MakeLogicType, connect, events, kea, key, path, props, selectors } from 'kea'
import { loaders } from 'kea-loaders'
import { subscriptions } from 'kea-subscriptions'
import posthog from 'posthog-js'

import api from 'lib/api'
import { BREAKDOWN_OTHER_DISPLAY } from 'scenes/insights/utils'

import {
    CachedErrorTrackingBreakdownsQueryResponse,
    ErrorTrackingBreakdownsQueryResponse,
} from '~/queries/schema/schema-general'

import type { DateRange } from '../../../../../frontend/src/queries/schema/schema-general'
import { errorTrackingBreakdownsQuery } from '../../queries'
import { breakdownFiltersLogic } from './breakdownFiltersLogic'
import { BREAKDOWN_PRESETS, BreakdownsEvents, LIMIT_ITEMS } from './consts'

export interface MiniBreakdownsLogicProps {
    issueId: string
}

export interface BreakdownSinglePropertyStat {
    label: string
    count: number
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface miniBreakdownsLogicValues {
    dateRange: DateRange // breakdownFiltersLogic
    filterTestAccounts: boolean // breakdownFiltersLogic
    getBreakdownForProperty: (property: string) => {
        properties: BreakdownSinglePropertyStat[]
        totalCount: number
    }
    response: ErrorTrackingBreakdownsQueryResponse | null
    responseLoading: boolean
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface miniBreakdownsLogicActions {
    loadResponse: () => any
    loadResponseFailure: (
        error: string,
        errorObject?: any
    ) => {
        error: string
        errorObject?: any
    }
    loadResponseSuccess: (
        response: ErrorTrackingBreakdownsQueryResponse,
        payload?: any
    ) => {
        response: ErrorTrackingBreakdownsQueryResponse
        payload?: any
    }
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface miniBreakdownsLogicMeta {
    key: string
    __keaTypeGenInternalSelectorTypes: {
        getBreakdownForProperty: (response: ErrorTrackingBreakdownsQueryResponse | null) => (property: string) => {
            properties: BreakdownSinglePropertyStat[]
            totalCount: number
        }
    }
}

export type miniBreakdownsLogicType = MakeLogicType<
    miniBreakdownsLogicValues,
    miniBreakdownsLogicActions,
    MiniBreakdownsLogicProps,
    miniBreakdownsLogicMeta
>

export const miniBreakdownsLogic = kea<miniBreakdownsLogicType>([
    path(['products', 'error_tracking', 'components', 'Breakdowns', 'miniBreakdownsLogic']),
    props({} as MiniBreakdownsLogicProps),
    key(({ issueId }: MiniBreakdownsLogicProps) => issueId),
    connect(() => ({
        values: [breakdownFiltersLogic, ['dateRange', 'filterTestAccounts']],
    })),
    loaders(({ props, values }) => ({
        response: [
            null as ErrorTrackingBreakdownsQueryResponse | null,
            {
                loadResponse: async () => {
                    const startTime = Date.now()
                    // Read connected values up front: the logic (and its connected breakdownFiltersLogic)
                    // may unmount while the query is in flight, and accessing values afterwards would throw.
                    const { dateRange, filterTestAccounts } = values
                    const breakdownProperties = BREAKDOWN_PRESETS.map((preset) => preset.property)
                    const result = await api.query(
                        errorTrackingBreakdownsQuery({
                            issueId: props.issueId,
                            dateRange,
                            filterTestAccounts,
                            breakdownProperties,
                            maxValuesPerProperty: LIMIT_ITEMS,
                        }),
                        { refresh: 'blocking' }
                    )

                    posthog.capture(BreakdownsEvents.MiniBreakdownsLoaded, {
                        issueId: props.issueId,
                        dateRange,
                        filterTestAccounts,
                        breakdownProperties,
                        duration: Date.now() - startTime,
                        cached: (result as CachedErrorTrackingBreakdownsQueryResponse).is_cached ?? false,
                    })

                    return result
                },
            },
        ],
    })),
    selectors(() => ({
        getBreakdownForProperty: [
            (s) => [s.response],
            (response: ErrorTrackingBreakdownsQueryResponse) => {
                return (property: string): { properties: BreakdownSinglePropertyStat[]; totalCount: number } => {
                    const properties: BreakdownSinglePropertyStat[] = []
                    let totalCount = 0

                    if (response && response.results) {
                        if (property && response.results[property]) {
                            const propertyData = response.results[property]

                            propertyData.values.forEach((value: any) => {
                                properties.push({
                                    label: value.value,
                                    count: value.count,
                                })
                            })

                            totalCount = propertyData.total_count || 0

                            const displayedCount = propertyData.values.reduce((sum, value) => sum + value.count, 0)
                            const otherCount = Math.max(0, totalCount - displayedCount)
                            if (otherCount > 0) {
                                properties.push({
                                    label: BREAKDOWN_OTHER_DISPLAY,
                                    count: otherCount,
                                })
                            }
                        }
                    }

                    return { properties, totalCount }
                }
            },
        ],
    })),

    subscriptions(({ actions }) => ({
        dateRange: (value, oldValue) => {
            if (oldValue !== undefined && value !== oldValue) {
                actions.loadResponse()
            }
        },
        filterTestAccounts: (value, oldValue) => {
            if (oldValue !== undefined && value !== oldValue) {
                actions.loadResponse()
            }
        },
    })),

    events(({ actions }) => ({
        afterMount: () => {
            actions.loadResponse()
        },
    })),
])

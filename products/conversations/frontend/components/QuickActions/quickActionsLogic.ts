export type quickActionsLogicType = MakeLogicType<
    quickActionsLogicValues,
    quickActionsLogicActions,
    Record<string, any>,
    quickActionsLogicMeta
>

export const quickActionsLogic = kea<quickActionsLogicType>([
    path(['products', 'conversations', 'frontend', 'components', 'QuickActions', 'quickActionsLogic']),

    connect(() => ({
        values: [teamLogic, ['currentTeamId']],
    })),

    actions({
        deleteQuickAction: (shortId: string) => ({ shortId }),
        quickActionSaved: (quickAction: QuickActionApi) => ({ quickAction }),
        quickActionDeleted: (shortId: string) => ({ shortId }),
        // Editor modal + form state
        openCreateModal: true,
        openEditModal: (quickAction: QuickActionApi) => ({ quickAction }),
        closeModal: true,
        setName: (name: string) => ({ name }),
        setDescription: (description: string) => ({ description }),
        setVisibility: (visibility: QuickActionVisibilityEnumApi) => ({ visibility }),
        setStatusAction: (status: TicketStatus | null) => ({ status }),
        setPriorityAction: (priority: TicketPriority | null) => ({ priority }),
        setTagsAction: (tags: string[]) => ({ tags }),
        setWorkflowId: (workflowId: string | null) => ({ workflowId }),
        saveQuickAction: (body: QuickActionBody | null) => ({ body }),
        setSaving: (saving: boolean) => ({ saving }),
    }),

    loaders(({ values }) => ({
        quickActions: [
            [] as QuickActionApi[],
            {
                loadQuickActions: async () => {
                    // Page through so teams with more quick actions than the page size still see all.
                    const teamId = String(values.currentTeamId)
                    const pageSize = 100
                    const all: QuickActionApi[] = []
                    let offset = 0
                    for (;;) {
                        const response = await conversationsQuickActionsList(teamId, { limit: pageSize, offset })
                        all.push(...response.results)
                        if (!response.next || response.results.length === 0) {
                            break
                        }
                        offset += pageSize
                    }
                    return all
                },
            },
        ],
    })),

    reducers({
        quickActions: {
            quickActionSaved: (state, { quickAction }) => {
                const existing = state.some((q) => q.short_id === quickAction.short_id)
                return existing
                    ? state.map((q) => (q.short_id === quickAction.short_id ? quickAction : q))
                    : [quickAction, ...state]
            },
            quickActionDeleted: (state, { shortId }) => state.filter((q) => q.short_id !== shortId),
        },
        isModalOpen: [
            false,
            {
                openCreateModal: () => true,
                openEditModal: () => true,
                closeModal: () => false,
                quickActionSaved: () => false,
            },
        ],
        editingShortId: [
            null as string | null,
            {
                openCreateModal: () => null,
                openEditModal: (_, { quickAction }) => quickAction.short_id,
                closeModal: () => null,
            },
        ],
        name: [
            '',
            {
                openCreateModal: () => '',
                openEditModal: (_, { quickAction }) => quickAction.name,
                setName: (_, { name }) => name,
            },
        ],
        description: [
            '',
            {
                openCreateModal: () => '',
                openEditModal: (_, { quickAction }) => quickAction.description ?? '',
                setDescription: (_, { description }) => description,
            },
        ],
        visibility: [
            Visibility.Team as QuickActionVisibilityEnumApi,
            {
                openCreateModal: () => Visibility.Team,
                openEditModal: (_, { quickAction }) => quickAction.visibility ?? Visibility.Team,
                setVisibility: (_, { visibility }) => visibility,
            },
        ],
        statusAction: [
            null as TicketStatus | null,
            {
                openCreateModal: () => null,
                openEditModal: (_, { quickAction }) =>
                    (quickAction.actions?.status as TicketStatus | undefined) ?? null,
                setStatusAction: (_, { status }) => status,
            },
        ],
        priorityAction: [
            null as TicketPriority | null,
            {
                openCreateModal: () => null,
                openEditModal: (_, { quickAction }) =>
                    (quickAction.actions?.priority as TicketPriority | undefined) ?? null,
                setPriorityAction: (_, { priority }) => priority,
            },
        ],
        tagsAction: [
            [] as string[],
            {
                openCreateModal: () => [],
                openEditModal: (_, { quickAction }) => quickAction.actions?.tags ?? [],
                setTagsAction: (_, { tags }) => tags,
            },
        ],
        workflowId: [
            null as string | null,
            {
                openCreateModal: () => null,
                openEditModal: (_, { quickAction }) => quickAction.workflow_id ?? null,
                setWorkflowId: (_, { workflowId }) => workflowId,
            },
        ],
        saving: [
            false,
            {
                setSaving: (_, { saving }) => saving,
            },
        ],
        // Distinguishes "no quick actions exist" from "the load failed" for the empty states.
        loadFailed: [
            false,
            {
                loadQuickActions: () => false,
                loadQuickActionsSuccess: () => false,
                loadQuickActionsFailure: () => true,
            },
        ],
    }),

    selectors({
        teamQuickActions: [
            (s) => [s.quickActions],
            (quickActions: QuickActionApi[]): QuickActionApi[] =>
                quickActions.filter((q) => q.visibility === Visibility.Team),
        ],
        personalQuickActions: [
            (s) => [s.quickActions],
            (quickActions: QuickActionApi[]): QuickActionApi[] =>
                quickActions.filter((q) => q.visibility === Visibility.Personal),
        ],
    }),

    listeners(({ actions, values }) => ({
        saveQuickAction: async ({ body }) => {
            const name = values.name.trim()
            if (!name) {
                return
            }
            const ticketActions: QuickActionActionsApi = {}
            if (values.statusAction) {
                ticketActions.status = values.statusAction
            }
            if (values.priorityAction) {
                ticketActions.priority = values.priorityAction
            }
            if (values.tagsAction.length) {
                ticketActions.tags = values.tagsAction
            }
            // A quick action bundles any of: a reply, ticket actions, and a workflow to run.
            const payload: QuickActionWriteInput = {
                name,
                description: values.description.trim(),
                content: body?.content ?? '',
                rich_content: body?.rich_content ?? {},
                actions: ticketActions,
                workflow_id: values.workflowId,
                visibility: values.visibility,
            }
            actions.setSaving(true)
            try {
                const saved = values.editingShortId
                    ? await conversationsQuickActionsPartialUpdate(
                          String(values.currentTeamId),
                          values.editingShortId,
                          payload
                      )
                    : await conversationsQuickActionsCreate(String(values.currentTeamId), payload)
                actions.quickActionSaved(saved)
                lemonToast.success(values.editingShortId ? 'Quick action updated' : 'Quick action created')
            } catch (error) {
                posthog.captureException(error)
                lemonToast.error('Failed to save quick action')
            } finally {
                actions.setSaving(false)
            }
        },
        deleteQuickAction: async ({ shortId }) => {
            try {
                await conversationsQuickActionsDestroy(String(values.currentTeamId), shortId)
                actions.quickActionDeleted(shortId)
                lemonToast.success('Quick action deleted')
            } catch (error) {
                posthog.captureException(error)
                lemonToast.error('Failed to delete quick action')
            }
        },
        loadQuickActionsFailure: () => {
            lemonToast.error('Failed to load quick actions')
        },
    })),

    afterMount(({ actions }) => {
        actions.loadQuickActions()
    }),
])

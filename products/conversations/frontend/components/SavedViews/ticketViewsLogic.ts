import {
    conversationsViewsCreate,
    conversationsViewsDestroy,
    conversationsViewsList,
    conversationsViewsPartialUpdate,
} from '../../generated/api'
import type { PatchedTicketViewApi, TicketViewApiFilters } from '../../generated/api.schemas'

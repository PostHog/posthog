export type ticketViewsLogicType = MakeLogicType<
    ticketViewsLogicValues,
    ticketViewsLogicActions,
    TicketViewsLogicProps,
    ticketViewsLogicMeta
>

export const ticketViewsLogic = kea<ticketViewsLogicType>([
    props({} as TicketViewsLogicProps),
    key((props) => props.id),
    path((key) => ['products', 'conversations', 'frontend', 'components', 'SavedViews', 'ticketViewsLogic', key]),

    connect(() => ({
        values: [teamLogic, ['currentTeamId'], supportTicketsSceneLogic, ['currentFilters', 'activeView']],
        actions: [supportTicketsSceneLogic, ['applyView', 'setActiveView']],
    })),

    actions({
        deleteView: (shortId: string) => ({ shortId }),
        updateView: (shortId: string, changes: TicketViewChanges) => ({ shortId, changes }),
        viewUpdated: (view: SavedTicketView) => ({ view }),
        toggleFavorite: (view: SavedTicketView) => ({ view }),
        loadView: (view: SavedTicketView) => ({ view }),
        openModal: true,
        closeModal: true,
        openSaveModal: true,
        closeSaveModal: true,
        setViewName: (viewName: string) => ({ viewName }),
        saveView: true,
        setSearchTerm: (searchTerm: string) => ({ searchTerm }),
    }),

    loaders(({ values }) => ({
        views: [
            [] as SavedTicketView[],
            {
                loadViews: async () => {
                    const response = await conversationsViewsList(String(values.currentTeamId))
                    return response.results as unknown as SavedTicketView[]
                },
                createView: async ({ name, filters }: { name: string; filters: TicketViewFilters }) => {
                    const created = (await conversationsViewsCreate(String(values.currentTeamId), {
                        name,
                        filters: filters as TicketViewApiFilters,
                    })) as unknown as SavedTicketView
                    lemonToast.success('View saved')
                    return [created, ...values.views]
                },
            },
        ],
    })),

    reducers({
        views: {
            deleteView: (state, { shortId }) => state.filter((v) => v.short_id !== shortId),
            viewUpdated: (state, { view }) => state.map((v) => (v.short_id === view.short_id ? view : v)),
        },
        favoritingShortIds: [
            [] as string[],
            {
                toggleFavorite: (state, { view }) =>
                    state.includes(view.short_id) ? state : [...state, view.short_id],
                viewUpdated: (state, { view }) => state.filter((id) => id !== view.short_id),
                // Clear on load completion (not the trigger) so opening the dropdown/modal
                // mid-flight doesn't drop the in-flight state early; also covers the
                // updateView failure path, which reloads views.
                loadViewsSuccess: () => [],
                loadViewsFailure: () => [],
            },
        ],
        isModalOpen: [
            false,
            {
                openModal: () => true,
                closeModal: () => false,
                loadView: () => false,
            },
        ],
        isSaveModalOpen: [
            false,
            {
                openSaveModal: () => true,
                closeSaveModal: () => false,
                createViewSuccess: () => false,
            },
        ],
        viewName: [
            '',
            {
                setViewName: (_, { viewName }) => viewName,
                closeSaveModal: () => '',
                createViewSuccess: () => '',
            },
        ],
        searchTerm: [
            '',
            {
                setSearchTerm: (_, { searchTerm }) => searchTerm,
                openModal: () => '',
                closeModal: () => '',
            },
        ],
    }),

    selectors({
        sortedViews: [
            (s) => [s.views],
            (views: SavedTicketView[]): SavedTicketView[] =>
                [...views].sort((a, b) => {
                    if (a.is_favorited !== b.is_favorited) {
                        return a.is_favorited ? -1 : 1
                    }
                    return b.created_at.localeCompare(a.created_at)
                }),
        ],
        favoriteViews: [
            (s) => [s.sortedViews],
            (sortedViews: SavedTicketView[]): SavedTicketView[] => sortedViews.filter((v) => v.is_favorited),
        ],
        filteredViews: [
            (s) => [s.sortedViews, s.searchTerm],
            (sortedViews: SavedTicketView[], searchTerm: string): SavedTicketView[] =>
                searchViews(sortedViews, searchTerm),
        ],
    }),

    listeners(({ actions, values }) => ({
        toggleFavorite: ({ view }) => {
            actions.updateView(view.short_id, { is_favorited: !view.is_favorited })
        },
        openModal: () => {
            // The header dropdown already loads views on open; skip a duplicate GET if one is in flight
            if (!values.viewsLoading) {
                actions.loadViews()
            }
        },
        saveView: () => {
            const name = values.viewName.trim()
            if (!name) {
                return
            }
            actions.createView({ name, filters: { ...values.currentFilters } })
        },
        deleteView: async ({ shortId }) => {
            try {
                await conversationsViewsDestroy(String(values.currentTeamId), shortId)
                lemonToast.success('View deleted')
            } catch {
                lemonToast.error('Failed to delete view')
                actions.loadViews()
            }
        },
        updateView: async ({ shortId, changes }) => {
            try {
                const updated = (await conversationsViewsPartialUpdate(
                    String(values.currentTeamId),
                    shortId,
                    changes as PatchedTicketViewApi
                )) as unknown as SavedTicketView
                actions.viewUpdated(updated)
                // Keep the header indicator and URL state in sync when the loaded view changes
                if (values.activeView?.short_id === shortId) {
                    actions.setActiveView(updated)
                }
                // Favoriting is a quiet, high-frequency action — no toast for it
                const isFavoriteToggleOnly = Object.keys(changes).length === 1 && 'is_favorited' in changes
                if (!isFavoriteToggleOnly) {
                    lemonToast.success('View updated')
                }
            } catch {
                lemonToast.error('Failed to update view')
                actions.loadViews()
            }
        },
        loadView: ({ view }) => {
            actions.applyView(view)
        },
        createViewFailure: () => {
            lemonToast.error('Failed to save view')
        },
        loadViewsFailure: () => {
            lemonToast.error('Failed to load saved views')
        },
    })),
])

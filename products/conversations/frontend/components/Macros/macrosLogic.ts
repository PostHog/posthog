export type macrosLogicType = MakeLogicType<macrosLogicValues, macrosLogicActions, Record<string, any>, macrosLogicMeta>

export const macrosLogic = kea<macrosLogicType>([
    path(['products', 'conversations', 'frontend', 'components', 'Macros', 'macrosLogic']),

    connect(() => ({
        values: [teamLogic, ['currentTeamId']],
    })),

    actions({
        deleteMacro: (shortId: string) => ({ shortId }),
        macroSaved: (macro: MacroApi) => ({ macro }),
        macroDeleted: (shortId: string) => ({ shortId }),
        // Editor modal + form state
        openCreateModal: true,
        openEditModal: (macro: MacroApi) => ({ macro }),
        closeModal: true,
        setName: (name: string) => ({ name }),
        setDescription: (description: string) => ({ description }),
        setVisibility: (visibility: MacroVisibilityEnumApi) => ({ visibility }),
        setStatusAction: (status: TicketStatus | null) => ({ status }),
        setPriorityAction: (priority: TicketPriority | null) => ({ priority }),
        setTagsAction: (tags: string[]) => ({ tags }),
        saveMacro: (body: MacroBody) => ({ body }),
        setSaving: (saving: boolean) => ({ saving }),
    }),

    loaders(({ values }) => ({
        macros: [
            [] as MacroApi[],
            {
                loadMacros: async () => {
                    // Page through so teams with more macros than the page size still see all of them.
                    const teamId = String(values.currentTeamId)
                    const pageSize = 100
                    const all: MacroApi[] = []
                    let offset = 0
                    for (;;) {
                        const response = await conversationsMacrosList(teamId, { limit: pageSize, offset })
                        all.push(...response.results)
                        if (!response.next || response.results.length === 0) {
                            break
                        }
                        offset += pageSize
                    }
                    return all
                },
            },
        ],
    })),

    reducers({
        macros: {
            macroSaved: (state, { macro }) => {
                const existing = state.some((m) => m.short_id === macro.short_id)
                return existing ? state.map((m) => (m.short_id === macro.short_id ? macro : m)) : [macro, ...state]
            },
            macroDeleted: (state, { shortId }) => state.filter((m) => m.short_id !== shortId),
        },
        isModalOpen: [
            false,
            {
                openCreateModal: () => true,
                openEditModal: () => true,
                closeModal: () => false,
                macroSaved: () => false,
            },
        ],
        editingShortId: [
            null as string | null,
            {
                openCreateModal: () => null,
                openEditModal: (_, { macro }) => macro.short_id,
                closeModal: () => null,
            },
        ],
        name: [
            '',
            {
                openCreateModal: () => '',
                openEditModal: (_, { macro }) => macro.name,
                setName: (_, { name }) => name,
            },
        ],
        description: [
            '',
            {
                openCreateModal: () => '',
                openEditModal: (_, { macro }) => macro.description ?? '',
                setDescription: (_, { description }) => description,
            },
        ],
        visibility: [
            Visibility.Team as MacroVisibilityEnumApi,
            {
                openCreateModal: () => Visibility.Team,
                openEditModal: (_, { macro }) => macro.visibility ?? Visibility.Team,
                setVisibility: (_, { visibility }) => visibility,
            },
        ],
        statusAction: [
            null as TicketStatus | null,
            {
                openCreateModal: () => null,
                openEditModal: (_, { macro }) => (macro.actions?.status as TicketStatus | undefined) ?? null,
                setStatusAction: (_, { status }) => status,
            },
        ],
        priorityAction: [
            null as TicketPriority | null,
            {
                openCreateModal: () => null,
                openEditModal: (_, { macro }) => (macro.actions?.priority as TicketPriority | undefined) ?? null,
                setPriorityAction: (_, { priority }) => priority,
            },
        ],
        tagsAction: [
            [] as string[],
            {
                openCreateModal: () => [],
                openEditModal: (_, { macro }) => macro.actions?.tags ?? [],
                setTagsAction: (_, { tags }) => tags,
            },
        ],
        saving: [
            false,
            {
                setSaving: (_, { saving }) => saving,
            },
        ],
        // Distinguishes "no macros exist" from "the load failed" for the picker/table empty states.
        loadFailed: [
            false,
            {
                loadMacros: () => false,
                loadMacrosSuccess: () => false,
                loadMacrosFailure: () => true,
            },
        ],
    }),

    selectors({
        teamMacros: [
            (s) => [s.macros],
            (macros: MacroApi[]): MacroApi[] => macros.filter((m) => m.visibility === Visibility.Team),
        ],
        personalMacros: [
            (s) => [s.macros],
            (macros: MacroApi[]): MacroApi[] => macros.filter((m) => m.visibility === Visibility.Personal),
        ],
    }),

    listeners(({ actions, values }) => ({
        saveMacro: async ({ body }) => {
            const name = values.name.trim()
            if (!name) {
                return
            }
            const macroActions: MacroActionsApi = {}
            if (values.statusAction) {
                macroActions.status = values.statusAction
            }
            if (values.priorityAction) {
                macroActions.priority = values.priorityAction
            }
            if (values.tagsAction.length) {
                macroActions.tags = values.tagsAction
            }
            const payload: MacroWriteInput = {
                name,
                description: values.description.trim(),
                content: body.content,
                rich_content: body.rich_content,
                actions: macroActions,
                visibility: values.visibility,
            }
            actions.setSaving(true)
            try {
                const saved = values.editingShortId
                    ? await conversationsMacrosPartialUpdate(
                          String(values.currentTeamId),
                          values.editingShortId,
                          payload
                      )
                    : await conversationsMacrosCreate(String(values.currentTeamId), payload)
                actions.macroSaved(saved)
                lemonToast.success(values.editingShortId ? 'Macro updated' : 'Macro created')
            } catch (error) {
                posthog.captureException(error)
                lemonToast.error('Failed to save macro')
            } finally {
                actions.setSaving(false)
            }
        },
        deleteMacro: async ({ shortId }) => {
            try {
                await conversationsMacrosDestroy(String(values.currentTeamId), shortId)
                actions.macroDeleted(shortId)
                lemonToast.success('Macro deleted')
            } catch (error) {
                posthog.captureException(error)
                lemonToast.error('Failed to delete macro')
            }
        },
        loadMacrosFailure: () => {
            lemonToast.error('Failed to load macros')
        },
    })),

    afterMount(({ actions }) => {
        actions.loadMacros()
    }),
])

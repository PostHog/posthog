import { MakeLogicType, actions, afterMount, connect, kea, listeners, path, reducers, selectors } from 'kea'
import { loaders } from 'kea-loaders'
import posthog from 'posthog-js'

import { lemonToast } from '@posthog/lemon-ui'

import { teamLogic } from 'scenes/teamLogic'

import {
    conversationsMacrosCreate,
    conversationsMacrosDestroy,
    conversationsMacrosList,
    conversationsMacrosPartialUpdate,
} from '../../generated/api'
import type { MacroActionsApi, MacroApi, MacroVisibilityEnumApi } from '../../generated/api.schemas'
import { MacroVisibilityEnumApi as Visibility } from '../../generated/api.schemas'
import type { TicketPriority, TicketStatus } from '../../types'

/** Payload accepted by the create/update endpoints (readonly fields stripped). */
export type MacroWriteInput = Parameters<typeof conversationsMacrosCreate>[1]

/** The reply body captured from the editor when saving a macro. */
export interface MacroBody {
    content: string
    rich_content: unknown
}


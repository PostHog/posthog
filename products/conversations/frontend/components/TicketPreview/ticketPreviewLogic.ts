import { MakeLogicType, afterMount, kea, key, path, props } from 'kea'
import { loaders } from 'kea-loaders'

import { getCurrentTeamId } from 'lib/utils/getAppContext'

import { conversationsTicketsMessagesList } from '../../generated/api'
import type { TicketMessageApi } from '../../generated/api.schemas'

export interface TicketPreviewLogicProps {
    ticketId: string
}

export interface TicketPreview {
    firstMessages: TicketMessageApi[]
    // The newest message in the thread, shown after the truncation divider. Null when the
    // whole thread already fits in firstMessages.
    lastMessage: TicketMessageApi | null
    totalCount: number
}

/** How many of the ticket's first messages the hover preview shows. */
export const TICKET_PREVIEW_MESSAGE_COUNT = 3

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface ticketPreviewLogicValues {
    preview: TicketPreview | null
    previewLoading: boolean
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface ticketPreviewLogicActions {
    loadPreview: () => any
    loadPreviewFailure: (
        error: string,
        errorObject?: any
    ) => {
        error: string
        errorObject?: any
    }
    loadPreviewSuccess: (
        preview: TicketPreview,
        payload?: any
    ) => {
        preview: TicketPreview
        payload?: any
    }
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface ticketPreviewLogicMeta {
    key: string
}

export type ticketPreviewLogicType = MakeLogicType<
    ticketPreviewLogicValues,
    ticketPreviewLogicActions,
    TicketPreviewLogicProps,
    ticketPreviewLogicMeta
>

export const ticketPreviewLogic = kea<ticketPreviewLogicType>([
    props({} as TicketPreviewLogicProps),
    key((props) => props.ticketId),
    path((key) => ['products', 'conversations', 'frontend', 'components', 'TicketPreview', 'ticketPreviewLogic', key]),

    loaders(({ props }) => ({
        preview: [
            null as TicketPreview | null,
            {
                loadPreview: async (): Promise<TicketPreview> => {
                    const teamId = String(getCurrentTeamId())
                    const first = await conversationsTicketsMessagesList(teamId, props.ticketId, {
                        limit: TICKET_PREVIEW_MESSAGE_COUNT,
                    })
                    const totalCount = first.count

                    // The thread is oldest-first, so the newest message lives on the last page.
                    // Only fetch it when it isn't already in the first batch.
                    let lastMessage: TicketMessageApi | null = null
                    if (totalCount > TICKET_PREVIEW_MESSAGE_COUNT) {
                        const last = await conversationsTicketsMessagesList(teamId, props.ticketId, {
                            limit: 1,
                            offset: totalCount - 1,
                        })
                        lastMessage = last.results[0] ?? null
                    }

                    return { firstMessages: first.results, lastMessage, totalCount }
                },
            },
        ],
    })),

    afterMount(({ actions, values }) => {
        if (!values.preview && !values.previewLoading) {
            actions.loadPreview()
        }
    }),
])

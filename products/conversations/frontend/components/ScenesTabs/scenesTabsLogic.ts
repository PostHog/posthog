import { MakeLogicType, actions, connect, kea, listeners, path, reducers, selectors } from 'kea'
import { router, urlToAction } from 'kea-router'

import { FEATURE_FLAGS } from 'lib/constants'
import { featureFlagLogic } from 'lib/logic/featureFlagLogic'
import type { FeatureFlagsSet } from 'lib/logic/featureFlagLogic'
import { urls } from 'scenes/urls'

import type { SceneTabKey } from '../../types'

export type SceneTabConfig = {
    key: SceneTabKey
    label: string
    href: string
}

export const SCENE_TABS: SceneTabConfig[] = [
    {
        key: 'tickets',
        label: 'Tickets',
        href: urls.supportTickets(),
    },
    {
        key: 'trends',
        label: 'Trends',
        href: urls.supportTrends(),
    },
    {
        key: 'settings',
        label: 'Settings',
        href: urls.supportSettings(),
    },
]

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface scenesTabsLogicValues {
    featureFlags: FeatureFlagsSet // featureFlagLogic
    activeTab: SceneTabKey
    tabs: SceneTabConfig[]
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface scenesTabsLogicActions {
    setActiveTab: (tab: SceneTabKey) => {
        tab: SceneTabKey
    }
    setTab: (tab: SceneTabKey) => {
        tab: SceneTabKey
    }
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface scenesTabsLogicMeta {
    __keaTypeGenInternalSelectorTypes: {
        tabs: (featureFlags: FeatureFlagsSet) => SceneTabConfig[]
    }
}

export type scenesTabsLogicType = MakeLogicType<
    scenesTabsLogicValues,
    scenesTabsLogicActions,
    Record<string, any>,
    scenesTabsLogicMeta
>

export const scenesTabsLogic = kea<scenesTabsLogicType>([
    path(['products', 'conversations', 'frontend', 'components', 'ScenesTabs', 'scenesTabsLogic']),
    connect(() => ({ values: [featureFlagLogic, ['featureFlags']] })),
    actions({
        setTab: (tab: SceneTabKey) => ({ tab }),
        setActiveTab: (tab: SceneTabKey) => ({ tab }),
    }),
    reducers({
        activeTab: [
            'tickets' as SceneTabKey,
            {
                setActiveTab: (_state: SceneTabKey, { tab }: { tab: SceneTabKey }) => tab,
            },
        ],
    }),
    selectors({
        tabs: [
            (s) => [s.featureFlags],
            (featureFlags: FeatureFlagsSet): SceneTabConfig[] =>
                featureFlags[FEATURE_FLAGS.PRODUCT_SUPPORT_TICKET_TRENDS]
                    ? SCENE_TABS
                    : SCENE_TABS.filter((tab) => tab.key !== 'trends'),
        ],
    }),
    listeners({
        setTab: ({ tab }: { tab: SceneTabKey }) => {
            const target = SCENE_TABS.find((sceneTab) => sceneTab.key === tab)?.href
            if (target) {
                router.actions.push(target)
            }
        },
    }),
    urlToAction(({ actions }) => {
        return {
            '/support/tickets': () => actions.setActiveTab('tickets'),
            '/support/tickets/:ticketId': () => actions.setActiveTab('tickets'),
            '/support/trends': () => actions.setActiveTab('trends'),
            '/support/settings': () => actions.setActiveTab('settings'),
        }
    }),
])

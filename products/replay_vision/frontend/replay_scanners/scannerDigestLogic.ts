export const scannerDigestLogic = kea<scannerDigestLogicType>([
    path(['products', 'replay_vision', 'frontend', 'replay_scanners', 'scannerDigestLogic']),
    props({} as ScannerDigestLogicProps),
    key((props) => props.scannerId),

    connect((props: ScannerDigestLogicProps) => ({
        values: [visionActionsLogic({ scannerId: props.scannerId }), ['visionActions', 'visionActionsLoading']],
        actions: [
            visionActionsLogic({ scannerId: props.scannerId }),
            ['loadActions', 'loadActionsSuccess', 'toggleActionEnabled', 'addAction'],
        ],
    })),

    actions({
        loadLatestRun: true,
        loadLatestRunSuccess: (run: VisionActionRunApi | null) => ({ run }),
        loadLatestRunFailure: true,
        createDigest: true,
        createDigestSuccess: true,
        createDigestFailure: true,
        runNow: true,
        runNowDone: true,
        setRunInProgress: (inProgress: boolean) => ({ inProgress }),
        toggleExpanded: true,
    }),

    reducers({
        // The digest's newest completed run; kept while a refresh is in flight so the card doesn't flash.
        latestRun: [
            null as VisionActionRunApi | null,
            {
                loadLatestRunSuccess: (_, { run }) => run,
            },
        ],
        latestRunLoading: [
            true,
            {
                loadLatestRun: () => true,
                loadLatestRunSuccess: () => false,
                loadLatestRunFailure: () => false,
            },
        ],
        digestCreating: [
            false,
            {
                createDigest: () => true,
                createDigestSuccess: () => false,
                createDigestFailure: () => false,
            },
        ],
        expanded: [
            false,
            {
                toggleExpanded: (state) => !state,
            },
        ],
        // Loading state for the digest card's "Run now" button (the ~1s trigger request).
        runningNow: [
            false,
            {
                runNow: () => true,
                runNowDone: () => false,
            },
        ],
        // A digest run is actively processing (from the runs list) — disables "Run now" and drives polling.
        runInProgress: [
            false,
            {
                setRunInProgress: (_, { inProgress }) => inProgress,
            },
        ],
        // Keep polling for a grace window after a manual run: the workflow creates its run row
        // asynchronously, so the RUNNING row usually isn't there on the refresh right after triggering.
        pollUntil: [
            0,
            {
                runNow: () => Date.now() + OBSERVE_POLL_GRACE_MS,
            },
        ],
    }),

    selectors({
        digest: [
            (s) => [s.visionActions],
            (visionActions: VisionActionApi[]): VisionActionApi | null =>
                visionActions.find((a) => a.is_scanner_digest) ?? null,
        ],
        shouldPoll: [
            (s) => [s.runInProgress, s.pollUntil],
            (runInProgress: boolean, pollUntil: number): boolean => shouldPollObservations(runInProgress, pollUntil),
        ],
    }),

    listeners(({ actions, props, values, cache }) => {
        // Reschedule after every settle so polling stops once no run is in flight and the grace window
        // has elapsed. Rescheduled on failure too, so a transient hiccup can't kill the cycle.
        const reschedulePoll = (): void =>
            scheduleObservationPoll(cache.disposables, values.shouldPoll, () => actions.loadLatestRun())
        return {
            loadActionsSuccess: () => {
                if (values.digest) {
                    actions.loadLatestRun()
                } else {
                    // No digest → nothing to fetch; clear the initial loading state so the opt-in card shows.
                    actions.loadLatestRunSuccess(null)
                }
            },

            loadLatestRun: async (_, breakpoint) => {
                const teamId = teamLogic.values.currentTeamId
                const digest = values.digest
                if (!teamId || !digest) {
                    actions.loadLatestRunFailure()
                    return
                }
                try {
                    const page = await visionActionsRunsList(String(teamId), digest.id, { limit: RUN_LOOKBACK })
                    breakpoint()
                    const results = page.results ?? []
                    // Surface an in-flight run so the card can show "Running…" and disable Run now.
                    actions.setRunInProgress(results.some((r) => r.status === VisionActionRunStatusEnumApi.Running))
                    const completed = results.find((r) => r.status === VisionActionRunStatusEnumApi.Completed)
                    if (!completed) {
                        actions.loadLatestRunSuccess(null)
                        return
                    }
                    // The run list is deliberately light (no report body); fetch the full run for the markdown.
                    const run = await visionActionsRunsRetrieve(String(teamId), digest.id, completed.id)
                    breakpoint()
                    actions.loadLatestRunSuccess(run)
                } catch {
                    // Silent: the card falls back to the "first digest is on its way" state.
                    actions.loadLatestRunFailure()
                }
            },

            createDigest: async () => {
                const teamId = teamLogic.values.currentTeamId
                if (!teamId) {
                    actions.createDigestFailure()
                    return
                }
                try {
                    const created = await visionActionsCreate(String(teamId), {
                        // Mirrors the backend provisioning defaults (digest.py) for scanners created before
                        // digests existed, or after the digest was deleted.
                        name: `Daily digest: ${props.scannerName}`.slice(0, 255),
                        scanner: props.scannerId,
                        is_scanner_digest: true,
                        trigger_config: {
                            rrule: SCANNER_DIGEST_RRULE,
                            timezone: teamLogic.values.currentTeam?.timezone || 'UTC',
                        },
                        delivery_config: [],
                    })
                    actions.createDigestSuccess()
                    lemonToast.success('Daily digest turned on')
                    // Insert the created digest optimistically instead of refetching the list — a refetch
                    // would blank the card (visionActionsLoading true, digest still absent) and flash it.
                    actions.addAction(created)
                    // A brand-new digest has no runs yet, so resolve the run state locally rather than
                    // fetching; this keeps the card from flashing through its latestRunLoading gap.
                    actions.loadLatestRunSuccess(null)
                } catch (error: any) {
                    actions.createDigestFailure()
                    lemonToast.error(`Couldn't turn on the daily digest${error?.detail ? `: ${error.detail}` : ''}`)
                }
            },

            runNow: async () => {
                const teamId = teamLogic.values.currentTeamId
                const digest = values.digest
                if (!teamId || !digest) {
                    actions.runNowDone()
                    return
                }
                try {
                    const { already_running } = await visionActionsRunCreate(String(teamId), digest.id)
                    lemonToast.success(
                        already_running
                            ? 'A digest is already being generated — it will appear here shortly.'
                            : "Generating the digest now. It'll appear here in a minute or two."
                    )
                    // Refresh + poll so the card picks up the running run and then the finished summary.
                    actions.loadLatestRun()
                } catch (error: any) {
                    lemonToast.error(`Couldn't run the digest${error?.detail ? `: ${error.detail}` : ''}`)
                } finally {
                    actions.runNowDone()
                }
            },

            loadLatestRunSuccess: reschedulePoll,
            loadLatestRunFailure: reschedulePoll,
        }
    }),

    afterMount(({ actions, values }) => {
        // visionActionsLogic loads on its own mount; if it already settled (tab was visited first),
        // loadActionsSuccess won't fire again, so resolve the card state here.
        if (!values.visionActionsLoading) {
            if (values.digest) {
                actions.loadLatestRun()
            } else {
                actions.loadLatestRunSuccess(null)
            }
        }
    }),
])

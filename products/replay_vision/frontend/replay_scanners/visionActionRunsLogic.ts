import { MakeLogicType, actions, afterMount, kea, key, listeners, path, props, reducers, selectors } from 'kea'

import { lemonToast } from 'lib/lemon-ui/LemonToast'
import { teamLogic } from 'scenes/teamLogic'

import { visionActionsRetrieve, visionActionsRunCreate, visionActionsRunsList } from '../generated/api'
import { VisionActionRunStatusEnumApi } from '../generated/api.schemas'
import type { VisionActionApi, VisionActionRunListApi } from '../generated/api.schemas'
import { OBSERVE_POLL_GRACE_MS, scheduleObservationPoll, shouldPollObservations } from '../logics/observationPolling'
import { visionActionSceneLogic } from './visionActionSceneLogic'

export interface VisionActionRunsLogicProps {
    actionId: string
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface visionActionRunsLogicValues {
    action: VisionActionApi | null
    actionLoading: boolean
    pollUntil: number
    runInProgress: boolean
    runningNow: boolean
    runs: VisionActionRunListApi[]
    runsCount: number
    runsLoading: boolean
    shouldPoll: boolean
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface visionActionRunsLogicActions {
    loadAction: () => {
        value: true
    }
    loadActionFailure: () => {
        value: true
    }
    loadActionSuccess: (action: VisionActionApi) => {
        action: VisionActionApi
    }
    loadRuns: () => {
        value: true
    }
    loadRunsFailure: () => {
        value: true
    }
    loadRunsSuccess: (
        runs: VisionActionRunListApi[],
        count: number
    ) => {
        count: number
        runs: VisionActionRunListApi[]
    }
    runNow: () => {
        value: true
    }
    runNowDone: () => {
        value: true
    }
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface visionActionRunsLogicMeta {
    key: string
    __keaTypeGenInternalSelectorTypes: {
        runInProgress: (runs: VisionActionRunListApi[]) => boolean
        shouldPoll: (runInProgress: boolean, pollUntil: number) => boolean
    }
}

export type visionActionRunsLogicType = MakeLogicType<
    visionActionRunsLogicValues,
    visionActionRunsLogicActions,
    VisionActionRunsLogicProps,
    visionActionRunsLogicMeta
>

export const visionActionRunsLogic = kea<visionActionRunsLogicType>([
    path(['products', 'replay_vision', 'frontend', 'replay_scanners', 'visionActionRunsLogic']),
    props({} as VisionActionRunsLogicProps),
    key((props) => props.actionId),

    actions({
        loadRuns: true,
        loadRunsSuccess: (runs: VisionActionRunListApi[], count: number) => ({ runs, count }),
        loadRunsFailure: true,
        loadAction: true,
        loadActionSuccess: (action: VisionActionApi) => ({ action }),
        loadActionFailure: true,
        runNow: true,
        runNowDone: true,
    }),

    reducers({
        runs: [
            [] as VisionActionRunListApi[],
            {
                loadRunsSuccess: (_, { runs }) => runs,
            },
        ],
        runsCount: [
            0,
            {
                loadRunsSuccess: (_, { count }) => count,
            },
        ],
        // Loading starts true so the page shows a spinner, not a flash of "no runs", before the first fetch.
        runsLoading: [
            true,
            {
                loadRuns: () => true,
                loadRunsSuccess: () => false,
                loadRunsFailure: () => false,
            },
        ],
        action: [
            null as VisionActionApi | null,
            {
                loadActionSuccess: (_, { action }) => action,
            },
        ],
        actionLoading: [
            true,
            {
                loadAction: () => true,
                loadActionSuccess: () => false,
                loadActionFailure: () => false,
            },
        ],
        // Loading state for the "Run now" button on the run-history page.
        runningNow: [
            false,
            {
                runNow: () => true,
                runNowDone: () => false,
            },
        ],
        // Keep polling for a grace window after a manual run: the workflow creates its run row
        // asynchronously, so the new RUNNING row usually isn't there on the refresh right after the
        // trigger returns. Poll until it lands (and then until it finishes).
        pollUntil: [
            0,
            {
                runNow: () => Date.now() + OBSERVE_POLL_GRACE_MS,
            },
        ],
    }),

    selectors({
        // A summary/alert run is actively processing — used to keep "Run now" disabled (a second run
        // coalesces server-side anyway, but disabling makes that obvious) and to drive polling.
        runInProgress: [
            (s) => [s.runs],
            (runs: VisionActionRunListApi[]): boolean =>
                runs.some((r) => r.status === VisionActionRunStatusEnumApi.Running),
        ],
        shouldPoll: [
            (s) => [s.runInProgress, s.pollUntil],
            (runInProgress: boolean, pollUntil: number): boolean => shouldPollObservations(runInProgress, pollUntil),
        ],
    }),

    listeners(({ actions, props, values, cache }) => {
        // Reschedule after every settle so polling stops once no run is in flight and the grace
        // window has elapsed. Rescheduled on failure too, so a transient hiccup can't kill the cycle.
        const reschedulePoll = (): void =>
            scheduleObservationPoll(cache.disposables, values.shouldPoll, () => actions.loadRuns())
        return {
            loadRuns: async () => {
                const teamId = teamLogic.values.currentTeamId
                if (!teamId) {
                    actions.loadRunsFailure()
                    return
                }
                try {
                    const response = await visionActionsRunsList(String(teamId), props.actionId, { limit: 100 })
                    actions.loadRunsSuccess(response.results ?? [], response.count ?? response.results?.length ?? 0)
                } catch (error: any) {
                    lemonToast.error(`Failed to load runs${error.detail ? `: ${error.detail}` : ''}`)
                    actions.loadRunsFailure()
                }
            },

            // Loads the action metadata for the page title + breadcrumb. Runs render independently of this.
            loadAction: async () => {
                const teamId = teamLogic.values.currentTeamId
                if (!teamId) {
                    actions.loadActionFailure()
                    return
                }
                try {
                    const action = await visionActionsRetrieve(String(teamId), props.actionId)
                    actions.loadActionSuccess(action)
                    visionActionSceneLogic.actions.setActionContext(action.name, action.scanner ?? null)
                } catch {
                    actions.loadActionFailure()
                }
            },

            runNow: async () => {
                const teamId = teamLogic.values.currentTeamId
                if (!teamId) {
                    actions.runNowDone()
                    return
                }
                try {
                    const { already_running } = await visionActionsRunCreate(String(teamId), props.actionId)
                    lemonToast.success(
                        already_running ? 'A run is already in progress.' : 'Running now — the new run appears below.'
                    )
                    // Refresh so the new (running) run shows up; the poll keeps refreshing until it lands
                    // (the workflow creates the row asynchronously) and then until it finishes.
                    actions.loadRuns()
                } catch (error: any) {
                    lemonToast.error(`Couldn't run this summary${error?.detail ? `: ${error.detail}` : ''}`)
                } finally {
                    actions.runNowDone()
                }
            },

            loadRunsSuccess: reschedulePoll,
            loadRunsFailure: reschedulePoll,
        }
    }),

    afterMount(({ actions }) => {
        actions.loadRuns()
        actions.loadAction()
    }),
])

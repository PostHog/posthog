// The impact endpoint caps its lookback at 90 days; the overview date range can go wider.
const MAX_IMPACT_WINDOW_DAYS = 90

/**
 * Backs the scanner's Overview tab: the built-in charts and stat panels, with their OWN filter set
 * (date range + verdict/tag), independent of the Observations tab's list filters. It loads the same
 * observations-stats endpoint as the Observations tab, but with these filters — so filtering the
 * overview never disturbs the observations list and vice versa. Impact lives here too, loaded with a
 * window derived from the overview date range so every widget honors the same timing.
 */
export const scannerOverviewLogic = kea<scannerOverviewLogicType>([
    path(['products', 'replay_vision', 'frontend', 'replay_scanners', 'scannerOverviewLogic']),
    props({} as ScannerOverviewLogicProps),
    key((props) => props.scannerId),

    connect((props: ScannerOverviewLogicProps) => ({
        values: [replayScannerLogic({ id: props.scannerId }), ['scanner']],
        actions: [replayScannerLogic({ id: props.scannerId }), ['loadScannerSuccess']],
    })),

    actions({
        loadOverviewStats: true,
        loadOverviewStatsSuccess: (stats: ObservationStatsApi) => ({ stats }),
        loadOverviewStatsFailure: true,
        setOverviewDateRange: (dateFrom: string | null, dateTo: string | null) => ({ dateFrom, dateTo }),
        setOverviewVerdictFilter: (values: ObservationVerdictValue[]) => ({ values }),
        setOverviewTagFilter: (values: string[]) => ({ values }),
        clearOverviewFilters: true,
    }),

    reducers({
        overviewDateFrom: [
            DEFAULT_DATE_FROM as string | null,
            {
                setOverviewDateRange: (_, { dateFrom }) => dateFrom,
                clearOverviewFilters: () => DEFAULT_DATE_FROM,
            },
        ],
        overviewDateTo: [
            null as string | null,
            {
                setOverviewDateRange: (_, { dateTo }) => dateTo,
                clearOverviewFilters: () => null,
            },
        ],
        overviewVerdictFilter: [
            [] as ObservationVerdictValue[],
            {
                setOverviewVerdictFilter: (_, { values }) => values,
                clearOverviewFilters: () => [],
            },
        ],
        overviewTagFilter: [
            [] as string[],
            {
                setOverviewTagFilter: (_, { values }) => values,
                clearOverviewFilters: () => [],
            },
        ],
        overviewStatsApi: [
            null as ObservationStatsApi | null,
            {
                loadOverviewStatsSuccess: (_, { stats }) => stats,
            },
        ],
        overviewStatsApiLoading: [
            false,
            {
                loadOverviewStats: () => true,
                loadOverviewStatsSuccess: () => false,
                loadOverviewStatsFailure: () => false,
            },
        ],
    }),

    selectors({
        // The date default alone isn't an "active" filter; only the pills (and a non-default date) are.
        hasActiveOverviewFilters: [
            (s) => [s.overviewVerdictFilter, s.overviewTagFilter, s.overviewDateFrom, s.overviewDateTo],
            (
                verdictFilter: ObservationVerdictValue[],
                tagFilter: string[],
                dateFrom: string | null,
                dateTo: string | null
            ): boolean =>
                verdictFilter.length > 0 || tagFilter.length > 0 || dateFrom !== DEFAULT_DATE_FROM || dateTo !== null,
        ],
        // Wrap the shared derivations in inline arrows with explicit return types so kea-typegen
        // can infer them (it can't resolve return types from bare imported-function references).
        availableTags: [
            (s) => [s.overviewStatsApi],
            (stats: ObservationStatsApi | null): string[] => availableTagsFromStats(stats),
        ],
        monitorStats: [
            (s) => [s.overviewStatsApi],
            (stats: ObservationStatsApi | null): MonitorStats => deriveMonitorStats(stats),
        ],
        classifierTagStats: [
            (s) => [s.overviewStatsApi],
            (stats: ObservationStatsApi | null): ClassifierTagStats => deriveClassifierTagStats(stats),
        ],
        scorerSummary: [
            (s) => [s.overviewStatsApi],
            (stats: ObservationStatsApi | null): ScorerSummary | null => deriveScorerSummary(stats),
        ],
        scorerHistogram: [
            (s) => [s.overviewStatsApi],
            (stats: ObservationStatsApi | null): ScorerHistogram | null => deriveScorerHistogram(stats),
        ],
        coverageStats: [
            (s) => [s.overviewStatsApi],
            (stats: ObservationStatsApi | null): CoverageStats => deriveCoverageStats(stats),
        ],
    }),

    loaders(({ props, values }) => ({
        overviewImpact: [
            null as ScannerImpactApi | null,
            {
                loadOverviewImpact: async () => {
                    const teamId = teamLogic.values.currentTeamId
                    // Impact only exists for monitors (a qualifier-free predicate); other types show no panel.
                    if (!teamId || props.scannerId === 'new' || values.scanner?.scanner_type !== 'monitor') {
                        return null
                    }
                    const windowDays = Math.min(
                        MAX_IMPACT_WINDOW_DAYS,
                        Math.max(1, daysFromDateRange(values.overviewDateFrom, values.overviewDateTo))
                    )
                    return await visionScannersImpactRetrieve(String(teamId), props.scannerId, {
                        window_days: windowDays,
                    })
                },
            },
        ],
    })),

    listeners(({ actions, props, values }) => {
        const reloadStats = (): void => actions.loadOverviewStats()
        // Only the date range changes the impact window; verdict/tag filters don't apply to impact.
        const reloadDateScoped = (): void => {
            actions.loadOverviewStats()
            actions.loadOverviewImpact()
        }
        return {
            setOverviewDateRange: reloadDateScoped,
            setOverviewVerdictFilter: reloadStats,
            setOverviewTagFilter: reloadStats,
            clearOverviewFilters: reloadDateScoped,
            // Impact needs the scanner type; refire once the scanner (and its type) resolves.
            loadScannerSuccess: () => actions.loadOverviewImpact(),

            loadOverviewStats: async (_, breakpoint) => {
                const teamId = teamLogic.values.currentTeamId
                if (!teamId || props.scannerId === 'new') {
                    actions.loadOverviewStatsFailure()
                    return
                }
                try {
                    const params: Record<string, string | number> = {
                        recent_days: daysFromDateRange(values.overviewDateFrom, values.overviewDateTo),
                    }
                    if (values.overviewVerdictFilter.length > 0) {
                        params.verdict = values.overviewVerdictFilter.join(',')
                    }
                    if (values.overviewTagFilter.length > 0) {
                        params.tags = values.overviewTagFilter.join(',')
                    }
                    if (values.overviewDateFrom) {
                        params.date_from = values.overviewDateFrom
                    }
                    if (values.overviewDateTo) {
                        params.date_to = values.overviewDateTo
                    }
                    const response = await visionScannersObservationsStatsRetrieve(
                        String(teamId),
                        props.scannerId,
                        params
                    )
                    breakpoint()
                    actions.loadOverviewStatsSuccess(response)
                } catch (error) {
                    if (error instanceof Error && isBreakpoint(error)) {
                        throw error
                    }
                    lemonToast.error('Failed to load overview stats')
                    actions.loadOverviewStatsFailure()
                }
            },
        }
    }),

    afterMount(({ actions }) => {
        actions.loadOverviewStats()
        // If the scanner is already cached, loadScannerSuccess won't refire — load impact now too.
        actions.loadOverviewImpact()
    }),
])

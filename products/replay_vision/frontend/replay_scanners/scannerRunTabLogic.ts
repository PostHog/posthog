/** Backs the on-demand recordings list: per-session observation lookup, scan bridging, and result polling. */
export const scannerRunTabLogic = kea<scannerRunTabLogicType>([
    path(['products', 'replay_vision', 'frontend', 'replay_scanners', 'scannerRunTabLogic']),
    props({} as ScannerRunTabLogicProps),
    key((props) => props.scannerId),

    connect((props: ScannerRunTabLogicProps) => ({
        // The sibling "Scan by session ID" box scans through replayScannerLogic; refresh the list when it lands
        // so a pasted session that's also visible here reflects its new observation.
        actions: [replayScannerLogic({ id: props.scannerId }), ['triggerOnDemandObservationSuccess']],
    })),

    actions({
        setVisibleSessionIds: (sessionIds: string[]) => ({ sessionIds }),
        startScan: (sessionId: string) => ({ sessionId }),
        startBulkScan: (sessionIds: string[]) => ({ sessionIds }),
        bulkScanDone: true,
        scanFailed: (sessionId: string) => ({ sessionId }),
        loadObservations: (background = false) => ({ background }),
        loadObservationsSuccess: (bySession: Record<string, RowObservation>) => ({ bySession }),
        loadObservationsFailure: true,
    }),

    reducers({
        visibleSessionIds: [
            [] as string[],
            {
                setVisibleSessionIds: (_, { sessionIds }) => sessionIds,
            },
        ],
        observationBySession: [
            {} as Record<string, RowObservation>,
            {
                // Merge over the previous map so rows scrolled out of the fetch keep their last-known state.
                loadObservationsSuccess: (state, { bySession }) => ({ ...state, ...bySession }),
            },
        ],
        // Per-row bridge from click to landed observation: each scanned session holds its own spinner, so
        // clicking several rows in a row all register instead of a single global scan blocking the rest.
        pendingSessionIds: [
            {} as Record<string, true>,
            {
                startScan: (state, { sessionId }) => (state[sessionId] ? state : { ...state, [sessionId]: true }),
                scanFailed: (state, { sessionId }) => {
                    if (!state[sessionId]) {
                        return state
                    }
                    const { [sessionId]: _dropped, ...rest } = state
                    return rest
                },
                // Drop the bridge for any session whose observation has now landed in the lookup.
                loadObservationsSuccess: (state, { bySession }) => {
                    const next = { ...state }
                    let changed = false
                    for (const sessionId of Object.keys(next)) {
                        if (bySession[sessionId]) {
                            delete next[sessionId]
                            changed = true
                        }
                    }
                    return changed ? next : state
                },
            },
        ],
        // Drives the table's loading bar on foreground refetches only; background polls reload silently.
        refreshingObservations: [
            false,
            {
                loadObservations: (state, { background }) => (background ? state : true),
                loadObservationsSuccess: () => false,
                loadObservationsFailure: () => false,
            },
        ],
        // Loading state for the "Scan N selected" bulk button.
        bulkScanning: [
            false,
            {
                startBulkScan: () => true,
                bulkScanDone: () => false,
            },
        ],
    }),

    selectors({
        shouldPoll: [
            (s) => [s.visibleSessionIds, s.observationBySession, s.pendingSessionIds],
            (
                visibleSessionIds: string[],
                observationBySession: Record<string, RowObservation>,
                pendingSessionIds: Record<string, true>
            ): boolean =>
                Object.keys(pendingSessionIds).length > 0 ||
                visibleSessionIds.some((id) => {
                    const observation = observationBySession[id]
                    return !!observation && IN_PROGRESS_STATUSES.has(observation.status)
                }),
        ],
    }),

    listeners(({ actions, props, values, cache }) => {
        // Rescheduled on failure too — a transient API hiccup shouldn't permanently kill the polling cycle.
        const reschedulePoll = (): void =>
            scheduleObservationPoll(cache.disposables, values.shouldPoll, () => actions.loadObservations(true))
        return {
            setVisibleSessionIds: ({ sessionIds }) => {
                if (sessionIds.length > 0) {
                    actions.loadObservations()
                }
            },

            // A scan fired from the sibling "Scan by session ID" box succeeded — pick up its observation.
            triggerOnDemandObservationSuccess: () => actions.loadObservations(),

            startScan: async ({ sessionId }) => {
                // Dedupe rapid repeat clicks on the same row: the reducer flips the row's spinner on
                // synchronously, but a second click can still land before that re-renders the button disabled.
                const inFlight: Set<string> = (cache.inFlightScans ??= new Set())
                if (inFlight.has(sessionId)) {
                    return
                }
                const existing = values.observationBySession[sessionId]
                if (existing && IN_PROGRESS_STATUSES.has(existing.status)) {
                    return
                }
                const teamId = teamLogic.values.currentTeamId
                if (!teamId) {
                    actions.scanFailed(sessionId)
                    lemonToast.error('Failed to start scan. Please try again.')
                    return
                }
                inFlight.add(sessionId)
                try {
                    await visionScannersObserveCreate(String(teamId), props.scannerId, { session_id: sessionId })
                    refreshVisionQuota()
                    // The scan creates a pending observation server-side — refetch to reflect it, then poll it through.
                    actions.loadObservations()
                } catch (error: any) {
                    lemonToast.error(`Failed to start scan${error?.detail ? `: ${error.detail}` : ''}`)
                    actions.scanFailed(sessionId)
                } finally {
                    inFlight.delete(sessionId)
                }
            },

            startBulkScan: async ({ sessionIds }) => {
                const teamId = teamLogic.values.currentTeamId
                if (!teamId || sessionIds.length === 0) {
                    actions.bulkScanDone()
                    return
                }
                try {
                    const response = await visionScannersBulkObserveCreate(String(teamId), props.scannerId, {
                        session_ids: sessionIds,
                    })
                    const results = response.results ?? []
                    const started = response.started
                    // The backend scans what fits and reports the rest — surface the split so the user
                    // knows a partial run happened rather than assuming everything started.
                    const limited = results.filter(
                        (r) => r.scan_outcome === 'skipped_limit' || r.scan_outcome === 'skipped_quota'
                    ).length
                    const failed = results.filter((r) => r.scan_outcome === 'failed').length
                    const extras = [
                        limited ? `${limited} skipped (limit reached)` : null,
                        failed ? `${failed} failed to start` : null,
                    ]
                        .filter(Boolean)
                        .join(', ')
                    if (started > 0) {
                        lemonToast.success(
                            `Started ${started} scan${started === 1 ? '' : 's'}${extras ? ` — ${extras}` : ''}`
                        )
                    } else if (limited > 0) {
                        lemonToast.warning("No scans started — you've hit the in-flight or monthly credit limit.")
                    } else {
                        lemonToast.error('No scans started. Please try again.')
                    }
                    // Started scans create pending observations server-side — refetch to reflect them.
                    actions.loadObservations()
                } catch (error: any) {
                    lemonToast.error(`Bulk scan failed${error?.detail ? `: ${error.detail}` : ''}`)
                } finally {
                    actions.bulkScanDone()
                }
            },

            loadObservations: async (_, breakpoint) => {
                const teamId = teamLogic.values.currentTeamId
                const sessionIds = values.visibleSessionIds
                if (!teamId || sessionIds.length === 0) {
                    actions.loadObservationsFailure() // Clear the foreground loading flag; a bare return spins forever.
                    return
                }
                try {
                    // No limit coupling to visible rows — retries stack observations and a truncated page hides scans.
                    // Ordering pinned: the newest-wins mapping below depends on it, not on the API default.
                    const response = await visionScannersObservationsList(String(teamId), props.scannerId, {
                        session_id: sessionIds.join(','),
                        order_by: '-created_at',
                    })
                    breakpoint()
                    const bySession: Record<string, RowObservation> = {}
                    for (const observation of response.results ?? []) {
                        // Results are newest-first — the first observation per session is the one the row reflects.
                        if (!(observation.session_id in bySession)) {
                            bySession[observation.session_id] = { id: observation.id, status: observation.status }
                        }
                    }
                    actions.loadObservationsSuccess(bySession)
                } catch (error) {
                    if (error instanceof Error && isBreakpoint(error)) {
                        throw error
                    }
                    // Best-effort enrichment — on failure the rows just stay scannable, but keep polling alive.
                    actions.loadObservationsFailure()
                }
            },

            loadObservationsSuccess: reschedulePoll,
            loadObservationsFailure: reschedulePoll,
        }
    }),
])

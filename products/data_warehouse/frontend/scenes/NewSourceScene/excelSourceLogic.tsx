import { MakeLogicType, actions, kea, listeners, path, reducers, selectors } from 'kea'

import { lemonToast } from '@posthog/lemon-ui'

import api from 'lib/api'

import { ExternalDataSourceType } from '~/queries/schema/schema-general'

import { sourceWizardLogic } from './sourceWizardLogic'

// Mirrors MAX_UPLOAD_SIZE_BYTES on the backend, so an oversized workbook fails instantly instead of
// after pushing 50MB+ over the wire only to be rejected.
export const MAX_EXCEL_UPLOAD_SIZE_BYTES = 50 * 1024 * 1024

export const EXCEL_UPLOAD_ACCEPT = '.xlsx,.xlsm'

// The backend source type, in one place so the wizard branch and the logic can't drift.
export const EXCEL_SOURCE_NAME: ExternalDataSourceType = 'Excel'

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface excelSourceLogicValues {
    uploading: boolean
    uploadedFilename: string | null
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface excelSourceLogicActions {
    selectFiles: (files: File[]) => {
        files: File[]
    }
    setUploading: (uploading: boolean) => {
        uploading: boolean
    }
    setUploadedFilename: (filename: string | null) => {
        filename: string | null
    }
    setSourceConnectionDetailsValues: (values: Record<string, any>) => {
        values: Record<string, any>
    } // sourceWizardLogic
}

export type excelSourceLogicType = MakeLogicType<excelSourceLogicValues, excelSourceLogicActions>

/**
 * Uploads the workbook, then hands the wizard a reference to it.
 *
 * The workbook can't ride the generic source form (that field type only takes JSON), so it goes to
 * the existing upload endpoint first — which just streams the bytes into our bucket — and the
 * resulting `upload_id`/`filename` are written into the wizard's payload. From there the standard
 * flow takes over: the source's `get_schemas` opens the workbook to list its sheets, and the user
 * picks sheets and columns like any other source.
 */
export const excelSourceLogic = kea<excelSourceLogicType>([
    path(['products', 'dataWarehouse', 'excelSourceLogic']),
    actions({
        selectFiles: (files: File[]) => ({ files }),
        setUploading: (uploading: boolean) => ({ uploading }),
        setUploadedFilename: (filename: string | null) => ({ filename }),
    }),
    reducers({
        uploading: [false, { setUploading: (_, { uploading }) => uploading }],
        uploadedFilename: [null as string | null, { setUploadedFilename: (_, { filename }) => filename }],
    }),
    selectors({}),
    listeners(({ actions }) => ({
        selectFiles: async ({ files }) => {
            const file = files[0]
            if (!file) {
                return
            }
            if (file.size > MAX_EXCEL_UPLOAD_SIZE_BYTES) {
                lemonToast.error('This file is larger than the 50MB upload limit.')
                return
            }

            const formData = new FormData()
            formData.append('file', file)
            formData.append('file_format', 'xlsx')

            actions.setUploading(true)
            try {
                const upload = await api.dataWarehouseTables.uploadFile(formData)
                // The wizard's "Next" then validates the reference and lists the workbook's sheets.
                sourceWizardLogic.actions.setSourceConnectionDetailsValues({
                    payload: { upload_id: upload.upload_id, filename: upload.filename },
                })
                actions.setUploadedFilename(upload.filename)
            } catch (e: any) {
                actions.setUploadedFilename(null)
                lemonToast.error(e.data?.message ?? e.message ?? 'Could not upload the workbook.')
            } finally {
                actions.setUploading(false)
            }
        },
    })),
])

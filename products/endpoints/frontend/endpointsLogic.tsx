import { MakeLogicType, actions, afterMount, connect, kea, path, reducers, selectors } from 'kea'
import { loaders } from 'kea-loaders'
import { urlToAction } from 'kea-router'

import api from 'lib/api'
import { createFuse } from 'lib/utils/fuseSearch'
import { teamLogic } from 'scenes/teamLogic'
import { urls } from 'scenes/urls'

import { ProductIntentContext, ProductKey } from '~/queries/schema/schema-general'
import { EndpointType } from '~/types'

export type EndpointsTab = 'endpoints' | 'usage'

export interface EndpointsFilters {
    search: string
    tags: string[]
}

export const DEFAULT_FILTERS: EndpointsFilters = {
    search: '',
    tags: [],
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface endpointsLogicValues {
    activeTab: EndpointsTab
    allEndpoints: EndpointType[]
    allEndpointsLoading: boolean
    endpoints: EndpointType[]
    filters: EndpointsFilters
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface endpointsLogicActions {
    addProductIntent: (
        properties: import('lib/utils/product-intents').ProductIntentProperties
    ) => import('lib/utils/product-intents').ProductIntentProperties // teamLogic
    loadEndpoints: () => any
    loadEndpointsFailure: (
        error: string,
        errorObject?: any
    ) => {
        error: string
        errorObject?: any
    }
    loadEndpointsSuccess: (
        allEndpoints: EndpointType[],
        payload?: any
    ) => {
        allEndpoints: EndpointType[]
        payload?: any
    }
    setActiveTab: (activeTab: EndpointsTab) => {
        activeTab: EndpointsTab
    }
    setFilters: (filters: Partial<EndpointsFilters>) => {
        filters: Partial<EndpointsFilters>
    }
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface endpointsLogicMeta {
    __keaTypeGenInternalSelectorTypes: {
        endpoints: (allEndpoints: EndpointType[], filters: EndpointsFilters) => EndpointType[]
    }
}

export type endpointsLogicType = MakeLogicType<
    endpointsLogicValues,
    endpointsLogicActions,
    Record<string, any>,
    endpointsLogicMeta
>

export const endpointsLogic = kea<endpointsLogicType>([
    path(['products', 'endpoints', 'frontend', 'endpointsLogic']),
    connect(() => ({
        actions: [teamLogic, ['addProductIntent']],
    })),
    actions({
        setFilters: (filters: Partial<EndpointsFilters>) => ({ filters }),
        setActiveTab: (activeTab: EndpointsTab) => ({ activeTab }),
    }),
    loaders(() => ({
        allEndpoints: [
            [] as EndpointType[],
            {
                loadEndpoints: async () => {
                    const response = await api.endpoint.list()
                    return response.results || []
                },
            },
        ],
    })),
    reducers({
        filters: [
            DEFAULT_FILTERS as EndpointsFilters,
            {
                setFilters: (state, { filters }) => ({ ...state, ...filters }),
            },
        ],
        activeTab: [
            'endpoints' as EndpointsTab,
            {
                setActiveTab: (_, { activeTab }) => activeTab,
            },
        ],
    }),
    selectors({
        endpoints: [
            (s) => [s.allEndpoints, s.filters],
            (allEndpoints: EndpointType[], filters: EndpointsFilters) => {
                let results = allEndpoints

                if (filters.tags.length > 0) {
                    const required = new Set(filters.tags)
                    results = results.filter((endpoint) => endpoint.tags?.some((tag) => required.has(tag)))
                }

                if (filters.search) {
                    const fuse = createFuse<EndpointType>(results, {
                        keys: ['name', 'description'],
                        threshold: 0.3,
                    })
                    results = fuse.search(filters.search).map((result) => result.item)
                }

                return results
            },
        ],
    }),
    afterMount(({ actions }) => {
        actions.loadEndpoints()
        actions.addProductIntent({
            product_type: ProductKey.ENDPOINTS,
            intent_context: ProductIntentContext.ENDPOINTS_VIEWED,
        })
    }),

    urlToAction(({ actions }) => ({
        [urls.endpoints()]: (_, searchParams) => {
            if (searchParams.tab === 'usage') {
                actions.setActiveTab('usage')
            } else {
                actions.setActiveTab('endpoints')
                actions.loadEndpoints()
            }
        },
    })),
])

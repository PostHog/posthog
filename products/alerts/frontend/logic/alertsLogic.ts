export type alertsLogicType = MakeLogicType<alertsLogicValues, alertsLogicActions, Record<string, any>, alertsLogicMeta>

export const alertsLogic = kea<alertsLogicType>([
    path(['lib', 'components', 'Alerts', 'alertsLogic']),
    actions({
        setPage: (page: number) => ({ page }),
        setFilters: (filters: Partial<AlertsFilters>) => ({ filters }),
        setAlertToggling: (alertId: string, isToggling: boolean) => ({ alertId, isToggling }),
        deleteAlert: (alert: AlertType) => ({ alert }),
        setAlertDeleting: (alertId: string, isDeleting: boolean) => ({ alertId, isDeleting }),
        toggleAlertEnabled: (alert: AlertType) => ({ alert }),
        updateAlertInList: (alert: AlertType) => ({ alert }),
    }),

    reducers({
        alertsResponse: [
            { results: [], count: 0 } as { results: AlertType[]; count: number },
            {
                updateAlertInList: (state, { alert }) => ({
                    ...state,
                    results: state.results.map((currentAlert) => (currentAlert.id === alert.id ? alert : currentAlert)),
                }),
            },
        ],
        page: [
            1,
            {
                setPage: (_, { page }) => page,
            },
        ],
        deletingAlertIds: [
            new Set<string>(),
            {
                setAlertDeleting: (state, { alertId, isDeleting }) => {
                    const nextState = new Set(state)
                    if (isDeleting) {
                        nextState.add(alertId)
                    } else {
                        nextState.delete(alertId)
                    }
                    return nextState
                },
            },
        ],
        togglingAlertIds: [
            new Set<string>(),
            {
                setAlertToggling: (state, { alertId, isToggling }) => {
                    const nextState = new Set(state)
                    if (isToggling) {
                        nextState.add(alertId)
                    } else {
                        nextState.delete(alertId)
                    }
                    return nextState
                },
            },
        ],
        filters: [
            DEFAULT_ALERTS_FILTERS,
            {
                setFilters: (state, { filters }) =>
                    objectClean({
                        ...state,
                        ...filters,
                    }),
            },
        ],
    }),

    loaders(({ values }) => ({
        alertsResponse: [
            { results: [], count: 0 } as { results: AlertType[]; count: number },
            {
                loadAlerts: async () => {
                    const search = values.filters.search.trim()

                    const response = await api.alerts.list(undefined, {
                        limit: ALERTS_PER_PAGE,
                        offset: (values.page - 1) * ALERTS_PER_PAGE,
                        ...(search ? { search } : {}),
                        ...(values.filters.createdBy !== 'All users' ? { created_by: values.filters.createdBy } : {}),
                    })
                    return { results: response.results, count: response.count ?? response.results.length }
                },
            },
        ],
    })),

    selectors(({ actions }) => ({
        alerts: [
            (s) => [s.alertsResponse],
            (response: { count: number; results: AlertType[] }): AlertType[] => response.results,
        ],
        alertsCount: [
            (s) => [s.alertsResponse],
            (response: { count: number; results: AlertType[] }): number => response.count,
        ],
        isFiltering: [
            (s) => [s.filters],
            (filters: AlertsFilters): boolean =>
                filters.search !== DEFAULT_ALERTS_FILTERS.search ||
                filters.createdBy !== DEFAULT_ALERTS_FILTERS.createdBy,
        ],
        alertsSortedByState: [
            (s) => [s.alerts],
            (alerts: AlertType[]): AlertType[] =>
                [...alerts].sort((a, b) => alertComparatorKey(a) - alertComparatorKey(b)),
        ],
        pagination: [
            (s) => [s.page, s.alertsCount],
            (page: number, count: number): PaginationManual => ({
                controlled: true,
                pageSize: ALERTS_PER_PAGE,
                currentPage: page,
                entryCount: count,
                onBackward: () => actions.setPage(page - 1),
                onForward: () => actions.setPage(page + 1),
            }),
        ],
    })),

    listeners(({ actions, values }) => ({
        deleteAlert: async ({ alert }) => {
            actions.setAlertDeleting(alert.id, true)
            try {
                await api.alerts.delete(alert.id)
                actions.loadAlerts()
                lemonToast.success(`${alert.name || 'Unnamed alert'} has been deleted`)
            } catch {
                lemonToast.error("Couldn't delete the alert. Try again.")
            } finally {
                actions.setAlertDeleting(alert.id, false)
            }
        },
        setPage: () => {
            actions.loadAlerts()
        },
        setFilters: async ({ filters }, breakpoint) => {
            if ('search' in filters && filters.search?.trim()) {
                await breakpoint(300)
            }
            if (values.page !== 1) {
                actions.setPage(1)
            } else {
                actions.loadAlerts()
            }
        },
        toggleAlertEnabled: async ({ alert }) => {
            actions.setAlertToggling(alert.id, true)
            try {
                const updatedAlert = await api.alerts.update(alert.id, { enabled: !alert.enabled })
                actions.updateAlertInList(updatedAlert)
            } catch {
                lemonToast.error('Failed to update alert')
            } finally {
                actions.setAlertToggling(alert.id, false)
            }
        },
    })),

    afterMount(({ actions }) => actions.loadAlerts()),
])

const alertComparatorKey = (alert: AlertType): number => {
    if (!alert.enabled) {
        return 100
    }

    switch (alert.state) {
        case AlertState.FIRING:
            return 1
        case AlertState.ERRORED:
            return 2
        case AlertState.SNOOZED:
            return 3
        case AlertState.NOT_FIRING:
            return 4
    }
}

import { MakeLogicType, actions, afterMount, kea, listeners, path, reducers, selectors } from 'kea'
import { loaders } from 'kea-loaders'

import { PaginationManual } from '@posthog/lemon-ui'

import api from 'lib/api'
import { objectClean } from 'lib/utils/objects'

import { AlertState } from '~/queries/schema/schema-general'

import { AlertType } from '../types'
import { AlertLogicProps } from './alertLogic'

export interface AlertsLogicProps extends AlertLogicProps {}

export const ALERTS_PER_PAGE = 30

export interface AlertsFilters {
    search: string
    createdBy: string
}

export const DEFAULT_ALERTS_FILTERS: AlertsFilters = {
    search: '',
    createdBy: 'All users',
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface alertsLogicValues {
    alerts: AlertType[];
    alertsCount: number;
    alertsResponse: {
        count: number;
        results: AlertType[];
    };
    alertsResponseLoading: boolean;
    alertsSortedByState: AlertType[];
    filters: AlertsFilters;
    isFiltering: boolean;
    page: number;
    pagination: PaginationManual;
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface alertsLogicActions {
    loadAlerts: () => any;
    loadAlertsFailure: (error: string, errorObject?: any) => {
        error: string;
        errorObject?: any;
    };
    loadAlertsSuccess: (alertsResponse: {
        count: number;
        results: AlertType[];
    }, payload?: any) => {
        alertsResponse: {
            count: number;
            results: AlertType[];
        };
        payload?: any;
    };
    setFilters: (filters: Partial<AlertsFilters>) => {
        filters: Partial<AlertsFilters>;
    };
    setPage: (page: number) => {
        page: number;
    };
}

// Generated by kea-typegen. Update if you're an agent, ignore if you're human.
export interface alertsLogicMeta {
    __keaTypeGenInternalSelectorTypes: {
        alerts: (alertsResponse: {
            count: number;
            results: AlertType[];
        }) => AlertType[];
        alertsCount: (alertsResponse: {
            count: number;
            results: AlertType[];
        }) => number;
        isFiltering: (filters: AlertsFilters) => boolean;
        alertsSortedByState: (alerts: AlertType[]) => AlertType[];
        pagination: (page: number, alertsCount: number) => PaginationManual;
    };
}

export type alertsLogicType = MakeLogicType<alertsLogicValues, alertsLogicActions, Record<string, any>, alertsLogicMeta>;

export const alertsLogic = kea<alertsLogicType>([
    path(['lib', 'components', 'Alerts', 'alertsLogic']),

    actions({
        setPage: (page: number) => ({ page }),
        setFilters: (filters: Partial<AlertsFilters>) => ({ filters }),
    }),

    reducers({
        page: [
            1,
            {
                setPage: (_, { page }) => page,
            },
        ],
        filters: [
            DEFAULT_ALERTS_FILTERS,
            {
                setFilters: (state, { filters }) =>
                    objectClean({
                        ...state,
                        ...filters,
                    }),
            },
        ],
    }),

    loaders(({ values }) => ({
        alertsResponse: [
            { results: [], count: 0 } as { results: AlertType[]; count: number },
            {
                loadAlerts: async () => {
                    const search = values.filters.search.trim()

                    const response = await api.alerts.list(undefined, {
                        limit: ALERTS_PER_PAGE,
                        offset: (values.page - 1) * ALERTS_PER_PAGE,
                        ...(search ? { search } : {}),
                        ...(values.filters.createdBy !== 'All users' ? { created_by: values.filters.createdBy } : {}),
                    })
                    return { results: response.results, count: response.count ?? response.results.length }
                },
            },
        ],
    })),

    selectors(({ actions }) => ({
        alerts: [(s) => [s.alertsResponse], (response: {
    count: number;
    results: AlertType[];
}): AlertType[] => response.results],
        alertsCount: [(s) => [s.alertsResponse], (response: {
    count: number;
    results: AlertType[];
}): number => response.count],
        isFiltering: [
            (s) => [s.filters],
            (filters: AlertsFilters): boolean =>
                filters.search !== DEFAULT_ALERTS_FILTERS.search ||
                filters.createdBy !== DEFAULT_ALERTS_FILTERS.createdBy,
        ],
        alertsSortedByState: [
            (s) => [s.alerts],
            (alerts: AlertType[]): AlertType[] =>
                [...alerts].sort((a, b) => alertComparatorKey(a) - alertComparatorKey(b)),
        ],
        pagination: [
            (s) => [s.page, s.alertsCount],
            (page: number, count: number): PaginationManual => ({
                controlled: true,
                pageSize: ALERTS_PER_PAGE,
                currentPage: page,
                entryCount: count,
                onBackward: () => actions.setPage(page - 1),
                onForward: () => actions.setPage(page + 1),
            }),
        ],
    })),

    listeners(({ actions, values }) => ({
        setPage: () => {
            actions.loadAlerts()
        },
        setFilters: async ({ filters }, breakpoint) => {
            if ('search' in filters && filters.search?.trim()) {
                await breakpoint(300)
            }
            if (values.page !== 1) {
                actions.setPage(1)
            } else {
                actions.loadAlerts()
            }
        },
    })),

    afterMount(({ actions }) => actions.loadAlerts()),
])

const alertComparatorKey = (alert: AlertType): number => {
    if (!alert.enabled) {
        return 100
    }

    switch (alert.state) {
        case AlertState.FIRING:
            return 1
        case AlertState.ERRORED:
            return 2
        case AlertState.SNOOZED:
            return 3
        case AlertState.NOT_FIRING:
            return 4
    }
}

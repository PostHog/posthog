#!/usr/bin/env tsx
/**
 * Copies shared prompt files into `shared/` (gitignored) so esbuild can inline
 * them via the `@shared/*` tsconfig alias at bundle time, and regenerates the
 * agent-platform playbook manifest from the agent-builder skills dir. Called from
 * build-hono.ts / dev-hono.ts before bundling, and standalone in CI.
 */
import { cpSync, mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from 'fs'
import { dirname, join, resolve } from 'path'

const ROOT_DIR = resolve(__dirname, '..')
const REPO_ROOT = resolve(ROOT_DIR, '../..')

// Single source of truth for the builder playbooks: `services/mcp/playbooks`,
// one `<id>/SKILL.md` per playbook. These are MCP-owned platform docs about the
// authoring tools — distinct from the concierge's code-bundled *kernel* skills
// (in the agent-builder bundle) and from team `skill_refs` store skills.
const PLAYBOOKS_DIR = resolve(ROOT_DIR, 'playbooks')

const PROMPTS = [
    {
        src: 'products/posthog_ai/skills/querying-posthog-data/references/guidelines.md',
        dest: 'shared/guidelines.md',
    },
    {
        // Builder playbooks embedded so the `agent-resolve-resource` tool can
        // serve them to any MCP consumer; the manifest below is generated from
        // the same dir.
        src: 'services/mcp/playbooks',
        dest: 'shared/playbooks',
    },
]

/** `auditing-the-fleet` → `auditingTheFleet` (valid JS identifier). */
function camelCase(id: string): string {
    return id.replace(/-([a-z0-9])/g, (_, c: string) => c.toUpperCase())
}

/** `auditing-the-fleet` → `Auditing the fleet`. */
function titleCase(id: string): string {
    const spaced = id.replace(/-/g, ' ')
    return spaced.charAt(0).toUpperCase() + spaced.slice(1)
}

/** Playbook ids = subdirs of PLAYBOOKS_DIR that contain a SKILL.md, sorted for determinism. */
function discoverPlaybookIds(): string[] {
    return readdirSync(PLAYBOOKS_DIR, { withFileTypes: true })
        .filter((e) => e.isDirectory())
        .map((e) => e.name)
        .sort()
}

// Write only if changed so dev watch rebuilds don't churn the git tree.
function writeIfChanged(path: string, content: string): void {
    let existing: string | undefined
    try {
        existing = readFileSync(path, 'utf-8')
    } catch {
        existing = undefined
    }
    if (existing !== content) {
        writeFileSync(path, content)
    }
}

const GEN_HEADER =
    '// AUTO-GENERATED by scripts/copy-instructions.ts from the MCP builder playbooks dir\n' +
    '// (services/mcp/playbooks/<id>/SKILL.md).\n' +
    '// Do not edit by hand — run `tsx services/mcp/scripts/copy-instructions.ts`.\n'

/**
 * Regenerate the playbook manifest (ids + titles) and content (the embedded
 * SKILL.md bodies) from whatever skills exist, so adding/renaming/relayouting an
 * agent-builder skill needs no hand edits here.
 */
export function generatePlaybookManifest(): void {
    const ids = discoverPlaybookIds()
    const agentPlatformDir = resolve(ROOT_DIR, 'src/tools/agentPlatform')

    const manifest =
        `${GEN_HEADER}export const PLAYBOOK_IDS = [\n` +
        ids.map((id) => `    '${id}',`).join('\n') +
        `\n] as const\n\n` +
        `export type PlaybookId = (typeof PLAYBOOK_IDS)[number]\n\n` +
        `export const PLAYBOOK_TITLES: Record<PlaybookId, string> = {\n` +
        ids.map((id) => `    '${id}': '${titleCase(id)}',`).join('\n') +
        `\n}\n`
    writeIfChanged(join(agentPlatformDir, 'playbookManifest.generated.ts'), manifest)

    const content =
        GEN_HEADER +
        ids.map((id) => `import ${camelCase(id)} from '@shared/playbooks/${id}/SKILL.md'`).join('\n') +
        `\n\nimport type { PlaybookId } from './playbookManifest.generated'\n\n` +
        `export const PLAYBOOK_CONTENT: Record<PlaybookId, string> = {\n` +
        ids.map((id) => `    '${id}': ${camelCase(id)},`).join('\n') +
        `\n}\n`
    writeIfChanged(join(agentPlatformDir, 'playbookContent.generated.ts'), content)
}

export function copyInstructions(): void {
    for (const prompt of PROMPTS) {
        const src = resolve(REPO_ROOT, prompt.src)
        const dest = resolve(ROOT_DIR, prompt.dest)
        mkdirSync(dirname(dest), { recursive: true })
        // Belt-and-braces: Node 24's `cpSync` has been observed to throw EEXIST
        // even with `force: true` on some platforms (notably macOS). Explicitly
        // remove the dest first so watch-mode rebuilds always succeed.
        rmSync(dest, { force: true, recursive: true })
        cpSync(src, dest, { recursive: true, force: true })
    }
    generatePlaybookManifest()
}

// Standalone execution: `tsx scripts/copy-instructions.ts` (used in CI).
if (require.main === module) {
    copyInstructions()
}

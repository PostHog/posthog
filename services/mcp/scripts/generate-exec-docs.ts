#!/usr/bin/env tsx
// Renders the exec tool's command reference into schema/exec-command-reference.md,
// a markdown fragment posthog.com fetches at build time and embeds in the MCP tools
// reference page, so the website documents the same syntax the server serves to
// agents at runtime.
import * as fs from 'node:fs'
import * as path from 'node:path'

const SECTIONS_DIR = path.join(__dirname, '../src/templates/sections')
const OUTPUT_PATH = path.join(__dirname, '../schema/exec-command-reference.md')

// Mirrors the `extraCommands` literal in src/lib/instructions-formatter.ts (buildClaudeExecCommandReference).
const LEARN_COMMAND_LINE = 'learn <topic...> - load one or more learning topics\n'

function readSection(name: string): string {
    return fs.readFileSync(path.join(SECTIONS_DIR, name), 'utf8').trim()
}

// Leading lines that only make sense inside a tool parameter description are dropped;
// the replaces are no-ops if the template wording changes, so drift shows up in review.
const cliSyntax = readSection('cli-syntax.md')
    .replace('CLI-style command string. Supported commands:', '')
    .replace('{extra_commands}', LEARN_COMMAND_LINE)
    .trim()

const execBlurb = readSection('exec-tool-blurb.md').replace('### Using the `posthog` tool', '').trim()

const fragment = `<!-- Generated by services/mcp/scripts/generate-exec-docs.ts (part of \`hogli build:openapi\`). Do not edit directly. -->

## CLI mode commands

In [CLI mode](/docs/model-context-protocol/faq#choosing-a-tool-mode), the default for most clients, the MCP server registers a single \`exec\` tool instead of one tool per endpoint. Agents pass it CLI-style command strings to list, search, inspect, and call the tools above on demand, which keeps context usage small.

This section is generated from the same templates the server serves to agents at runtime, so it always matches the deployed command surface.

${cliSyntax}

The \`learn\` command is only registered on hosts that use the guided help catalog (currently Claude web and desktop). On hosts that support MCP apps, CLI mode also registers a separate \`render-ui\` tool for rendering interactive visualizations.

### How agents use these commands

This is the workflow the tool description asks agents to follow.

${execBlurb}
`

fs.writeFileSync(OUTPUT_PATH, fragment)
process.stdout.write(`Wrote ${path.relative(process.cwd(), OUTPUT_PATH)}\n`)

#!/usr/bin/env tsx
/**
 * Generates UI app entry points and resource registry from YAML definitions.
 *
 * Reads ui_apps sections from definitions/*.yaml and products/X/mcp/tools.yaml.
 *
 * Produces:
 * - src/ui-apps/apps/generated/app-name.tsx — entry point per detail/list app
 * - src/resources/ui-apps.generated.ts — URI constants, UiAppKey type, withUiApp, UI_APPS
 *
 * Custom apps (type: custom) get registry entries but no generated entry point.
 *
 * Convention-based defaults: most fields are derived from the app key + product dir.
 * See resolveDetailApp() and resolveListApp() for the full derivation logic.
 */
import { spawnSync } from 'node:child_process'
import * as fs from 'node:fs'
import * as path from 'node:path'
import { fileURLToPath } from 'node:url'
import { parse as parseYaml } from 'yaml'

import { discoverDefinitions } from './lib/definitions.mjs'
import {
    type CategoryConfig,
    CategoryConfigSchema,
    type ResolvedDetailUiApp,
    type ResolvedListUiApp,
    type UiAppConfig,
} from './yaml-config-schema'

const MCP_ROOT = path.resolve(__dirname, '..')
const REPO_ROOT = path.resolve(MCP_ROOT, '../..')
const DEFINITIONS_DIR = path.resolve(MCP_ROOT, 'definitions')
const PRODUCTS_DIR = path.resolve(REPO_ROOT, 'products')
const GENERATED_APPS_DIR = path.resolve(MCP_ROOT, 'src/ui-apps/apps/generated')
const GENERATED_REGISTRY_PATH = path.resolve(MCP_ROOT, 'src/resources/ui-apps.generated.ts')

const AUTO_GENERATED_HEADER = '// AUTO-GENERATED by generate-ui-apps.ts — do not edit\n'

// ------------------------------------------------------------------
// Convention-based default resolution
// ------------------------------------------------------------------

function kebabToPascal(s: string): string {
    return s
        .split('-')
        .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
        .join('')
}

function kebabToTitle(s: string): string {
    return s
        .split('-')
        .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
        .join(' ')
}

/**
 * Derive the component import path from the YAML file location.
 * For products/actions/mcp/tools.yaml -> products/actions/mcp/apps
 * For services/mcp/definitions/core.yaml -> null (no product dir)
 */
function deriveComponentImport(yamlFilePath: string): string | null {
    const mcpDir = path.dirname(yamlFilePath)
    if (mcpDir.includes('/products/')) {
        return path.relative(REPO_ROOT, path.join(mcpDir, 'apps'))
    }
    return null
}

function resolveDetailApp(
    appKey: string,
    raw: Extract<UiAppConfig, { type: 'detail' }>,
    componentImport: string
): ResolvedDetailUiApp {
    const pascal = kebabToPascal(appKey)
    return {
        type: 'detail',
        view_prop: raw.view_prop,
        app_name: raw.app_name ?? `PostHog ${kebabToTitle(appKey)}`,
        description: raw.description ?? `${kebabToTitle(appKey)} detail view`,
        component_import: raw.component_import ?? componentImport,
        data_type: raw.data_type ?? `${pascal}Data`,
        view_component: raw.view_component ?? `${pascal}View`,
    }
}

function resolveListApp(
    appKey: string,
    raw: Extract<UiAppConfig, { type: 'list' }>,
    componentImport: string
): ResolvedListUiApp {
    // Strip -list suffix to get the singular entity key
    const singularKey = appKey.replace(/-list$/, '')
    const singularPascal = kebabToPascal(singularKey)
    const pascal = kebabToPascal(appKey)
    return {
        type: 'list',
        detail_tool: raw.detail_tool,
        detail_args: raw.detail_args ?? '{ id: item.id }',
        item_name_field: raw.item_name_field ?? 'name',
        click_prop: raw.click_prop ?? `on${singularPascal}Click`,
        entity_label: raw.entity_label ?? singularKey.replace(/-/g, ' '),
        app_name: raw.app_name ?? `PostHog ${kebabToTitle(appKey)}`,
        description: raw.description ?? `${kebabToTitle(appKey)} view`,
        component_import: raw.component_import ?? componentImport,
        list_data_type: raw.list_data_type ?? `${singularPascal}ListData`,
        item_data_type: raw.item_data_type ?? `${singularPascal}Data`,
        view_component: raw.view_component ?? `${pascal}View`,
    }
}

// ------------------------------------------------------------------
// Entry point generators
// ------------------------------------------------------------------

function generateDetailApp(appKey: string, config: ResolvedDetailUiApp): string {
    const pascalName = kebabToPascal(appKey)
    return `${AUTO_GENERATED_HEADER}import '../../styles/tailwind.css'

import { createRoot } from 'react-dom/client'

import { type ${config.data_type}, ${config.view_component} } from '${config.component_import}'

import { AppWrapper } from '../../components/AppWrapper'

function ${pascalName}App(): JSX.Element {
    return (
        <AppWrapper<${config.data_type}> appName="${config.app_name}">
            {({ data }) => <${config.view_component} ${config.view_prop}={data!} />}
        </AppWrapper>
    )
}

const container = document.getElementById('root')
if (container) {
    createRoot(container).render(<${pascalName}App />)
}
`
}

function generateListApp(appKey: string, config: ResolvedListUiApp): string {
    const pascalName = kebabToPascal(appKey)
    return `${AUTO_GENERATED_HEADER}import '../../styles/tailwind.css'

import type { App } from '@modelcontextprotocol/ext-apps'
import { useCallback } from 'react'
import { createRoot } from 'react-dom/client'

import { type ${config.item_data_type}, type ${config.list_data_type}, ${config.view_component} } from '${config.component_import}'

import { AppWrapper } from '../../components/AppWrapper'

function ${pascalName}App(): JSX.Element {
    return (
        <AppWrapper<${config.list_data_type}> appName="${config.app_name}">
            {({ data, app }) => <${pascalName}Content data={data!} app={app} />}
        </AppWrapper>
    )
}

function ${pascalName}Content({ data, app }: { data: ${config.list_data_type}; app: App | null }): JSX.Element {
    const fallbackToChat = useCallback(
        (name: string) => {
            app?.sendMessage({
                role: 'user',
                content: [{ type: 'text', text: \`Show me the details for ${config.entity_label} "\${name}"\` }],
            })
        },
        [app]
    )

    const handleClick = useCallback(
        async (item: ${config.item_data_type}): Promise<${config.item_data_type} | null> => {
            if (!app) {
                fallbackToChat(item.${config.item_name_field})
                return null
            }
            try {
                const result = await app.callServerTool({
                    name: '${config.detail_tool}',
                    arguments: ${config.detail_args},
                })
                if (result.isError || !result.structuredContent) {
                    fallbackToChat(item.${config.item_name_field})
                    return null
                }
                return result.structuredContent as unknown as ${config.item_data_type}
            } catch {
                fallbackToChat(item.${config.item_name_field})
                return null
            }
        },
        [app, fallbackToChat]
    )

    return <${config.view_component} data={data} ${config.click_prop}={handleClick} />
}

const container = document.getElementById('root')
if (container) {
    createRoot(container).render(<${pascalName}App />)
}
`
}

// ------------------------------------------------------------------
// Registry generator
// ------------------------------------------------------------------

interface RegistryEntry {
    appKey: string
    appDir: string
    constName: string
    uri: string
    name: string
    description: string
}

function toConstName(appKey: string): string {
    return appKey.toUpperCase().replace(/-/g, '_') + '_RESOURCE_URI'
}

function toUri(appKey: string): string {
    return `ui://posthog/${appKey}.html`
}

function generateRegistry(entries: RegistryEntry[]): string {
    const constants = entries.map((e) => `export const ${e.constName} = '${e.uri}'`).join('\n')

    const keyUnion = entries.map((e) => `'${e.appKey}'`).join(' | ')

    const uriMapEntries = entries.map((e) => `    '${e.appKey}': ${e.constName}`).join(',\n')

    const appEntries = entries
        .map(
            (e) =>
                `    {\n        name: '${e.name}',\n        uri: ${e.constName},\n        description: '${e.description}',\n        appDir: '${e.appDir}',\n    }`
        )
        .join(',\n')

    return `${AUTO_GENERATED_HEADER}
${constants}

export type UiAppKey = ${keyUnion}

export const URI_MAP: Record<UiAppKey, string> = {
${uriMapEntries},
}

export const UI_APPS: Array<{
    name: string
    uri: string
    description: string
    appDir: string
}> = [
${appEntries},
]
`
}

// ------------------------------------------------------------------
// Main
// ------------------------------------------------------------------

function main(): void {
    const definitionSources = discoverDefinitions({ definitionsDir: DEFINITIONS_DIR, productsDir: PRODUCTS_DIR })

    fs.mkdirSync(GENERATED_APPS_DIR, { recursive: true })

    const registryEntries: RegistryEntry[] = []
    const generatedFiles: string[] = []

    // Collect all ui_app keys for cross-referencing with tool ui_app fields
    const allUiAppKeys = new Set<string>()

    for (const def of definitionSources) {
        const content = fs.readFileSync(def.filePath, 'utf-8')
        const parsed = parseYaml(content)
        const result = CategoryConfigSchema.safeParse(parsed)
        if (!result.success) {
            console.error(`Invalid YAML config in ${def.filePath}:`)
            for (const issue of result.error.issues) {
                console.error(`  ${issue.path.join('.')}: ${issue.message}`)
            }
            process.exit(1)
        }
        const config: CategoryConfig = result.data
        if (!config.ui_apps) {
            continue
        }

        const componentImport = deriveComponentImport(def.filePath)

        for (const [appKey, appConfig] of Object.entries(config.ui_apps)) {
            allUiAppKeys.add(appKey)
            const constName = toConstName(appKey)
            const uri = toUri(appKey)

            if (appConfig.type === 'detail') {
                if (!componentImport && !appConfig.component_import) {
                    console.error(
                        `detail app "${appKey}" in ${def.filePath}: cannot derive component_import (not in a product dir). Set component_import explicitly.`
                    )
                    process.exit(1)
                }
                const resolved = resolveDetailApp(appKey, appConfig, componentImport!)
                const code = generateDetailApp(appKey, resolved)
                const outPath = path.join(GENERATED_APPS_DIR, `${appKey}.tsx`)
                fs.writeFileSync(outPath, code)
                generatedFiles.push(outPath)

                registryEntries.push({
                    appKey,
                    appDir: `generated/${appKey}`,
                    constName,
                    uri,
                    name: resolved.app_name,
                    description: resolved.description,
                })
            } else if (appConfig.type === 'list') {
                if (!componentImport && !appConfig.component_import) {
                    console.error(
                        `list app "${appKey}" in ${def.filePath}: cannot derive component_import (not in a product dir). Set component_import explicitly.`
                    )
                    process.exit(1)
                }
                const resolved = resolveListApp(appKey, appConfig, componentImport!)
                const code = generateListApp(appKey, resolved)
                const outPath = path.join(GENERATED_APPS_DIR, `${appKey}.tsx`)
                fs.writeFileSync(outPath, code)
                generatedFiles.push(outPath)

                registryEntries.push({
                    appKey,
                    appDir: `generated/${appKey}`,
                    constName,
                    uri,
                    name: resolved.app_name,
                    description: resolved.description,
                })
            } else {
                // type: custom — entry point is handwritten, appDir matches appKey directly
                registryEntries.push({
                    appKey,
                    appDir: appKey,
                    constName,
                    uri,
                    name: appConfig.app_name,
                    description: appConfig.description,
                })
            }
        }
    }

    // Validate ui_app references in tools
    for (const def of definitionSources) {
        const content = fs.readFileSync(def.filePath, 'utf-8')
        const parsed = parseYaml(content)
        const config = CategoryConfigSchema.parse(parsed)
        for (const [toolName, toolConfig] of Object.entries(config.tools)) {
            if (toolConfig.ui_app && !allUiAppKeys.has(toolConfig.ui_app)) {
                console.error(
                    `Tool "${toolName}" references ui_app "${toolConfig.ui_app}" which is not defined in any ui_apps section.`
                )
                process.exit(1)
            }
        }
    }

    // Sort registry entries alphabetically for stable output
    registryEntries.sort((a, b) => a.appKey.localeCompare(b.appKey))

    // Write registry
    const registryCode = generateRegistry(registryEntries)
    fs.writeFileSync(GENERATED_REGISTRY_PATH, registryCode)
    generatedFiles.push(GENERATED_REGISTRY_PATH)

    process.stdout.write(
        `Generated ${generatedFiles.length - 1} UI app entry point(s) + registry (${registryEntries.length} app(s))\n`
    )

    // Format generated files
    spawnSync(path.join(REPO_ROOT, 'bin/hogli'), ['format:js', ...generatedFiles], { stdio: 'pipe', cwd: REPO_ROOT })
}

// Export for testing
export {
    generateDetailApp,
    generateListApp,
    generateRegistry,
    kebabToPascal,
    resolveDetailApp,
    resolveListApp,
    toConstName,
    toUri,
}

// Run main when executed directly
function stripExt(filePath: string): string {
    return filePath.replace(/\.[jt]s$/, '')
}

const isDirectRun =
    typeof process !== 'undefined' &&
    process.argv[1] &&
    stripExt(path.resolve(process.argv[1])) === stripExt(fileURLToPath(import.meta.url))

if (isDirectRun) {
    main()
}

import { vi } from 'vitest'

// These are deliberately unset for tests. The list is shared between the
// cloudflare:workers mock and a process.env scrub because the `@/lib/env`
// proxy falls back to process.env — a dev shell exporting the local stack's
// values (localhost API base URL, analytics credentials) would otherwise flip
// URL resolution and analytics onto different code paths than CI exercises.
const UNSET_ENV_KEYS = [
    'INKEEP_API_KEY',
    'POSTHOG_API_BASE_URL',
    'POSTHOG_PUBLIC_URL',
    'MCP_APPS_BASE_URL',
    'POSTHOG_MCP_APPS_ANALYTICS_BASE_URL',
    'POSTHOG_UI_APPS_TOKEN',
    'POSTHOG_ANALYTICS_API_KEY',
    'POSTHOG_ANALYTICS_HOST',
]

vi.mock('cloudflare:workers', () => ({
    env: Object.fromEntries(UNSET_ENV_KEYS.map((key) => [key, undefined])),
}))

for (const key of UNSET_ENV_KEYS) {
    delete process.env[key]
}


/**
 * CDP producer names + their typed env-var config.
 *
 * Each producer is named explicitly after the cluster it targets so deployment
 * config is unambiguous. Env-var prefixes mirror the names of pre-existing
 * legacy producers so deployed values continue to apply without coordination:
 *
 * - `MSK_PRODUCER` reuses `KAFKA_METRICS_PRODUCER_*` (the old `'METRICS_PRODUCER'`
 *   target — same physical MSK cluster).
 * - `WARPSTREAM_INGESTION_PRODUCER` reuses `KAFKA_MONITORING_PRODUCER_*` for the
 *   CDP deployments. Production has those vars pointed at the shared
 *   warpstream-ingestion cluster (same physical cluster the analytics
 *   ingestion deployments call by `KAFKA_INGESTION_PRODUCER_*`). The env-var
 *   namespaces can be unified across deployments later.
 */
